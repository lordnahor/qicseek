<div id="content_title">Einar Mykletun spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>Customer Confidential </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: gabriela marcu" height="281" src="../../../bin/img/photos/people/einar_mykletun.jpg" width="171"/>
<p>Einar Mykletun<br/>
in Kenya
</p>
</div>
<p>Einar Mykletun, a Bren School Ph.D. student, can trace his passion for algorithms back to childhood when he thought it was &#8220;super cool&#8221; to be able to send hidden messages back and forth on the computer. </p>
<p>A native of Oslo, Norway, Mykletun spent four childhood years in France before returning to Norway until age 17 when he left for America as a foreign exchange student to play basketball. </p>
<p>While pursuing his bachelor degree at Macalester College in Saint Paul, Minnesota, Mykletun took a Computer Algorithms class that introduced him to Yao's Millionaire Problem. </p>
<p>This classic cryptography problem, where several millionaires wish to find out who is richest, without revealing to each other their net worth, led him to write his senior thesis on modern and quantum cryptography. </p>
<p><strong>ON TO GRADUATE SCHOOL </strong></p>
<p>After graduating with his bachelor degree, Mykletun worked as a programmer, but that was not satisfying enough and he ultimately decided to pursue a Ph.D. in the area of security and cryptography. </p>
<p>After narrowing his graduate school choices down to 4 that offered him similar financial packages and had comparable security/cryptography faculty, he chose UCI partly because of the mild Southern California climate but mostly because of professor Gene Tsudik. </p>
<p>The two hit it off over email and telephone prior to Mykletun making his decision and has praised Tsudik as a great advisor. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;Einar's typical demeanor is unassuming and somewhat understated,&#8221; Tsudik said. &#8220;However, his research quality and productivity are top-notch. His work is both thorough and disciplined. He is also a wonderful collaborator -- a very important trait for a systems-oriented Computer Science researcher.&#8221; </p>
<p>In laymen's terms, Mykletun&#8217;s research involves looking into security issues related to vendors providing the service of hosting databases for companies (outsourcing of databases). </p>
<p>"Our main focus is on how to protect the confidentiality of the customers' data stored at these vendors (who may not be trusted with the data contents) through the use of data encryption, while still allowing the vendors to operate on the data (run queries over it), which, when encrypted, is unintelligible to the vendors. </p>
<p>Other work we have done relates to security in wireless sensor networks (protecting data stored on very small and weak computational devices), speeding up secure web servers, and providing for authentication and integrity in an outsourced database model," Mykletun said. </p>
<p>Mykletun does not believe the impact of his current work on society will be felt anytime in the near future, but rather in the long term. </p>
<p>Quoting his advisor, he says, &#8220;If it solves today&#8217;s problems, it may not qualify as research.&#8221; Here again, Mykletun exhibits focus, patience and a commitment to his work.</p>
<p>While at UCI Mykletun has served as a Teaching Assistant (TA) and an instructor during the 10 week summer session in 2005. He considers both experiences valuable in that they taught him to appreciate all that is necessary for a professor to do when teaching. He has also spent time in Germany interning as a researcher at NEC Europe. </p>
<p>Deepening his knowledge in the area of security and cryptography has been very gratifying, yet Mykletun acknowledges that one&#8217;s world can become a bit isolated. </p>
<p>&#8220;We can become very specialized in our own little world, while knowing too little about many other important areas,&#8221; he said. &#8220;Attending conferences to present our work has allowed us to discuss our ideas with other researchers, meet several interesting individuals, and travel to new cities &#8211; I very much enjoyed this.&#8221;</p>
<p>The last conference he attended was in Taipei, Taiwan, where he presented his research group&#8217;s work on improving the performance of SSL servers. He has also contributed to journal publications and workshops.</p>
<p><strong>MAINTAINING A BALANCE </strong></p>
<p>Mykletun maintains a sense of balance in his life by keeping active at the ARC where he has played in an intramural basketball league, participated in the triathlon team and taken swimming and CPR classes, he also has enjoyed several dance concerts at the Irvine Barclay Theater. </p>
<p>Since he has lived in graduate housing at Verano Place on campus in the past and is currently residing in an apartment across the street from UCI with fellow Ph.D. students, it&#8217;s easier for Mykletun to enjoy what is available on campus recreationally speaking.</p>
<p>&#8220;UCI is not a very happening campus, but there are tons of graduate students to hang out with!&#8221; Mykletun said. </p>
<p>He also has a brother living in Newport Beach whom he gets to escape and hang out with when he needs a break.</p>
<p>Mykletun acknowledges his parents as terrific role models and &#8220;very cool people&#8221; and expresses gratitude for his two brothers. </p>
<p>&#8220;I feel very privileged for having arrived to this point in life with all the experiences I have had along the way &#8211; I look forward to continuing this adventure called life!&#8221;</p>
<p>Mykletun plans to stay on at Quest Software in Aliso Viejo, where he works as the Security and Compliance Architect, after completing his Ph.D. in Fall 2006. </p>
<p> &#8220;I think that we are all quite privileged to have the lives we have while being graduate students,&#8221; he said. &#8220;Yes, it is very stressful at times, and it&#8217;s difficult to block out your work, even during vacations.  But we do have a lot of flexibility and freedom that most people that work in corporations would be very grateful to have.  </p>
<p>Of course, the level of freedom and flexibility depends a lot on one&#8217;s advisor, and we in our research team have been very lucky to have had Gene as our 'boss'.&#8221;</p>
<p><strong>TIME FOR REFLECTION </strong></p>
<p>Reflecting on his time as a Ph.D. student, Mykletun offers the following advice for those considering the Ph.D. path. </p>
<p>Don&#8217;t settle for the bare minimum, push yourself whenever you get the chance, as new opportunities will open up. </p>
<p>For example, being active and showing interest can lead to new relationships with professors or students which may be of great help to you during.</p>
<p>&#8220;Be focused on what you wish to accomplish. Take the opportunity to work as an intern if the possibility arises or create that opportunity (and if it does not, then create that opportunity!),&#8221;  Mykletun said. &#8220;Don&#8217;t lose confidence in yourself &#8211; it is a big department/school and not everyone will have time to think about your needs and your work.&#8221; </p>
<p>And of course, he reminds students to be respectful and friendly when interacting with the Bren School staff. </p>
<p>&#8220;When you encounter moments of stress and the feeling of being overwhelmed, do not forget to enjoy yourself and to remember that you are in a good situation,&#8221; he said. &#8220;Post-graduation may be harsher than you think.&#8221;</p>
<p><i>- Diane Triantis</i></p>


