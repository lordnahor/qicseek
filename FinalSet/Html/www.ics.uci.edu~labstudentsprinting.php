		<div id="content_title">Printing in the labs &raquo;</div>

			<p>
			</p><blockquote>

			<div id="content_text">

        <p>There is one printer available in CS 364 for student use. ICS Labs
          use a pay-per-sheet printing system that is provided by OIT. Each page
          costs 10 cents to print and you must have a valid print card.
        </p>

        <ul>
          <li><a href="http://www.oit.uci.edu/computing/printing.html">Where to get a print card</a></li>
          <li><a href="#print_doc">How to send and print documents</a></li>
          <li><a href="#">Getting Refunds</a></li>
        </ul>

        <hr>
        <h5><a name="print_doc"></a>How to send and print documents</h5>
        <p>Windows:</p>
        <ul>
          <li>Click on <em>print</em> from the application.</li>
          <li>Make sure that the printer name is PayPrint CS364.</li>
          <li>Go to the printer and follow the direction on the print station</li>
          <li>On the screen, find your print job and then hit <em>Print</em>.</li>
          <li>You can continue to print other jobs or click <em>Logout</em> when
            you are done. </li>
          <li>The station will display how much value is left on the card.
            <p>PDF Files - if you are having problems printing a PDF file, please
              make sure that the postscript level is set to 1. To change the level,
              do the following:</p>
            <ul>
              <li>Click on <em>print --&gt; Properties... --&gt; Advanced...</em></li>
              <li>Under <em>Document Options</em>, find <em>PostScript Options</em></li>
              <li>Change <em>PostScript Language Level</em> to 1</li>
            </ul>
            <p>If you find the print job is coming out slowly, try printing a
              couple of pages at a time. Some PDF files are quite large and the
              printer cannot process it all at once.</p>
          </li>
        </ul>
        <p>UNIX:</p>
        <ul>
          <li>ASCII (text) files: <font face="Courier New, Courier, mono">mp</font>
            <em>asciifile.tx</em>t <font face="Courier New, Courier, mono">| lpr
            -Pppcs364</font></li>
          <li>PostScript files: <font face="Courier New, Courier, mono">lpr -Ppcs364</font>
            <em>postscript.ps</em></li>
          <li>TEX DVI files: <font face="Courier New, Courier, mono">dvisp -Pppcs364</font>
            <em>file.dvi</em></li>
        </ul>
        <blockquote>
          <p>For more information, please refer to the <a href="http://www.ics.uci.edu/computing/unix/printing.php">ICS
            Support UNIX Printing Guide</a>.</p>
        </blockquote>
        <h5><a name="refund"></a>Getting Refunds</h5>
        <p>If your print job does not print out properly (ie. streaks, paper jam)
          you can get a refund by contacting the Lab Manager in CS346L. If the
          Lab Manager is not available, please contact <a
href="http://www.oit.uci.edu/org/directions.html">OIT</a>.
        </p>
        <p>If the print card dispenser malfunctioned and you did not get a card
          or it did not add value to your card, please contact <a
href="http://www.oit.uci.edu/org/directions.html">OIT</a>.
        </p>

        <p>&nbsp; </p>

