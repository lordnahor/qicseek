<div id="content_text">

<p><strong>The Second Time Around </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: david lamb" height="166" src="../../../bin/img/photos/people/david_lamb.jpg" width="126"/>
<p>
David Lamb
</p>
</div>
At 45, David Lamb, a senior ICS major sometimes mistaken for a professor, is only months away from graduating; reaching a goal he never dreamed he was capable of achieving and beginning his second career as a computer scientist.
<p>Lamb used to be an electrician, installing traffic signals and street lighting, a physically demanding job he enjoyed until he suffered an injury and nerve damage from a subsequent surgery. Forced to pursue another career, Lamb decided to go back to school and pursue a degree in computer science. </p>
<p>&#8220;Because of my earlier work with motor control, trouble-shooting, and some exposure to programming Programmable Logic Controllers (PLC), I felt that ICS would offer the same mental challenges of electrical work without the physical labor,&#8221; Lamb said. &#8220;I haven&#8217;t been disappointed yet.&#8221;</p>
<p>But the computer science bug had bitten Lamb long before, when he was working for a poultry company in the San Joaquin Valley. </p>
<p>&#8220;More and more, devices with embedded software were being used as part of process control systems,&#8221; Lamb said. &#8220;I was amazed that an entire wall of relays and timers could be replaced by a little 10&#8221; x 12&#8221; box. I wanted to know more about them. Those devices were my first introduction to programming.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>His interest peaked, Lamb took a basic electronics course at Modesto Junior College, but as work became more time consuming, he found little time to take structured courses. Only after his hernia surgery would he find the time to feed his interest in computer science once again. </p>
<p>&#8220;While recuperating from my surgery, I bought an algebra book and went through the problems. I treated them like crossword puzzles. After finishing it, I bought a trigonometry book at a second hand store,&#8221; Lamb said. &#8220;I was halfway through that when my wife suggested that I might as well get a real degree, so I enrolled at Yuba Community College.&#8221; </p>
<p><strong>TRANSITION</strong></p>
<p>While attending Yuba College, Lamb was invited to a Society for Advancement of Chicanos and Native Americans in Science (SACNAS) conference in Phoenix, where he meet Kika Friend from UCI&#8217;s Center for Educational Partnerships. Friend, who Lamb recalls as a ball of fire and enthusiasm, encouraged him to check out UCI. </p>
<p>&#8220;I came out the following December, and liked what I saw and heard,&#8221; Lamb said. &#8220;The school seemed a little more business oriented than some of the other universities I was interested in. There also seems to be more opportunity for employment in ICS in this area.&#8221;</p>
<p>Lamb found the transition to life at UCI difficult at first, mainly because he had to leave his wife behind and endure a six month wait from the time he started classes to the time he received an apartment in Verano Place. After three months, his wife&#8217;s company transferred her to the Irvine area, but the couple was still forced to live apart for a while longer. </p>
<p>&#8220;We knew that Verano was going to have an opening soon and no one was going to rent us an apartment for just three months, so we rented a room in a different neighborhood for her,&#8221; Lamb said. &#8220;That was really odd.&#8221;</p>
<p>There was also a transition to life inside the classroom. </p>
<p>&#8220;I think I was a little spoiled by the professors at Yuba Community College,&#8221; Lamb said. &#8220;The classes were much smaller than at UCI so I was able to get more personal attention. Here it just takes a little more effort to get one-on-one time with professors. That is only because the classes are so much larger here.</p>
<p>I find that the TA&#8217;s are a very important resource to take advantage of. If you can&#8217;t reach a professor, the TA&#8217;s are your next best source for assistance. Also, e-mail helps to fill in gaps. The professors and TA&#8217;s are usually very responsive to questions asked by e-mail.&#8221;</p>
<p><strong>TRANSFER TRIALS </strong></p>
<p>Lamb offers the following advice to help fellow transfer students and those who are looking to change careers. </p>
<p>&#8220;Take advantage of Intersegmental General Education Transfer Curriculum (IGETC),&#8221; Lamb said. &#8220;If you are like me and maybe your high school transcripts aren&#8217;t exactly stellar, go to a community college and make sure that their curriculum is approved for credit transfer to a university.&#8221;</p>
<p>Lamb learned the hard way that UCI does not recognize computer science courses from some community colleges and had to be prepared to repeat ICS courses taken there. </p>
<p>&#8220;If I had known that from the beginning of my college experience, I would have focused more on general education at the community college level and just taken courses in the ICS major at UCI,&#8221; Lamb said. </p>
<p>To avoid having to repeat ICS courses already taken, local community college students thinking of transferring to the Bren School should ask if their school is aware of the <a href="../../../ugrad/admissions/transfer/smart/index.php">SMART-ICS program</a>. The program encourages partnership with area community colleges in an effort to provide student transfers to ICS subject credit for all lower division math and ICS courses required for the ICS degree.</p>
<p>Once on campus, Lamb suggests students find a study buddy to help in the more difficult classes as two heads are better than one. Students should also live on or near campus to save their sanity and time by skipping the grinding daily commute. </p>
<p>&#8220;Get a bicycle,&#8221; Lamb said. &#8220;Parking lots are expensive, bike racks are free.&#8221;</p>
<p><strong>HANDS ON LEARNING </strong></p>
<p>Despite the early transition adjustments, Lamb has enjoyed his experience at UCI, especially his classes. Two in particular were especially memorable, Introduction to Artificial Intelligence and Mobile Gaming. </p>
<p>&#8220;I was expecting a philosophy class that pondered the existence of HAL9000, Pinocchio and Star Trek&#8217;s Data,&#8221; Lamb said. &#8220;I was relieved when I found it covered legitimate algorithms and methods for mimicking intelligent behavior. It took a subject that I felt was unrealistic or magic and revealed it for what it really is: algorithms, data structures, probability and logic used to generate computationally intelligent decisions or behavior in computers.&#8221;</p>
<p>Alarmed at the violence perpetuated in videos games, Lamb was initially apprehensive about taking a course that could take a violence promoting direction. But his fears were allayed on the first day of his Mobile Gaming class. </p>
<p>&#8220;The professor laid down some ground rules for the programs we would be writing,&#8221; Lamb said. &#8220;The most prominent was that there was to be no killing in any games we wrote. It made the class much more creative.&#8221;</p>
<p>Lamb and his team went on to create a music program with a Dance Revolution theme. The twist being that the person playing would learn how to read music. It was a very challenging project and even though the game was playable on an emulator, the team ran into trouble getting it on a mobile device. </p>
<p>The programming language the team was using to create the program did not offer the detail of control they wanted. Had it been available, the program would have been much more compact. The mobile device specifications dictated that the device could handle only one Musical Instrument Digital Interface (MIDI) and one sound file at the same time.</p>
<p>Once a MIDI was initiated, it would run until it stopped, which is it could not be interrupted. The program required the simulation of two sound files playing simultaneously. Since MIDI operates with 16 channels (each one representing a different instrument), that is more than enough instruments to play a song and make it sound full. </p>
<p>What the team needed was the ability to control one MIDI channel that would take inputs from the user and feed it directly to the MIDI player. Unfortunately, the programming language did not give the team that detail of control and the problem couldn&#8217;t be solved in the short ten weeks of the course. But Lamb looks forward to getting back to this project in the future. </p>
<p><strong>WITH AGE COMES WISDOM </strong></p>
<p>Lamb&#8217;s past experience as an electrician has also helped him in his classes as he is able to understand the importance of some teachings that other students may see as trivial. </p>
<p>&#8220;One of the lessons I learned in electrical work was to document my work. That meant label every wire, and make a schematic of any thing I wired up,&#8221; Lamb said. &#8220;That way when I came back to fix something a year later, I had a reference to work with. I learned that lesson the hard way. </p>
<p>So when it comes to class work, professors are usually sticklers on project documentation. A comment that seems trivial five minutes after a piece of code is written could make a huge difference a year or two later when troubleshooting what you did when you knew everything. So when I have to run new cables or label machinery, I really don&#8217;t the mind the documentation; it&#8217;s just as important as the rest of the job.&#8221;</p>
<p>Juggling class, married life and a job as a network assistant in the Computer Science Department, has left Lamb little time for hobbies, though he still manages to sneak in some time to play his guitar. </p>
<p><strong>EYES ON THE FUTURE </strong></p>
<p>With graduation on the horizon, Lamb is looking forward to reaching the road mark he never dreamed he was capable of achieving. But graduation may not be the last stop on Lamb&#8217;s educational road, he is pondering pursing a Master&#8217;s or even a Ph.D., but he is also putting out resumes to companies in the area.</p>
<p>&#8220;I feel that work is not what defines me. Don&#8217;t get me wrong, I enjoy my major, but that is just one facet,&#8221; Lamb said. &#8220;But if you asked me what would be my dream job, I would have to say, one that combines my new skills with what I learned in the electrical field. I like to see things happen. I see myself creating software for automatic machinery.&#8221;</p>
<p>Lamb is also looking forward to returning to the leisure activities he has given up in pursuit of his goal; photography, cycling, kite building, working on cars, small engines, Scrabble, fishing (actually he is more into the eating of fish than the act of fishing itself), camping and hiking.</p>
<p><i>- Eric Kowalik</i></p>


