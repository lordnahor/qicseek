<p> <a id="allowed" name="allowed"></a> <b>Q: Am I eligible to download MSDNAA software?</b>          </p>
<p> If you are enrolled in any course under the Bren School of ICS course
          for credit, you are eligible to download the MSDNAA software. &#160;Faculty,
          instructors, and TAs are eligible to receive the software if you have
          an active ICS account.</p>
<a id="avail" name="avail"></a> <b>Q: What software is available for download?</b>
<p> Please login to the <a href="https://elms.ics.uci.edu/">MSDNAA</a>
          page and look under the Software section for an updated listing.</p>
<a id="unavail" name="unavail"></a> <b>Q: What if I want a software that
        has not been made available?</b>
<p> If you need a particular software for a class or research project,
          please email <a href="mailto:helpdesk@ics.uci.edu">helpdesk</a> with an explanation of why you need it and we will look into its availability. &#160;<i>NOTE: Microsoft Office is not included in the MSDN Academic Alliance
          program.</i> </p>
<a id="cost" name="cost"></a> <b>Q: How much do I need to pay for the software?</b>
<p> In an agreement with Microsoft, the MSDN Academic Alliance program
          provides this software free of charge. &#160;There is a small charge if you
          choose to have the installation CD mailed to you.</p>
<a id="what" name="what"></a> <b>Q: What can the software being provided be used for?</b>
<p> The software is provided for instructional and non-profit research purposes. &#160;It cannot be used for infrastructure
          or profit. </p>
<a id="uninstall" name="uninstall"></a> <b>Q: Do I have to uninstall the software once I graduate or once I am no
          longer a student of the Bren School of ICS?</b>
<p> The license is perpetual. &#160;This means that you do not need to uninstall
          the software. &#160;The software and product keys you have received are yours
          to keep, but you will lose the ability to download the MSDNAA software once
          your ICS account expires.</p>
<a id="download" name="download"></a> <b>Q: How can I obtain the software?</b>
<p> You may download the software from <a href="http://msdn07.e-academy.com/ucirvine_cs"></a>the
          <a href="https://elms.ics.uci.edu/">MSDNAA website</a>. </p>
<p> Alternatively, the burner machine in CS 364 lab has the ISO images
          of the software in the "MSDNAA Software" folder. &#160;You can burn your own
          CDs/DVDs to take home or bring a USB flash drive and transfer the ISO files you might need. &#160;However, you will still need to login into 
          MSDNAA site and "purchase" the software from their website
          to get the necessary product keys. </p>
<a id="problems" name="problems"></a><b>Q: Why is the size of the file I downloaded so small or unexecutable?</b>
<p>If you are downloading the MSDNAA software, the file that first gets
          downloaded may not have the .exe file extension. &#160;This has occurred when
          Mozilla Firefox is used as the browser. &#160;Add the .exe extension if necessary
          and double-click the file. &#160;It is a downloader file of a very small size
          that is used to download an SDC file. &#160;After the SDC file is downloaded,
          the downloader is required to extract the actual software from it. </p>
<a id="error" name="error"></a><b>Q: I am getting an error in the middle
        of my download, what does it mean?</b>
<p>In many cases, you should be able to figure out what is wrong by the
          content of the error code. &#160;If it is not immediately obvious what you
          should do, check the <a href="msdnaa_error.php"> Download Error Help
          Page</a>. &#160;If you still cannot complete your download, please send an
          email message to <a href="mailto:helpdesk@ics.uci.edu">helpdesk</a>.</p>
<a id="qualified" name="qualified"></a> <b>Q: How do I receive my login/password?</b>
<p> Please user your ICS account username and password to login.</p>
<p><a id="unzip" name="unzip"></a> <b>Q: What is an SDC file and why can't
          I open or unpack it?</b> </p>
<p> When you double click the executable file that you downloaded from MSDNAA, it will ask you where to unpack the
          file. &#160;There will be an SDC file inside the folder you choose to store the downloaded file. &#160;This is a zip file that the
          Downloader executable will extract for you when the download is completed. &#160;You need to use the Downloader to extract the
          install files from the SDC file. &#160;The SDC file is safe to delete after the setup files are extracted from it. </p>
<a id="space" name="space"></a> <b>Q: How much hard disk space do I need?</b>
<p>Some files required at least 2GB of space for the SDC file that is downloaded plus
          the files that are extracted from it. &#160;If you do not have enough disk space for the SDC file and the extracted files, you
          will need to download the file again in order to unzip the SDC file. &#160;Make sure you have enough disk space before proceeding
          to download. </p>
<a id="reinstall" name="reinstall"></a> <b>Q: How can I reuse product
        keys issued to me if I need to reinstall my computer?</b>
<p> If you made a hardware change or got rid of your old computer and
          need to reinstall software that needs to be activated, such as Windows 7, Visio, and Project, the first step is to try telephone registration
          to speak to a Microsoft representative. &#160;If you need to be reissued
          new product keys because the telephone registration did not work, email
          <a href="mailto:helpdesk@ics.uci.edu">helpdesk</a> and state your reasons for the request thoroughly. </p>


