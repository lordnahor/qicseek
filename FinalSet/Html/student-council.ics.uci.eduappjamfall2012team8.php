            <div class="title white">
                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>Our Android application, entitled ZotPark, provides users with a convenient way to navigate the   confusing parking permit system at UC Irvine. ZotPark determines and displays parking areas the user can park in based on permit type. If users so desire, ZotPark can determine and display extra parking lots and structures the user can park in, depending on the time of day. Once the user has selected their permit type, ZotPark displays the location of available parking areas on a map. From this view, the user can select to view navigational directions from the user's current location to the parking area they would like to reach. Alternatively, the user can store a home address for ZotPark to use as a starting point for directions. The user also has the option of accessing an information page for each parking area from the map display. This page informs the user of any special conditions that are required to park in said parking area, as well as general information about their parking permit.</p>
			</div>

