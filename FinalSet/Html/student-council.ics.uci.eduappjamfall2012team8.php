					<br />Not only could we not get together sometimes, but our entire team is new to coding (hence our name, "n00bz"). Three of us are first years, and two second years. However, we pulled through with research, hard work, and stress.
					<br />For quite some time, we could not figure out how to format our databases.
					<br />The information itself was also difficult to interpret (this is the point of our app - to make the parking information on certain permits less confusing).
					<br />We had problems making a pop up in Google API, and had to write our own code and overlay it.
