                <h1>Sources of inspiration for the project</h1>
			</div>
			<div class="description cms">
				<p> On the first day of our adventure this week, n00bz sat pondering ideas for applications that would help UCI students academically, socially, and professionally. As we shuffled through numerous ideas along the lines of social networking, blogging, and posting pictures, we asked ourselves, "What is something that UCI students complain about?" And instantly, "Parking!" was shouted out. That was our breakthrough; we would make an app that made parking permits less confusing! I remember getting my parking permit, and talking to the people in the booth for 20 minutes, and still leaving confused. The permit said, "Lots 17A, 18R, CT. Valid in the Anteater Parking Structure (APS) ONLY Monday - Friday between 5:00pm and 8:00am, and all day Saturday and Sunday." What??? And on top of that, it took me 20 minutes driving up and down East Peltason trying to find lot 17A (it is tucked away on the other side of the bridge, and not reachable directly from Palo Verde Apartments or East Peltason). Our app will tell people where the lots are, and when their specific permits are valid!  </p>
