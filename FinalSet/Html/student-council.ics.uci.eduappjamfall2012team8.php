            <div class="title white">
                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>We began our work by determining what kind of application the UCI student body needed, and more importantly, would enjoy using. After the deliberation of several ideas, we settled on developing an application that would help users navigate the confusing system of permit parking here at UCI and avoid receiving expensive parking citations. We drew up documentation for how the app would work and what features it would have. After determining this, we embarked on creating a database that would serve our purposes and contain every possible combination of permits, parking areas and times. After creating this database, we began to create the code for the application. After hitting several setbacks over the weekend in determining how to write the code for the features we wanted, we finally had a breakthrough and created a usable, working app. After coding a basic framework, we put some final touches to make the UI experience pleasant for users.</p>
			</div>

            <div class="title white">
