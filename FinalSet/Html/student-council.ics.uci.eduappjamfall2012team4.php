            <div class="title white">
                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>Our application is designed to allow users to create, and join groups. Within Groups they can look at user information like: Name, Email, and Phone Numbers. This allows users to have their own study or social group information available to them from any android phone. This application could convince Middle School students that studying computing is cool because it will allow them to keep track of their groups too. With more and more kids using smart phones and no longer memorizing numbers, this app will allow them to easily keep track of their friends' numbers, or look up the numbers of their friends from a different phone. In this way if someone breaks their phone they don't have to lose their ability to call their friends.</p>
			</div>

            <div class="title white">
                <h1>Programming languages, compilers and IDEs used</h1>
			</div>
			<div class="description cms">
				<p>We used XML, Java, SQL, and PHP languages. NetBeans and Eclipse were the two IDEs we used and they also served as the compilers.</p>
			</div>

            <div class="title white">
                <h1>Notable algorithm or other academic material learned in class</h1>
			</div>
			<div class="description cms">
				<p>No complex algorithms were used.</p>
			</div>

            <div class="title white">
                <h1>Sources of inspiration for the project</h1>
			</div>
			<div class="description cms">
				<p>We actually figured what we wanted to do pretty quickly.  Our idea stemmed from the fact that we have all needed to work with study groups and it is often very difficult to contact members. Just like we needed to contact each other to create this app. There are limited resources for sharing this information without exposing it to large communities like Facebook, which also contains a lot of personal information. This app would allow users to share contact information on a more discrete level. We originally wanted to create a visual database showing where other group members were, but decided that just having access to user information was also very valuable. Ultimately, there is more functionality that could be added in the future but as is this app is valuable as a study, social, and professional tool.</p>
			</div>

            <div class="title white">
                <h1>Libraries, frameworks and other tools used</h1>
			</div>
			<div class="description cms">
				<p>We used Android, Java, MySQL, and PHP libraries.</p>
			</div>

            <div class="title white">
                <h1>Art assets, logos and content not created by the team</h1>
