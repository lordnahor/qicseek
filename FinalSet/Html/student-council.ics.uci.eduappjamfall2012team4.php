            <div class="title white">
                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>Creating the app started with each of us getting the Android SDK working on our computers. We then installed Saros, a code sharing extension. We then worked individually and together to create the database (SQL code) and the UI (XML / Java code) separately. Once we had both parts working we integrated the two of them. This of course caused bugs which we had to work to fix. We met and worked in the ICS computer lab to integrate all portions of the app and make sure that all of the app was functional. This took a lot of time and effort but was worth it as our app has turned out well.</p>
			</div>

            <div class="title white">
                <h1>Major hurdles that had to be overcome</h1>
			</div>
			<div class="description cms">
				<p>The hurdles we faced while making this app included our busy schedule, lack of experience working with Android SDK, and creating our database. We used Saros code sharing, and Google Docs to overcome our time conflicts. We all read and watched Android development tutorials to learn how to create our User Interface. Finally, we received help from our advisor, Adam Brenner, who created a website and MySQL account for us.</p>
			</div>

            <div class="title white">
                <h1>Lessons Learned</h1>
			</div>
			<div class="description cms">
				<p>We learned a lot from creating this application. First, we learned how much work is involved to simply work in a group. Everyone has his or her own needs, schedule, and capabilities. We also learned how much can get done in such a short period of time. We were all wary of only having one week to create an application, especially once we realized none of us had ever made an application before. In the end, we all worked hard together and individually to produce something that we are all very proud of.</p>
			</div>

