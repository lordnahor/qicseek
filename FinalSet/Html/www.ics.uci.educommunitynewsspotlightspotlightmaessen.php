<p>When Rebecca Maessen was considering where to attend college, she wanted a place that would allow her to fulfill her passion for basketball and computer science, even if it meant going half way around the world.</p>
<p>A native of Nijmegen, Holland, Maessen played for the Dutch Junior National Team from 1999 through 2002 and became a member of the Dutch National Basketball Team in 2002.</p>
<p>Maessen&#8217;s basketball success caught the eye of UCI coaches and she was offered a scholarship to play for the women&#8217;s basketball team. </p>
<p>But Maessen, a sophomore information and computer science major, wanted more than just a challenge on the court, she wanted a challenge in the classroom too and felt there was no better place for that than at UC Irvine's Bren School.</p>
<p>&#8220;The Bren School is known all throughout the country as being a great school and I&#8217;ve always been interested in computers.&#8220; Maessen said. &#8220;I feel ICS is really going to be a challenging major.&#8221;&#160; </p>
<p><strong>A NEW GAME</strong></p>
<p>But unlike basketball, Maessen would be coming to computer science without any prior background, a prospect that made her a little nervous.</p>
