</p>
</div>
<p>&#8220;Before ICS 21 I had pretty much zero experience in programming and I was a little nervous about how I would do in the class.&#8221; Maessen said. &#8220;But I realized a lot of people in my class didn&#8217;t have any experience and going through the class they gradually teach you the basics of programming.&#8221;</p>
<p>Like her teammates and coaches, Maessen found the Bren School faculty and teaching assistants provided her and her fellow classmates the coaching and support they needed to be successful. </p>
<p>&#8220;The Bren School helps you out a lot and they are interested in their students and their success,&#8221; Maessen said. </p>
<p>She found the podcasts of ICS 21 particularly helpful, since she had to miss some classes due to basketball practice, the podcast of lectures allowed her to stay current with the material.</p>
<p>Maessen also found that what she learned on the court could help her succeed in the classroom too. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;You learn discipline and team work in basketball as well as how to deal with other people and how to learn from them,&#8221; Maessen said. &#8220;You also learn time management in basketball and you can apply that to studying.&#8221;</p>
<p>Her experience with ICS 21 has stirred her passion for computer science even more and she is looking forward to learning more.</p>
<p>&#8220;It was really interesting learning the basics of programming and seeing how the programs work,&#8221; Maessen said. &#8220;I was really excited to take the ICS 21 class this year.&#8221;</p>
<p>Though Maessen hopes to pursue a professional basketball career after graduation, she knows the skills she is learning in her classes will allow her to succeed in the information technology field. </p>
<p><strong>GIVE AND GO </strong></p>
<p>Despite moving a continent away, Maessen has found the transition to college life in Southern California pretty easy, thanks mostly to her coaches and teammates who have helped her out along the way. </p>
<p>&#8220;I miss my family a lot but at the same time I&#8217;m so busy with basketball and school that I really don&#8217;t have time to be home sick,&#8221; Maessen said. </p>
<p>Her decision to attend school half a world away hasn&#8217;t just provided her an opportunity to continue her basketball career or get a degree in a field she enjoys, it has also provided her with many opportunities she may not have gotten if she had stayed in Holland. </p>
<p>&#8220;Being at UCI has really helped learn how to deal with a lot of workload both from basketball and my studies and I&#8217;ve also meet a lot of people I wouldn&#8217;t have meet if I stayed in Holland,&#8221; Maessen said. </p>
<p><i>- Eric Kowalik</i></p>


