<blockquote>
<div id="content_text">

<p><strong>Starting Early </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: bryant hornick" height="170" src="../../../bin/img/photos/people/bryant_hornick.jpg" width="200"/>
<p>Bryant Hornick 
</p>
</div>
<p>Traditionally,  undergraduate education has taken place in the classroom, while research has  been for graduate students and faculty.</p>
<p>Not at UC Irvine. </p>
<p>Third year Informatics  major Bryant Hornick is one of the more than one-third of UCI undergraduates who  do research work with faculty.</p>
<p>&#8220;I started doing  undergraduate research under professor Bill Tomlinson the end of winter quarter  my freshman year, so I guess that means I've been doing research for almost two  years now,&#8221; Hornick said. </p>
<p><a href="http://www.urop.uci.edu/SURP.html">UC Irvine's Summer Undergraduate  Research Program</a> provided Hornick an opportunity to do full time research  during the summer between his freshman and sophomore year. Between his  sophomore and junior year, he did full time research as a <a href="http://www.urop.uci.edu/surf-it.html">Summer Undergraduate  Research Fellowship in Information Technology</a> fellow. </p>
<p>Among the projects he has  had the opportunity to work on include the <a href="http://www.ics.uci.edu/informatics/research/research_highlight_view.php?id=12">EcoRaft Project</a>, an interactive  educational installation that teaches children and adults about restoration  ecology. </p>
<p>&#8220;I didn't have much  experience at this point, but I worked on the project as a designer,  programmer, and evaluator,&#8221; Hornick said. &#8220;One of my responsibilities was to  figure out how we could test whether or not participants are actually learning  anything about restoration ecology while interacting with the exhibit. It was  not an easy task, but in the end the evaluations were a success.&#8221;</p>
<p>His work on EcoRaft allowed  Hornick to attend SIGGRAPH, the definitive conference on computer graphics and  interactive techniques. The group ran on very little sleep and had to commute  to and from Los Angeles  during the conference, but it was all worth it. </p>
<p>&#8220;We demo'd our project to  over a thousand people over the course of a week, including George Lucas,&#8221;  Hornick said. &#8220;I met some amazing people and saw the technology of tomorrow  coming out of companies and universities all over the world.&#8221;</p>
<p>Another project he worked  on was called Dreams, an interactive program that uses dreams as a metaphor for  visualizing the processes behind adaptive user interfaces. </p>
<p>&#8220;I worked on this project  both from the design level and the programming level, coding up the  cinematography system and some of the behind-the-scenes memory management  stuff,&#8221; Hornick said. </p>
<p>The paper he co-authored  with his research group was recently accepted into CHI, a conference on  human-computer interaction, making him an officially published author, which he  finds pretty exciting.</p>
<p>&#8220;Overall I would say that  I've learned more out of my experience as an undergraduate researcher than I  have in any of my classes,&#8221; Hornick said. &#8220;It's one of the best things you can  do as an undergraduate.&#8221;</p>
<p><strong>ACADEMIC TRAILBLAZER </strong></p>
<p>Hornick, a Corona native, isn&#8217;t just  on the cutting edge of HCI research, he is also on the bleeding edge of  computer science education as a member of the inaugural Informatics class. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;I started programming when  I was a sophomore in high school, so I knew I wanted to go into computer  science,&#8221; Hornick said. &#8220;It wasn't until I heard about the Informatics program  through the orientation lectures that I thought I could do something  computer-related without focusing on the programming-aspect of things.&#8221;</p>
<p>The Informatics major  covers the design, implementation, use and impact of information technology.  Compared to traditional computer science, which primarily focuses on the  internal features of computer systems, Informatics is also concerned with  computer systems and their surrounding context -- the people and organizations  that use them and the problems they need to solve.</p>
<p>&#8220;I enjoy programming, but  it's certainly not what I want to do as a career,&#8221; Hornick said. &#8220;Informatics  offered a path to do something different and something more people-oriented.  That's really what sold me. It's a people-oriented major.&#8221;</p>
<p>Though he is filled with a  sense of pride and excitement, there are also some uncertainties that which can  make being on the cutting edge of education a tad bit interesting. </p>
<p>&#8220;Many of the Informatics  courses are being taught for the first time when I take them,&#8221; Hornick said. &#8220;Some  can be a little rough around the edges in terms of content. But that's only  because we're apart of something brand new and there is no reference out there  as to how these classes should be taught.&#8221;</p>
<p>Since it is a comparatively  small major, it is also imperative that students come up with and stick to a four-year  plan as most of the Informatics-specific classes are only offered once a year. </p>
<p>&#8220;From my experience,  Informatics is not that friendly to those pursuing a double major, or even a  minor,&#8221; Hornick said. &#8220;Hopefully, though, this will change if more people start  signing up.&#8221;</p>
<p>Despite a few rough edges,  Hornick heartily recommends Informatics to prospective students who feel it is  the right major for them and are not enamored with programming or the hardware  aspect of computers. &#160;&#160;</p>
<p>&#8220;It's exciting!&#8221; Hornick  said. &#8220;I really feel like this major is cutting edge, and that I'm learning  skills that not many traditional computer science students are.&#8221;</p>
<p>Computer science is  constantly changing and Hornick credits the Informatics program for teaching  him the skills to adapt to a constantly changing field. </p>
<p>&#8220;I never took a class on  C++ or Python, but classes like ICS 141 and Informatics 142 have given me the  skills to learn any language with relative ease,&#8221; Hornick said. &#8220;The Bren School  has given me a very strong foundation for me to build on, and both the classes  I have taken and the research I have done have given me the skill set to take  on the challenging problems that will face me after college.&#8221;</p>
<p><strong>ENJOYING DOWN TIME</strong> <br/>
            <br/>
          Even with a busy schedule of class and research, Hornick still finds time to  enjoy the social aspect of college life like going to the movies with friends,  listening to music, or reading a good book. &#160;&#160;</p>
<p>He also attends the Video  Game Development Club meetings and tries to take a class at the ARC every  quarter. </p>
<p>&#8220;I recently took African  dance, which was pretty interesting,&#8221; Hornick said. &#8220;My mom is a huge  volleyball nut, so you might catch me and her attending some of UCI's home  games from time to time.&#8221;</p>
<p>With graduation less than a  year away, Hornick hopes to attend graduate school for his Masters before  entering the video game industry, either from the design side or the production  side.</p>
<p><i>- Eric Kowalik</i></p>


