<div id="content_title">Research centers</div>

<blockquote>
<div id="content_text">

<img alt="photo::faculty doing research" class="left" height="150" src="../../bin/img/photos/ics_research_centers.jpg" width="200"/>
<p>Organized research programs provide a mechanism and organizational
  structure within which collective research activities can take place
  that are fundamentally different from those that occur normally within
  the schools and departments. </p>
<p>They are intended to foster the development
  of short and long term research programs that span disciplines and
  academic units thereby making it possible for faculty to acquire
  extramural resources for which they might not otherwise qualify.</p>
<hr/>
<p>
<a href="http://abrc.uci.edu">ABRC - Ada Byron Research Center</a>
<br/>
ABRC studies and promotes diverse access to and participation in computer science, engineering, digital media and related information technology fields. </p>
<p>
<a href="http://www.calit2.uci.edu">Calit2 - California Institute for Telecommunications and Information Technology</a>
  <br/>
A multidisciplinary research institute in collaboration with UCSD, Calit2@UCI integrates academic research with industry experience to seek innovative IT approaches that will benefit society and ignite economic development. </p>
<p>
<a href="http://www.ics.uci.edu/%7etheory/">Center for Algorithms and Theory of Computation</a>
   <br/>
   The goal of research in theoretical computer science is to produce results, supported by rigorous proof, about problems dealing with computers and their applications.
</p>
<p><a href="http://www.cecs.uci.edu/">Center for Embedded Computer Systems</a>
  <br/>
Conducting leading-edge interdisciplinary research in embedded systems, the center develops innovative design methodologies, and promote technology and knowledge transfer for the benefit of the individual and society.</p>
<p><a href="http://cml.ics.uci.edu/">Center for Machine Learning and Intelligent Systems</a> <br/>
Addresses the challenges of the modern data-driven world, using computer algorithms to discover useful information from vast data archives.</p>
<!-- -->
<!-- -->
<p><a href="http://socialcomputing.uci.edu/">Center for Social Computing<br/></a>Conducts
                  foundational research into the relationship between
                  information technology and society. Based at UCI, the
                  center brings together an interdisciplinary group of
                  researchers from leading U.S. universities.</p>
<!-- -->
<!-- -->
<p><a href="http://www.ics.uci.edu/%7eprojects/cert/">CERT -  Center for Emergency Response Technologies</a> <br/>
Works to radically transform
the ability of responding organizations to gather, manage, use, and
disseminate information
within emergency response networks and to the general public. By using more
robust information systems, response can focus on activities that have
the highest
potential to save lives and property.</p>
<p><a href="http://www.cor.web.uci.edu/">COR - Center for Organizational Research</a> <br/>
 COR contributes to the development of organization theory by connecting scholars from many disciplines who bring their knowledge and methods to a common understanding of these issues.</p>
<p>
<a href="http://www.cpcc.uci.edu/">CPCC - Center for Pervasive Communications and Computing</a>
<br/>
Dedicated to serving the vision of wearable
computers with wireless connections that enable anyone to have continuous
voice, video, and data connectivity.</p>
<p>
<a href="http://www.crito.uci.edu/">CRITO - Center for Research on Information Technology and Organizations</a>
<br/>
One of the world's leading think tanks on the impact of information technology on organizations and society and on the management of information technology.</p>
<p><a href="http://vision.ics.uci.edu/">Computational Vision Lab<br/>
</a>The Computational Vision Lab focuses on understanding the information processing capabilities of biological visual systems and on developing computational systems for processing visual media.</p>
<p>
<a href="http://www.igb.uci.edu/">IGB - Institute for Genomics and BioInformatics</a>
<br/>
Fostering innovative basic and applied research in genomics and BioInformatics, IGB works with established companies, start-ups, government agencies and standards
bodies to develop and transition these technologies to widespread and practical
application. </p>
<p>
<a href="http://www.isr.uci.edu/">ISR - Institute for Software Research</a>
<br/>
Works toward advancing software and information technology through research partnerships and educating the next generation of software researchers and practitioners in advanced software technologies.</p>
<!-- -->
<!-- -->
<p><a href="http://ivecg.uci.edu">IVECG - Institute for Virtual Environments and Computer Games</a><br/>
   Brings together researchers for the common goal of understanding and creating technology and applications that transform how we: see the world through immersive visualization and virtual tours; interact and socialize with global communities; communicate and collaborate with colleagues in virtual collaborative space; provide medical care and training to remote corners of the world; and educate all ages and populations using virtual environments.
</p>
<!-- -->
<!-- -->
<p><a href="http://luci.ics.uci.edu/">LUCI - Laboratory for
Ubiquitous Computing and Interaction</a>
<br/>
Addresses the entire range of research problems that arise
from the ubiquitous computing vision: the design of novel devices,
the structure of software systems, techniques for designing and
building systems, patterns of interaction, and the cultural implications
of ubiquitous computing.</p>
<p><a href="http://www.ssllab.org/">Secure Systems and Software Laboratory</a> <br/>
The Secure Systems and Software Laboratory at the University of California, Irvine.</p>
<p><a href="http://sconce.ics.uci.edu/">SCONCE - Secure Computing and Networking Center </a> <br/>
SCONCE focuses on research for protecting information and computing infrastructure with an emphasis in areas like applied cryptography,  network security and  information assurance. </p>


