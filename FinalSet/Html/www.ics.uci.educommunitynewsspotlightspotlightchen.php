<div id="content_text">

<p><strong>A Perfect Fit  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: rex chen" height="166" src="../../../bin/img/photos/people/rex_chen.jpg" width="171"/>
<p>Rex Chen 
</p>
</div>
<p>After completing his Bachelor degree at UCI, Ph.D. student Rex Chen saw no reason to leave the friendly and supportive confines of Irvine when he choose to pursue his Ph.D. </p>
<p>"I really like the atmosphere at UCI and in the Bren School,&#8221; Chen said. &#8220;The student community and faculty are all very friendly and supportive. Since a Ph.D. takes several years, I thought it was important to choose a place that I would enjoy."</p>
<p>Chen&#8217;s research interest is on peer-to-peer (P2P) and wireless mesh networks.</p>
<p>P2P comprises many participating peers that form a network and utilize computing resources from these machines rather than dedicated servers. Numerous P2P systems already exist today such as the popular BitTorrent for file-sharing and Skype for Voice over IP.</p>
<p>In many cities around the U.S., Wi-Fi connectivity is becoming ubiquitous.  However, there can still be unreachable &#8220;dead spots&#8221; and wireless mesh network can alleviate this problem by relaying traffic from the individual WLAN devices to provide greater coverage.</p>
<p>&#8220;UCI and the Bren School is a dynamic environment with researchers collaborating together and working on many interesting problems in Computer Science,&#8221; Chen said. &#8220;The Bren School is growing very fast, and is a top research &#8216;think-tank.&#8217;&#8221;</p>
<p>In addition to participating in emerging technologies and addressing relevant research problems, Chen sees other intangible benefits to pursuing a Ph.D. </p>
<p>&#8220;I wanted to gain the experiences and training that one goes through in pursuing a Ph.D.,&#8221; Chen said. &#8220;I think it enables one to acquire new critical thinking skills and be able to approach and solve problems differently.&#8221;</p>
<p>Though graduation is still a few years down the line, Chen has already thought of life after his time at the friendly confines of UCI.</p>
<p>&#8220;I&#8217;d like to work on innovative technologies that have the potential to change our lives for the better,&#8221; Chen said. &#8220;My passion is to pursue entrepreneurship some day. It takes a team of people sharing common goals and requires courage, dedication, and persistence.&#8221; </p>
<p><strong>ENTREPRENEURSHIP IN ACTION</strong></p>
<p>As an undergraduate, Chen got to experiences entrepreneurship and team work through the <a href="../../events/extreme/index.php">Bren School&#8217;s hITEC OCTANe</a>, (formerly extreme computing), product competition. </p>
<p>The cornerstone of the Bren School entrepreneurship program, hITEC OCTANe is designed to foster a spirit of entrepreneurship among Bren School and UC Irvine students, and fuel the development of new technologies that have the potential to positively impact the marketplace.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
"One of the greatest experiences during my undergraduate studies at UCI was participating in the Extreme Computing Competition.&#8221; Chen said. &#8220;It was the first year the competition was offered, and my former roommate Albert Udompanyavit and I thought it would be fun trying a project outside of the classroom."
<p>Eventually the duo enlisted another friend, Robert On to join the team and although they weren&#8217;t really serious about the competition at first, the trio&#8217;s mentors, Bren School alumnus Gregory Moulton and Professor Amelia Regan, motivated and challenged them to &#8220;think outside the box.&#8221; </p>
<p>During weekly meetings at Diedrich Coffee in the University Town Center, the mentors gave the team advice on analyzing the business proposition and its weaknesses. </p>
<p>&#8220;After several meetings, we finally came together agreeing the product will involve cell phones, and leverage the use of software in these devices to take advantage of its communication capabilities,&#8221; Chen said. &#8220;One of our mentors was an expertise in the transportation industry, and we then connected the idea and the market together.&#8221; </p>
<p>With an idea to run with, the trio spent several all-nighters completing the business plan paper and implementing the product prototype. They even went out to buy one of the latest cell phones, so they could program new software and test Internet browsing on the device. </p>
<p>Their work paid off and their project, called Monolog, a cellular phone and Web-based application designed to improve efficiency of small-scale trucking companies, earned the $3,000 first place award. </p>
<p><strong>REST &amp; RELAXATION</strong></p>
<p>When he isn&#8217;t conducting research, Chen, a resident of the Palo Verde graduate housing community, takes advantage of the recreational activities UCI has to offer. </p>
<p>He volunteers with the UCI Buddhist Association, attends UCI&#8217;s improv troupe, Live Nude People (with clothes on) and spends time indulging in his athletic hobbies which include kayaking, racquetball, swimming, and swing dancing.</p>
<p>Chen also enjoys hanging out with friends and watching Lakers basketball.</p>
<p>Jorge Cham, the Ph.D comic guy, gave a talk at UCI two months ago and mentions to the audience about the "Power of Procrastination."  &#8220;The journey for a Ph.D will be long and I agree with what Jorge says,&#8221; Chen said.  &#8220;It is important to stay focused and take one step at a time.&#8221; </p>
<p>Something he can do in the friendly and supportive confines of UCI.</p>
<p><i>- Eric Kowalik</i></p>


