<blockquote>
<div id="content_text">

<h4>OVERVIEW</h4>
<p> ICS H197 provides an opportunity for undergraduate honors students in the Donald Bren School of Information and Computer Sciences to learn about the research taking place in the School and to begin to get involved with this research.</p>
<p><strong>IMPORTANT FORMS</strong></p>
<ul>
<li><a href="../../bin/pdf/ugrad/honors/H197FacultyContactReport.pdf">Faculty Contact Report for the Honors Project (PDF, 65kb)</a> </li>
<li><a href="../../bin/pdf/ugrad/honors/H197InitialPlan.pdf">Initial Plan for the Honors Project (PDF, 65kb)</a></li>
<li><a href="../../bin/pdf/ugrad/honors/H197FinalReport.pdf">Final Report for the Honors Project (PDF, 65kb)</a></li>
</ul>
<p>We assume that you would like to learn what sort of research is going on, which people do what, and how to get connected with a research project. The major purpose of this course is to help you answer those questions.</p>
<p>Two types of students take this course: students in the ICS Honors Program (ICSHP), and students in the Campuswide Honors Program (CHP). In many ways the requirements are the same for both types of students: you are to select a faculty advisor, do two or more quarters of research with that advisor, and produce a report which is considered to be of honors quality. </p>
<p>However, students in the Campuswide Honors Program should discuss their plans with counselors in the CHP, in order to make sure that they are meeting the requirements of that program.</p>
<p>Two people primarily oversee the ICS Honors Program: the honors program advisor (or more briefly just the program advisor) is the faculty member in charge of the program, currently Rick Lathrop. The counselor for the program, currently Diana Tien, will help orient students and track their progress. Feel free to ask us any questions about the Program.</p>
<p>The most important person for you is your faculty advisor (note that this is different from the program advisor). This is the faculty member with whom you will do your project, and varies from student to student.</p>
<p>Several types of meetings are planned for this seminar, including the following:</p>
<ul>
<li>We will meet with you to give you an overview of the program.</li>
<li>Many of the faculty will give a presentation about their research interests.</li>
<li>We may have other visitors, such as a panel of current ICS Honors Program students, a panel of graduate students, etc.</li>
<li>We will ask students in the class to talk about research projects they are investigating.</li>
</ul>
<p>One of your major goals in this course is to gain an understanding of what research is being pursued in the School and what areas might be of interest to you for your project. </p>
<p>This is crucial, since it should help you complete your second goal, selection of your faculty advisor. Remember that the arrangement must be mutually acceptable, so there is no guarantee that the first faculty member you talk to will say yes; it may be, for example, that they have no suitable project in mind at the time you ask. </p>
<p>It is not imperative that you begin your research immediately next quarter if your schedule for graduation allows you enough time. However, there are some important advantages to starting fairly soon, and we encourage you to begin by spring quarter. </p>
<p>Starting early gives you more flexibility with regard to finishing the rest of your degree requirements, especially if you want to continue the research for more than two quarters. </p>
<p>Also, students often find that faculty who have supervised their honors research project are good sources for letters of recommendation; they will be in the best position to comment on your ability if you are well into your research project.</p>
<h4>COURSE REQUIREMENTS</h4>
<p>This course is graded Pass/Not Pass, and the principal formal requirement is that you attend and participate. </p>
<p>Some of the class exercises may involve students giving feedback to fellow students, and your active participation is necessary for everyone to get the most benefit from this. Occasional absences are inevitable given busy schedules, and will be excused on a case-by-case basis; make sure you sign the attendance sheet each class period you attend.</p>
<p>Students who are in the ICSHP are required to enroll in ICS H197 in the fall of their first quarter of the ICSHP.&#160; If they have a time conflict or are unable to attend a class session, they must make prior arrangements with the ICS H197 instructor.&#160; </p>
<h4>ICS H198: HONORS RESEARCH</h4>
<p>To complete the honors program you must do two quarters of independent, supervised work (ICS 198) with your faculty advisor. (In some cases, your advisor might ask you to take a graduate course to substitute for the first quarter.) </p>
<p>Passing the 198s does not necessarily guarantee successful completion of the honors program, however. </p>
<p>For the ICSHP, the results of your honors work must be certified by your faculty advisor and by the program advisor to be of honors quality. (Again, students in the CHP should keep in touch with counselors in that program about the requirements.)</p>
<ul>
<li>Students majoring in ICS or CS can count completion of two ICS H198s as one project course.</li>
<li>Students majoring in CSE can count CSE 181ABC as one of the ICS H198s required for the Honors Program.</li>
<li>Students majoring in CSE can count Engr H199 as one of the ICS H198s required for the Honors Program.</li>
</ul>
<h4>FINDING A FACULTY ADVISOR</h4>
<p>You are responsible for making contact with faculty members to discuss their research and find a faculty advisor. </p>
<p>We require you to make at least three contacts every quarter until you have found an advisor and submitted the "Initial Plan" described next. Until the "Initial Plan" has been submitted, you must submit a "Faculty Contact Report" to the Counseling Office shortly after the end of each quarter. </p>
<p>This gives a brief one-paragraph summary of the faculty contacts you have made in the previous quarter (at least three). This requirement begins the quarter you take ICS H197.</p>
<p>To make a faculty contact, <strong>(a)</strong> review the faculty profiles to determine their research interest; <strong>(b)</strong> call or email in advance, ask for any background reading material, and schedule an appointment; <strong>(c)</strong> read the background material, if any, and possibly make a trip to the library to find out more about the general area; <strong>(d)</strong> show up for the interview prepared with a list of points about the research area that you find interesting, and a list of questions about the area that you would like to know more about; <strong>(e)</strong> after the interview, send a thank-you note thanking the faculty member for their time.</p>
<p>It is important not to be discouraged if your first few contacts don't result in finding an appropriate faculty advisor. Keep trying. </p>
<p>You can improve your chances by first doing even more background reading in the area before you talk to a faculty member, so that you appear to be interested and informed. </p>
<p>You can also improve your chances by talking to a wider variety of faculty members, which will expose you to a wider range of research possibilities. Do be sensitive to their time constraints; don't be a bother, but do be persistent.</p>
<h4>GETTING STARTED ON YOUR RESEARCH</h4>
<p>When you have found a faculty advisor, work out a plan for how you will begin to tackle the research. Then fill out the "Initial Plan" form at the end of this document. (In some cases your plan might be a program of study at first, rather than a description of your specific project. </p>
<p>In any case, though, a brief description of your plans and goals should be included.) This should be approved by both you and you faculty advisor, and submitted to the program advisor. </p>
<p>You are encouraged to discuss your plans with the program advisor as they are forming. It is important for you to maintain contact with your faculty advisor, and to make sure you know whether s/he feels that your work is progressing adequately. </p>
<p>As an extreme example, you want to avoid a situation in which you don't talk to your advisor after your first meeting, turn in a paper after two quarters of 198, and then find that the advisor considers it to be inadequate. To help avoid this sort of thing, schedule regular meetings with your advisor to discuss your progress.</p>
<p>If you are in the CHP you should finish your research in accordance with the requirements of the CHP. The remainder of this document formally applies only to ICSHP students. However, we encourage ICS students in the CHP also to follow these procedures so that we can keep track of your progress.</p>
<h4>COMPLETING THE PROGRAM</h4>
<p>At the end of your second quarter of independent study with your faculty advisor, you should prepare a paper describing the project and the results. Again, the format may vary considerably from one project to another. </p>
<p>For example, one student might produce a well-documented program to be used in a lab in one of our courses, and also provide a paper describing the design choices, rationale, and experience with the use of the program. </p>
<p>Another student might not do any programming, but rather do a statistical study of the use of computers in some organization, and write a careful scholarly interpretation of the results (perhaps 20 pages) with appropriate references to the literature.</p>
<p>After the paper is complete, first have your faculty advisor sign the "Final Report on the Honors Project" form, then bring the signed form and the paper to the program advisor to sign, and finally bring the form with both signatures and the paper to the Counseling Office. </p>
<p>The normal deadline is June 1; if you will be late, please make arrangements with the ICS Student Affairs office well in advance. Once the paper is approved by your advisor and the form is signed by both advisors, you are considered to have successfully completed the ICSHP.</p>
<p>If at any point in this process you have any questions, don't hesitate to discuss them with the program advisor or ICS SAO.</p>


</div>
</blockquote>
