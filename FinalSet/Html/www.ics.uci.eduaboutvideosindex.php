<blockquote>
<div id="content_text">

<p><a href="http://www.ics.uci.edu/itunesu"><img alt="link to the bren school on itunes u" border="0" class="left" src="http://www.ics.uci.edu/bin/img/logos/itunesulogo.jpg"/></a>iTunes U is a free service provided by the Bren School and Apple that provides easy access to Bren School educational content, including lectures and interviews from such distinguished individuals as <strong>Marvin Minsky</strong>, <strong>John Seely Brown</strong> and <strong>Larry Rowe</strong>.</p>
<p>Through iTunes U, users can download content to their Macs or PCs regardless of their location. They can then listen to and view content on their Mac or PC or transfer that content to iPod for listening or viewing on the go.</p>
<p>In order to access the Bren School iTunes U content, you will need to have iTunes installed on your computer. Visit the apple website for the <a href="http://www.apple.com/itunes/download/">free download of iTunes</a>. </p>
<p><strong>Access the <a href="http://www.ics.uci.edu/itunesu">Bren School on iTunes U &#187;</a></strong> </p>
<hr/>
<p><strong><a href="http://www.youtube.com/UCIBrenICS">Turn us on @ YouTube</a></strong><br/>
                We also encourage you to visit our <a href="http://www.youtube.com/UCIBrenICS"><img alt="visit our YouTube page" border="0" height="15" src="http://www.ics.uci.edu/bin/img/graphics/iconyoutube.gif" width="40"/></a> page and watch videos focusing on the  life changing research, cutting edge computer science  education and the history of the the University of California's first  and only information and computer science school. <a href="http://www.youtube.com/UCIBrenICS">more &#187;</a></p>
