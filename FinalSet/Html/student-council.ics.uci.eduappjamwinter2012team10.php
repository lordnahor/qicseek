			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
				We came up with the idea for AwkTalk on the night of the AppJam kickoff event. Upon analysis of
the functional requirements of our application, we became worried that all the ideas we had for the
application were going to be overwhelming and unfeasible with only a weeks worth of work. For
this reason, after we had agreed upon the ideal version of the app, we broke it down it into simpler
versions of the app with varying degrees of functionality. Our plan was then to develop the application
incrementally so that we would have a working product even if we were not able to implement all of
the features. We developed prototypes of our system and at the end of each version, we would test
this prototype and then move on to the next highest level of difficulty, implementing more and more
features along the way. Using this method, we were able to pace ourselves towards our ideal design, but
always have a working system as a backup.
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
				<em>Client<br /></em>
				One of the major hurdles of the client team was none of us were experienced in Android development.
We knew some Java, but vast majority of our time developing was actually spent researching online to
learn the methods and capabilities of Android mobile development.<br /> <br />

				<em>Server/Database<br /></em>
				We originally decided to use an object database in order to get quick query results for our chat system.
We did not have a complicated enough database schema that required use of a relational database
system so we proceeded with this idea. Unfortunately the free object database that we choose was
full of bugs. The debugging of these database issues was very time consuming and after several
wasted hours, we decided to switch to MySQL. Fortunately the object database used the standard Java
Persistence API so we did not have to change much of our code. We did however have to add the object
relational mapping.<br /><br />

				<em>Design<br /></em>
				Though designated as the design team, some members were unable to utilize Photoshop to its fullest
potential due to lack of experience. When implementing our original graphic designs into our application
such as the splash screen and icon, we discovered that scaling the dimensions was a difficult task.
			</p>
			

            <div class="page-header">
				<div class="styled_title"><h1>Lessons Learned</h1></div>
			</div>
			<p>
				The greatest lesson we learned was that our team can accomplish great things if we put our time, effort,
dedication, and heart into our application. When our team first met, we had no idea that we would able
to accomplish the design and implementation of an application of this scale. Between our combined
knowledge, optimal use of specialized skills, and careful planning, and tireless work, we were able to
design and implement AwkTalk within a week.
			</p>

