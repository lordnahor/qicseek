            <div class="page-header">
				<div class="styled_title"><h1>Sources of inspiration for the project</h1></div>
			</div>
				<p>
					The theme for the current AppJam competition was self-improvement so we sat down and
thought "what is an area that a lot of the people need to improve on and that does not have a current
straight-forward solution?" Being all computer science or engineering majors, we determined that the
answer to this question was "social skills!" We were all aware that many of our colleagues in the fields
of computer science and engineering (ourselves included) lack excellent socials skills. While we may
be well versed in areas such as programming languages and mathematics, socials skills are needed to
communicate this knowledge and to convince the general public that our ideas matter. The importance
of social skills becomes obvious in areas such as job interviews, networking events, and asking a member
of the opposite sex out on a date. For these reasons, we were inspired to make AwkTalk, a revolutionary
Android application to help people build their social skills.
				</p>

