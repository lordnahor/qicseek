			<div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
				AwkTalk is Android application that helps users improve their social skills. By utilizing location services
provided by android, the app will pair a user with the next closest user in terms of geographical distance.
After this connection is made, the two users are able to chat using SSA's proprietary real time chat
system. The goal of the chat is only to establish a location to meet. The chat is therefore limited in
length. After the users have met, they are both giving a conversation topic based on the preferences
that the users specified during sign-up. The each user is then expected to socialize with their new
AwkTalk friend. After the users have socialized, they are directed to a feedback screen where they will
be required to input ratings, suggestions, and comments on each other. This feedback is then saved to
the user's feedback history section in the application. The users can refer to this history for insight on
areas of their social skills that they might need to improve on.
				</p>

