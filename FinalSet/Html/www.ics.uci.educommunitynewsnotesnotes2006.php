<p>STORYmixer, an interactive artistic game created by Bren School students,  has been selected from a range of submissions for the final screening of  &#8220;Games for 5 joysticks&#8221; at the <a href="http://www.telic.info/">TELIC Arts Exchange</a> in  Los Angeles. </p>
<p>The screening will take place on Friday, December 1 from 7 to 9 p.m.</p>
<p>The exhibit   focuses on  what kind of games are possible when 5 people with 5 joysticks are sitting in the same room, looking at the same screen.</p>
<p>STORYmixer allows  players to be a part of a combinatorial art piece by collaboratively telling stories and simultaneously mixing a sound-collage. </p>
<p>The STORYmixer constitutes an interface for playful collaborative art creation that combines literary and audio elements.</p>
<p>Art and collaboration are the main game elements and in applying these elements the player is involved in an interactive process to create an artistic piece. </p>
<p>The four students,  <strong>Marisa Cohn</strong>, <strong>Silvia Lindtner</strong>, <strong>Jeff Ridenour</strong>, <strong>Luv Sharma</strong>, from the department of Informatics and Arts, Computation and Engineering program, drew their inspiration from John Cage's Ars Combinatoria and Mozart's dice game.</p>
