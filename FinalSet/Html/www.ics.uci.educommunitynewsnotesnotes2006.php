<p><a name="yang_nsf"></a><strong>Yang receives NSF awards to improve Internet architecture</strong> </p>
<div class="img_caption_left"> <img alt="photo: xiaowei yang" src="../../../bin/img/photos/faculty/xwy.jpg"/>
<p>Xiaowei<br/>
                  Yang</p>
</div>
<p><strong>Xiaowei Yang</strong>, assistant professor of computer science, is the recipient of two NSF awards to study ways to improve Internet architecutre.</p>
<p>The first award, provides $300,000 to NeTS FIND: An Internet Architecture For User Controlled Routes. The project will attempt to develop an Internet architecture that enables users or their end-systems to select the paths their packets take through the network.                </p>
<p>Such user-controlled routes are desirable for both economic and technical reasons.  </p>
<p>Economically, user-controlled routes are a key factor in maintaining the competitiveness of the ISP marketplace, just as long-distance carrier selection has had a lasting impact on telephony.  </p>
<p>Technically, user-controlled routes provide a fundamental means for improving the performance and reliability of network communications. </p>
<p>The second project, awarded $200,000 and entitled a CT-ER: A DoS-Resistant Internet Architecture, funds research to design and evaluate an Internet architecture that is resistant to Denial-of-Service (DoS) flooding attacks of any scale.                </p>
<p>DoS attacks have become an increasing threat to the reliability of the Internet. These attacks are capable of knocking any user, site, and server right off the Internet. </p>
<p>This research targets the root cause of the DoS flooding problem: the network's inability to intelligently handle overload.When DoS attacks occur, the network drops legitimate traffic and attack traffic alike, leading to heavy losses of legitimate traffic. </p>
<p>Yang's research area is in networked and distributed systems, with an emphasis on protocol and architecture design, performance analysis, modeling, and measurement.</p>
