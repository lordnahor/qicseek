<p><a name="telic_game"></a><strong>Students' game to be screened at TELIC Arts Exchange </strong></p>
<div class="img_caption_left">
<img alt="photo:: a scene from STORYmixer, an interactive artistic game created by bren school students" height="150" src="../../../bin/img/photos/noteworthy/photo_112006.jpg" width="200"/>
<p class="left">
A scene from STORYmixer, an <br/>
