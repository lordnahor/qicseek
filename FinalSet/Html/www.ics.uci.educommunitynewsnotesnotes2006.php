<p><a name="bozorgzadeh_paper"></a><strong>Student and professor win IEEE conference Best Paper Award</strong></p>
<div class="img_caption_left">
<img alt="photo::love singhal presented with best paper award" height="165" src="../../../bin/img/photos/noteworthy/photo_102006.jpg" width="200"/>
<p class="left">
Computer science Ph.D. <br/>
candidate Love Singhal, (l) <br/> 
accepts the Best Paper Award <br/> 
at the 2006 IEEE International <br/>
Conference on Field <br/>
Programmable Logic <br/>
and Applications. 
</p>
</div>
<p>Love Singhal, a Ph.D. candidate in computer science, and professor of computer science <strong>Eli Bozorgzadeh</strong> received the Best Paper Award at the 2006 IEEE International Conference on Field Programmable Logic and Applications (FPL'06) that took place in Madrid, Spain, August 28-30 2006. </p>
<p>The awarded paper is titled "Multi-layer Floorplanning on a Sequence of Reconfigurable Designs".</p>
<p>The dynamic partial reconfigurability of programmable devices such as FPGAs is characterized by their ability to reconfigure subsets of their logic and routing resources at runtime. </p>
<p>This intrinsic dynamic reconfiguration results in accommodation of complex and dense designs, on-fly adaptivity, and design modification.</p>
<p>In this paper, a new multi-layer sequence-pair-based floorplanner is proposed for a given sequence of reconfigurable designs. In the experiments, compared to a traditional sequential floorplanner, the new floorplanner removes infeasibility in many designs, and provide faster timing closure.</p>
<p>FPL is the largest conference on programmable logic which encompasses a wide range of research area on FPGAs, including applications, novel system architectures and CAD tools, embedded processors, dynamic reconfiguration, etc. </p>
<p>The acceptance rate in FPL 2006 was 28 percent, out of 307 submissions.		        </p>
