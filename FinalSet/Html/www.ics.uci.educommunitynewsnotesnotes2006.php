<div id="content_title">Noteworthy achievements 2006</div>

<blockquote>
<div id="content_text">

<p>Bren School faculty, students and research initiatives are some of the most well regarded successes on the UC Irvine campus. We are pleased to announce the following noteworthy achievements for 2006.</p>
<p>Awards, grants and other honors can be sent to <a href="mailto: ic%73%6ee%77%73@ics.%75ci%2e%65d%75 ">icsnews@ics.uci.edu </a> to be considered for publication. </p>
<p><strong>December  2006</strong> </p>
<p><a name="sim_seed"></a><strong>Sim awarded funds to study traceability in extreme programming <br/>
                </strong></p>
<div class="img_caption_left"> <img alt="photo: susan elliot sim" src="../../../bin/img/photos/faculty/sesim.jpg"/>
<p><strong>Susan </strong><br/>
                        <strong>Sim</strong></p>
</div>
<p>Assistant professor of informatics <strong>Susan Elliot Sim</strong> has been awarded $5,000 from the Ted and Janice Smith Faculty Seed Fund for her project entitled <i>Maintaining Traceability Links in Extreme Programming</i>.</p>
<p>Traceability is the problem of establishing relationships between products of the development process, for example, predecessor-successor or master-subordinate relationships between requirements and source code. </p>
<p>Currently, associate links between User Stories and code are created during check-in to a revision control system. However, these links are not properly maintained and synchronized as User Stories, test cases, and source code change over time. </p>
<p>Sim has implemented a prototype Eclipse plug-in, STITCH (STory Integration and Tracking for CompreHension) to support traceability under Extreme Programming (XP), an agile software development model that emphasized the ability to adapt to change, by associating User Stories with test cases and source code.</p>
<p>The proposed research project will address this shortcoming by implementing a mechanism to maintain traceability links as the artifacts on either end of the relation evolve.</p>
<p>The competitive Faculty Seed Fund awards provide seed funding for Bren School faculty projects and was made possible through the generous donation of local philanthropists Ted and Janice Smith. </p>
