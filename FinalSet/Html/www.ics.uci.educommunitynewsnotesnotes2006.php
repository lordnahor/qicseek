Fifth year Ph.D. candidate in computer science <strong>Jun Lu</strong> and professors of computer science <strong>Lichun Bao</strong> received the best paper award at the 2005 IFIP International Conference on Embedded And Ubiquitous Computing (EUC 2005). The conference was held in Nagasaki, Japan, December 6 - 9,  2005.
<p>The EUC 2005 is a major conference specializing in embedded systems and ubiquitous computing with 30 technical paper sessions, 3 panel sessions and 5 workshops. Two best papers were awarded among 330+ paper submissions.</p>
<p>The paper, titled Coverage-Aware Sensor Engagement in Dense Sensor Networks, proposed a novel coverage maintenance scheme for sensor networks, which considerably improves the energy efficiency and reduces the computation and communication costs to maintain the required coverage degree in a dense sensor network. </p>
<p>Jun Lu's research is on sensor networks and ad hoc networks, he received his BECS and MSCS from Beijing University of Posts and Telecommunications, China, in 1997 and 2000, respectively and worked for Lucent Technologies as a software engineer before entering the Bren School Ph.D. program.</p>
