<p><a name="gillen_fda"></a><strong>Gillen appointed to FDA advisory committee </strong></p>
<div class="img_caption_left"> <img alt="photo: dan gillen" src="../../../bin/img/photos/faculty/dgillen-backup.jpg"/>
<p>Daniel<br/>
                      Gillen</p>
</div>
<p><strong>Daniel Gillen</strong>, associate professor of statistics has been appointed to the Federal Drug Administration's advisory committee for Reproductive Drugs as a statistician for the panel.  </p>
<p>Gillen was recommended and selected for the four-year appointment based upon his work in the design and analysis of clinical trials.</p>
<p>Professor Gillen's research focuses on the development of statistical methods for the analysis of survival time data and group sequential methods for the design and monitoring of clinical trials. </p>
<p>In particular, Gillen's interests include the development of methods that robustly incorporate time-varying effects on survival in the settings of observational cohort studies and clinical trials. </p>
<p>His primary areas of application are in nephrology and cancer.		        </p>
<p>FDA advisory committees offer the FDA advice on related questions posed by the Agency on a product of regulated industry.  The FDA has 30 advisory committees,  divided along product lines (e.g., food, devices, drugs, and biologics) and body systems (e.g., cardiovascular or gastrointestinal products).</p>
<p>For more on FDA advisory committees please visit,  <a href="http://www.fda.gov/oc/advisory/">http://www.fda.gov/oc/advisory/</a></p>
