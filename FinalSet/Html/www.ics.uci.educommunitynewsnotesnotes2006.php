<p>Professors <strong>Thomas A. Alspaugh</strong>, <strong>Cristina V. Lopes</strong> and <strong>Richard Taylor</strong> were recently awarded IBM's Eclipse Innovation Award.  </p>
<p>The Bren School faculty are among 43 recipients of the 2006 award worldwide -- the largest number of honorees of any university.    This brings the total number of Eclipse award winners at UC Irvine to eight, which is also the most of any university worldwide. </p>
<p>The IBM Eclipse Innovation Award program is an international award competition designed to encourage the use of open source and open standards-based tools for academic curricula and research. Qualified faculty and researchers submit proposals for work with applications in teaching, research or community building around Eclipse. </p>
<p>Eclipse is an open universal platform for tool integration - an open extensible Integrated Development Environment (IDE), and an open source community. </p>
<p>The Eclipse community creates royalty-free technology as a universal platform for tools integration. Eclipse-based tools give developers freedom of choice in a multi-language, multi-platform, multi-vendor supported environment supported by multiple vendors. </p>
<p>The operating system platforms that Eclipse has been targeted to includes Windows, Linux, Solaris, HP-UX, AIX, and Mac OS. In addition, Eclipse provides a unique environment for members of the academic community to build new tools for teaching, research, and further growth of the Eclipse community. </p>
<p>Alspaugh's research is in the area of requirements engineering. He is interested in how we can describe and reason about software, especially at points when code has not yet been written or will be re-written, and while the software's specification is still incomplete and its requirements are in flux. His recent work has investigated the use of scenarios to specify the behavior of a system. </p>
<p>Lopes' research is related to languages and communication systems. The ultimate goal of her research is to deepen the knowledge about communication, in particular in systems that involve humans and machines. </p>
<p>Taylor's research is focused on design -- the issues, techniques, and agents involved in creating and evolving software artifacts and processes. Specific emphases include software architecture and architecture-based software development environments. </p>
<p>Past Eclipse award recipients from the Bren School include David Redmiles (2005), Susan Sim (2004, 2005) and Andr&#233; van der Hoek (2004, 2005). </p>
