<p><a name="dean_broadband"></a><strong>Dean Richardson appointed to Governor's Broadband Task Force <br/>
			    </strong></p>
<div class="img_caption_left"> <img alt="photo: debra j. richardson" src="../../../bin/img/photos/faculty/djr.jpg"/>
<p><strong>Debra J. </strong><br/>
                        <strong>Richardson</strong></p>
</div>
<p>California Governor Arnold Schwarzenegger  announced the appointment of Bren School Dean <strong>Debra J. Richardson</strong> and Calit2 Director Larry Smarr, and   19 other experts to a newly-created, bipartisan Broadband Task Force.		        </p>
<p>The Broadband Task Force will bring together public and private stakeholders to remove barriers to broadband access, identify opportunities for increased broadband adoption and enable the creation and deployment of new advanced communication technologies.  </p>
<p>In October, the Governor signed an Executive Order to clear the government red tape for expanding broadband networks and to create the Broadband Task Force, which was expanded to 21 members earlier this month.		        </p>
<p>"California is number one in so many different things, whether it is biotechnology, stem cell research, protecting our environment, creating jobs or our university system. The Golden State must remain competitive in the telecommunication revolution so that we can continue to attract the best, the brightest and the most creative workforce in the world," said Governor Schwarzenegger.  "Broadband will help build California so we can grow our economy, create great jobs and stay ahead in the global marketplace."
		        </p>
<p>Dean Richardson, 52, came to UCI in 1987 and was appointed chair of the then-department of ICS in July 2000. </p>
<p>Under her leadership, the department was promoted to the first computer science school in University of California history in December 2002. She was named the Ted and Janice Smith Dean of the new school in January 2003. </p>
<p>As a member of the  Informatics faculty, she has developed leading edge tools, and has worked with several companies in adopting technology to improve the quality of critical software systems.</p>
<p>Calit2's Smarr, has served as director of Calit2 and a professor of computer science and engineering in UCSD's Jacobs School of Engineering since 2000, after 15 years as director of the National Center for Supercomputing Applications at the University of Illinois at Urbana-Champaign.		        </p>
<p>Fourteen others were also named to the Broadband Task Force: Ellis Berns, Rachelle Chong, Charles Giancarlo, William Huber, Wendy Lazarus, Lloyd Levine, Michael Liang, Bryan Martin, Timothy McCallion, Sunne Wright McPeak, Milo Medin, Peter Pennekamp, Rollin Richmond, Emy Tseng and Jonathan Taplin.		        </p>
<p>For more information on the Broadband Task Force, <a href="http://gov.ca.gov/index.php?/press-release/4823/">read Governor Schwarzenegger's</a> news release.   </p>
