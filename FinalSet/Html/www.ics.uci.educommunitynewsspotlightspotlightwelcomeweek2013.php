<p><b>Bren School of ICS welcomes record-setting class of new students</b></p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<div>
<iframe frameborder="0" height="253" src="//www.youtube.com/embed/XvbMfgu2lvY" width="450"></iframe>
</div>
<!-- -->
<!-- -->
<p>
As the UCI Spirit Team practices its human pyramids with a &#8220;5-6-7-8&#8221; and a 
booming voice exhorts the swarming students in Aldrich Park to &#8220;come join the 
Anteater Band,&#8221; the telltale rumble of the drum and clash of cymbals declares 
that UCI&#8217;s Southern Young Tigers Lion Dance team has taken the stage. An arc of 
onlookers at the annual Anteater Involvement Fair enjoys the shade in front of the 
stage as the lion rears up and unfurls a red banner from its mouth that reads, &#8220;Good 
Luck.&#8221;
</p>
<p>
It&#8217;s Sept. 23, the start of Welcome Week at UCI, a big day for all students, faculty 
and staff as they shake off the summer slows and get ready for full classrooms and 
the return of rush-hour foot traffic to Ring Road. But for the Donald Bren School 
of Information and Computer Sciences (ICS), it&#8217;s a <em>really</em> big day &#8212; in a sense, the 
biggest in school history.
</p>
<p>
As Dean Hal Stern pointed out during his welcome address to undergraduates, 469 
freshmen and 164 transfer students enrolled in a Bren School major this fall. Four 
years ago, the incoming undergraduate class numbered only some 125 students.
&#8220;We&#8217;re prisoners of our own success this year,&#8221; Stern noted with a grin, looking out 
over the UCI Student Center&#8217;s Pacific Ballroom. &#8220;You are such a large group that you 
will not fit in any room in our building.&#8221;
</p>
<p>
Stern shared with the newcomers some of the school&#8217;s &#8220;points of pride,&#8221; including
ICS being the only stand-alone school of computing in the UC system; notable
graduates such as Roy Fielding (B.S. &#8217;88, M.S. &#8217;93, Ph.D. &#8217;00), primary architect of
HTTP/1.1; the <a href="https://techjobs.uci.edu/">Techjobs</a> website, run jointly by ICS and The Henry Samueli School of 
Engineering to connect their students with potential employers; and a recent <a href="http://www.payscale.com/college-salary-report-2014/best-schools-by-major/computer-science">survey</a>
that rated the earning potential of UCI computer science grads as the fourth-best in 
the country (tied with Stanford).
</p>
<p>
&#8220;We&#8217;re very excited to have you here,&#8221; Stern continued. &#8220;The University of 
California, Irvine is a spectacular place, and we have a truly outstanding program in 
computing.&#8221;
</p>
<p class="image_with_wrapped_text">
<img alt="Tony Givargis" class="right" src="../../../bin/img/photos/spotlight/welcome_week_2013/WelcomeWeek-IMG_2881.jpg"/>
Earlier in the day, in his welcome to Bren School graduate students, Associate Dean
Tony Givargis spoke to an overflowing lecture hall &#8212; so full that chairs had to be
brought in from other classrooms. He lauded both the quantity and quality of the 
incoming class of master&#8217;s and doctoral students, pointing out just how stiff the 
competition was for admission.
</p>
<p>
&#8220;Think about it: 150 of you here, 2,500 applications &#8212; how smart does that make
you?&#8221; he asked. &#8220;We&#8217;re so happy to have you here, and I think you&#8217;ll do wonderfully.&#8221;
</p>
<p>
Both the graduate and undergraduate welcome programs also offered introductory
presentations from departments that provide key services to students, including
student affairs and computing support. Christine Kelly of the UCI Career Center also
gave the grad students useful tips. For instance, when it comes to internships, &#8220;The 
really good ones tend to get filled in the fall and winter,&#8221; she said.
Meanwhile, several student groups gave brief talks at the undergraduate welcome.
At their booths during the Anteater Involvement Fair in Aldrich Park, student club 
representatives said they noticed a strong new-student presence. &#8220;There&#8217;s been a 
steady stream,&#8221; said senior Audrey Auyang, president of the <a href="http://student-council.ics.uci.edu/">ICS Student Council</a>, as 
she and her ICSSC cohorts stood besieged by students signing up for the council&#8217;s 
newsletter.
</p>
<p class="image_with_wrapped_text">
<img alt="MAISS" class="left" src="../../../bin/img/photos/spotlight/welcome_week_2013/WelcomeWeek-IMG_2920.jpg"/>
Senior Vivian Le, finance director of the <a href="http://maissuci.weebly.com/">Management Information Student Society</a> 
(MAISS), said she&#8217;d heard about the large incoming class, and was seeing plenty of 
them at her group&#8217;s booth. &#8220;There are a lot of freshmen today,&#8221; she observed. &#8220;There 
are quite a few transfers, too, and that helps our club because transfers tend to be 
really interested in professional development.&#8221;
</p>
<p>
Ayato French, a junior <a href="http://www.ics.uci.edu/prospective/en/degrees/computer-game-science/">computer game science</a> major, is one of those transfers, 
having come from El Camino and Harbor community colleges in Los Angeles County. 
&#8220;I really wanted to go into game design,&#8221; he said. &#8220;I knew that UC Irvine had a 
computer game science [major], so that was a really big motivator for me.&#8221;
For junior Andrew Picard, a computer science and engineering major who 
transferred from Pace University in New York City, being part of the only computing 
school in the UC &#8220;was a positive for me. And also knowing that the school has a good 
relationship with a lot of big companies.&#8221;
</p>
<p class="image_with_wrapped_text">
<img alt="VGDC" class="right" src="../../../bin/img/photos/spotlight/welcome_week_2013/WelcomeWeek-IMG_2927.jpg"/>
Freshman Calvin Choi of Gilroy, a computer science major, also found ICS&#8217; status 
as its own school appealing. &#8220;At the moment, I don&#8217;t really have that good a grasp 
of what I want to do, so having a school of information and computer sciences 
will actually help me focus,&#8221; he said. &#8220;I also heard good things about the computer 
science department here.&#8221;
</p>
<p>
Dean Hal Stern, for one, is certainly pleased that the school&#8217;s strong reputation has 
drawn such a large, enthusiastic crop of new students.
</p>
<p>
&#8220;You have made an outstanding choice,&#8221; Stern said, as he looked over the sea of 
undergraduates. &#8220;The time has never been better to major in computer science 
or another computing related field. So, congratulations to you, or your mother, or 
whoever encouraged you to make this choice,&#8221; he added, to a burst of laughter from 
the crowd.</p>
