<div id="content_title">Computer Science Degree</div>

<blockquote>
<div id="content_text">

<p>The <a href="http://catalogue.uci.edu/">UCI General Catalogue </a>
is the official guide to all degree and graduation requirements; the information 
below is intended for general planning purposes only. For previous calendar years, please click <a href="http://catalogue.uci.edu/previouseditions/">here</a>.</p>
<hr/>
<h3>
<p><a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofcomputerscience/#graduatetext"><strong>Computer Science (CS) (M.S. and Ph.D.)</strong></a></p>
</h3>
<p>The Computer Science degree is a broad and flexible program which offers 
students opportunities for graduate study in the full spectrum of intellectual 
activity in computer science.&#160;</p>
<ul>
<li><a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofcomputerscience/#graduatetext"><strong>CURRENT DEGREE REQUIREMENTS</strong></a></li>
</ul>
<strong>Additional Information</strong>
<strong>
<p>The set of core and elective courses chosen by a student must be 
approved by the student's research advisor before advancement to candidacy. 
Faculty associated with each research area will provide suggested curricula for 
that area to guide students in their selection of courses. These curricula will 
also help Ph.D. students to prepare for their candidacy examination (see below) 
which must be taken in a specific research area.</p>
</strong>
<div class="green_bar"><strong> </strong></div>
<p><strong>Similarly, students may not take and count undergraduate courses in areas where they have already moved into graduate-level coursework. (The purpose of these undergraduate elective options are to help students to remedy undergraduate background deficiencies).</strong></p>
<strong>Ph.D. Candidacy Exam</strong>
<p>The objective of the candidacy exam is to demonstrate in-depth knowledge of 
an area of computer science and readiness to carry out independent research at 
the doctoral level in that area. The student must complete all course 
requirements and the research project prior to advancing to candidacy. </p>
<p>All requirements for candidacy including the candidacy exam must be completed 
by the end of the third year (or, for students with a previous MS in computer science or a related field, by the end of the second year). If the student does not pass on the first trial, 
they will be allowed until the end of the first quarter of the next year to 
advance to candidacy. </p>
<p>The candidacy committee will consist of five faculty members, the majority of 
whom must be members of the student&#8217;s program, to administer the exam according 
to UCI Senate Policy. Please see the ICS Graduate Office for policies 
regarding the advancement committee membership. </p>
<p>The student takes an oral exam, administered by the five-member committee, 
during which s/he is tested on knowledge relevant to the chosen area of 
specialization. Each area is defined by a set of topics and reading list, which 
are available in the documents below: </p>
<ul>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/AIMachineLearning.pdf">Artificial 
Intelligence and Machine Learning Syllabus (PDF)</a> </li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/CompArchEmbeddedSystems.pdf">Computer 
Architecture and Embedded Systems Syllabus (PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/GraphicsVisualization.pdf">Computer 
Graphics and Visualization Syllabus (PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/ComputationalNeuroscience.pdf">Computational 
Neuroscience (PDF)</a> </li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/ComputerNetworks.pdf">Computer 
Networks Syllabus (PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/CryptoSecurity.pdf">Cryptograph and 
Computer Security Syllabus (PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/DataManagement.pdf">Data Management 
(PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/IBAM.pdf">Informatics in Biology and 
Medicine (PDF)</a> </li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/DistributedSystems.pdf">Parallel and 
Distributed Systems Syllabus (PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/ScientificComputing.pdf">Scientific 
Computing Syllabus (PDF)</a></li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/SystemsSoftware.pdf">Systems Software 
Syllabus (PDF)</a> </li>
<li><a href="../../bin/pdf/grad/cs_area_syllabi/Theory.pdf">Theory Syllabus 
(PDF)</a></li>
</ul>
The field of computer science is concerned with the design, analysis and 
implementation of computer systems as well as the use of computation as it is 
applied to virtually every field of study and use in the everyday world.
<p>Computer systems can range in scope from tiny embedded systems to the 
internet as a whole. Research in computer science involves mathematical 
analysis, empirical experimentation and the implementation of proto-type 
systems. </p>
<p>Core research areas include artificial intelligence and machine learning, 
bio-informatics, computer architecture, embedded systems, graphics and visual 
computing, databases and information management, multimedia, networked and 
distributed systems, programming languages and compilers, security and 
cryptography, design and analysis of algorithms, scientific computing, and 
ubiquitous computing.</p>


</div>
