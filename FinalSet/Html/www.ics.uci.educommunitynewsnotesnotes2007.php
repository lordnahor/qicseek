<p>Our predictions may be accurate for 5 million years, or 5 billion, or anywhere in between.</p>
<p>Hayes' primary interest is currently in testing the reliability of large-scale physical simulations such as galaxy and cosmological n-body simulations as performed by astronomers. </p>
<p>More generally, he is interested in rigorous verification of numerical solutions of differential equation systems using validated interval arithmetic. </p>
<p>He is also interested in combinatorial graph theory, for example in bounding the Graph Ramsey Numbers.</p>
