<p><strong><strong>Raja Jurdak</strong><span style="font-weight: normal;">, a former graduate student of professors <strong>Crista Lopes</strong> and <strong>Pierre Baldi</strong>, and now a post-doctoral fellow at the University College, Dublin, has recently published a book entitled "Wireless AdHoc and Sensor Networks -- A Cross-Layer Design Perspective."</span></strong></p>
