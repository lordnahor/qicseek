<p><strong><a name="academicanalytics"></a>Networked Systems program ranked #1 by Academic Analytics </strong></p>
<p>The Bren School's computer science program in Networked Systems has been ranked number one in new study by Academic Analytics, a for-profit company that partners with the State University of New York.</p>
<p>Also, in this year's study, Information Science/Studies at the UC Irvine's Bren School ranks fourth. </p>
<p><strong>About Networked Systems<br/>
		        </strong>The Networked Systems program at the Bren School provides education and research opportunities to graduate students in the areas of computer networks and telecommunication networks.</p>
<p>Networked Systems include telephone networks, cable TV networks, cellular phone networks, and the Internet, as well as other emerging networks.</p>
<p>Networked Systems are inherently interdisciplinary. By their design, they connect devices such as computer and phones using communications methods. Networked Systems therefore must address the combination of software, hardware, and communications.</p>
<p>As a result, the Networked Systems area spans traditional departmental boundaries. At a minimum, the area draws heavily from Computer Science, Computer Engineering, and Electrical Engineering.</p>
<p>At UCI, these areas are housed in two departments, ICS and EECS. The Networked Systems program unites the strengths of these two departments and provides more integrated M.S. and Ph.D. degrees in this area.</p>
<p><strong>About Informatics (Information Science)</strong><br/>
		        Informatics is the interdisciplinary study of the design, application, use, and impact of information technology. It goes beyond technical design to focus on the relationship between information system design and use in real-world settings.</p>
<p>These investigations lead to new forms of system architecture, new approaches to system design and development, new means of information system implementation and deployment, and new models of interaction between technology and social, cultural, and organizational settings.</p>
<p>In the Bren School, Informatics is concerned with software architecture, software development, design and analysis, programming languages, ubiquitous computing, information retrieval and management, human-computer interaction, computer-supported cooperative work, and other topics that lie at the relationship between information technology design and use in social and organizational settings</p>
<p>Academic Analytics bases their ranking of doctoral programs on publications, citations of journal publications, federal research funding, and awards and honors.</p>
<p>For more information please view the <a href="http://www.academicanalytics.com/TopPhysMathSci2006-07.html">Academic Analytics web site</a>.   </p>
