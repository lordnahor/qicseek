<p><strong><a name="student_labs"></a>Graduate student's work selected as top-rated abstract of Leonardo Abstracts Service (LABS)</strong></p>
<p>The International Society for the Arts, Sciences and Technology (Leonardo) has announced that the paper entitled "Landscape Denatured: Digitizing the Wild" by graduate student <strong>Eric Kabisch</strong>  is a top-rated abstract in the Leonardo Abstracts Service Databases during the first half of 2007.</p>
<p>The paper presents motivation and documentation of four technologically enabled artworks. </p>
<p>These artworks explore ways in which digital technologies impact society and culture, focusing particularly on the impacts of information technologies on physical and cultural geography. </p>
<p>In addressing the impacts of digital technologies on culture, these artworks employ the very technologies being investigated.  </p>
<p>Through the production and exhibition of this work,  Kabisch hopes to engage the public with these important issues and to help shape the ways that technological methodology embeds itself in our world and in our daily experience.</p>
<p>Leonardo Abstracts Service (LABS) is a comprehensive collection of Ph.D., Masters and MFA thesis abstracts on topics in the emerging intersection of art, science  and technology. </p>
<p>Individuals receiving advanced degrees in the arts (visual, sound, performance, text), computer sciences, the sciences and/or technology that in some way investigate philosophical, historical or critical applications of science or technology to the arts are invited to submit abstracts of their theses for consideration.</p>
<p>Top-rated abstracts in both the English and Spanish language databases are chosen twice annually by peer-review panels and published in the Leonardo Electronic Almanac.</p>
