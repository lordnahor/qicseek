<p><strong><a name="student_eclipse"></a>Graduate students win award at OOPSLA 2007 for Eclipse plugin </strong></p>
<p>Informatics students <strong>Sushil Bajracharya</strong> and <strong>Joel Ossher</strong>, and Brazilian visiting student <strong>Otavio Lemos</strong>, all in professor <strong>Crista Lopes </strong>lab, have won the Student Research Competition at the <a href="http://www.oopsla.org/oopsla2007/">Object-Oriented Programming, Systems, Languages and Applications (OOPSLA) 2007</a> with their work on CodeGenie.</p>
<p><a href="http://sourcerer.ics.uci.edu/codegenie/">CodeGenie</a> is an Eclipse plugin that uses test cases as the interface for searching existing open source code on the internet. </p>
<p>With this award, Sushil, Joel and Otavio have been selected for the final of the prestigious <a href="http://www.acm.org/src/">ACM Student Research Competition</a>.</p>
