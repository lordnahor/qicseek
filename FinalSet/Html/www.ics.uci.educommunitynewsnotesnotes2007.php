<p><strong><a name="kim_rtss"></a>Doctoral student wins two awards at RTSS 2007</strong></p>
<div class="img_caption_left"> <img alt="photo: minyoung kim" src="../../../bin/img/photos/students/minyounk.jpg"/>
<p><strong>Minyoung<br/>
                      Kim</strong></p>
</div>
<p><strong>Minyoung Kim</strong>, a doctoral student in Computer Science, received "Best System Architecture Award" and "Best Overall Idea Award" at the 2007 IEEE Real Time Systems Symposium (RTSS) PhD Forum held in Tucson, AZ.</p>
<p>Kim received the awards out of 30 participants, who were in turn selected from a field of international applicants.</p>
<p>The RTSS PhD Forum, sponsored by the National Science Foundation, is designed to discuss innovative research challenges and application ideas in deeply embedded real-time computing systems, encourage student involvement in new research directions, and reward the most innovative student ideas in an exciting emerging research field.</p>
<p>Kim's research focuses on power-aware distributed embedded systems, formal methods and multimedia systems.</p>
<p>She is co-advised by professors <strong>Nikil Dutt</strong> and <strong>Nalini Venkatasubramanian</strong>. </p>
