<p><strong>NOVEMBER 2007</strong></p>
<p><strong><a name="dutt_ieeefellow"></a>Dutt named IEEE Fellow</strong></p>
<div class="img_caption_left"> <img alt="photo: nikil dutt" src="../../../bin/img/photos/faculty/dutt.jpg"/>
<p><strong>Nikil<br/>
                      Dutt</strong></p>
</div>
<p>The Institute   of Electrical and  Electronics Engineers (IEEE) has named Chancellor's Professor <strong>Nikil Dutt</strong> an  IEEE fellow, one of the organizations highest honors.<br/>
&#160;<br/>
The honor, for "contributions to  architecture description languages and for the design and exploration of  customized processors," recognizes Dutt's work in the area of embedded  systems and computer-aided design, with a specific focus on the exploration,  evaluation and design of domain-specific embedded systems spanning both  software and hardware.</p>
<p>His research group has developed a novel architectural  description language that facilitates rapid exploration of programmable  embedded systems, as well as automatic generation of software toolkits  supporting embedded systems development (including optimizing compilers and  simulators). </p>
<p>Other projects within his group include low-power/low-energy  compilation and synthesis, validation and verification of pipelined processors,  software/hardware interfaces for distributed embedded systems, and memory  architecture exploration for embedded systems.</p>
<p>The IEEE Fellow designation is granted each year to a select  group of the organization's 370,000 members for accomplishments that have  contributed importantly to the advancement or application of engineering,  science and technology.</p>
<p>IEEE is the world's leading professional organization and standards-setting  authority for a wide range of high-tech fields, including electrical  engineering, aerospace systems, computer engineering, telecommunications,  biomedical engineering.</p>
