<p><strong><a name="patterson_africa"></a><strong>Patterson to study context-aware networks in Africa <br/>
		        </strong></strong></p>
<div class="img_caption_left"><strong> <img alt="photo: donald patterson" height="90" src="../../../bin/img/photos/faculty/djpatter.jpg" width="70"/>
</strong>
<p><strong><strong>Donald </strong><br/>
                        <strong>Patterson</strong></strong></p>
</div>
<p>UC Irvine's Academic Senate Council has awarded <strong>Donald Patterson</strong>, assistant professor of informatics, $3,500 in travel funding to study context-aware networking routing algorithms in Africa.</p>
<p>Context-aware networking algorithms allow users to transmit data even when the local network infrastructure has been destroyed.</p>
<p>For example, if a natural disaster destroyed an area&#8217;s local network infrastructure, aid workers with mobile data devices on the ground would still be able to transmit data back to headquarters for coordinating logistics. </p>
<p>By creating a routing system that knew which device belonged to the "coordinator" and which device belonged to a "field nurse" it could make better decisions about which direction to push data when the devices come in contact. </p>
<p>The coordinator's device could offload data to the Internet when it returns to a location with established infrastructure.</p>
<p>Patterson, working with two non-governmental health organizations, will be surveying two sites in South Africa and Zambia.</p>
<p>Patterson plans to evaluate work flow and movement patterns of participants in the organizations to develop an architecture which routes on the basis of contextual clues, but is also helpful to the organizations on the ground.</p>
<p>Patterson&#8217;s areas of research interest lie at the intersection of Artificial Intelligence and Ubiquitous computing and he has applied this work to transportation and activity assistance.		        </p>
