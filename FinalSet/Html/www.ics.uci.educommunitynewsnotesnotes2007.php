<p><a name="rowe_lauds"></a><strong>Alumnus to be recognized at annual Lauds &amp; Laurels ceremony</strong></p>
<div class="img_caption_left"> <img alt="photo: lawrence rowe" src="../../../bin/img/photos/noteworthy/photo_052007.jpg"/>
<p>Lawrence<br/>
                    Rowe</p>
</div>
<p>Lawrence A. Rowe, who earned a Ph.D. in Information and Computer Science from the Bren School in 1976, will be honored with the Distinguished Alumni Award at the UCI Alumni Association&#8217;s annual Lauds &amp; Laurels ceremony.</p>
<p>Rowe, who also earned a B.A. in Mathematics from UCI in 1970, was professor of Electrical Engineering and Computer Science at the University of California, Berkeley from 1976 &#8211; 2003.<br/>
                    <br/>
			      While at Berkeley, Rowe was the founding director of the Berkeley Multimedia Research Center (BMRC), which was created in 1995 to explore the application of multimedia technology to education and research. </p>
<p>BMRC taught classes on multimedia authoring, established and operated authoring studios and distributed collaboration and distance learning rooms and services, and provided advice and technical support on a wide range of issues relating to multimedia authoring and distributed collaboration. </p>
<p>A major BMRC accomplishment was the development and deployment of the Berkeley Webcasting System, which produces over 30 hours a week of Berkeley course lectures that are viewed over 300,000 times per month by people anywhere on the Internet. </p>
<p>His past research interests have included development tools for database applications, distributed operating systems, computer networks (multicast protocols and applications), distributed streaming media toolkits, multimedia applications (video-on-demand, webcasting, videoconferencing, multimedia authoring, etc.), and computer-integrated manufacturing. </p>
<p>Rowe was co-founder of Ingres Corporation, and under his direction the company became well-known by industry as the pioneer in the development of easy-to-use tools for database access. </p>
<p>He also co-founded Orinda Software and more recently n-Cast Corporation, a company likely to play a very important role in the further development of presentation capture and streaming media technologies.</p>
<p>Rowe continues to work as consultant for many companies including Apple, Dust Inc., Eloquent, Fast Forward Networks, FX Palo Alto Laboratories, Harris Semiconductor, Hughes Aircraft, Motorola, Sematech, Siemens and Tandem. </p>
<p>He has also published more than 70 papers on multimedia systems and applications, programming systems, and database systems. </p>
<p>Rowe is a member of the editorial board of the Association of Computing Machinery (ACM) Multimedia Systems Journal, past chair of ACM Special Interest Group on Multimedia, a member of the Society of Motion Picture and Television Engineers (SMPTE), and has served on many governmental advisory committees. </p>
<p>In 1998, Rowe was inducted as a Fellow of ACM for his seminal contributions to programming languages, relational database technology, user interfaces and multimedia systems. </p>
<p>Rowe will be recognized along with 16 alumni, faculty, staff, students and friends  for their ongoing contributions to the university during the  37th annual ceremony.</p>
<p>Initiated in 1971, the Lauds and Laurels ceremony provides a  way to recognizes alumni, students, faculty, staff and friends who have brought  recognition to the university through their achievements, studies or  profession.</p>
<p>To date, tributes to more than 600 community members have  been a part of the ceremony. This year&#8217;s event will take place May 10, 2007, at  the Hyatt Regency Irvine. </p>
