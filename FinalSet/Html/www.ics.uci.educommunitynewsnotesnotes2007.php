<p><strong><a name="kay_moot"></a>Kay serves as Moot Court Judge</strong></p>
<div class="img_caption_left"> <img alt="photo: david g. kay" src="../../../bin/img/photos/faculty/kay.jpg"/>
<p><strong>David G. <br/>
                      Kay</strong></p>
</div>
<p>On November 16 and 17, 2007, Senior Lecturer <strong>David G. Kay</strong> served as Chief Justice in three rounds of the National Entertainment Law Moot Court Competition.</p>
<p>Law students from across the country competed by making Supreme Court arguments in a hypothetical case involving copyright and trademark infringement on the World-Wide Web.  </p>
<p>One party in the case hosted a web site with videos posted by users; a user posted a copyrighted video and the copyright owner wanted to hold the web site host responsible for the infringement.  </p>
<p>Another issue was the use of a competitor's domain name in a metatag, and whether that constituted trademark infringement by diverting users away from the competitor.  </p>
<p>Kay, who is a lawyer as well as a computer scientist, said, "Internet issues come up frequently in the entertainment industry, and those issues often have novel legal implications.  It's useful for computer scientists to have an understanding of the legal system because increasingly, technologists and lawyers need to work together in the development of new products and services."  </p>
<p>The Bren School has a long history of addressing these issues in its curriculum. </p>
<p>Informatics 131, Social Analysis of Computerization, and Informatics 269, Computer Law, are two courses on this theme.  A general education course on technical issues in public policy is also planned for the 2008-09 academic year.</p>
