<p><strong><strong>Natasa Przulj</strong><span style="font-weight: normal;">, assistant professor of computer science, has received a $570,000 National Science Foundation CAREER grant in support of her 5-year project entitled "</span><i><span style="font-weight: normal;">CAREER: Tools for Analyzing, Modeling, and Comparing Protein-Protein Interaction Networks</span></i><span style="font-weight: normal;">".</span></strong></p>
<p>The project proposes improvements in tools for analyzing and modeling of Protein-Protein Interaction (PPI) networks.  </p>
