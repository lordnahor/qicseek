<p>Nature Physics has published "Is the outer Solar System chaotic?", a paper by computer science professor <strong>Wayne Hayes</strong>.</p>
<p>The orbits of the inner planets (Mercury, Venus, Earth, and Mars) are known to be practically stable in the sense that none of them will collide or be ejected from the Solar System for the next few billion years.  </p>
<p>However, their orbits are chaotic in the sense that we cannot predict their angular positions those stable orbits for more than about 20 million years.  </p>
<p>The picture is less clear for the outer planets (Jupiter, Saturn, Uranus and Neptune).  </p>
