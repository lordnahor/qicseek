<div id="content_title">Noteworthy achievements 2007</div>

<blockquote>
<div id="content_text">

<p>Bren School faculty, students and research initiatives are some of the most well regarded successes on the UC Irvine campus. We are pleased to announce the following noteworthy achievements for 2007.</p>
<p>Be sure to subscribe to the <a href="../rss/index.php">Bren School's  RSS (Really Simple Syndication) feed</a> to get noteworthy news, <a href="../press/index.php">press releases</a> and <a href="../articles/index.php">articles</a> about the Bren School delivered directly to your desktop! </p>
<p>Awards, grants and other honors can be sent to <a href="mailto: ic%73%6ee%77%73@ics.%75ci%2e%65d%75 ">icsnews@ics.uci.edu </a> to be considered for publication. </p>
<p><strong>DECEMBER 2007</strong></p>
<p><strong><a name="tsudik_chair"></a>Tsudik Program Chair of Financial Cryptography and Data Security Conference</strong></p>
<div class="img_caption_left"> <img alt="photo: gene tsudik" src="../../../bin/img/photos/faculty/gtsudik.jpg"/>
<p><strong>Gene<br/>
                      Tsudik</strong></p>
</div>
<p>Professor of computer science <strong>Gene Tsudik</strong> is serving as the Program Chair at the 12th annual Financial Cryptography and Data Security Conference to be held at the end of January  in Cozumel, Mexico.</p>
<p>Financial Cryptography and Data Security is a major international forum for research, advanced development, education, exploration, and debate regarding information assurance in the context of finance and commerce. </p>
<p>The conference covers all aspects of securing transactions and systems. Submissions focusing on both theoretical (fundamental) and applied real-world deployments are solicited. </p>
<p>The goal of the conference is to bring security/cryptography researchers and practitioners together with economists, bankers, implementers, and policy-makers. Intimate and colorful by tradition, the FC program features invited talks, academic presentations, technical demonstrations, panel discussions as well as a research poster session. </p>
<p>Tsudik's research interests are in computer/network security, privacy and applied cryptography. His recent work focuses on privacy in Internet services, RFID systems and mobile ad hoc networks, as well as security in sensor networks and storage systems. His research also covers secure group communication, in particular, group key agreement, group signatures and group access control.</p>
<hr size="0"/>
<p><strong><a name="nalini_chair"></a>Venkatasubramanian General Chair of Middleware 2007 conference</strong></p>
<div class="img_caption_left">
<div align="center">
<img alt="photo: nalini venkatasubramanian" src="../../../bin/img/photos/faculty/nalini.jpg"/></div>
<p><strong>Nalini<br/>
                  Venkatasubramanian</strong></p>
</div>
<p>Computer science professor <strong>Nalini Venkatasubramanian</strong> recently led the ACM/IFIP/USENIX Middleware 2007 conference as General Chair.</p>
<p>The broad scope of the conference is the design, implementation, deployment, and evaluation of distributed systems platforms and architectures for future computing environments.</p>
<p>Venkatasubramanian's research focuses on enabling effective management and utilization of resources in the evolving global information infrastructue. </p>
<p>In the coming years, multimedia to the desktop and home is likely to become a pervasive technology. </p>
<p>The composition of multiple resource management services is necessary to guarantee safe, cost-effective Quality-of-Service (QoS) to global multimedia applications. She addresses the problem of composing resource management services in distributed systems.</p>
