<p>
<i>Bren School students were given two opportunities to meet with potential employers on Oct. 13, when the UCI Career Center hosted an Engineering, Science, and Technology Career Fair, while the ICS Student Council sponsored a quarterly Reverse Career Fair.  Both were held at the Student Center.</i></p>
<b>Engineering, Science and Technology Career Fair</b>
<p class="image_with_wrapped_text" style="margin-top: 1em;">
<img alt="image 1" class="left" src="../../../bin/img/photos/spotlight_career_fair/Image1.jpg"/>
At the UCI Engineering, Science and Technology Career fair, hundreds of undergraduate and graduate students talked to representatives from 89 companies with hopes of gaining a job or internship. Companies included Google, Microsoft, Amazon, Intel, Verizon Wireless and Blizzard Entertainment. The Career Center hosts various career fairs throughout the academic year. For more information, visit <a href="http://www.career.uci.edu">http://www.career.uci.edu</a>.
</p>
<p class="image_with_wrapped_text">
<img alt="image 2" class="right" src="../../../bin/img/photos/spotlight_career_fair/Image2.jpg"/>
Hessam Kooti, a Computer Science graduate student, talks to a potential employer. Kooti will finish his graduate program in six months and is hoping to secure a full-time job before graduating. He says the career fair is a good way to get in contact with companies: &#8220;Online you don&#8217;t get to talk to people. Here, you&#8217;re talking to people who have similar careers and interests.&#8221;
</p>
<p class="image_with_wrapped_text">
<img alt="image 3" class="left" src="../../../bin/img/photos/spotlight_career_fair/Image3.jpg"/>
Badhri Sridharan, a Computer Science graduate student, speaks with a representative from Blizzard Entertainment after days of preparing for the fair by researching his companies of interest. He noted that the fair featured a large number of organizations, including many of the big time players in his field. 
</p>
<p class="image_with_wrapped_text">
<img alt="image 4" class="right" src="../../../bin/img/photos/spotlight_career_fair/Image4.jpg"/>
A Verizon Wireless recruiter said she was hired through a career fair at her alma mater. &#8220;The [students] who stand out to me are the ones who&#8217;ve done their research,&#8221; she noted. She encourages students to learn about the companies before attending the fair. &#8220;Do your homework and always send a follow-up email. It helps the recruiters remember you as a person.&#8221;
</p>
<b>ICSSC Reverse Career Fair</b>
<p class="image_with_wrapped_text" style="margin-top: 1em;">
<img alt="image 5" class="left" src="../../../bin/img/photos/spotlight_career_fair/ICSStudentCouncil.jpg"/>
The Reverse Career Fair gave students a unique opportunity to display individual or team projects to prospective employers in the hopes of gaining a job or internship. First-year student council member Delian Petrov (far right) states: &#8220;In a regular career fair&#8230; students only have 30 to 40 seconds to make an impression. This way, companies can see the students&#8217; hard work.&#8221;
</p>
<p class="image_with_wrapped_text">
<img alt="image 6" class="right" src="../../../bin/img/photos/spotlight_career_fair/ChrisLang.jpg"/>
Senior Informatics major Chris Lang is the project manager of &#8220;UCI Field Study Web Portal.&#8221; The yearlong project&#8217;s aim is to transform UC Irvine&#8217;s current information-only Field Study website into community partner listings for which students can apply. Lang hopes to secure a job as an associate producer by the time he graduates.
</p>
<p class="image_with_wrapped_text">
<img alt="image 7" class="left" src="../../../bin/img/photos/spotlight_career_fair/RyanSkinner.jpg"/>
Ryan Skinner, a junior Computer Science major, displays his self-made project, which facilitates the building of worlds in video games. Skinner enjoys video games and programming, and hopes to find an internship with a company like Blizzard Entertainment. 
</p>
<p class="image_with_wrapped_text">
<img alt="image 8" class="right" src="../../../bin/img/photos/spotlight_career_fair/PresidioMobile.jpg"/> Kenny Compass and Steven Nguyen, both senior Informatics majors, demonstrate their project titled &#8220;Presidio Mobile.&#8221;  The project, affiliated with the Presidio Trust (which preserves Presidio National Park), is a mobile database for plant life. Nguyen said the goal of the project was &#8220;to get experience.&#8221; 
 
</p>
<p class="image_with_wrapped_text">
<img alt="image 9" class="left" src="../../../bin/img/photos/spotlight_career_fair/YoonLee.jpg"/>
Yoon Lee, a Computer Science major graduating this quarter, presents his solo project, &#8220;ZOT Mobile.&#8221; The app is a flagship application for students, faculty and visitors, wherein features and functionality of the app simplify student life at UCI and deliver useful information. Some features of the app include a campus map, directory, shuttle schedule and library look-up. Lee says he has gained valuable training through a previous internship with MobilityWare. Ideally, he would like to work as a compiler or with mobile computer applications.
</p>
