<div id="content_text">

<p><strong>Sweet Music</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: sam archer" height="187" src="../../../bin/img/photos/people/sam_archer.jpg" width="200"/>
<p> Sam Archer
</p>
</div>
<p>Sam Archer is accustomed to creating fluid rhythms as a member of the UCI Pep Band, a trait he has carried over to the class room. </p>
<p>&#8220;Being around computers as much as I was, I got to be fairly good at solving problems with them,&#8221; Archer said. &#8220;I really enjoyed working with computers, and fixing them for friends and family members turned me on to computer science as a career.&#8221;</p>
<p>Despite its unfamiliar name and fledgling beginnings, the Mission Viejo native was excited about being part of the inaugural Informatics class. </p>
<p>&#8220;The major sounded much more along the lines of what I would enjoy doing; more design and critical thinking, slightly less programming, and this was a large part of my motivation for going into the major,&#8221; Archer said. &#8220;Secondly, Informatics is still a relatively new field, and to have received an education and degree in it will open a lot of doors for careers in the future.&#8221;</p>
<p>The Informatics major covers the design, implementation, use and impact of information technology. </p>
<p>Compared to traditional computer science, which primarily focuses on the internal features of computer systems, Informatics is also concerned with computer systems and their surrounding context -- the people and organizations that use them and the problems they need to solve.</p>
<p>It was the major&#8217;s focus on both people and technology that provided the strongest draw for Archer. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;I enjoy coding and it is rewarding to see a piece of software you've built come to life, but I wanted to spend my life doing something slightly more tangible and creative,&#8221; Archer said. &#8220;The Informatics major has more focus on fulfilling customers needs, and high-level design, which as a career I think I will enjoy.&#8221;</p>
<p><strong>SKILLS TO PAY THE BILLS </strong></p>
<p>With graduation a year away Archer is focusing on life after college, one he hopes will entail a job with responsibilities that lie somewhere between customers and developers solving problems on a design level.</p>
<p>Archer credits his Informatics classes for providing him the skills he will need to achieve his career goals. </p>
<p>&#8220;I had no practical programming experience prior to starting the Informatics program,&#8221; Archer said. &#8220;The program is definitely set up to teach anyone without programming experience how to program. There's very little hand-holding though, so you'll need to be willing to learn a little bit on your own.&#8221;</p>
<p>Archer also credits the Informatics major with teaching him how to learn, a trait which will be extremely beneficial in the technology field where changes come so quickly that the specific technology students learn will probably be obsolete by the time they graduate. </p>
<p>For Archer, changing to meet the needs of an ever changing technology field will be as easy as moving his fingers to a different valve on his Tuba.</p>
<p><i>- Eric Kowalik</i></p>


