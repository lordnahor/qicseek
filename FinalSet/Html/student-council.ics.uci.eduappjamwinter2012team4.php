			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
			Our process initially began with figuring out what our application does. We tried to make sure that we thought out of the box, and that everyone agrees and are excited for the project. If there was one person not excited, then we move on to the next idea. It was important for us to be on the same level. Once we had our idea, we figured out the design of our interfaces. At the same time, we had the basic design of our database figured out. After designing the UI, we hard-coded the basic layout of our whole program. Then we had different people working on the different activities and getting them to merge together. We used Saros, an eclipse add-on, to get our project linked together. At the same time, we had someone working with linking the client-side with the database. Then we worked more on our program's functionality, and creating more options. Getting our application to do everything we wanted it to do, obviously, took the most time. It took about a span of four full work-days, and, by full, I mean 12 hour work-days. Then came testing and debugging on the last day. We were still touching up our functionalities as we were debugging. The last part was optimization. We attempted to make sure our program runs as fast as possible. In the end, we felt we were really successful in our process.	
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
			Some of our major hurdles include time conflicts, limitations to the scope of our initial design/database implementation, and lack of experience with the back-end implementation. This was, for most of us, the first time working on a full-blown android application. We only had one team member with initial experience with databases. We spent the majority of our first few days figuring out our design so that it was not too far-reaching and learning all the aspects of android development. In the beginning, everything seemed like alien code, but by the end of the week, we all had a good understanding of the client-side and back-end processes and implementation. During the week days was when we had limited time restrictions when it came to meet up. We were still figuring out our design during those first few days, so it was hard to get people working on individual assignments. However, by Friday, we ended up spending more time with each other than sleeping. 
			</p>
			

            <div class="page-header">
				<div class="styled_title"><h1>Lessons Learned</h1></div>
			</div>
			<p>
			We learned to make sure we have the full design implementation down before we start. The design is absolutely necessary to have implemented first in order to have the scope of the project down and it gives us a general idea of how much time we will be needing together. We also learned that we have to make sure that the design of the databases have been completely implemented before anything so we can fully understand the scope of the project. We also learned that time organization and leadership is essential in projects like these. If you don't have a leader to organize the meet-up times, transportation, to-do lists, then the project quickly becomes unorganized. We also learned that getting adequate amount of rest and food before we start working is extremely important in the quality of our work. Buying food beforehand for the group in preparation for 10+ hours of work was a big help. Also, beer helps make things more exciting. Importantly, making sure every member is excited and motivated for the project keeps the group moral high and brings more quality to our work.
			</p>

