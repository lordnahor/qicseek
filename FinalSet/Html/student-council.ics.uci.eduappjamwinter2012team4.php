            <div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
				We have created an application for people to set goals for themselves and make positive changes in their daily lives by joining a support group with friends that are sharing a similar list of goals in order to encourage, motivate, and accomplish selected goals.

				<br /><br />Our application allows you to create groups which are publicly shared and add goals to that group. Anyone can join that group and will have that group's goals added to their "remaining goals" list. When a user has accomplished a goal, it gets put into that user's completed goals list which then allows you to rate that goal. Other user's can view the rating of that goal and decide if they want to do it. We have implemented a list of preset goals with descriptions as well as the ability to add their own goals. We also have a "friends" functionality which allows the user to get updates on when other friends have completed a goal. User's also have their own statistics on the goals they have worked on.

				</p>

