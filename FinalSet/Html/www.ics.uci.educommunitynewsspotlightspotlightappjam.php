</p>
<h3>FALL 2011 APPJAM WINNERS:</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="TimeToGo" class="right" src="../../../bin/img/photos/spotlight_appjam/AppJam_TimeToGo.jpg"/>
</p>
<p><b>&#8220;TimeToGo&#8221;<br/><b>1st place</b>&#160;</b> 
</p>
<p>
<b>Prize:</b> $500 and lunch at Google<br/>
<b>Members:</b> Adithya Gajulapally (lead), Yuen Hei Lam, Kevin Lee, Jared Leung and Alexis Hoshino<br/>
<b>App description:</b> Ever wonder how long it will take to get to class from your current location on campus? TimeToGo is an Android app sensitive to user location when issuing calendar reminders. The app also provides a searching capability and the ability to see friends&#8217; schedules. Future goals for the team include implementing a tool on the app that takes into account multiple means of transportation.<br/>
<b>Team comment:</b> &#8220;We learned to accommodate for everyone's busy schedules by holding meetings through Skype and Facebook, rather than meeting in person. Another valuable lesson this project reinforced was to let no one's talents go to waste. Even though we are all coming from different programming skill levels, we all wanted to contribute to the best of our abilities for the sake of our common goal.&#8221;
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="ZotPark" class="right" src="../../../bin/img/photos/spotlight_appjam/AppJam_ZotPark.jpg"/>
</p>
<p><b>&#8220;ZotPark&#8221;<br/><b>2nd place</b>&#160;</b>
</p>
<p><b>Prize:</b> $300 and Google bags<br/>
<b>Members:</b> Chelsea Clark (lead), Derek Liang, Kevin Jonaitis, Myron Lozano and Delian Petrov<br/>
<b>App description:</b> Designed to help students navigate the UCI parking system, ZotPark allows users to look up valid locations to park based on their permit type. ZotPark can also determine and display extra parking lots and structures available to the user, depending on time of day. Additionally, the app provides information about parking lots and permit types, as well as navigation capabilities. &#8220;When I was new to campus, I spent 20 minutes driving up and down Peltason looking for Lot 17A and no one could help me find it,&#8221; said Clark. &#8220;But this app can.&#8221; <br/>
<b>Team comment:</b> &#8220;We met every day for two to three hours. We had little to no coding experience, but we pulled through with research, hard work and adrenaline.&#8221; 
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Groups" class="right" src="../../../bin/img/photos/spotlight_appjam/AppJam_Groups.jpg"/>
</p>
<p><b>&#8220;Groups&#8221;<br/><b>3rd place</b>&#160;</b>
</p>
<p>
<b>Prize:</b> $100 and Android watches<br/>
<b>Members:</b> Marc Touchette (lead), Richard Creencia, Alex Taylor, Won Kim and Weiwei Jiang<br/>
<b>App description:</b> Allows users to create and join study or social groups, and makes the information available to them from any Android phone. Within Groups, users can access information like name, email and phone numbers. &#8220;It&#8217;s much more compact and discreet than Facebook because it doesn&#8217;t carry your life story,&#8221; said Touchette. <br/>
<b>Team comment:</b>&#160;&#8220;This application could convince middle school students that studying computing is cool because it will allow them to keep track of their groups, too. With more and more kids using smart phones and no longer memorizing numbers, this app will allow them to easily keep track of their friends' numbers, or look up the numbers of their friends from a different phone.&#8221;
</p>
