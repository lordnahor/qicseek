</p>
<p>
<b>Members:</b> Tri Pham (lead), Chris Miliotti, Brett Buford, Ki Lee, Kelly Yin and Siripen Pongpaichet<br/>
<b>App description:</b> Displays university activities to users. The activities can be user-created through the application and generated from RSS feeds around UCI. Users may view and join upcoming events, and invite other users to the event. <br/>
<b>Team comment:</b> &#8220;Creating an app in one week was significantly more difficult than expected, but worthwhile.&#8221;</p>
