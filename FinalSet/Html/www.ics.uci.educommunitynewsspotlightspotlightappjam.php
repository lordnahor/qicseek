<p><b>Bren School students build apps in one week</b></p>
<p>
Eight teams of Bren School students with varying levels of programming experience &#8212; ranging from proficient to none &#8212; created fully functional mobile applications or "apps" in just one week, as part of the inaugural ICS Student Council AppJam Tournament. 
</p>
<p>
The challenge: Create a mobile app that will improve the lives of UC Irvine students academically, professionally and socially.  
</p>
<p>
The results: Apps ranging from the grand prize winner, &#8220;TimeToGo,&#8221; which alerts users of their appointments at a time appropriate to their location; to second-place finisher &#8220;ZotPark,&#8221; which enables users to easily navigate the UCI parking system; and third-place winner &#8220;Groups,&#8221; a way for students to keep track of their social and academic circles.
</p>
<p>
<!-- -->
<!-- -->
<iframe frameborder="0" height="260" src="http://www.youtube.com/embed/gciaOIrhDfg" width="450"></iframe>
<!-- -->
<!-- -->
</p>
<p>
&#8220;The ICS Student Council thought AppJam would be a great way for students to learn or master coding skills while gaining real-life team project experience,&#8221; said ICSSC project management coordinator Nikola Metulev, a fourth-year Computer Science and Engineering major.&#160;&#8220;In honor of <a href="http://www.csedweek.org/" title="CSEdweek">CSEdWeek</a>, we also encouraged participants to build an app that would be appealing to middle and high school students."</p>
<p>
The competition kicked off Nov. 14, and the judging event, which featured presentations and demos, took place Nov. 22. Judges included John Libby, cofounder of MobilityWare, Farsheed Atef, chief technology officer and cofounder of Drumbi, and faculty members Michael Franz, Dan Frost and Shannon Tauro. Student peers also participated in the voting process. 
</p>
<p>&#8220;The entire tournament was amazing,&#8221; Tauro said. &#8220;My fellow judges and I were very impressed by what Bren School students were able to learn and accomplish during a very short and busy time in the quarter.&#8221; 
</p>
<p>
According to Metulev, most teams plan to add features to their apps before posting them to the Android market. Select apps may also be available for download from the AppJam <a href="http://appjam.roboteater.com/" title="AppJam">website</a> in January. ICSSC plans to organize the next AppJam Tournament during winter quarter.</p>
<p>
View the Bren School's Flickr 
