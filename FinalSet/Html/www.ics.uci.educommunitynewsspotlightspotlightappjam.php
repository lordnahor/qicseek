<h3>OTHER PARTICIPANTS:</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Peter's Organizer" class="right" src="../../../bin/img/photos/spotlight_appjam/AppJam_Peters_Organizer.jpg"/>
</p>
<p>
<b>&#8220;Peter&#8217;s Organizer&#8221;</b>
</p>
<p>
<b>Members:</b> Nithin Jilla (lead), Matthew Ito, Quan Nguyen, Daryl Alfaro and Ashwin Amit<br/>
<b>App description:&#160;</b>Peter&#8217;s Organizer is a calendar Android app that lists events specific to the Bren School. Events are categorized as academic, social or professional, and could be added by students either through their phone or a web page. Users may filter events by category.  <br/>
<b>Team comment:</b> &#8220;Many Bren School students find out about jobs through events such as company visits. We wanted an easier way for students to find about these school events, so we thought this calendar would be useful. We then decided to expand this idea to include academic events and social events as well."</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Metis" class="right" src="../../../bin/img/photos/spotlight_appjam/AppJam_Metis.jpg"/>
</p>
<p>
<b>&#8220;Metis&#8221;</b>
</p>
<p>
<b>Members:</b> Orson Teodoro (lead), Peter Cao, Justin Krakes and Sohrob Raja<br/>
<b>App description:</b> Metis functions as an interactive scheduling tool, allowing users to coordinate their schedule with friends, family and fellow students through a visual timeline feature. Users may sync their schedules with another user through the merge feature.  <br/>
<b>Team comment:</b> &#8220;The app was inspired by our need to coordinate free time to get together to work on the app.&#8221;
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="ICS Earth" class="right" src="../../../bin/img/photos/spotlight_appjam/AppJam_ICS_Earth.jpg"/>
</p>
<p>
<b>&#8220;ICS Earth&#8221;</b>
</p>
<p>
<b>Members:</b> Johnson Liu (lead), Zhihang Xiao, Alexander Richberg, Jonathan Stroud and Pramodh Gopal<br/>
<b>App description:</b> ICS Earth was developed to help Bren School students locate professors and events on campus. The app allows users to search through a list of professors by name, phone number, office location or department, and through a list of events by name, date, time, location or category. The application also includes a campus map as a reference point.  <br/>
<b>Team comment:</b> &#8220;We really wanted to emphasize simplicity.&#8221; 
</p>
