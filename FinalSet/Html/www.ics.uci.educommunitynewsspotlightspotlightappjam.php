<b>&#8220;Simple Scheduler&#8221;</b>
</p>
<p>
<b>Members:</b> Matthew Thornton (lead), Hongde Jin, Ryan Sullivant, Irvin Huang and Phillip Huang<br/>
<b>App description:</b> Allows students to input their classes and information/assignments pertaining to each class.  <br/>
<b>Team comment:</b> &#8220;Networking with our peers was one of the best parts of this project since we are now able to call upon each other for help or to hang out.&#8221; 
</p>
