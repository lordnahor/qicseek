<P>
Prospective and current UCI students interested in learning more about the Bren School’s degree 
options are encouraged to meet with the school’s associate dean of student affairs, counselors and 
student ambassadors. They will help you determine which of our majors and minors best support your 
academic strengths and interests. Call our Student Affairs Office at 949-824-5156 to make an 
appointment or to inquire about campus visit opportunities.  
<P>
