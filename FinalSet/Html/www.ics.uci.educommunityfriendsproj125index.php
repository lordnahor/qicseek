<p>Through Project:ICS, the Bren School's in-class internship program, your company can augment its IT staff for a 
10-week (1 Quarter Project),
20-week (2 Quarter Project), or 
30-week (3 Quarter Project)
partnering with dynamic faculty and a senior-level team of Bren School students to start, develop or complete an existing technology project.              </p>
<p>
Project:ICS is comprised of upper division classes designed to give our students real-world experience specifying, designing, constructing, testing and documenting complete projects in partnership with local companies, University offices and non-profit organizations. </p>
<p>This program and the courses associated with it compliment  the computer science curriculum by providing students an opportunity to experience an assignment motivated by industry needs and constrained by business demands.</p>
