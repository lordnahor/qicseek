<div class="section">
<h3>BUTTERWORTH COMPETITION 2012 FINALISTS:</h3>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="APPROVE" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Approve.jpg"/></p>
<p><b>&#8220;APPROVE&#8221;</b></p>
<p>Public transport (e.g., bus service) has always had the problem of delay associated with boarding time of passengers. There are hassles associated with the current system of paying by cash in the fare box or by card swiping. Approve is a mobile application that lets a passenger pay for the ticket or verify the details of the bus pass prior to boarding the bus, while waiting at the stop. There are several other features, like real-time location tracking and providing a platform for advertisements for the public transport authority, thus making the experience of public transport smart and comfortable. </p>
<p><b>Team Approve:</b> Venu Madhav Gundeti (leader), Pramit Choudhary, Swapnil Chavan </p>
<p><b>Mentor:</b> Channa Samynathan (B.S. &#8217;96, Bren School of ICS), Solutions Architect, Independent </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="DEVUE MUSIC" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Devue.jpg"/></p>
<p><b>&#8220;DEVUE MUSIC&#8221;</b></p>
<p>Devue aims to be a mobile platform through which users can submit etudes, scores, music pieces and other excerpts through a website. Through pitch detection from the mobile application, users will be able to receive instant feedback as to how well they played a piece. With Devue, the user can have access to potentially unlimited etudes to practice from, through the Devue store. Each etude is submitted by other musicians and can be accessed by other users to practice a special skill set, or just general playing skills. Devue offers a mobile, end-to-end solution that is revolutionary in music education. </p>
<p><b>Team Kiwi:</b> Jason J. Lee (leader), Juan Cortez, Kevin Lu, Albert Law,  Andrew Ouyang </p>
<p><b>Mentor:</b> Kojiro Umezaki, Assistant Professor, Music, Trevor School of Arts </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="OPTINERARY" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Optinerary.jpg"/></p>
<p><b>&#8220;OPTINERARY&#8221;</b></p>
<p>Optinerary (OPTimal itINERARY) is an activity recommendation and scheduling mobile application that generates optimized itineraries for business and time-constrained travelers on the go. The system optimizes itineraries based on information regarding different activities and spatio-temporal conditions; it analyzes users&#8217; travel information such as location, time, travel mode and preference settings to generate personalized itineraries with routing information for activities, including tourist attractions, local businesses (restaurants, shopping centers, etc.) and local events (concerts, sports games, festivals, etc.). Optinerary makes complex activity planning on mobile phones an easy task. </p>
<p><b>Team Optinerary:</b> Hang Liu (leader), Dmitri Arkhipov </p>
<p><b>Mentor:</b> Brian Dao (B.S. &#8217;03, Bren School of ICS), Managing Partner, Alpha Sprouts </p>
<p><b>Advisor:</b> Joseph Chow (Ph.D. &#8217;10, Samueli School of Engineering), Postdoctoral Scholar, UCI Institute of Transportation Studies </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="PICTORRIA" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Pictorria.jpg"/></p>
<p><b>&#8220;PICTORRIA&#8221;</b></p>
<p>Pictorria is a web-based image understanding service capable of analyzing a photo&#8217;s visual and contextual data and extracting information from it in order to facilitate the development of photo-related applications. Pictorria is capable of recognizing faces, people, landmarks, scenes, text and many other objects of interest. Users of our service can use this data to create personalized applications such as finding and matching a specific set of visual objects. </p>
<p><b>Team Pictorria:</b> Mohsen Hejrati (leader), Brian Russell Reinhart </p>
<p><b>Mentor:</b> Ramesh Jain, Bren Professor, Computer Science, Bren School of ICS </p>
<p><b>Advisors:</b> Mohammad Rastegari, Maryland University, and Matthew Redmond (B.S. &#8217;11, Samueli School of Engineering) </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="RIMS (RFID INVENTORY MANAGEMENT SYSTEM)" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Rims.jpg"/></p>
<p><b>&#8220;RIMS (RFID INVENTORY MANAGEMENT SYSTEM)&#8221;</b></p>
<p>Utilizing the latest in RFID technological development, Rims (RFID Inventory Management System) takes aim at industry standard inventory management for retail businesses. Rims is a revolutionary new form of inventory management and business solution that can completely alter the landscape of how products are managed and sold to consumers. Rims provides not only analytical statistics on products moving in and out of a store, but also provides live location-based inventory statistics. The beauty of Rims is that it not only provides retail businesses a new solution in managing inventory, but it also provides a gateway for consumers to view select inventory statistics (e.g., physical location in store and number of stock) at the discretion of the retail business. </p>
<p><b>Team ShakeNBake:</b> Minda Wang (leader), Manuel Yang, Jonathan Chang </p>
<p><b>Mentor:</b> Don Patterson, Associate Professor, Informatics, Bren School of ICS </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="SMARTHOME" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/SmartHome.jpg"/></p>
<p><b>&#8220;SMARTHOME&#8221;</b></p>
<p>SmartHome aims to provide an easier method for users to control their home appliance by their cell phones with a graphic user interface through telecommunication networks. It allows users to communicate with the appliance remotely and at their needs. </p>
<p><b>Team Megaline:</b> Zonglin Guo (leader), Zi-shun Huang, Xiaoyi Zhang </p>
<p><b>Mentor:</b> Ian Harris, Associate Professor, Computer Science, Bren School of ICS </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="WHOBOT" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/WhoBot.jpg"/></p>
<p><b>&#8220;WHOBOT&#8221;</b></p>
<p>WhoBot is a shopping application that gives users real-time news feed of products that their friends have recently purchased or wanted to recommend to their social group. It serves as a place where people come to talk about shopping only. Through WhoBot, users can create a social network with the use of their contact list and share their shopping adventures with their friends. This application aims to create a more reliable and enjoyable shopping experience for users. </p>
<p><b>Team WhoBot:</b> Maryam Khademi (leader), Bryant Pham </p>
<p><b>Mentor:</b> Don Patterson, Associate Professor, Informatics, Bren School of ICS </p>
