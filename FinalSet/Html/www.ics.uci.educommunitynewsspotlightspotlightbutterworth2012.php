<div class="section">
<h3>BUTTERWORTH COMPETITION 2012 WINNERS:</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="PORTFELO" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Portfelo.jpg"/></p>
<p><b>&#8220;PORTFELO&#8221; <br/> 1<sup>st</sup> place &#8212; $5,000</b></p>
<p>Portfelo is creating a new experience for people in the modeling industry (photographers, models, make-up artists, hair stylists and wardrobe stylists) to market their portfolio and network with each other. While current tools (such as Linkedin, Facebook, Twitter, 4ormat, ModelMayhem) solves a small portion of the challenges the industry currently experiences, Portfelo aims to be the solution to the industry&#8217;s pain points.</p>
<p><b>Team Portfelo:</b> Michael Cupino (leader), Ferdinand Lucero, Jordan Speer, Evan White, Danielle Yu</p>
<p><b>Mentor:</b> Tony Crisp (B.S. &#8217;92, School of Biological Sciences), CEO and Brand Strategist, CRISP Brand Agency</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="SPAWND" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Spawnd.jpg"/></p>
<p><b>&#8220;SPAWND&#8221; <br/> 2<sup>nd</sup> place &#8212; $3,500</b></p>
<p>Spawnd is a social networking website for online gamers that couples the concept of online social networking with an innovative incentive where the more you play, the more you can earn discounts and rewards on your favorite games and accessories. Spawnd works to tear down console-to-console gaming barriers through its patent-pending universal &#8216;Spawnd Point&#8217; system that allows gamers to compare stats and rankings across different gaming platforms. Users of Spawnd can surf profiles of gaming friends, get the latest gaming news, and even host custom tournaments all while racking up &#8216;Spawnd Points&#8217; that can be allocated toward rewards and discounts.</p>
<p><b>Team Spawnd:</b> Rohan Venapusala (leader), Ryan Reyes, Bellal Labanieh, Naren Sathiya, John Junyszek </p>
<p><b>Mentor:</b> Ash Kumra, Co-Founder of DesiYou and DreamItAlive.com </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="M-PRESCRIPTION" class="right" src="../../../bin/img/photos/spotlight/butterworth_2012/Mprescription.jpg"/></p>
<p><b>&#8220;M-PRESCRIPTION&#8221; <br/> 3<sup>rd</sup> place &#8212; $1,500</b></p>
<p>M-prescription is an alternative solution for prescription transaction. It uses a patient&#8217;s mobile phone to store prescription information, instead of paper or an Internet server. In this way, M-prescription provides more security and privacy protection, as well as convenience, to users (patients). The prescribers can also benefit from its simplified work flow and prescribing decision support. In addition, the pharmacies are less dependent on Internet connection during prescription transactions; thus, a more robust transaction base is achieved. Moreover, the pharmacies that are outside the M-prescription network can also use its service through land call or multimedia messaging service. </p>
<p><b>Team M-prescription:</b> Jianlin Shi (leader), Chandra Bhavanasi, Jeffrey Tse </p>
<p><b>Mentor:</b> Channa Samynathan (B.S. &#8217;96, Bren School of ICS), Solutions Architect, Independent </p>
