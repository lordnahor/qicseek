<p><b>Annual student contest spotlights bright ideas</b></p>
<p>
Ten teams of future entrepreneurs gathered May 17 in Bren Hall to compete in the final round of the 9th Annual Butterworth Product Development Competition. Organized by the Donald Bren School of Information &amp; Computer Sciences, the contest promotes the creation of commercially viable technology and products while fostering entrepreneurship among undergraduate and graduate students. 
</p>
<p>
<!-- -->
<!-- -->
<iframe frameborder="0" height="260" src="http://www.youtube.com/embed/t3aU2tstm-g" width="450"></iframe>
<!-- -->
<!-- -->
</p>
<p>
The competition began in January with 19 teams and more than 60 students. Its namesake, Paul Butterworth (B.S. &#8217;74, M.S. &#8217;81), is chief technical officer of AccessG2 Inc.
</p>
<p>
&#8220;This was, by far, the most impressive group of competitors we&#8217;ve seen over the last nine years,&#8221; said judge Kevin Kinsey, CEO and co-founder of Netreo, who has been involved with the Butterworth Competition since it debuted in 2003. &#8220;Each of the finalists gave me a sense that they were trying to solve a real need.  It was a tremendously difficult decision to come up with the Top 3.&#8221;
</p>
