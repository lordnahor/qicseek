<div id="content_title">UC iSchools Workshop spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>iSchools Insight</strong></p>
<p><img alt="photo: uc ischools 2008 workshop" height="266" src="../../../bin/img/photos/people/ischools2k9.jpg" width="470"/><br/>
		  <i><strong>More than 50 doctoral students from three UC campuses discussed a variety of issues at the inagural UC iSchools Workshop.</strong></i>
		</p>
<hr/>
<p>More than 50 doctoral students representing UC Berkeley&#8217;s School of Information, UCLA&#8217;s Department of Information Studies, and UC Irvine&#8217;s Donald Bren School of Information and Computer Science, which make up the University of California iSchools, <a href="http://uc-ischools.differenceengines.com/">held the inaugural UC iSchools workshop</a> January 22 through the 24. </p>
<p>iSchools are emergent academic programs committed to understanding the role of information in human endeavors and nature. The schools conduct research into the fundamental aspects of information and information technologies and into the relationships between people, information, and technology. </p>
<p>In the United States and Canada, 19 information schools have joined together to form the "<a href="http://www.ischools.org/">iSchool Project</a>", which defines iSchools as sharing a fundamental interest in the relationship between information, technology and people. </p>
<p>Attendees discussed their individual research interests with their peers, received feedback, learned about different approaches to information studies in the different schools, and collaborated in mini workshops on potential research agendas for some of the challenging questions facing information studies.</p>
<p>"I absolutely loved the event. It is not every day that Ph.D. student can put down her everyday work (meetings, class, teaching, and --particularly-- email) to engage with other students who share a
  commitment to interdisciplinary research," Daniela Rosner, a Ph.D. student at UC Berkeley's School of Information said. "This was a very supportive 
  group. I found most people eager to discuss each others topics and
  listen closely without the pressures of time or other investments."</p>
<p> The students research and academic interests spanned a wide range of diverse topics, but they shared a desire to shape the interdisciplinary fields of research that are found in information schools and the workshop fostered opportunities for future collaboration.</p>
<p>"I've been sharing papers and recommendations of related research," Lilly Irani, a Bren School doctoral student said. "The workshop also helped me initially get to know some students with whom I've had great research conversations in the last few weeks. I think I'll be collaborating with some of these folks for many years to come.&#8221;</p>
<p><strong>IMPROMPTU INCEPTION</strong></p>
<p>The workshop came out of a casual conversation between Bren School of ICS Dean, Debra Richardson, Dean of UC Berkeley's School of Information, Anna Lee Saxenian, and graduate students Megan Finn and Dan Perkel at the 2008 iSchools Conference at UCLA. </p>
<div class="highlight_box_left"> <a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni. </div>
<p>Finn was able to secure $5,000 in seed funding for planning from Bears Breaking Boundaries (a Berkeley fund for encouraging cross-department and cross-campus collaborations) additional support from Intel Research, Yahoo! , the Institute for Software Research at UCI, and the three schools made it possible for students to attend without any out-of-pocket expenses.</p>
<p>Richardson volunteered UCI's Bren Hall since its facilities were flexible and spacious -- a conductive environment for the collaborative activities planned for the workshop.</p>
<p>"We wanted to keep the workshop small to encourage a close atmosphere and lively conversation. Since we hadn't put on such a workshop before, we thought it best to start small," Irani said.</p>
<p><strong>INNOVATION, COLLABORATION AND STIMULATION</strong></p>
<p>Following an evening of informal introductions on the 22nd, participants spent the next two days getting a better sense of the breadth of research being conducted at these three different schools and wrestling with important intellectual issues. </p>
<p>The workshop was divided up into three sections: activities focused on learning about one another's research and providing peer feedback, small group topic workshops on thematic topics whose results were presented to and discussed with the larger body of participants, and reflecting on the workshop and projecting future challenges we faced as a research community.</p>
<p>The topic workshops reflected the diversity of intellectual exploration and debate in the iSchools. Sessions included explorations of design as a form of knowledge production, politics of information systems, privacy, social theory in information studies, and sociotechnical studies of crisis.</p>
<p>Irani presented on Turkopticon, a project investigating labor practices and politics of interface design that she has been working on with fellow UCI graduate student Six Silberman. She also participated in the social theory workshop group to develop ideas about how to rigorously use different theories in research. </p>
<p>"I really enjoyed talking with students from other schools, sharing ideas, receiving feedback on my work, and learning from the various bodies of theory and methods that we learn in our different schools," Irani said. "The workshop helped me meet people with similar research interests with whom I can have ongoing conversations."</p>
<p>Due to the positive feedback of the attendees and higher than expected attendance, there are currently plans to hold a similar event next year.</p>


