<p><b>Students, faculty recognized for outstanding contributions to undergraduate instruction</b></p>
<!-- -->
<!-- -->
<!-- -->
<p>
<img alt="Kay Family Foundation Innovation Lab" class="left" height="300" src="../../../bin/img/photos/spotlight/celebration_of_teaching_2013/group.jpg" style="notpadding: 0.5em;" width="450"/>
<br/>
<small>
Celebration of Teaching honorees Jed Brubaker, Eric Hennigan and Don Patterson with Dean Hal Stern</small>
<!-- -->
<!-- -->
<!-- -->
</p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<p>
Three members of the Bren School community were honored at UC Irvine&#8217;s <a href="http://www.tltc.uci.edu/teachingAwards2013.html">20th Annual Celebration of Teaching</a>. Hosted by the Dean of the Division of Undergraduate Education and the Teaching, Learning &amp; Technology Center (TLTC) since 1993, the Celebration of Teaching event recognizes faculty, lecturers and teaching assistants (TAs) throughout the UCI campus for their outstanding contributions to undergraduate instruction. 
</p>
<p>
The 2013 Bren School honorees are:
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="Jed Brubaker" class="right" src="../../../bin/img/photos/spotlight/celebration_of_teaching_2013/Jed.jpg"/>
<b>
Jed Brubaker <br/> 
Most Promising Future Faculty Member
</b>
</p>
<p>
Jed Brubaker, a Ph.D. student in the Department of Informatics, was selected as one of two most promising future faculty members. The award comes with a one-quarter dissertation fellowship and fee waiver from the Graduate Division dean. Recipients of this award are judged on their excellence in teaching, research and service. 
</p>
<p>
Brubaker studies digital identity, social media and human-centered computing with a focus on death on social network sites. His recent projects include a study of social network profiles during the three years after their owners die; a linguistic analysis of online expressions of grief; and an interview-based study exploring the impact of Facebook on how individuals encounter and engage with death.
</p>
<p>
His advisor, associate professor Gillian Hayes, says Brubaker&#8217;s work has inspired a wide variety of research and design projects addressing the impact of encountering information about people&#8217;s deaths through online social networking sites. 
</p>
<p>
As for Brubaker&#8217;s teaching style, Hayes adds: &#8220;Jed&#8217;s lectures are dynamic, engaging and extraordinarily popular. He has a way of presenting highly theoretical material in a manner that is engaging for undergraduate students. The students with whom he has worked all speak very highly of him, and many have gone or are applying to graduate school.&#8221;
<!-- -->
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="Eric Hennigan" class="right" src="../../../bin/img/photos/spotlight/celebration_of_teaching_2013/Eric.jpg"/>
<b>
Eric Hennigan <br/>
Pedagogical Fellow
</b>
</p>
<p>
Eric Hennigan, a Ph.D. student in the Department of Computer Science, was honored for his involvement in the Pedagogical Fellows Program. Pedagogical Fellows are a group of productive apprentice scholars who have a keen interest in providing superlative instruction in their roles as TAs. They also plan and lead training for more than 400 fellow UC Irvine TAs each fall. Fellows are selected through a competitive process based on their teaching evaluations, letters of recommendation and demonstrated interest in pedagogy. 
</p>
<p>
TLTC associate director Chris O&#8217;Neal says this year&#8217;s Pedagogical Fellows &#8220;are an outstanding group of graduate students, scholars, teachers, university servants &#8212; and just an all-around great group of people.&#8221;
</p>
<p>
Hennigan works in the Secure Systems and Software Lab, where his research involves securing JavaScript, so that browsing the web can be made safer.
<!-- -->
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="Don Patterson" class="right" src="../../../bin/img/photos/spotlight/celebration_of_teaching_2013/Don.jpg"/>
<b>
Don Patterson <br/>
Excellence in Undergraduate Teaching
</b>
</p>
<p>
Associate professor of informatics Don Patterson was recognized as a Bren School Dean&#8217;s honoree for Excellence in Undergraduate Teaching.
</p>
<p>
Hal Stern, the Ted and Janice Smith Family Foundation Dean, says Patterson is known as a passionate and dedicated teacher. &#8220;Don&#8217;s students are very enthusiastic about his courses because they receive practical information and they recognize that he cares about their learning,&#8221; Stern says. &#8220;When asked how Don could improve his teaching, a majority of the students say he can&#8217;t. In fact, one asked, &#8216;How can you be more awesome than awesome?&#8217; &#8221;
</p>
<p>
Patterson directs UC Irvine&#8217;s Laboratory for Ubiquitous Computing and Interaction. His current research interests focus on context-aware computing and how to make a computer operate appropriately when it leaves the office and moves into the greater world.</p>


