<div id="content_text">

<p><strong>Best of Both Worlds    </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: michelle ang" src="../../../bin/img/photos/people/michelle_ang.jpg"/>
<p>Michelle Ang flashes the <br/>Rip'em Eaters sign. 
</p>
</div>
<p>In high school Michelle Ang had the opportunity to take AP Computer Science and found herself inspired. </p>
<p>&#8220;I like how computer science allows the freedom of choosing how I wanted to solve problems and still be able to arrive at the correct answer,&#8221; Ang said.</p>
<p>But when it came time to pick a college major, Ang, a fourth year student, didn&#8217;t know if she wanted to concentrate on computer software or hardware.</p>
<p>Luckily UC Irvine made the San Diego native's decision easy by offering the <a href="../../../ugrad/degrees/degree_cse.php">Computer Science and Engineering (CSE) major</a>. </p>
<p><strong>A PERFECT FIT </strong></p>
<p>&#8220;When I saw CSE on the application, I thought it was perfect because it incorporated both choices,&#8221; she said. &#8220;I do not regret choosing CSE at all because as it turns out it was exactly what I wanted to study.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Offered jointly by the Bren School of ICS, the first independent computer science school within the UC system, and The Henry Samueli School of Engineering, the Computer Science and Engineering major gives students access to multidisciplinary problems with a focus on total systems engineering.</p>
<p>It is designed to provide students with the fundamentals of computer science, both hardware and software, and the application of engineering concepts, techniques and methods to both computer systems engineering and software system design. </p>
<p>&#8220;My advice to students who are considering CSE is to just try it,&#8221; Ang said. &#8220;Take a couple of the required classes and get a feel for the major.  If you decide it&#8217;s not to your liking then the classes you have taken might be able to lead you into what you really are interested in.&#8221;</p>
<p>Ang credits the classes in the major for providing her an opportunity to integrate and gain hands-on-experience with a lot of CSE concepts. </p>
<p>She has also had the opportunity to expand her knowledge by doing research with professor Ian Harris on VOIP (Voice-over IP) technology and security. </p>
<p>&#8220;My responsibility was to install and learn about Asterisk, an open-source PBX software that networks VOIP phones,&#8221; Ang said. &#8220;We are now at the stage of figuring out what we want to test on our new Asterisk server.&#8221;</p>
<p><strong>OUTSIDE THE LAB </strong></p>
<p>Ang has also become actively involved in the <a href="http://www.acmuci.org/">Association for Computing Machinery (ACM)</a> student group at UCI. </p>
<p>ACM is an event-based organization that helps expose students to the technology industry by holding information sessions and tours of local technology companies.</p>
<p>&#8220;I found ACM to be a very helpful organization. ACM has given me the chance to explore and meet different companies that are in the technology industry which improved my networking skills,&#8221; Ang said. &#8220;ACM also allowed me to meet more of my peers and I am really glad that I was able to meet such incredible people.&#8221; </p>
<p>As the ACM publicist, Ang is actively involved in publicizing the group&#8217;s events to the student body. </p>
<p>&#8220;I was really happy to see how many people came out to our Google Tech Talk in October and I hope that our other events are just as successful,&#8221; Ang said.</p>
<p>ACM also holds events that allow students the chance to interact with other students and professors. </p>
<p>Ang also does outreach for the <a href="http://www.ics.uci.edu/&#126;wics/">Women in Information and Computer Sciences (WICS)</a> group on campus, and is a member of the <a href="http://www.geocities.com/uci_esc/">Engineering Student Council (ESC)</a> service committee and <a href="http://www.phisigmarho.org/">Phi Sigma Rho</a>, a national science and engineering sorority. </p>
<p>&#8220;It took me a while to learn how to balance my time between school and fun but once you've learned to manage, everything just comes together,&#8221; Ang said. </p>
<p>Ang is especially excited about the re-opening of the Zot Zone which has been closed since the start of the student center expansion. </p>
<p>"The old Zot Zone had so many pool tables and my friends loved the Dance Dance Revolution machine they had,&#8221; Ang recalled. "I just hope that the new Zot Zone will be able to live up to our experience in the old one."</p>
<p><i>- Eric Kowalik </i> </p>


