<div id="content_title">About Donald Bren</div>

<blockquote>
<div id="content_text">

<div class="img_caption_left">
<img alt="photo::donald bren" height="174" src="../../bin/img/heads/bren.jpg" width="140"/>
<p>
Donald Bren
</p>
</div>
<p>Donald Bren is Chairman of the <a href="http://www.irvinecompany.com/">Irvine Company</a>, one of the most respected and diversified privately held real estate companies in America.</p>
<p>Founded more than 140 years ago, the company is known for its best-of-class portfolio of premier California real estate including office, apartment, retail, resort and new housing properties in Orange County, San Diego, Los Angeles and Silicon Valley.</p>
<p>Bren is a leader and innovator within the real estate industry, one of the first to combine well-designed homes with such amenities as parks and substantial open space, schools, employment centers and shopping centers to create balanced master-planned communities.</p>
<p>Through his accomplishments, he has achieved a reputation as a national expert in the interrelated fields of planning, design, construction, marketing and finance.</p>
<p>Bren is a nationally prominent philanthropist, with special focus on education, conservation and research, and has directed more than $200 million to support K-12 public schools in Southern California and other institutions such as University of California at Irvine and UC Santa Barbara.  </p>
<p>He is consistently ranked high on BusinessWeek magazine&#8217;s list of &#8220;The 50 Most Generous Philanthropists&#8221; in the nation.  In 2008, the magazine placed Bren at No. 9, citing the more than $907 million he has given or pledged since 2003.  His lifetime giving exceeds $1.3 billion, according to the magazine&#8217;s estimates.  </p>
<p>&#8220;Bren&#8217;s commitment to education runs the gamut from students to principals to school districts to university scholars on (The) Irvine Ranch,&#8221; BusinessWeek noted. </p>
<p>Bren&#8217;s extensive educational and research philanthropy includes funding endowed distinguished professor chairs at Caltech, the University of California, Chapman University and Marine Corps University.  </p>
<p>At the University of California, his support has created more endowed chairs than any other single donor in UC history.  In total, he has endowed 50 chairs for distinguished faculty and researchers at universities and research institutions.  </p>
<p>In recognition of his wide-ranging support for the UC system, Bren was awarded in 2004 its highest honor, the University of California Presidential Medal. </p>
<p>&#8220;For well over two decades, Mr. Bren&#8217;s focus on the creation of endowment funds to support faculty chairs has helped bring to the University of California some of the best researchers and scholars in the nation and the world,&#8221; former UC President Robert Dynes said when Bren was awarded the UC Presidential Medal.  &#8220;His passionate philanthropy and commitment to educational excellence have helped strengthen the university.&#8221;</p>
<p>Bren&#8217;s contributions to public schools on The Irvine Ranch and elsewhere in Orange County and Southern California comprise major gifts that are benefiting tens of thousands of children.  </p>
<p>For example, in a gift that underscored Bren&#8217;s passion for public education and desire to help children succeed, in 2008 he gave $8.5 million to THINK Together, one of the largest gifts ever to an after-school program in California history.  </p>
<p>The gift brought to $10 million Bren&#8217;s contributions to the innovative and successful program, which helps at-risk children in Eastern Los Angeles County and Santa Ana and other Orange County schools.  </p>
<p>Meanwhile, Bren gave $20 million to local schools to protect and expand enrichment programs in music, art and science&#8212;programs he believes are vital to a child&#8217;s well-rounded academic and personal development. </p>
<p>Bren for many years has been at the forefront of efforts to preserve environmentally sensitive land in Southern California, a commitment that was recognized in 2006 with the designation of much of The Irvine Ranch&#8217;s open space and parks as a National Natural Landmark by the U.S. Department of the Interior.  In 2008, the land was designated as the first California Natural Landmark by the State of California.</p>
<p>Bren has protected more than half of the 93,000-acre ranch&#8212;more than 50,000 acres&#8212;as permanent open space, parks and trails.  </p>
<p>His conservation efforts have protected a wide range of endangered plant and animal species, while expanding the public&#8217;s access to and enjoyment of the land.  </p>
<p>Bren also has committed $50 million for the long-term management, preservation and restoration of the natural resources on The Irvine Ranch, efforts now being carried out by the Irvine Ranch Conservancy, which Bren created.  </p>
<p>Its mission also is to protect and enhance the lands while increasing the public&#8217;s opportunity to enjoy them.  These beautiful, permanently protected lands represent one of the nation&#8217;s largest natural and recreational resources located within minutes of millions of urban residents. </p>
<p>Bren is on the board of trustees of the University of California, Irvine Foundation, the Los Angeles County Museum of Art, the Orange County Museum of Art and California Institute of Technology.  </p>
<p>In 2007, he was elected a Fellow of the prestigious American Academy of Arts &amp; Sciences in the category of Business, Corporate and Philanthropic Leadership&#8212;Private Sector.  </p>
<p>Bren holds a bachelor's degree in business administration and economics from the University of Washington.</p>


</div>
</blockquote>
