<div id="content_title">Amber Israelsen spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>Fueling a Passion  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: amber israelsen" height="166" src="../../../bin/img/photos/people/amber_israelsen.jpg" width="171"/>
<p>
Amber Israelsen
</p>
</div>
<p>Amber Israelsen, a senior information and computer sciences (ICS) major, had previously attended college with an emphasis in math and physics, but did not have a clear idea of what she wanted to do with it.</p>
<p>This indecision led her to leave school and work for several years in a variety of industries. It was during these journeyman years, that she discovered her passion for computers. </p>
<p>"In all my jobs, I worked with computers in some way and found that I really enjoyed it," Israelsen said. "I quickly became the go-to person for anything computer related, and found myself tinkering around on the computer in my spare time."</p>
<p>After a few years working with computers, the twenty-eight year old Israelsen decided that she wanted to go back and get her degree, this time in computer science. </p>
<p>"This was shortly after the Internet bubble popped, but given the cyclical nature of such trends, and given that computers are not going away anytime soon, I felt that the timing would actually put me in a good position to catch the next up-cycle when I was done with my degree," Israelsen said.</p>
<p>Her prior experience in Orange County's commercial real estate market was one of the factors that lead her to focus on UCI as a place to get her degree. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>"At the time, I could see that Orange County was, and would continue to be, a high-growth area, attracting a variety of companies, many of which would have a focus on technology," Israelsen said. "I felt that UCI was uniquely positioned to be a major player in this technological growth."</p>
<p>But Orange County's future as a high-growth area wasn't the only thing that attracted Israelsen to UCI. She did quite a bit of research on the standing of UCI and its computer program. </p>
<p>Though the program's high ranking played a role, she was impressed with the program's goal of becoming a top ten school. Donald Bren's $20 million transformational gift also helped confirm Israelsen's decision to attend the Bren School.</p>
<p>"If someone with his vision and influence was making such a substantial investment in the future of technology in Orange County, I knew I had made the right decision." Israelsen said. </p>
<p><strong>HANDS ON LEARNING</strong></p>
<p>Israelsen hasn't been disappointed with her choice and enjoys the diversity of the student body which allows her to share perspectives with students from different countries and backgrounds. </p>
<p>She has also found a higher caliber of professors at UCI compared to her previous school experiences.</p>
<p>"Collectively, they bring an impressive blend of experience in cutting-edge research and industry that I think really adds to the overall experience at UCI." Israelsen said.</p>
<p>Nearing graduation, Israelsen said her biggest take-away lesson is that school should teach you how to learn, and teach you how to think. </p>
<p>"In computer science especially, you will never know everything. And even if you really do know everything today, it will all change tomorrow," Israelsen said. "You should not leave school having memorized a lot of useless facts and information. Rather, you should leave with the ability to assimilate information, learn quickly, and apply what you've learned in a variety of situations." </p>
<p>Project ICS, one of three project courses Bren School of ICS students must take to graduate, reinforced her belief.</p>
<p>The senior-level software development class is designed to give students real-world experience specifying, designing, constructing, testing and documenting complete software systems in partnership with local companies, university offices and non-profit organizations. </p>
<p>Israelsen and her team worked with WebReach, a local technology company, in creating a prescription reminder service for patients.</p>
<p>"Our project essentially served as a proof of concept," Israelsen said. "WebReach wanted to determine if such a system could be built and what, if any, obstacles would need to be overcome in order to do so." </p>
<p>Called the RSR Project, its purpose is to help increase adherence and compliance within the healthcare industry, thus helping to reduce the overall costs of healthcare and specifically the costs of prescriptions.</p>
<p>Using the RSR system, doctors can create a prescription, and also set up a personalized reminder that will be sent to the patient when it is time to take their medication. </p>
<p>The reminders are sent as a text message to the patient's cell phone, or as an instant message. The patients can reply to these messages, letting the doctor know if they took their medication or not, which will alert the doctor to any problems the patient might be having with their treatment plan.</p>
<p>"Our team was able to demonstrate that it is technically feasible and, at this point, WebReach will determine if further development makes sense from a business standpoint." Israelsen said. </p>
<p>Israelsen found her Project ICS experience to be very practical, not only because she learned a lot of new technical skills, but also because it forced her to work on the "soft skills", like teamwork and communication, too. </p>
<p><strong>RETURN TO THE REAL WORLD</strong></p>
<p>In part because of her experience with Project ICS, Israelsen hopes to become a consultant after graduation. She is particularly interested in how technology relates to business and seeing the big picture of how it can help business run more efficiently. </p>
<p>When not working to meet a Project ICS deadline, Israelsen, a Snowflake, AZ native (the name actually comes from its two founders, Erastus Snow and William Flake, not because of the fair amount of snow it receives) can be found on a bench or rock in Aldrich Park with a cup of coffee in hand enjoying the solitude. </p>
<p>She also tries to find time to keep up with some of her many hobbies, such as hiking, traveling, flying planes, writing, reading, music, astronomy, culinary arts, painting, drawing, gardening, marathons and yoga.  She also recently became involved with Big Brothers Big Sisters of Orange County.</p>
<p>Preparing to return to the real world once again, Israelsen offers future students these parting insights for a more successful college experience. </p>
<p>- In most cases, avoid giving advice unless and until it's asked for. </p>
<p>- Even if you're in computer science, learn how to write, and write well. </p>
<p>- Think.  Have opinions.</p>
<p>- Get an internship or work experience as soon as you can.  School is often very different from life in the real world, and you need to be prepared before you graduate.</p>
<p>- If you push yourself, you can do a lot more than you thought you could.</p>
<p>- Don't get so caught up in your major that you forget that there are other things out there.  The world is full of more experiences than you can begin to imagine.  Make sure that when it comes time to leave this world, you have tried as many of them as you possibly could have.</p>
<p><i>- Eric Kowalik</i></p>


