<div>
<p>Light, J. (1999). When computers were women, <i>Technology and Culture</i>; Jul 1999; 40, 3;</p>
<p><a href="http://labweb.education.wisc.edu/steinkuehler/elpa940/readings/Light.pdf">http://labweb.education.wisc.edu/steinkuehler/elpa940/readings/Light.pdf</a></p>
<p>This article relates the history of women&#8217;s involvement in
the ENIAC, the first electronic computer in the United States.</p>
</div>
<div>
<p>Hill, C., Corbett,
C. and St. Rose, A. (2010) Why So Few: Women in Science, Technology,
Engineering, and Mathematics. Washington D.C.: American Association of American
Women. </p>
<p><a href="http://eric.ed.gov/PDFS/ED509653.pdf">http://eric.ed.gov/PDFS/ED509653.pdf</a></p>
<p>This study shows that in elementary and secondary schools,
math and science courses are taken equally by girls and boys and about the same
percentage leave high school prepared. But there is a far smaller proportion of
women declaring computer science as a major and graduating with bachelor&#8217;s
degrees.</p>
</div>
<div>
<p>Hyde, J. and Linn, M. (2006). Gender Similarities in
Mathematics and Science. <i>Science</i> 27
October 2006. Vol. 314 no. 5799 pp. 599-600</p>
<p><a href="http://www.sciencemag.org/content/314/5799/599.summary">http://www.sciencemag.org/content/314/5799/599.summary</a></p>
<p>This Science article discusses how meta-analyses of various
measures basically show no differences between girls and boys in math and
science performance yet girls comprise only 17% of assistant professors in
engineering and earn only 15% of Ph.Ds in engineering. The authors argue that
we need to increase awareness of gender similarities in order to not discourage
girls from entering STEM fields.</p>
</div>
<div>
<p>Berrett, D. (2011). What Spurs Students
to Stay in College and Learn? Good Teaching Practices and Diversity. <i>The Chronicle of Higher Education</i>.</p>
<p><a href="http://chronicle.com/article/What-Spurs-Students-to-Stay-in/129670/">http://chronicle.com/article/What-Spurs-Students-to-Stay-in/129670/</a></p>
<p>The title says it all.</p>
</div>
