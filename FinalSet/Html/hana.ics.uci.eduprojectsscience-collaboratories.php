<h1>The Science of Collaboratories</h1>
<p>Science has always been a form of what we now call &quot;distributed knowledge work.&quot; Scientists were among the first to recognize the potential of emerging information and communication technologies. For instance, electronic mail first became widespread within scientific sub-communities. As additional networked tools became available a more coherent vision has emerged of how technology-mediated science can be conducted. By the late 1980s the concept of a collaboratory was being discussed at places like the National Science Foundation and the National Research Council. Collaboratories were defined as a &quot;…'center without walls,' in which the nationals researchers can perform their research without regard to geographical location [Wulf, 1989].&quot; The vision was that scientists who are geographically dispersed could work together using appropriate technology to access each other, remote tools, databases, and instruments (National Research Council, 1993; Kouzes, Myers &amp; Wulf, 1996; Finholt &amp; Olson, 1997). </p>
<p>Over the past twenty years there have been a series of collaboratory projects funded by NSF, DOE, NIH, and other agencies, some successful and some less so. These projects provide us with a base of experience from which we have begun to form generalizations about the conditions for success. These projects have demonstrated the promise of the vision. Indeed, it is feasible and useful to use networks to link teams of people, data, tools, and facilities to reduce the barriers of time and distance. </p>
<p>However, the design, deployment, and adoption of new collaboratories remain difficult and uncertain processes. Each collaboratory has been built as an independent effort. Since these efforts involved complex responses to often idiosyncratic mixtures of social and technical factors, general lessons about collaboratory design remain elusive. The large effort required to produce the first prototype collaboratories has not allowed careful reflection about broader principles of collaboratory development. These principles are needed to expand collaboratory use beyond narrow application in a few scientific fields.</p>
<p>We seek to change this. We aim to define, abstract, and codify the broad underlying technical and social elements that lead to successful collaboratories. We seek to synthesize the vocabulary, associated principles, design methods, and technical infrastructure for propagating and sustaining collaboratories across a wide range of circumstances. Our goal is for users with a need for collaboratory infrastructure to be able to create successful collaboratories on their own. </p>
<p>There are two major parts to this effort.  The database, described and accessible here, attempts to collect a standard set of information on each of the collaboratories we have found.  We have found, interestingly, a growing set of collaboratories in the humanities as well as science and engineering.   Second, we have compiled a set of factors that we believe must be in place if a collaboratory is to succeeds.  This is now embodied in an online assessment tool, known as the Collaboration Success Wizard.&lt;link&gt;</p>
<p>The Science of Collaboratories data base contains:<br />
  •	Name<br />
  •	URL<br />
  •	Start date and end date if applicable<br />
  •	Type of collaboratory<br />
  •	Domain<br />
  •	A brief description<br />
  •	What it offers (access to instruments, information resources, people)<br />
  •	Funding agency<br />
  •	Number of people participating and their general roles<br />
  •	What communication tools they use<br />
  •	Technical capabilities<br />
  •	Key publications<br />
  •	Representative images (where found)</p>
