<p><strong>Supernatural conclusions  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: robert olson" height="133" src="../../../bin/img/photos/people/jessica_utts.jpg" width="200"/>
<p> Jessica Utts
</p>
</div>
<p>If members of the scientific community once debated the existence of psychic abilities, Jessica Utts&#8217; research has helped put the argument to rest. </p>
<p>By applying statistical analyses to paranormal phenomena such as extra-sensory perception, telepathy and precognition, Utts has shown that paranormal phenomena occur more often than can be explained by random chance, and she has created a basis for moving on to a deeper understanding of such phenomena.</p>
<p>&#8220;Parapsychology gives us a clue into what we don&#8217;t know about physics &#8211; time and space as well as human consciousness and the brain,&#8221; says Utts, a statistics professor who joined UC Irvine&#8217;s information and computer science faculty in July.</p>
<p>Utts was first exposed to parapsychology in 1984 while serving as a statistical consultant for a team researching the subject for the government.</p>
<p>&#8220;I got more and more interested as I started seeing results that seemed to indicate something was going on,&#8221; Utts says.</p>
<p>She gained recognition in 1995 when she prepared a report for Congress and the American Institutes on Research on the formerly classified Stargate Project that sought to determine whether psychic functioning or &#8220;remote viewing&#8221; could be used to gather intelligence.</p>
<p>Utts&#8217; report concluded that some psychic functioning was being exhibited and that more study should be done to make it as useful as possible. A parallel investigation by another researcher came to the opposite conclusion and the project, funded by the Central Intelligence Agency and Defense Intelligence Agency, was discontinued.</p>
<p>Utts has since continued her research to statistically demonstrate that some people have psychic abilities &#8211; that they can, for example, correctly identify a hidden picture more often than one would expect by chance alone.</p>
<p>&#8220;My guess is everyone has some psychic ability &#8211; much like everyone has some degree of musical talent,&#8221; she says. &#8220;Why can some do it better than others? I have no idea any more than I have an idea of why some are naturally better musicians.&#8221;</p>
<p>Until recently, Utt says, scientific researchers have described psychic insights as something sent and received. Telepathy and ESP, for example, are defined as the ability to pick up thoughts transmitted through the air, much like radio waves.</p>
<p>Utts&#8217; research suggests something beyond send-and-receive abilities. She has statistically verified the existence of precognition &#8211; a phenomenon where the subject accurately identifies an image or idea even before the tester or a computer has selected the target.</p>
<p>&#8220;The fact that some people can predict targets before they are even chosen is something different,&#8221; she says. &#8220;This tells me we don&#8217;t fully understand the concept of time. These areas hold huge mysteries we still need to solve.&#8221;<br/>
                <br/>
                <i>&#8212; Jason Mednick, University Communications</i></p>


