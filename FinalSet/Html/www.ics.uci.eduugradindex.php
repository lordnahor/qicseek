<p><strong>Software Engineering, (Major)</strong><br/>
The Software Engineering major prepares students to be productive members of software engineering teams in a variety of application domains, with focus on the domains of major importance within each decade. It combines a solid foundation in computer science with knowledge of how to engineer modern software systems, and how to function within teams. The major emphasizes the design and implementation of large software systems.
