<p>The Bren School is the only computer science school in the University of California and one of only a handful at top-ranked universities in the nation. </p>
<p>With this comes a reputation for excellence. An education here provides you with a blend of science, scholarship, technical skills, and practical experience that prepares you to achieve your dreams.</p>
<p>With over 70 faculty members in three departments - <a href="../../computerscience/index.php">Computer Science</a>, <a href="../../informatics/index.php">Informatics</a>&#160;and <a href="../../statistics/index.php">Statistics</a>  - the Bren School is in the unique position to offer <a href="degrees/index.php">eight modern undergraduate degrees</a> in computing, and several minors. </p>
<p>These programs are dynamic and continually updated to stay ahead of future trends and technological advances. </p>
<p><a href="courses/index.php">We add new courses</a> to our curriculum with exciting topics such as bioinformatics, computer security, gaming, and more.</p>
<p>Computer science now is as exciting as ever. Despite fears of offshoring, 2 million new <a href="https://jobs.ics.uci.edu/">information technology jobs</a> will be created in the United States over the next ten years. </p>
<p>Imagine the opportunities available to you.</p>
