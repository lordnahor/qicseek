            <div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
				Nappkin is a mind-mapping application for the Android environment. It allows you to note down any ideas that spontaneously come, any ideas that you are not familiar with and want to find out more about, or anything that you want to get other peoples opinions on. You can then use the knowledge of others to self-improve on the topic of interest. With Nappkin, there is an intuitive interface in which the user can create a "bubble" about any topic by tapping on the screen. This "bubble" can then be sent to anyone in your contact list and they have the ability to add their insights and comments. These will then become connected to original bubble and you will have a map of ideas.
				</p>

