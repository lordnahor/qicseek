			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
				We first made all team members install Eclipse with Android SDK with Platform 2.1, 2.3 and 4.0 along with Subclipse that would be required for subversion and for a group of people editing the same code. The idea of Nappkin was decided on the first day, but we discussed the GUI a little and agreed upon rounded rectangles for text to go in and translate functionality to scroll through the map. We broke off into two teams, a back-end and front-end group that would focus on their respective tasks. The back-end group worked on the server-client interaction, on how to send the map to specified users in the contact list as well as sending updated maps to the involved contacts. The front-end group worked on the GUI from a blank slate. They also worked on gesture control, canvas, the bubbles for the ideas, and the actual application that would go on the Android device. We finished the front-end part first and ran into a lot of trouble on client-server notifications. We finally made a breakthrough with OSC (sockets) and then compiled an efficient program for Android users.
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
				The obvious hurdle for all teams would be time. Writing an application in 7 days is no easy task. A challenge we ran into was that Week 8 was midterms for some of our members so it was hard to find time to get together everyday. We had to do most of the work that was to spread out over 7 days in 4 days, spending over 10 hours a day and sacrificing sleep and food. In addition to this, the client-server on the back-end was extremely difficult to implement. We went through 4 different implementations with C2DM, HTTPPOST, XMPP, and finally chose OSC. On the front-end the translate functionality on the GUI took hours to figure out.
			</p>
			

            <div class="page-header">
				<div class="styled_title"><h1>Lessons Learned</h1></div>
			</div>
			<p>
				Time management is always an important thing for any project. Because of this, we learned you should always start early. Even if it is for an hour a day, put time in everyday to discuss the app. If an idea isn't working, it is a big waste of precious time to try to force it to make it work. There is no harm in scrapping an original idea and searching for a new, more efficient method. Most of the time was wasted in finding a server-client that worked and the networking aspect. Everyone has their own skills so decompose bigger tasks to subtasks and assign it to individuals.
			</p>

