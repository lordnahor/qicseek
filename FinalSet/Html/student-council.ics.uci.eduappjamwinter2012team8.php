            <div class="page-header">
				<div class="styled_title"><h1>Sources of inspiration for the project</h1></div>
			</div>
				<p>
					We met at the Kick-off event and started brainstorming ideas on the first day. There were two main ideas that stood out based on the theme of self-improvement. We realized that there are so many ideas that the average person has and these ideas are just lost because they don't write them down. In addition to this, self-improvement seemed like such a broad topic. There are no two people with identical things to improve on. We wanted an application that could apply to everyone. While thinking about this, one of the group members just thought of a napkin to jot down notes. When someone has random thoughts at a coffee shop, restaurant, or outside place, they write it down on a napkin. In that same way we thought of Nappkin to document the idea. We also knew for self-improvement, we would need the help of other people. This is where the mapping ideas came about where the user would write a thought they were curious about and then send it to various people in their contact list. Other people can then add to the topic and help the original user to learn about their desired thought.
				</p>

