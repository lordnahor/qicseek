<p><strong>DEGREES AVAILABLE: Minor</strong></p>
<p> <img alt="photo: woman doing an equation" class="left" height="133" src="../../bin/img/photos/degrees/stats_header.jpg" width="160"/> </p>
<p>The Department of Statistics offers lower-division undergraduate courses designed to introduce students to the field of statistics (Statistics 7, 8, 67) and upper-division undergraduate courses on the theoretical foundations of probability and statistics (Statistics 120A-B-C, 121) and statistical methodology (Statistics 110-111-112). </p>
<p>The Department is in the process of planning an undergraduate degree program in Statistics. In the interim, students interested in focusing on statistics are encouraged to consider a minor in Statistics along with a major in a field of interest. </p>
<p> <cite> &#8250; <a href="degree_stats.php#uci">Is the Statistics minor for me?</a><br/>
