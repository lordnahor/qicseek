<p>See the <a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofstatistics/">General Catalogue</a> for a full listing of the Statistics minor requirements.</p>
<a href="degree_stats.php#startcontent">&#8593; Back to top</a><br/>
<p>
                <a name="what"></a> </p>
<h4>What can I do with this minor? </h4>
<p><img alt="photo: student doing an equation on a whiteboard" class="left" height="240" src="../../bin/img/photos/degrees/stats_do.jpg" width="160"/>The Statistics minor will add a statistical perspective to a student's major field of study.  The statistical perspective emphasizes the important role that uncertainty and variation play in understanding the world around us.   </p>
<p>A student's  enhanced quantitative reasoning skills will be useful in any future career path -- this includes study towards a professional degree (e.g., law or medicine), graduate study in  a field of interest, or a position in industry or government. </p>
<p><strong>Students who complete the minor requirements will be able to:<br/>
              </strong>
			  
			  </p>
<ul>
<li>recognize the important role of uncertainty and variability in
                empirical studies of real world phenomenon</li>
<li>employ basic statistical methods to analyze and explore data</li>
<li>understand the theoretical underpinnings of basic statistical methods</li>
</ul>
