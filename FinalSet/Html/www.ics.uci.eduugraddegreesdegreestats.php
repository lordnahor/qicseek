<p> <a name="uci"></a> </p>
<h4>Is the Statistics minor for me? </h4>
<p>Statistics is  the field associated with the collection, analysis, and interpretation of data.  Statistical theory and methods are relevant in a wide range of fields in the social, physical, and biological sciences, as well as in a range of practical areas like public policy, education, engineering and medicine.   The minor in Statistics is designed to provide students with exposure to both statistical theory and practice.</p>
<p>The minor requires a total of seven courses. These include a mathematics course, five core statistics courses, and an elective that may be taken from among several departments.  Some of the courses used to complete the minor may include prerequisites that may or may not be part of a student's course requirements for their major. Because of this the minor is somewhat intensive, but it is a useful complement to a variety of undergraduate fields for mathematically inclined students.</p>
<p>The minor, supplemented with a few additional courses (mathematics and computing), would provide sufficient background for graduate study in statistics. Students considering a minor in Statistics should meet with the Director of Undergraduate Studies in Statistics as early as possible to plan their course work.</p>
<p><i><strong>NOTE: Students may not receive both a minor in Statistics and a specialization in Statistics within the Mathematics major.</strong></i></p>
