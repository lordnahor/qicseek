<div id="content_title">Budding entrepreneurs shine in competition</div>

<blockquote>
<div id="content_text">

<p>
<a href="#"><img alt="Alumni David Cheng (left) and Paul Butterworth (right) with first-place winners Arthur Valadares, Jackie Doong and Boaz Gurdin" height="216" src="../../../bin/img/photos/people/butterworth2011.jpg" width="360"/></a>
<br/>
<i><strong>Alumni David Cheng (left) and Paul Butterworth (right) with first-place winners Arthur Valadares, Jackie Doong and Boaz Gurdin.</strong></i></p>
<hr/>
<p>
Ever feel overwhelmed when you grocery shop? Which of the 50 soup varieties should you buy? What brand of pasta sauce has the most flavor? 
</p>
<p>
For those who need help with making an informed choice at the market: There&#8217;s an app for that.
</p>
<p>
As part of the Bren School&#8217;s Butterworth Product Development Competition, three students &#8212; Jackie Doong, Boaz Gurdin and Arthur Valadares &#8212; teamed up to develop GroceryTime, a mobile app that lets shoppers discover the best products (according to user ratings) in the store via a barcode scanning technology. 
</p>
<p>
The judges were so impressed by the group&#8217;s idea, product and business plan that they awarded the trio with the top prize: $5,000 and space at the 
<a href="http://www.irvineic.org/">Irvine Incubation Center</a>. 
</p>
<p>
&#8220;GroceryTime started as a project for a class about computing in everyday life. As food lovers and Yelp users, we thought a review site seemed like an obvious missing part of the grocery-shopping experience,&#8221; says Gurdin, speaking on behalf of his team. &#8220;We were fortunate to have David Cheng (B.S. &#8217;91) as our mentor, coaching us on how to build a business at the early stage, based on his experiences as both an entrepreneur and an angel investor.&#8221;
</p>
<p>
Scheduled for release in app stores by 2012, GroceryTime assists shoppers in two ways: by helping users decide what to buy and by giving food connoisseurs a place to share their knowledge with friends and the larger food community. Users can scan barcodes to quickly read and write reviews. Plus, brands and grocery stores can advertise directly to shoppers, influencing their decision at the point of sale. 
</p>
<p>
&#8220;The competition motivated us to do things that we've wanted to do for a while but hadn't made time for before &#8212; like designing and building a mobile app, planning a business model and playing entrepreneur,&#8221; adds Gurdin. &#8220;It's rewarding to get recognition for a project and competition that aligns so well with our personal and professional goals.&#8221;
</p>
<p>
The Butterworth Product Development Competition <a href="http://www.flickr.com/photos/brenschool/sets/72157627022884884/" title="Butterworth Competition Awards Ceremony">awards ceremony</a>&#160;took place June 2 at Donald Bren Hall. Held annually, the contest aims to promote the creation of commercially viable technology and products while fostering entrepreneurship among Bren School students. Sponsors of the 2011 competition included Paul Butterworth (B.S. 
&#8217;74, M.S. &#8217;81), Ilie Ardelean (B.S. &#8217;96), Google and Yahoo. The panel of judges featured Greg Bolcer (B.S. &#8217;89, Ph.D. &#8217;98), CTO and founder of Kerosene and a Match; Matthew Jenusaitis, president and CEO of OCTANe; Kevin Kinsey, CEO of Netreo; Roger Lloyd, CMO of Immerz; Brian Roach, principal consultant and CEO of Evolve Partners Inc.; and Bob Romney, chair of the Bren School&#8217;s Leadership Council.
</p>
<p>
Dean Hal Stern recognized the top three teams at the June event. Second place and $3,500 went to Azia Foster, Hiroe Ono and Garrett Kim of Team AntColony, mentored by Informatics Assistant Professor Don Patterson. Their product, Seon, is an Android and web-based application that uses GPS technology to pinpoint various locations on campus, filter them, and show them in real time via the phone&#8217;s camera. UCI students and visitors can use the app to find a building, look up operational hours, or discover a new spot on or around campus.
</p>
<p>
Alberto Pareja-Lecaros, Ankita Raturi and Nathan Fulton, mentored by Bren Professor Ramesh Jain, were awarded third place and $1,500 for their project, Ubiquity, a private networking client that delivers digital content from any device, allowing the user 24/7 access to their files.
</p>
<p>
This year&#8217;s competition began in January with 16 teams and 62 students. Its namesake, Paul Butterworth, is the co-founder and chief technical officer of AmberPoint, Inc. (recently sold to Oracle Corp.), a leading provider of management software for service-oriented architectures. In 2002, he was named one of Red Herring&#8217;s &#8220;Top Innovators,&#8221; a distinction that honors executives whose pioneering spirit lead to breakthrough contributions for their companies and the information industry. 
</p>
