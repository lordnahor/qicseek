<div id="content_text">

<p><strong>On the Job Experience    </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: david purpura" src="../../../bin/img/photos/people/david_purpura.jpg"/>
<p> David Purpura 
</p>
</div>
<p>Heading into the end of his sophomore year David Purpura already has more job experience than most college graduates.</p>
<p>Purpura, an Orange native, spent the summer of 2005 completing an eight-week internship, during which he was responsible for redesigning and rewriting a program used to map a directory of employee workplace locations, creating an Access database of corporate software licenses, and responding to Help Desk tickets like peripheral device/software troubleshooting and ghosting new disk images on computers.</p>
<p>In the summer of 2006 he completed a twelve-week internship during which he tested latencies for the Yankee Entertainment and Sports (YES) Network Interactive broadcast; working alongside producers for US Open Tennis Interactive; setup studio equipment for an internal television broadcast to customer service personnel and spent some time as a cameraman, prompter, and video editor.</p>
<p>Purpura credits his internship opportunities to the knowledge and experience he attained in the classes he has taken as part of the Informatics major. </p>
<p>&#8220;I think the greatest preparation that I have received is teaching me how to learn,&#8221; Purpura said. &#8220;Though I have always done well in school, the classes and assignments I have been given new ways to approach tasks initially allowing me to complete the assignment both quicker and more correctly.&#8221;</p>
<p>For example in Informatics 43 (software engineering) Purpura developed a library and checkout system for a fictional video rental store. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;Many classes that focus on skills tend just to give students a theoretical understanding of a process, expecting them to remember it and know how to apply it later on in life,&#8221; Purpura said. &#8220;I enjoyed the fact that this class, in addition to providing us with this theoretical basis, gave us a project to experience the dilemmas that occur in software engineering.&#8221;</p>
<p>Purpura has his dad, a software engineer, to thank for his interest in computer science and his choice of Informatics. </p>
<p>&#8220;I have been interested in computers ever since I was quite young, always seeming to have a natural knack for them,&#8221; Purpura said. &#8220;My mom would used to say that I&#8217;m a clone of my dad&#8212;referring to our similar interests and personalities.&#8221;</p>
<p><strong>"BLAME" DAD </strong></p>
<p>As for choosing the Informatics major, Purpura places the &#8220;blame&#8221; squarely on his dad. </p>
<p>He had originally applied and been accepted to UCI as a Computer Science and Engineering major, however during the ICS Scholar&#8217;s Day in March of his senior year the family witnessed a presentation on the Informatics major. </p>
<p>&#8220;My dad suggested that I look at Informatics, saying that he thought it would best prepare me for a career in software engineering,&#8221; Purpura recalled. </p>
<p>Only in its second year, the Informatics major covers the design, implementation, use and impact of information technology. </p>
<p>Compared to traditional computer science, which primarily focuses on the internal features of computer systems, Informatics is also concerned with computer systems and their surrounding context -- the people and organizations that use them and the problems they need to solve.</p>
<p>Despite the fruitful and positive experience Purpura has had as an Informatics majors, the relative newness of major does leave him with mixed feelings. </p>
<p>&#8220;On one hand, I at times wonder how the outside world would perceive the major, at times it can be frustrating explaining exactly what Informatics is,&#8221; Purpura said. &#8220;On the other hand, it is a neat feeling knowing that I am partially responsible introducing a new major into the world, almost as though I am ushering in a new period of learning.&#8221;</p>
<p><strong>SPREADING THE WORD </strong></p>
<p>Through his work as vice-president of the Informatics Students Association (INSA), Purpura is working to educate the world about Informatics and ensure future classes will not have to explain what Informatics is. </p>
<p>INSA's mission is to help Informatics majors succeed academically and professionally, and to help build a strong Informatics community by helping all Informatics majors in different classes get to know each other and get to know their professors.</p>
<p>The group is not exclusive to Informatics majors and INSA tries to hold joint events with other clubs in order to give Informatics majors more opportunities to meet people, and give others opportunities to learn about Informatics.</p>
<p>The group has held various events to achieve its mission, including Scheme, Java and Eclipse workshops and review sessions.</p>
<p>INSA also holds social events and game nights with free pizza and put together a Robocode Tournament which pitted undergraduates, graduates and professors in a robot building challenge.</p>
<p>The group has also held recruiting and information sessions with companies such as Apple, Microsoft, Deloitte, Hitachi Consulting, and Sun Microsystems and has worked to expose students to other careers and companies that are less known and which represent some more nontraditional areas of computer science.</p>
<p>But life at UCI isn&#8217;t all work for Purpura, he is an office assistant in Middle Earth and enjoys hanging out with the residents of Middle Earth and his roommates at the ICS Theme House in Arroyo Vista. </p>
<p>&#8220;I think I&#8217;ve formed a great bond with others in my suite, which I would have greatly missed had I decided to commute to UCI,&#8221; Purpura said. </p>
<p>He also started teaching himself to play the piano and the guitar and taking advantage of the social events on campus and shops across the street in University Town Center.</p>
<p>I have greatly enjoyed my time at UCI and the Bren School,&#8221; Purpura said. &#8220;In fact I think it embodies one of the best possible environments; its sheer size and number of classes, give a wide range of depth and choices for students, while it&#8217;s small class sizes allows students to interact and get to know professors.&#8221;  </p>
<p><i>- Eric Kowalik</i></p>


