<div class="newsheadline">SOC Seminar Series</div>

<p>In 2003, the SOC project organized a seminar series on scientific
collaboration.  Some of the seminar presentations are available here.</p>

<p>November 6th: &nbsp;&nbsp;<i>Jeremy Birnholtz and Daniel Horn</i>: Shake, Rattle and Roles: Design implications from experimental earthquake engineering &nbsp;(<a href="/NewsEvents/Shake_rattle_socsem.ppt">PowerPoint presentation 585KB</a>)</p>

<p>September 25th: &nbsp;&nbsp;<i>Kyoung Shin Park</i>: Supporting Scientific Collaboration in Amplified Collaboration Environments &nbsp;(<a href="/NewsEvents/KYOUNG-UM-Continuum.ppt">PowerPoint presentation 10M</a>)</p>

<p>September 11th: &nbsp;&nbsp;<i>Ann S. Zimmerman</i>: Data Sharing and Secondary Use of Scientific Data: Experiences of Ecologists &nbsp;(<a href="/NewsEvents/zimmerman_soc_091103.ppt">PowerPoint presentation 388K</a>)</p>

<p>May 27th: &nbsp;&nbsp;<i>Jason Leigh</i>: The Optiputer &nbsp;(<a href="/NewsEvents/Michigan2003.ppt">PowerPoint presentation 37.8 MB</a>)</p>

<p>May 8th:&nbsp;&nbsp; <i>Paul Avery</i>: Data Grids for 21st Century Data Intensive Science. (<a href="/NewsEvents/avery_michigan_2003may08.ppt">PowerPoint Presentation 4.5 MB</a>)</p>

