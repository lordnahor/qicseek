<div class="newsheadline">MIT's Technology Review reports on findings from the SOC project</div>

<p>The November 23, 2004 edition of MIT's Technology Review reports on
some of the findings from the SOC project.  The article by Eric
Bender, which is entitled, &quot;<a href="http://www.technologyreview.com/articles/04/11/wo_bender112304.asp?trk=nl">Rules of the Collaboratory Game</a>,&quot;
discusses some of the factors that contribute to collaboratory
success.</p>

<div class="newsheadline">The SOC team presents a Symposium at the Academy of Management Conference 2004 <br>August 6-12 in New Orleans, Louisiana</div>

<p>TITLE: Data sharing in large-scale scientific collaborations: facing the technical and organizational barriers to data aggregation and re-use</p>

<p>
1. Bietz, M. & Birnholtz, J. <a href="/NewsEvents/AOM/Bietz_Data.ppt">How Data Contribute to Scientific Practice</a> (PowerPoint, 986KB)<br>
