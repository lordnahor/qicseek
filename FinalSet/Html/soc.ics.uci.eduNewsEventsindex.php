<div class="newsheadline">Gary Olson elected to the CHI Academy</div>
<p>Selection to the <a href="http://sigchi.org/documents/awards/">CHI Academy</a> signifies exceptional lifetime contributions to the HCI field. Gary joins fellow SOC researcher Judy Olson, who was elected to the innaugural class of CHI Academy honorees in 2001. </p>

<div class="newsheadline">Atkins chaired NSF committee issues report on development of cyberinfrastructure</div>

<p>Professor Daniel Atkins recently chaired an NSF blue ribbon advisory committee on cyberinfrastructure, and released <a href="http://www.cise.nsf.gov/sci/reports/toc.cfm">this report</a>. The NSF has a "once-in-a-generation opportunity," according to the committee, to lead the scientific and engineering community in the coordinated development and expansive use of cyberinfrastructure. Read about the report at: <a href="http://intel.si.umich.edu/cfdocs/si/news/news-detail.cfm?NewsItemID=295">http://intel.si.umich.edu/cfdocs/si/news/news-detail.cfm?NewsItemID=295</A>.</p>
