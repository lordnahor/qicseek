<h3>MISSION</h3>
The Bren ICS Corporate Partners Program promotes the exchange of the most advanced information in the field between university researchers and their corporate counterparts, and provides access to the abilities and interests of UC Irvine&#8217;s students through exclusive recruiting and networking opportunities.
<h3>OBJECTIVES</h3>
<ol>
<li>Excellent customer service/individualized attention
   </li>
<li>Streamlined communication
   </li>
<li>Priority access to students and faculty within your specific area(s) of interest
</li>
</ol>
<h3>BASIC MEMBERSHIP</h3>
Basic members receive the following benefits:
<ul>
<li><b>Recruiting</b> &#8211; Customized attention to your recruiting needs including:
      
<ul>
<li>ICS Jobs  - Access to students through student website featuring resumes, and ability to sort by   
G.P.A, major, specific skills &amp; more
         </li>
<li>Early Career Fair featuring first access to top students, on-campus interviews, company info sessions
         </li>
<li>Facilitation of interns
      </li>
</ul>
</li>
<li><b>Networking</b> &#8211; Discuss leading research and innovations with faculty and other Corporate Partners through our quarterly Distinguished Speaker Series.  Attend VIP receptions (4 tickets) prior to each Distinguished Speaker event.
   </li>
<li><b>Student Showcase</b> &#8211; Attend the annual Student Showcase event in spring featuring top Bren School student projects
   </li>
<li><b>Host Career Nights</b> - Direct and individualized access to students through company hosted career nights
</li>
</ul>
<i>The fee for basic membership is $10,000.</i>
<h3>PREMIER MEMBERSHIP</h3>
Premier members receive the following additional benefits:
<ul>
<li><b>Center Access</b>
      
<ul>
<li>Direct access to a specified research center including regular exchanges of research and information
         </li>
<li>Access to office space, computer facilities, research labs, library
      </li>
</ul>
</li>
<li><b>Visiting Scholars</b> &#8211; Partners may send a company researcher to join a faculty member&#8217;s research group for a period of time (up to 12 months).  Includes auditing courses, attending research seminars.
   </li>
<li><b>Meetings &amp; Workshops</b> &#8211; Attend topic specific annual meeting where faculty, grad students and industry leaders present latest research findings. 
</li>
</ul>
<i>The fee for premium membership is $25,000.</i>
