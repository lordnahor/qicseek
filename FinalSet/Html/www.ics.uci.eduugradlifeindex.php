<h4>Join a Student Group </h4>
<p>In addition to the plethora of <a href="http://search.dos.uci.edu/organizations/">UCI student groups</a>,  the Bren School sponsors a number of student groups, listed below, that you may join that address  specific interests in the field of information and computer sciences. </p>
<ul>
<li><strong>ACM</strong>, Student chapter of the Association for Computing Machinery<br/>
		  The Association for Computing Machinery is the world's largest society of information technology students and professionals. The chapter represents the needs of the UCI computing community.</li>
<br/>
<li>  <strong>INSA</strong>, Informatics Student Association<br/>
          Works to unite the Informatics community at UCI and to promote the <a href="../degrees/degree_in4matx.php">Informatics major</a> to students and companies alike. We offer workshops, corporate visits and tours, individual tutoring and mentoring, tournaments and competitions and networking/recruiting events with technology companies. 
            
          </li>
<li><a href="http://www.ucf.ics.uci.edu/">UCF</a>, Undergraduate Computing Federation<br/>
          Promotes interaction between and provide resources to students with an interest in Unix, Linux, FreeBSD, Network Security, Cryptography, or Ethical Hacking. Open to all UCI undergraduate or graduate students.</li>
<br/>
<li><a href="http://www.ics.uci.edu/%7ewics">WICS</a>, Women in Computer Sciences<br/>
           By joining WICS, female students can surround themselves with their peers and positive influences who, not only work with and can assist them with their classes and curriculum, but can also help them network with other successful women, giving them even more opportunities to succeed.</li>
</ul>
<h4>Live in the Bren School Theme House </h4>
<p><img alt="photo:: arroyo vista housing community" class="left" height="150" src="../../bin/img/photos/buildings/avthemehouse.jpg" width="200"/>The school sponsors a theme house in the <a href="http://www.housing.uci.edu/av/">Arroyo Vista housing community</a>, which is in walking distance of the <a href="http://www.campusrec.uci.edu/">Anteater Recreation Center</a>.</p>
<p>The theme house provides informal, academic and social functions for students interested in the field of information and computer science. </p>
<p>The goals of this house are to expose residents to faculty, alumni and businesspersons in the information and computer sciences field.</p>
<p>Activities include visits to corporations, opportunities to participate in symposiums and workshops centering on academic success, community and career opportunities, preparation for graduate/professional schools and learning skills.&#160;</p>
<p>The house is open to all students with an interest in information and computer sciences.</p>


