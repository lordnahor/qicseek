<p><b>Tsudik spearheads major security classification overhaul</b></p>
<!-- -->
<!-- -->
<p>
<img alt="Gene Tsudik" src="../../../bin/img/frontpage/GeneTsudik_cropped.jpg" style="padding: 0.5em;" width="400"/>
<!-- -->
<!-- -->
</p>
<p>When the Association for Computing Machinery (ACM) launched a
major effort to overhaul and update its Computing Classification System &#8212; which
provides the standard nomenclature for the entire computer science field &#8212; it sought
experts from academia and industry to serve as team leaders for this
challenging and long-overdue task. The organization tapped Gene Tsudik, director
of UCI&#8217;s Secure Computing and Networking Center and of the interdisciplinary
Networked Systems graduate program, with overseeing a brand-new section on Security
and Privacy. </p>
<p>ACM introduced its classification system in 1964 and has not
published an update since 1998. &#8220;The terminology had become archaic and new fields have emerged
during the past 15 years,&#8221; notes Tsudik. &#8220;For instance, cloud computing did not
exist at all, and security and privacy was a fringe element back then.&#8221;</p>
<p>Tsudik &#8212; who also serves as editor-in-chief
of the ACM <i>Transactions on Information and System Security</i> (TISSEC), a leading
archival research journal covering computer/network/information security and
privacy &#8212; recruited Columbia University professor Angelos
Keromytis to collaborate in leading a team of half a dozen prominent researchers
to serve as a mini-editorial board. From late 2010 until about two years later,
the all-volunteer group composed and vetted the complex list, which encompasses
such heterogeneous categories as cryptography, intrusion detection and malware
mitigation, as well as security in hardware, software and applications,
database/storage, networks, services and systems.</p>
<p>Tsudik and Keromytis also collaborated with other team leaders
in the overarching ACM project to ensure consistent terminology throughout the
revised classification system, and to minimize overlap between categories. </p>
<p>The final product is a specification system that helps authors
appropriately label their articles with keywords that accurately reflect the
topic, explains Tsudik. And that means anyone doing research in the field will have
an easier time locating needed information.</p>
<p>&#8220;All documents today are digitized &#8212; from journal articles to
conference proceedings &#8212; so consistent terminology is important,&#8221; says Tsudik,
who was one of only 17 team leaders worldwide on the entire project. &#8220;It&#8217;s like
Twitter and hashtags. It allows you to conduct searches more effectively.&#8221;</p>
<p>The new ACM taxonomy may be viewed at <a href="http://dl.acm.org/ccs.cfm">http://dl.acm.org/ccs.cfm</a>.</p>


