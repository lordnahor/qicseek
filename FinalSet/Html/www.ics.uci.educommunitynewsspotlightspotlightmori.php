<p>
Four years ago, Kevin Mori &#8217;13 stepped inside the Laboratory for Ubiquitous Computing and Interaction 
(<a href="http://luci.ics.uci.edu/#overview">LUCI</a>) 
for the first time &#8212; and fell in love with UC Irvine. 
</p>
<p>
Then a prospective student, he was drawn to the university&#8217;s interdisciplinary strengths, particularly the Donald Bren School of Information and Computer Sciences&#8217; focus on maximizing technology&#8217;s ability to enrich the social, cultural and aesthetic fabric of people&#8217;s lives.
</p>
<p>
&#8220;I realized this place wasn&#8217;t just about people working at computers,&#8221; he says. &#8220;It was also about people trying to solve social issues.&#8221;
</p>
<p>
Mori, who majored in business information management and minored in conflict resolution, is committed to the human benefits of analyzing big data, and to helping people who are marginalized and at risk. A Phi Beta Kappa inductee, he garnered a 2013 Chancellor&#8217;s Award of Distinction for academic excellence and his contributions to the campus community. 
</p>
<p>
&#8220;My identity is tied closely to my values of social justice and inclusion,&#8221; he says. &#8220;I really try to make sure, in all the different work I do &#8212; including academics &#8212; to keep in mind where I come from. My research has been informed by trying to understand my own community and applying my knowledge to serve people&#8217;s needs.&#8221;
</p>
<p>
He explored these issues as part of UCI&#8217;s Social and Technological Action Research 
(<a href="http://www.star-uci.org/">STAR</a>) Group, whose mission is to conduct scientific and scholarly research in computing, technology and social processes for the greater good. Through his STAR thesis project, Mori led a study that examined how individuals who identify as both queer and Asian Pacific Islander experience a sense of community in both offline and online spaces. 
</p>
<p>
In 2011, thanks to the US-UK Fulbright Commission Summer Institute Scholarship Program, Mori was able to broaden his research by spending about five weeks at the University of Roehampton. &#8220;In the morning, we&#8217;d learn about the economy or social issues,&#8221; he recalls. &#8220;Then we would go into the city of London and see how that plays out in real life.&#8221; Mori&#8217;s research team conducted a project on British societal gaps and inequity.
</p>
<p>
On campus, Mori interned at the Cross-Cultural Center (CCC) and served as chair, and later advisor, for one of CCC&#8217;s founding organizations &#8212; the Asian Pacific Student Association, where he was instrumental in hosting national programs and conferences for the broader Asian Pacific Islander community. As a peer academic advisor and member of committees to support campus climate, he also helped ensure students from a variety of backgrounds felt safe and valued at the university. 
</p>
<p>
&#8220;Kevin has a natural ability to influence and encourage others to explore perspectives that may be different from their own,&#8221; says Darlene Esparza, CCC assistant director. &#8220;As a UCI student, he was committed to creating a supportive environment, and his infectious positivity inspired others to do just a little bit more as well.&#8221;
</p>
<p>
Mori&#8217;s penchant for helping others extended beyond the campus, through his service on the executive boards of the OCA-Orange County Chapter &#8212; part of a national organization dedicated to the social, political and economic well-being of Asian Pacific Americans &#8212; and the Japanese American Citizens League, the editorial board of the Pacific Citizen newspaper, and the Youth Advisory Council of the Asian American Justice Center. 
</p>
<p>
He excelled in the classroom as well. Mori took part in UCI&#8217;s 
<a href="http://www.honors.uci.edu/about.php">Campuswide Honors Program</a> 
and the Bren School&#8217;s 
<a href="http://honors.ics.uci.edu/">ICS Honors Program</a>, and was on the Dean&#8217;s Honors List every single quarter.
</p>
<p>
As much as he appreciates being recognized with a Chancellor&#8217;s Award, Mori says he is far more grateful to UCI for the education and mentoring he received, the experiences he gained and the people he met. 
</p>
<p>
&#8220;The faculty and staff at the Bren School really care about the students, make time for them and help them grow,&#8221; says Mori, now a consultant with Claraview, a division of Teradata. 
</p>
<p>
And despite his extensive service, academic honors and promising future, the Southern California native remains modest. &#8220;I just see myself more as a member of the community,&#8221; he says, &#8220;someone who can provide support where needed.&#8221;
</p>


