<p><strong>Predicting Properties    </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: chloe azencott" height="187" src="../../../bin/img/photos/people/chloe_azencott.jpg" width="200"/>
<p> Chloe Azencott 
</p>
</div>
<p>Pharmaceutical companies spend years conducting expensive lab experiments to make the next great drug discovery.</p>
<p>Doctoral student Chloe Azencott hopes her research into statistical machine learning and data mining can help reduce the time and cost of the drug development process.</p>
<p>&#8220;I am working at predicting molecular properties (such as their melting point, toxicity, or effectiveness again a target disease) using computers instead of expensive and time consuming wet lab experiments,&#8221; Azencott said. &#8220;The goal is to help chemists and biologists in the process of drug discovery, by limiting and guiding the experiments they will have to conduct.&#8221;</p>
<p>Azencott is also working on the prediction of chemical reactions (what will happen if you have a given mixture of compounds under a given set of conditions) and presented a poster and talk on the topic at the American Chemical Society Spring 2009 Meeting.</p>
<p>&#8220;By being able to simulate chemical reactions in silico, we hope to augment the problem-solving ability of chemists and facilitate the exploration of the many unknown areas of the chemical space,&#8221; Azencott said.</p>
<div class="highlight_box_left"> <a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni. </div>
<p>Azencott&#8217;s advisor, Chancellor&#8217;s Professor of Computer Science, Pierre Baldi, is one of the premier researchers in bio informatics and recently received a Special Grant in Chemical Sciences from the Camille and Henry Dreyfus Foundation to support the development of Reaction Explorer, an interactive tutorial system for organic chemistry reactions, which enables students to learn about reactions in ways previously unrealized.</p>
<p>The Reaction Explorer aims to provide a richer learning experience including: dynamic generation of customized multi-step synthesis design problems; context-specific feedback messages; and support for inquiry-based learning through experimentation and interactive dialogue with the system. </p>
<p>&#8220;When I was given the opportunity to work with Pierre Baldi, it was not something you turn down,&#8221; Azencott said. &#8220;He is one of the most respected scientists in the field of bioinformatics.&#8221;</p>
<p><strong>NOT IN THE NUMBERS</strong></p>
<p>For the longest time Azencott, a native of Digne a small town north of Marseilles, wanted to pursue work in mathematics. But after high school, she had to choose between a mandatory engineering science or computer science class.</p>
<p>&#8220;I did not like what was taught in the engineering science class, so I switched to computer science,&#8221; Azencott said. &#8220;I had a great professor who helped me discover computer science and I decided that was what I wanted to do.&#8221;</p>
<p>It was while attending Ecole Nationale Sup&#233;rieure des T&#233;l&#233;communications de Bretagne, a top-tier engineering school in France, where she realized she wanted to pursue her Ph.D.</p>
<p>&#8220;In pursuing my Masters in Mathematics and Computer Science, I got to work in a research lab for four months,&#8221; Azencott said. &#8220;I knew then that I wanted to do research and I needed a Ph.D. to do it.&#8221;</p>
<p><strong>ACCEPTED</strong></p>
<p>&#8220;One of my most memorable experience at UCI, as for many young researchers, was when my first paper was accepted in a top-tier journal,&#8221; Azencott said. &#8220;Knowing that the value of my work had been recognized by experts in my field and seeing my name as a first author gave me an unforgettable meaning of scientific achievement.&#8221;</p>
<p>The research Azencott has done at UCI provided her the opportunity to talk at the Workshop for Women in Machine Learning, and in 2007 she won first place in a data mining competition, and as a result was invited to give a talk at the corresponding workshop, Agnostic Learning vs. Prior Knowledge that took place at IJCNN07 (International Joint Conference on Neural Networks 2007).</p>
<p>&#8220;The Bren School has excellent professors and research programs,&#8221; Azencott said. &#8220;Collaborations between labs and departments are numerous, which makes for an very productive research environment.&#8221;</p>
<p>She advises other doctoral students to always document your code and keep a lab book so you can easily reference the result of experiments you conducted six months ago. </p>
<p>&#8220;Be sure to take care of your health too; the extra time you spend going to the gym and getting sleep from time to time will make you more productive in the long run,&#8221; Azencott said. &#8220;Still, it is important to stay focused and not lose your final goals from sight.&#8221;</p>
<p>To stay healthy, Azencott, a resident of the Palo Verde graduate housing community, takes advantage of the recreational activities UCI has to offer.</p>
<p>She often swims laps at the pool in the Anteater Recreation Center and also takes dance classes, mostly swing and Argentine tango and she also likes to kick back with an occasional brew at the Anthill Pub in the student center.</p>
<p>&#8220;I like the pub&#8217;s friendly atmosphere and selection of beers, it's within walking distance of my apartment, and a great place to hang out with friends,&#8221; Azencott said. </p>
<p>Though she is still not sure where to go after graduation, stay in academia or pursue a career in industry, Azencott has fond memories of her time as a teaching assistant. </p>
<p>&#8220;I really enjoy teaching, which is one of the reasons why I want to stay in academia,&#8221; Azencott said. &#8220;To see the gleam of understanding in a student's eyes when you have managed to explain something to her she did not previously understand is well worth the hard work.&#8221;</p>
<p><i>- Eric Kowalik </i> </p>


