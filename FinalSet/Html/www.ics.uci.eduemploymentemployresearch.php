<strong><a name="Mjolsness_IGB_CS_2013"></a>
Institute for Genomics &amp; Bioinformatics<br/>
Department of Computer Science<br/>
Postdoctoral Scholar Position <br/>
University of California, Irvine<br/>
</strong></p>
<p>The Institute for Genomics and Bioinformatics and the Department of Computer Science at UC Irvine has a full-time postdoctoral scholar position available in the area of mathematical methods for machine learning and computational science. The successful application will conduct research in Professor Eric Mjolsness&#8217;s research group and their responsibilities will include conducting basic research on high-level mathematical languages and new mathematical methods for modeling complex datasets, including gene expression data, spatiotemporal data, and relational data.</p>
<p>Further responsibilities include research presentations and technical writing, as well as collaborating with graduate students, postdoctoral scholars, and faculty in the department. </p>
<p>It is preferred that applicants should have strong research experience, publications and broad knowledge in the foregoing fields of research. Applicants must have earned a Ph.D. in Computer Science, the physical sciences, Applied Mathematics, Statistics, Electrical Engineering, or a closely-related discipline. Strong mathematical skills are required. Strong software experience including both numerical and symbolic programming and computer algebra is preferred.</p>
<p>The salary range for this position will be commensurate with training and experience. The appointment will initially be for a one-year period with extensions to additional years possible subject to availability of funding. Earliest date of appointment is January 15, 2014.</p>
