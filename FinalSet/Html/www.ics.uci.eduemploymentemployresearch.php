<p>
<strong><a name="6565_Informatics_Assistant_Project_Scientist_January_2014"></a>
Department of Informatics<br/>
Assistant Project Scientist <br/>
University of California, Irvine<br/>
</strong></p>
<p>
The Department of Informatics at UC Irvine has a 50%-time position available for a Assistant Project Scientist. This position requires a Ph.D. degree in Informatics, Science Studies or a related area. The position entails engaging with a big data project to chosen (probably in the area of genomics) with a view to embedding in the project and contributing ethical, legal, social and cultural input into the design process. The research involves ethnographic field work and candidates with a background in social science research methods, science and technology studies, and new media studies would be preferred. 
</p>
<p>
The position is year-round at 50% starting in February 2014, and will run for two years. The salary for this position is based on $72,000 annually.
</p>
<p>
To apply, submit a letter outlining your skills and relevant experience, a copy of your curriculum vitae, and at least three letters of reference via e-mail to 
<a href="mailto:gbowker@uci.edu">gbowker@uci.edu</a>. 
Applicants should respond no later than February 14, 2014. The appointment will begin in February of 2014.
</p>
<p>
Geoffrey C. Bowker  <br/>
Professor of Informatics <br/>
Director, Values in Design Laboratory <br/>
Department of Informatics <br/>
Donald Bren School of Information and Computer Sciences <br/>
University of California, Irvine <br/>
Irvine, CA 92697-3440
