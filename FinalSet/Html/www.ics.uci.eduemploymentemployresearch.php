<p><b><a name="6537_Carey_Junior_Specialist_2013"></a>
Department of Computer Science <br/>
Junior Specialist Position <br/>
University of California, Irvine </b></p>
<p>
The Department and Computer Science in the Donald Bren School of Information and Computer Sciences is anticipating that one or more part-time Junior Specialist positions will become available in the general area of Big Data management. This position would be particularly well suited for individuals with an M.S. degree who are planning to pursue Ph.D. work on this topic in the future and who want to gain experience with Big Data R&amp;D and open source Big Data software in the context of a leading university group.
</p>
<p>
Work will be primarily focused on parallel query processing and storage management in Big Data management systems. Appointment will be on a month-to-month basis. The position requires a Bachelor's degree in Computer Science or related field, M.S. preferred, and requires demonstrated hands-on familiarity with existing Big Data technologies. Salary to be at a 69.2% appointment, starting at $2000.00 a month when calculated on a basis of $34,680 annually. 
</p>
<p>
Anticipated start date will be January 1, 2014. Interested applicants should respond by November 30, 2014 by forwarding a cover memo, Curriculum Vitae, and the names and addresses of three references to:
