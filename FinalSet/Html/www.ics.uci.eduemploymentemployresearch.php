<p>
<strong><a name="6560_Stats_Postdoctoral Scholar_January_2014"></a>
Department of Statistics<br/>
Postdoctoral Scholar Position <br/>
University of California, Irvine<br/>
</strong></p>
<p>
The Statistics Department at UC Irvine has a full-time position available for a Postdoctoral Scholar. This position requires a Ph.D. degree in statistics and a strong background in differential geometry. The position entails performing research in the area of Bayesian analysis, stochastic processes, and computational statistics. The research involves developing computationally efficient Gaussian process models for fast and accurate prediction of disease dynamics.
</p>
<p>
The appointment is for a one year appointment starting in early 2014. The annual salary range for this position will be between $39,264 and $41,364 contingent on knowledge and experience. The position is dependent upon extramural funding. 
</p>
<p>
To apply, submit a letter outlining your skills and relevant experience, a copy of your curriculum vitae, and the contact information of three references via e-mail to Rosemary Busta, 
<a href="mailto:rbusta@uci.edu">rbusta@uci.edu</a>. Applicants should respond no later than February 10, 2014.
</p>
<p>
Babak Shahbaba, PhD <br/>
Department of Statistics <br/>
University of California, Irvine </p>
