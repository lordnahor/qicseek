Postdoctoral Scholar Position <br/>
University of California, Irvine </b></p>
<p>
The Department of Computer Science at UC Irvine has a full-time postdoctoral scholar position available in the area of statistical machine learning. The successful application will conduct research in Professor Padhraic Smyth&#8217;s research group and their responsibilities will include conducting basic research on new statistical learning algorithms for modeling complex data sets, including text data, relational/network data, and temporal data. Further responsibilities include research presentations and technical writing, as well as collaborating with graduate students, postdoctoral scholars, and faculty in the department. A joint postdoctoral appointment in the Department of Statistics is also possible for candidates who wish to have an affiliation with the UCI Statistics Department. 
