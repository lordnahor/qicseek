<div id="content_text">

<p><strong>Overcoming Overload  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: norman makoto su" height="181" src="../../../bin/img/photos/people/norman_su.jpg" width="300"/>
<p>Norman Makoto Su</p>
</div>
<p>Workers of today are expected to be competent in negotiating through a variety of communication technologies like email, instant messaging and mobile phones. </p>
<p>Studying how people combine certain media types, and how they manage
          their interactions can reveal what strategies and policies
          organizations can employ to alleviate "communication" overload as well
          as inform the design of systems to manage communications.</p>
<p>Over the past couple years, doctoral student Norman Makoto Su has been shadowing employees during their work day, using his watch to notate the start and end of events, like interactions and computer usage to answer this question. </p>
<p>&#8220;It really allows one to ingrain themselves into the culture of the organization and into the work lives of employees,&#8221; Su said.</p>
<p>His recent research has investigated "communication chains", interactions that happen one after another. </p>
<p>These chains consist of face-to-face (F2F) conversations, instant messages (IM), emails, and phone discussions. The study revealed that you can sometimes predict how long these chains are based on the first "link" of a chain. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>If the first link of the chain was an interaction you didn't plan on (e.g., an interruption like someone popping his or her head into your office and speaking to you), the resulting chain will be longer (have more links). </p>
<p>&#8220;We found that people who experience long chains during the workday reported having more work stress,&#8221; Su said. </p>
<p>On the other hand, people whose chains consisted of a variety of media reported less stress. </p>
<p>If you had a chain like F2F-F2F-F2F, that might be more stressful than Email-F2F-IM. </p>
<p>"We reason that those who are able to use a variety of media to
          communicate have more control and thus can more easily (and less
          stressfully) self-manage their interactions and multitasking at work,"
          Su said.</p>
<p><strong>MORE THAN JUST BITS AND BYTES</strong></p>
<p>It was the ability to focus on the social side of computing that drew the San Martin, CA native to UCI&#8217;s Bren School after he completed his bachelor&#8217;s degree at UC Berkeley.</p>
<p> &#8220;The school offers an unparalleled program in multidisciplinary studies. Many schools emphasize the technical aspects of computer science, but UCI has a great tradition in looking at the social side of computing,&#8221; Su said. </p>
<p>Su was always interested in pursuing a Ph.D., but after participating in the UC EAP (exchange abroad program) and doing research at Osaka University in Japan was his heart really set on it. </p>
<p>&#8220;It was there that I finally got a taste of what real research was like and I found that I enjoyed it,&#8221; Su said. &#8220;I was able to do Java programming on a cell phone in Japan, right when the technology came out (before the US). I found that to be very enthralling.&#8221;</p>
<p>Pursuing a Ph.D. also allowed Su the freedom to satisfy his intellectual curiosity and challenge the status quo, something that can be difficult to find elsewhere. </p>
<p>&#8220;Academics don't have to follow the status quo; they can seek new methodologies, pose new questions and question existing policies and procedures,&#8221; Su said. &#8220;A Ph.D. is also simply exciting work--you always feel like you are pushing the envelope.&#8220;</p>
<p><strong>FRIENDLY ADVICE</strong></p>
<p>For those considering pursuing a Ph.D., Su stresses that starting to do research early, even if you aren&#8217;t sure what your dissertation topic will be, will help you immensely.</p>
<p>&#8220;It will help train you to tackle future research, as well as give you a heads up when seeking internship opportunities,&#8221; Su said. &#8220;You have to be proactive. Also keep an eye out for scholarships and apply to them. I received an ARCS scholarship, and it has opened my door to meet a lot of interesting industry people.&#8221;</p>
<p>Su also recommends Ph.D. students become Teaching Assistants as it is an invaluable experience. </p>
<p>&#8220;By being a TA, you begin to appreciate the amount of work needed to become an effective teacher. Moreover, being a TA can be an extremely satisfying experience,&#8221; Su said. &#8220;It's nice to hear from students who tell you they will always remember and use what they learned from your discussion sections.&#8221;</p>
<p><i>- Eric Kowalik</i> </p>


