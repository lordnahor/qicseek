<div id="content_text">

<p><strong>DEGREES AVAILABLE: Major<br/>
			    Offered jointly with The Henry Samueli School of Engineering
</strong>
			  </p>
<p>			    <img alt="photo:: computer science student" class="left" height="200" src="../../bin/img/photos/degrees/cse_header.jpg" width="156"/>
		      </p>
<p>The Computer Science and Engineering major gives students access to multidisciplinary problems with a focus on total systems engineering.               
		      </p>
<p>Students will learn the computer science principles that are critical to development of software, hardware and networking of computer systems.               
		      </p>
<p>From that background, engineering concepts and methods are added to give students exposure to circuit design, network design and digital signal processing.              
		      </p>
<p>Elements of engineering practice include the systems view, manufacturing and economic issues, and multi-disciplinary engineering applications.              
		      </p>
<p>This program is designed to provide students with the fundamentals of computer science, both hardware and software, and the application of engineering concepts, techniques and methods to both computer systems engineering and software system design.
	          </p>
<p> <cite> &#8250; <a href="degree_cse.php#uci">Is Computer Science and Engineering for me?</a><br/>
			    &#8250; <a href="degree_cse.php#get">What do I take?</a><br/>
			    &#8250; <a href="degree_cse.php#what">What can I do with this degree?</a><br/>
			    &#8250; <a href="degree_cse.php#visit">Still not sure? </a></cite> </p>
<p> <a name="uci"></a> </p>
<h4>Is Computer Science and Engineering for me?</h4>
<p>Graduates of the program will:</p>
<ol>
<li>establish a productive Computer Science and Engineering career in industry, government, or academia;
   </li>
<li>engage in professional practice of computer systems engineering and software systems engineering;
   </li>
<li>promote the development of innovative systems and solutions using hardware and software integration;
   </li>
<li>promote design, research, and implementation of products and services in the field of Computer Science and Engineering through strong communication, leadership, and entrepreneurial skills.
</li>
</ol>
For more information on CSE goals and objectives see
<a href="http://plaza.eng.uci.edu/degree-program/cse/mission">http://plaza.eng.uci.edu/degree-program/cse/mission</a>.
<p>&#8593; <a href="degree_cse.php#startcontent">Back to top</a></p>
<a name="get"></a>
<h4>What do I take?</h4>
<p>
For a full listing of the degree requirements, see the <a href="http://catalogue.uci.edu/interdisciplinarystudies/computerscienceandengineering/">General Catalogue</a>.</p>
<p> To better help students plan their upcoming quarters, <a href="degree_cse_electives.php">a list of approved science elective courses*</a> for satisfying the CSE major's science elective requirement has been made available.</p>
<p><i>*The science elective must be taken at UCI if the student is currently enrolled at UCI.</i> </p>
<p>Students are encouraged to consult an academic advisor 
in the Bren School of ICS to determine the coursework designed to meet 
their educational goals.</p>
&#8593; <a href="degree_cse.php#startcontent">Back to top</a><br/>
<p>
              <a name="what"></a> </p>
<h4>What can I do with this degree?  </h4>
<p><img alt="photo:: computer science student" class="left" height="140" src="../../bin/img/photos/degrees/cse_do.jpg" width="160"/>Computer Science and Engineering majors are involved in building the hardware infrastructure &#8211; computers, networks, embedded devices, as well as operating systems, compilers and networking software. </p>
<p>The focus is on cooperation between hardware and software to yield the highest performance. </p>
<p>Examples of such problem areas would be in traffic management, flight control, earthquake monitoring, automotive control and smart homes.</p>
<p>Many students also go on to graduate school, continuing their studies, conducting research, and earning graduate degrees in computer engineering, computer science, information science, management or law. </p>
