            <div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
					Our application is built for the Android phone 2.3, it is a self guided tour through the campus here at UCI. It will give new students and visitors the opportunity to learn information about the campus and about the different buildings by themselves. This is beneficial for those who do not have the time for a scheduled campus tour or those who like to explore for themselves. This will help individuals by knowledge of their surroundings, giving them access to healthier eating locations, better study locations and a more complete understanding of their University and town. We have specific tours put into the app in which will direct the user to the buildings, explain a brief description and then take them to the next building on the specific tour. We have also added a feature in which any user can upload specific locations and descriptions so they can create their own tour to share or for themselves. 
<br /><br />
					There is also a feature to look up the location of specific buildings. You type the building name into a search bar and with our String search algorithm and we return the closest matching building that fits the description of the search. We incorporated ‘augmented reality' in which the user can use their camera feature on their phone incorporated with GPS and locate a specific building. This is helpful for new students who do not know their way around the campus yet and are not sure what buildings are what.  It provides a map and a GPS updates on where their location is compared to their destination. There is also a local saved profile in which will save the searches the user did and the tours that they have taken. 

				</p>

