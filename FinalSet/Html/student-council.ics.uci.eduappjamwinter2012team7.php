			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
The first night we basically hashed out ideas left and right until we settled in on the Learn UCI. We started by getting the ‘augmented reality’ feature, Google map function, and building incorporated into the app. This was instrumental in testing and figuring out how to put other features in. We had to test to make sure the ‘augmented reality’ feature was correct so we would test it on the phones we had to ensure that it located the specific building. We got exercise by walking around outside testing and debugging the features of our app. 
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
Time conflicts for everyone was a challenge that goes without saying because we are all student and not on the same schedule. We had to meet as a team for a few hours each day and also work individually when we could. People had prior commitments and conflicts which we worked around. Only 2 members actually worked previously on apps that are publishable so we relied on them and we used a lot of online tutorials as resources. Most of us had to learn the different aspects of android on the run so that we can incorporate our specific feature. Writing the code for the augmented reality and location listener rather than importing libraries. 

			</p>
			

