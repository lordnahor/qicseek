<p><b>Bluetooth Assisted Tracking wins 10<sup>th</sup> annual contest </b></p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<p>
How can museums maximize the visitor experience through travel pattern analysis? What can help eliminate the where-to-eat dilemma? How can patients easily connect with their physicians during times of natural disaster? Winners of the latest Butterworth Product Development Competition aim to address these challenges, while fellow finalists focused on tackling such topics as online language learning and the college application process.
</p>
<p>
Designed to promote the creation of commercially viable technology and products while fostering entrepreneurship among students, the Butterworth Competition is named for Bren School alumnus Paul Butterworth (B.S. &#8217;74, M.S. &#8217;81), chief technical officer of AccessG2 Inc.   
</p>
<p>
This year&#8217;s grand prize winning team took home $5,000 for Bluetooth Assisted Tracking, or BAT, which monitors the travel patterns of people in a given space. Second place and $3,500 went to the creators of TasteBuds, a mobile application that provides personalized restaurant recommendations. Developers of Life Buoy, winner of the 2012 
<a href="http://www.ics.uci.edu/community/news/spotlight/spotlight_medappjam_2012">Med AppJam</a>, 
came in third and received $1,500 for their iOS and web application that wirelessly connects patients to physicians.
</p>
