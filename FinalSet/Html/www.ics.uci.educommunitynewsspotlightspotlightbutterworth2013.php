<p><b>&#8220;MY SALON&#8221;</b></p>
<img alt="My Salon" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/MySalon.jpg"/>
MySalon is an interactive web platform that enables stylist-client communication and provides users with
access to professional stylist galleries, powerful search tools and intelligently matched hairstyle suggestions.
<p><b>Team My Salon:</b> Vishal Patel, Nicholas Ceglia, Maryna Taranova
</p>
