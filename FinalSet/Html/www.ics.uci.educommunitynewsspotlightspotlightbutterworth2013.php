<h3>MORE ABOUT THE 2013 WINNERS:</h3>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
<p>
<b>&#8220;BLUETOOTH ASSISTED TRACKING (BAT)&#8221; <br/>
1st Place &#8212; $5,000 </b>
</p>
<!-- -->
<img alt="BAT" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/BAT.jpg"/>
Bluetooth Assisted Tracking (BAT) monitors the travel patterns &#8212; including origin, destination, travel time, dwell times and travel paths for a given period of time &#8212; of people indoors and outdoors. In museums and art galleries, knowledge of visitor travel patterns is crucial in assessing the impact of temporary and permanent exhibits, and in determining staff management and allocation of resources. BAT provides an integrated experience across museums while leveraging the indoor tracking capabilities that Bluetooth technology offers. This solution comes at a considerably lower cost than current available technologies such as Wi-Fi triangulation.
<p>
<b>Team BAT:</b> Jaime Enrique Duarte, Joan Aguilar Mayans, Robert Regue Grino, Ana Papio Toda, Estel Carb&#243; Vinaixa, Yue Yu
</p>
<p>
<b>Mentors:</b> Arie Shen (BS &#8217;88, Bren School of ICS), Vice President, Business Development, Independent Consultant, and Jim Doane, Vice President, Marketing, Independent Consultant
<!-- -->
<!-- -->
<!-- -->
</p>
</div>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
<p>
<b>&#8220;TASTEBUDS&#8221; <br/>
2nd Place &#8212; $3,500 </b>
</p>
<img alt="TasteBuds" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/TasteBuds.jpg"/>
TasteBuds is a free mobile application that eliminates the &#8220;where to eat&#8221; dilemma by providing personalized restaurant recommendations based on the time of day and the user&#8217;s mood or activity. It focuses on providing a dynamic user experience with engaging gaming elements that allow users to discover foods and restaurants based on their personal preferences. It also offers food suggestions for various activities, including a post-workout brunch, date night dinner, or a late-night snack. The first iteration of this application was focused on creating an engaging food recommendation application specifically targeted toward UC Irvine students.
<p><b>Team TasteBuds:</b>  Nithin Jilla, Michael Thomas, Candace Wu, Derek Wong, Norman Tran, Bryan Cuevas
</p>
<p><b>Mentor:</b> Mayur Deshpande (PhD &#8217;07, Bren School of ICS), Software Engineer, Google
<!-- -->
<!-- -->
<!-- -->
</p>
</div>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
<p>
<b>&#8220;LIFE BUOY&#8221; <br/>
3rd Place &#8212; $1,500 </b>
</p>
<img alt="Life Buoy" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/LifeBouy.jpg"/>
Life Buoy is an iOS and web application that wirelessly connects patients to physicians. It provides a fast and reliable means for patients to connect with their physicians and health care team; lowers the costs of health care administration by lowering utilization of expensive and unnecessary health services; and improves patient health and well-being by preventing acute health-related events before they start. Life Buoy enables video consultations with physicians over any geographical distance; real-time updates to secure patient-centered personal health records; and instantaneous e-prescription of medicines to nearby pharmacies. In times of natural or man-made disaster, Life Buoy is capable of doubling as an emergency relief aid, providing up-to-date GPS locations of relief centers and emergency rooms. Patients use the iPhone or Web app to store their own data and to connect with physicians, and physicians use the iPad version to handle messages and patient data.
<p><b>Team Life Buoy:</b> Matthew Chan, Jacob Heller, Bryan Lam, Lita Patel, Peggy Bui, Joe Hanson
</p>
<p><b>Mentor:</b>  David Cheng (BS &#8217;90, Bren School of ICS), IBM
<!-- -->
<!-- -->
<!-- -->
</p>
