<h3>ABOUT THE 2013 FINALISTS:</h3>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
<p><b>&#8220;DORBL&#8221;</b></p>
<img alt="Dorbl" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/Dorbl.jpg"/>
Dorbl is an online marketplace focusing on locality. It lists products that may be impractical to ship via standard package delivery services because of fragility (art piece), special transport requirements (ice sculpture) or time sensitivity (flower bouquet). It takes the direct sales aspects of Etsy and the regional search of Yelp to create something entirely new and useful. By using Dorbl, the buyer can easily find products made and delivered by a local business.
<p><b>Team Dorbl:</b> Alex Van Buskirk, Nick Bennett
</p>
<p><b>Mentor:</b>  Art Hitomi (PhD &#8217;96, Bren School of ICS), CTO &amp; Founder, Numecent
</p>
</div>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
<p><b>&#8220;LICADEMIA&#8221;</b></p>
<img alt="Licademia" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/Licademia.jpg"/>
Licademia provides a user-friendly platform for students, professors and universities all over the world to make the college application process more efficient. It eliminates the need for exhausting admissions paperwork by organizing and classifying applicants&#8217; documents. Licademia also provides an opportunity for professors to choose the best students that fit their research groups.
<p><b>Team Licademia:</b>  Pooria Mohammadiyaghni, Ashkan Eghbal, Hossein Taji
</p>
<p><b>Mentor:</b>  Ash Kumra, Co-Founder, DreamitAlive.com
</p>
</div>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
<p><b>&#8220;LINGUIN&#8221;</b></p>
<img alt="Linguin" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/Linguin.jpg"/>
Linguin helps learners to study foreign languages while watching popular online content using smartphone, tablet and PC devices.
<p><b>Team Megaline:</b> Xinlu Tong, Le (Debbie) Yu, Sreevatsa K. Sreeraman, Jinelle Savia D'souza, Zhiyong Zhang, Huaying (Alan) Gu
</p>
<p><b>Mentor:</b> Tom Taulli
</p>
