<div class="entry">
<p><b>&#8220;MOBILECAST&#8221;</b></p>
<img alt="MobileCast" class="wrapped_image right" src="../../../bin/img/photos/spotlight/butterworth_2013/MobileCast.jpg"/>
MobileCast provides a platform for advertising information that is non-intrusive, location-based and highly customized. Users can receive location-based information in the physical vicinity of the content provider; store the information for future use; and respond back to the content provider.
<p><b>Team MobileCast:</b> Vishal Patel, Santanu Sharma, Venu Madhav Gundeti
</p>
<p><b>Mentor:</b> Channa Samynathan (BS &#8217;88, Bren School of ICS), Solutions Architect, Independent
