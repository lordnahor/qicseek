<div id="content_title">Special programs</div>

<blockquote>
<div id="content_text">

<p>There are several ways to add depth, scope, and experiential 
learning to your academic program of study. An academic counselor will 
help you determine the most efficient way to add another major, minor, 
or concentration to your academic plan, and will refer you to other 
campus programs and resources that support your interests and future 
goals.
			  </p>
<p><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> 
CONCENTRATIONS:</strong></p>
<li style="padding-left: 30px;"><strong>Engineering and Computer Science in the Global Context</strong><br/>
 
<em><br/>
All Bren School majors in good standing may propose an academic plan 
that demonstrates the ability to complete the concentration and other 
requirements for graduation in a reasonable timeframe.</em>
<p>
The globalization of the marketplace for information technology services
 and products makes it likely that Bren ICS graduates will work in 
multicultural settings or be employed by companies with extensive 
international operations, or customer bases.  The goal of the 
concentration is to help students develop and integrate knowledge of the
 history, language and culture of a country or geographic region outside
 the United States, through coursework both at UCI and an international 
host campus, followed by a technology related internship in the host 
country.
</p>
<strong>Application:</strong><br/>
Each student&#8217;s proposed program of study, specifying coursework and a 
guided internship experience in a region outside the U.S., must be 
approved in advance by the Bren School of ICS Associate Dean for Student
 Affairs.  It is expected that each student&#8217;s proposal will reflect a 
high degree of planning that includes the guidance of academic 
counselors and those at the UCI Center for International Education 
regarding course selection, as well as considerations related to 
internship opportunities, housing and financial aid.  The academic 
counselors and the Associate Dean will be available to assist qualified 
students with the development of a satisfactory academic plan, as 
needed.<br/>
<br/>
<strong>Requirements:</strong><br/> 
<ol style="font-weight: bold;" type="A">
<li><span style="font-weight: normal;">A minimum of eight courses at UCI or at the international campus 
with an emphasis on the culture, language (if applicable and necessary),
 history, literature of the country that corresponds to the 
international portion of the program, international law, international 
labor policy, global issues, global institutions, global conflict and 
negotiation and global economics; </span></li>
<li><span style="font-weight: normal;">A one or two semester sequence of technical courses related to 
the major and, possibly, cultural, history, and literature courses taken
 at an international university;</span></li>
<li><span style="font-weight: normal;">A two month or longer technical internship experience in the same
 country as the international educational experience.</span></li>
</ol>
</li>
<strong><span style="color: #6699cc;"><strong>&#187;</strong></span> MINORS</strong>
<p>
Students inside and outside of the Bren School of ICS may want to pursue
 a minor, a coordinated set of courses which together take a student 
well beyond the introductory level in an academic field, subject, and/or
 discipline.  Note:  Some minor requirements may be fulfilled by the 
student&#8217;s major requirements.  Some major/minor combinations are not 
allowed.
</p>
<ul>
<li><a href="../degrees/degree_bmc.php#how">Biomedical
 Computing (10 courses)</a></li>
<li><a href="../degrees/degree_dis.php">Digital Information Systems (7 courses)</a></li>
<li><a href="../degrees/degree_in4matx.php#get">Informatics
 (8 courses)</a></li>
<li><a href="../degrees/degree_ics_minor.php">Information
 and Computer Science (7 courses)</a></li>
<li><a href="../degrees/degree_stats.php">Statistics
 (7 courses)</a></li>
</ul>
<p><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> OPPORTUNITIES
 FOR HONORS-LEVEL STUDY</strong></p>
Students who are majoring in Biomedical Computing, Business Information 
Management, Computer Science, Computer Science and Engineering, and 
Information and Computer Science, and ICS Undeclared Pre-Majors are 
invited to apply for admission to the Bren School of ICS's introductory 
honors sequence (ICS H21/H22/H23).  These courses provide a uniquely 
challenging and intimate learning environment in which conventional 
course material is explored in greater depth.
<p>  
   A student must meet at least one of the following criteria to be 
eligible to enroll in ICS H21: 
</p>
<ul>
<li>Minimum 620 on each of 2 of 3 sections of the SAT; or</li>
<li>Member of the UCI Campuswide Honors Program; or</li>
<li>Verification of score of 3 or higher on AP Computer Science A exam; 
or</li>
<li>By petition to, and approval from the Bren ICS Associate Dean for 
Student Affairs.</li>
</ul>
<br/>
Eligible students will be provided an authorization code (required) for 
course enrollment at the time of their summer orientation/registration 
session.
<p><strong><span style="color: #6699cc;"><strong>&#187;</strong></span>
 BREN SCHOOL OF ICS HONORS PROGRAM </strong> <br/>
                <br/>
              Allows juniors and seniors majoring in Computer Science, 
Computer Science and Engineering, Informatics, or Information and 
Computer Science to pursue advanced work in one of many research areas.</p>
<p>Students admitted to the program participate in an 
honors seminar, conduct independent research under the guidance of a 
faculty member, and write a research paper. </p>
<p>More information about eligibility criteria and 
benefits of successful completion of the honors program is available at 
the <a href="../honors/index.php">Honors 
program web site</a>. </p>
<p><strong><span style="color: #6699cc;"><strong>&#187;</strong></span>
 INDEPENDENT RESEARCH OPPORTUNITIES</strong> <br/>
                <br/>
              The School encourages all majors to take advantage of this
 valuable experience, and faculty advertise many research opportunities 
every year. </p>
<p>Students are also encouraged to participate in the 
campus <a href="http://www.urop.uci.edu/">Undergraduate Research 
Opportunities Program (UROP)</a>, which offers guidance and grant 
funding for student-proposed independent research, and sponsors programs
 which provide summer stipends for students working on faculty-guided 
research.</p>
<p><strong><span style="color: #6699cc;"><strong>&#187;</strong></span>
 CAMPUSWIDE HONORS PROGRAM</strong> <br/>
                <br/>
              Available to selected high-achieving students from all 
academic majors from their freshman through senior years. For more 
information see the <a href="http://www.honors.uci.edu/">CHP web site</a>.
 </p>
<p><strong><span style="color: #6699cc;"><strong>&#187;</strong></span>
 EDUCATION ABROAD PROGRAM</strong><br/>
                <br/>
              Upper-division students have the opportunity to experience
 different cultures while making progress toward degree objectives 
through the <a href="http://www.cie.uci.edu/index.shtml">Education 
Abroad Program (EAP)</a>. </p>
<p>EAP is an overseas study program which operates in 
cooperation with host universities and colleges throughout the world. </p>
