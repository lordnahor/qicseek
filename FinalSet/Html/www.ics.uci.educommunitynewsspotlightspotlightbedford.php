</p>
</div>
<p>So far her most interesting  Informatics class has been Seminar in Informatics Research. The class is  composed completely of guest Informatics faculty who speak about what research  they are currently conducting. </p>
<p>&#8220;It was really interesting  to get to know most of the faculty in Informatics, and get to hear about all  the different areas of research going on in Informatics,&#8221; Bedford said. &#8220;The variety of research was  amazing--no two professors were doing anywhere near similar things.&#8221;</p>
<p>Graduation is still two  years away but her experience thus far in the Informatics major has helped her  get a better picture about her career after UCI. </p>
<p>&#8220;I hope to work in either  consulting or some aspect of software development, something that will enable  me to talk to people about what they want their software to accomplish, and  working with them to achieve their goals,&#8221; Bedford said. </p>
<p><strong>SPREADING THE WORD </strong></p>
<p>Bedford recommends the major for those who are interested  in the management or development side of the computer science, which entails a  lot of interaction with people and working in teams. </p>
<p>&#8220;If you do not enjoy  meeting new people or communicating with others, this may not be the major for  you,&#8221; Bedford  said. &#8220;However, if you are interested in how an idea and becomes a reality,  then studying Informatics will show you all the work that goes into software development.&#8221;</p>
<p>Bedford is so passionate about Informatics she joined the  <a href="http://www.clubs.uci.edu/insa">Informatics Student Association (INSA)</a> where she is currently the Treasurer. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>INSA puts together numerous  events for Informatics, and other Bren School majors, such as company visits,  resume and interview workshops, and events with company representatives to talk  about what their company does and how student can prepare for a job with them. </p>
<p>She is also a Community  Programmer in Middle Earth undergraduate housing community which has provided  her a chance to interact with first year residents and will be a resident  advisor next year in Mesa Court. </p>
<p>&#8220;I have great respect for  those that do these jobs, and look forward to being a good role model for  younger students and making a positive impact in their lives,&#8221; Bedford said. &#8220;I also love  being a part of the student staff community, as they are all very fun,  interesting, and caring people.&#8221;</p>
<p>Despite her rather hectic  schedule Bedford  finds time to dabble in the social experience of college life by going to  hockey games, visiting museums and aquariums, and going to concerts. </p>
<p>&#8220;Irvine  is a wonderful place to live, as it is a very safe community and there are a  lot of places to hang out, such as Irvine Spectrum, The Lab, the beach, Fashion Island  and many more,&#8221; Bedford  said. &#8220;I love it here.&#8221;</p>
<p><i>- Eric Kowalik</i></p>


