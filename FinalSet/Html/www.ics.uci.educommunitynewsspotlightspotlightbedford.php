<p><strong>Out of the Cube  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: aurora bedford" height="157" src="../../../bin/img/photos/people/aurora_bedford.jpg" width="200"/>
<p>Aurora Bedford</p>
</div>
<p>Aurora Bedford knew she  wanted to major in computer science, but she didn't want to churn out code or work with computer hardware building.</p>
<p>During her visit to  UCI, the Pomona  native saw some presentations on the Informatics major from Professor Andre van  der Hoek and was hooked. </p>
<p>&#8220;I was immediately interested,  as it appeared to be computer science for people who were interested in the  human aspect of computers, not just hardware stuff,&#8221; Bedford said. </p>
<p>The Informatics major  covers the design, implementation, use and impact of information technology.  Compared to traditional computer science, which primarily focuses on the  internal features of computer systems, Informatics is also concerned with  computer systems and their surrounding context -- the people and organizations  that use them and the problems they need to solve.</p>
<p><strong>AN INTERESTING MAJOR </strong></p>
<p>Bedford is a member of the second class of Informatics  majors which made her a little nervous, since she would still be part of an  experiment to see if the major would be a success. </p>
<p>However, it also gave her a  source of pride and an incentive not to fail.</p>
<p>&#8220;I believe that Informatics  is a great program and I want to prove that its students will learn a lot from  this field of study and succeed after graduation,&#8221; Bedford said. </p>
<div class="img_caption_left">
