<p>
<b>Alumna joins Google.org, continues research on technology use in developing countries</b>
</p>
<div class="floating_image_with_caption left" style="width: 142px; margin-bottom: 1em;">
<img alt="Nithya Sambasivan" src="../../../bin/img/photos/spotlight/nithya_sambasivan/Nithya.jpg" style="height: 200px; width: 141px;"/>
<br/>
Nithya Sambasivan
</div>
<p>
<a href="http://www.ics.uci.edu/&#126;nsambasi/" title="Nithya Sambasivan">
Nithya Sambasivan</a>&#8217;s academic career began in Chennai, India, where she earned a B.S. in engineering, but she decided to pursue her graduate studies 8,000 miles away from home. 
</p>
<p>
After earning a master&#8217;s degree in Human Computer Interaction at the Georgia Institute of Technology, Sambasivan (Ph.D. '12) enrolled in the informatics doctoral program at UC Irvine, conducting research that allowed her to combine her knowledge and skills with her passion for making a difference, especially in medium- to low-income countries, like her native India.
</p>
<p>
She successfully defended her dissertation, &#8220;Production of Use: Re-conceptualizing &#8216;The User&#8217; in Low-Income Communities in Urban India,&#8221; in spring 2012 and continues her work on developing countries in her new role at <a href="http://www.google.org/" title="Google.org">Google.org</a>. As a user experience researcher with the philanthropic division of Google, Sambasivan researches Internet technologies in Ghana and other countries in sub-Saharan Africa. 
</p>
<p>
&#8220;Some of the largest digital technology user groups are emerging in developing countries,&#8221; Sambasivan says. &#8220;For example, India is the world&#8217;s fastest-growing mobile market, adding 7 million phones per month, and Kenya has 9 millions users of M-PESA, a mobile money service (as of March 2012). Most of these people are first-time users of information technology and come from areas that are economically poor with a high rate of illiteracy. In addition, many of these areas have unstable infrastructures and expensive bandwidth. It is important for researchers to understand how these users are creating access to and using technology, flavoring them with their social and cultural values. These insights may not only help us design better technologies but also possibly leverage them toward socio-economic development.&#8221;
</p>
<div class="floating_image_with_caption right">
<img alt="Nithya field" src="../../../bin/img/photos/spotlight/nithya_sambasivan/Nithya_field.jpg"/>
<br/>
Sambasivan (far right) with domestic workers</div>
As part of her doctoral thesis, Sambasivan spent 12 months in India, researching the role that digital technology plays among disadvantaged groups in the cities of Bangalore and Chennai. Her work &#8212; conducted in conjunction with Dr. Edward Cutrell at Microsoft Research India and supported by a Google Anita Borg scholarship and a UCI Dean&#8217;s fellowship &#8212; has contributed to the understanding of technology use among the low-income communities of slums, urban sex workers and urban micro-enterprises (low-capital, informal shops like street-side flower sellers) in India. Her work has been awarded the Best of CHI (Computer-Human Interaction) honorable mention three times and has appeared in the popular press, including <a href="http://www.newscientist.com/article/mg21028095.100-cellphones-get-help-to-indias-secret-sex-workers.html">NewScientist</a> magazine. In 2010, she received a UC Irvine Public Impact Fellowship honorable mention.
<p>
While mobile phones are not uncommon in the slums of Bangalore, many people lack literacy or the skills to operate them. Sambasivan found the problem is circumvented by intermediated interactions &#8212; when people ask other, more tech-savvy community members to use the devices on their behalf. 
</p>
<p>
&#8220;A person might ask a neighbor who is considered to be a local technology expert to help with mobile phone use, and in return they will watch the neighbor&#8217;s children, cook a meal for them, or trade favors,&#8221; Sambasivan explains. &#8220;The spirit of community is very strong.&#8221; 
</p>
<div class="floating_image_with_caption left">
<img alt="Nithya printout" src="../../../bin/img/photos/spotlight/nithya_sambasivan/Nithya_printout.jpg"/>
<br/>
NGO worker shares information from a printout
</div>
&#8220;We also saw an expansion of information boundaries, otherwise closed to the community, through a social process,&#8221; she recalls. &#8220;For example, since the slum sites had zero PC penetration, in response to a slum resident&#8217;s medical problem, a nongovernmental organization field worker went to the nearest cyber caf&#233;, looked up the condition online, printed out relevant information, and went back to the slum and read it aloud to the residents.&#8221;
<p>
Sambasivan said she hopes her work in this area can help companies better meet the needs of economically disadvantaged technology users &#8212; by creating devices that are easier to use and that support multiple users in cultural and economic contexts where sharing is common. 
</p>
<p>
Sambasivan and Cutrell also collaborated with Pragati, a nonprofit organization that helps urban sex workers in Bangalore by offering health care, microfinance and counseling services. Pragati&#8217;s field workers, who were senior sex workers themselves, visited the sex workers in the field to collect loan payments and monitor health checkups. Sambasivan and Cutrell found that while this face-to-face system was built upon trust, it suffered from logistical challenges. They soon discovered that mobile phones could be an ideal platform for communicating with this segment of the population, as sex workers in Bangalore rely heavily on their cell phones to conduct business. (About 97 percent of the sex workers own a cell phone, compared to the national average of 61 percent.)
</p>
<div class="floating_image_with_caption right">
<img alt="Nithya interview" src="../../../bin/img/photos/spotlight/nithya_sambasivan/Nithya_interview.jpg"/>
<br/>
Sambasivan conducts field interview</div>
Sambasivan, Cutrell and fellow researcher Bill Thies then developed a phone broadcasting system that sent audio reminders to sex workers about medical testing for HIV and other sexually transmitted diseases, as well as payment deadlines for microfinance loans offered by Pragati. The women were contacted in regional languages with pre-recorded audio messages that used the voice of a senior field worker at Pragati, who many of the sex workers recognized and trusted.
<p>
The initial success of the system was remarkable. Approximately 82 percent of the women picked up the calls; of those, 81 percent listened to the entire message. Later, the number of women who listened to the entire message jumped to 90 percent as the reminders decreased in length from 31 to 13 seconds. 
</p>
<p>
&#8220;I was fortunate to have had a fantastic advisor in Professor <a href="http://www.artifex.org/&#126;bonnie/" title="Bonnie Nardi">Bonnie Nardi</a>, who was very supportive in letting me explore new directions in research and has helped me become a better scholar,&#8221; Sambasivan says of her time at UC Irvine. &#8220;One of the greatest things about UCI and the Bren School of Information and Computer Sciences is the focus on interdisciplinary work, which was extremely helpful in broadening my intellectual perspective. I took courses in Social Ecology on poverty policies, in Anthropology on ethnography, in Management on social media, in Sociology on statistics &#8212; all of which helped me understand the big picture of people and technology in context and taught me ways to more effectively gather, understand and analyze data.&#8221;</p>


</div>
