			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
				<br /><br/>In our first meeting, we started brainstorming on different kinds of apps that can help people in improving themselves by letting them make effective use of time. So after coming up with different ideas, we finally decided on creating an app that will let users to share their ideas about certain categories. So users with similar category interest can view these ideas. Before the start of day 2, we made sure that each one of us had the Android set-up on their computer and that we went through the online tutorial. Day 2 was all about designing wherein we thought of different designs for our application and zeroed upon one.  We then set up the Mercurial repository for managing the code. We started Day 3 by splitting up the work on GUI design and backend. Every team member was responsible for completing his own task. 
				<br /><br/>Once we implemented the basic functionalities of our app, we spent time on testing these. Debugging was major task in everyone's to-do list. 

				<br /><br/>Next step was to include Google Maps in our app to allow users to set a location for a particular idea. So using this feature the user could tag an idea with a location. Then we started designing images for different buttons and tabs based on the functionality of the same. Simultaneously we were working on removing the minor bugs in the app.

				<br /><br/>We decided to include another field, description, for an idea which would contain some extra information for the same. We realized that some activities, for example an upcoming event in the neighborhood, would come with an expiration date. Beyond this date, IdeaFactory should not suggest that activity to the user. We added this functionality to IdeaFactory too. 
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
				<br /><br/>Except for one team member, no one else had any experience with any type of mobile application development. So we had to quickly go through the basics of android development by watching the videos of android development workshop.
			<br /><br/>Team having mid-terms and assignments to finish was a hurdle and we had to incorporate this fact in our schedule for AppJam. However, except for one day, we managed to meet every day and worked throughout the day.
			<br /><br/>We used Mercurial as our code repository. Merging the code-changes done by team using Mercurial was a challenge as it involved removing many conflicts. We dedicated time for this merging, everyday, so that we sail smoothly.
			<br /><br/>Since most of the functionality of our application is interlinked with each other, dividing the tasks so that everyone can work on it independently was also one of the challenges that we faced.
			<br /><br/>IdeaFactory uses My-SQL database to store the data related to the application. Over the course of development, we had to make many changes to the database schema as we added more features to the application. This involved removing the data, changing the schema and adding the data again for testing purpose.
			<br /><br/>IdeaFactory uses Google Map API to show the location of the activities. Getting these APIs working on the system took time and involved a lot of research.
			</p>
			

