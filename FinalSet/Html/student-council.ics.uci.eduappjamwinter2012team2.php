				<div class="styled_title"><h1>Notable algorithm or other academic material learned in class</h1></div>
			</div>
			<p>
				These are important algorithms that IdeaFactory has incorporated:
				<br /><br />Ideas Tab:
				<br /><br />This tab displays the best ideas according to the user's preferences. First, the ideas are fetched by user's preferences from the database. Then we apply the following heuristics to the fetched results: from the fetched ideas, we calculate the score of the idea. The idea is not displayed if the idea has been viewed more than a certain number of times, if the score is zero or below, or if the idea has already been rated by the user.
				<br /><br />Searching Location from Google Maps:
				<br /><br />In the Add location field, we take the input from the user for the location and pass this location string to the backend which calls Google Maps API for GeoCode using a PHP script and finds the latitude and longitude for the location string. The script sends back these coordinates which are used to project the location on Google Map. These coordinates are also stored in the database.
				<br /><br />Search Tab:
				<br /><br />The search algorithm was perhaps the most complex part in our project. We implement the UNIX grep like feature for SQL. We get the queries from the database for the corresponding category and then apply a LIKE operator for checking all the combination of the words in the appearing in the query to implement a effective search function.
				<br /><br />Data Structures Used:
				<br /><br />We have used ArrayList and HashMap in different parts of the project. ArrayList is used for storing the selected categories and simultaneously HashMap is used to improving the time complexity from O(n) to O(1). 
			</p>
			
