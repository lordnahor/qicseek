            <div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
				-Suggests activities for people to do based on user preferences and category.
				<br /><br />-Pushes the user to have a more dynamic social life.
				<br /><br />-Provides an interface to get people to share ideas.

				<br /><br />Many times when spending time with friends, no one can think of an activity to do at all. IdeaFactory helps people think of ways to spend their time by suggesting activities for them to do. It pushes the user to have a more active social life by suggesting exciting places to go or new things to do. By using our app, people can enrich their lives and become more whole and interesting.
				<br /><br />IdeaFactory suggests Ideas to the user based on their preferences and input. For example, if the user wanted an Idea that has to do with "Fitness", the app might suggest that the user "Run a lap around the structure you're in." If the user wanted to go to a "Social Event", the app might suggest that they visit Dumbledore's Anteaters, a club at UCI that discusses Harry Potter. 
				<br /><br />The user has the ability to give the app their preferences and it would suggest Ideas based on them. If the user thinks that an idea is particularly good, then they can "Like" the Idea and it would come up more often in the future for other users. Conversely, they can "Dislike" a bad Idea.
				<br /><br />Sometimes people don't need any help with thinking of great activities to do; in that case they can post their ideas for other people to try out using this app. Using GoogleMaps, they can pinpoint specific times and locations that these activities take place, like where and when Dumbledore's Anteaters meet up each week.
				<br /><br />Using this app, it is much easier for people to become more involved with their communities and to do activities to better themselves. By performing things that are outside of a person's comfort zone, they can grow to become a better and more interesting individual.
				</p>
