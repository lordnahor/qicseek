				<div class="styled_title"><h1>Sources of inspiration for the project</h1></div>
			</div>
				<p>
					We see a lot of interest in the social-web space. Many people upload pictures of themselves and give status information to the world. We use this interest in being socially to help the user make productive use of his/her time. By posting his/her idea, the user also helps the Social world know some interesting way to use time. Our thought-process, on one end, was to think of making an application that would help the user in making good use of his/her time. Secondly, a parallel view to build something that would not only help the user alone. We wanted to use this "interest" of being socially active, in a way that it would help other users too. By posting ideas, a user helps other users know of some interesting way of making use of their time.
				</p>

