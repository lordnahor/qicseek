</div>
<p>
Have you ever wondered how mobile apps are made? 
Want to tap into the vast power of your android smartphone? 
<br />Then <a href =  "http://tinyurl.com/androidworkshop3" target="_blank"><strong> sign up </strong></a>for The ICSSC Android workshop today!
<br /><br />
The ICSSC Android workshop will help you learn and develop the necessary skills to enter the rapidly expanding mobile app market. 
Android is an open-source platform that is continuously being developed by Google. Developing on Android with Java or C is free and open to everyone. 
With plenty of resources available and an abundance of services such as Google Maps, Google Search, and voice recognition, 
Android lets you develop dynamic applications for multiple phones and tablets.
<br /><br />
Learn the basics of Android application development and get hands on experience creating applications! 
Come to the ICSSC  Android workshops for everything you need to start developing Android applications and prepare for AppJam this winter quarter!
<br /><br />
