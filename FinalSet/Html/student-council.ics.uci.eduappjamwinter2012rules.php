<div class="page-header">
	<div class="styled_title"><h1>Rules</h1></div>
</div>
    <p>
        <ul>
			<li>All participants must be UCI students</li>
			<li>Teams are chosen by the members of the ICSSC Project Management Team</li>
			<li>The application must be developed for a mobile operating system</li>
			<li>Any environment or language may be used</li>
			<li>Any previously created code by members may be used as long as it is fully documented and it is less than 20% of the final application</li>
			<li>3rd party libraries may not be used unless approved by AppJam advisors</li>
			<li>Each team will have exactly one week (7 days) to finish their application. The final application must be submitted online before the deadline, or the team will be disqualified</li>
			<li>Each team will have a dedicated advisor. Team advisors are only allowed to assist with non-technical questions and resources</li>
			<li>Each team must submit a daily blog post (total of 8) including Monday kick off, and Monday drop off. The first blog post should only contain the name of the team, leader, team members, team advisor and mobile platform</li>
        </ul>
	</p>

