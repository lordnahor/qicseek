            <div class="title white">
                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>Because other Android event reminder applications on the market are not sensitive to the user's location, they often ring too early or too late. Our app is designed to alert the user of their appointments at a time appropriate to their location.
					<br />In addition to reminders, users can also use it as a regular calendar app: viewing, adding, sharing, searching, and editing events and appointments as needed.
					</p>
			</div>

            <div class="title white">
                <h1>Programming languages, compilers and IDEs used</h1>
			</div>
			<div class="description cms">
				<p>Java, Eclipse</p>
			</div>

            <div class="title white">
                <h1>Notable algorithm or other academic material learned in class</h1>
			</div>
			<div class="description cms">
				<p>The search function for events is facilitated by the SQL database.
					<br />Conversion from distance to time was facilitated by Google, so we did not need to implement our own algorithm.
					</p>
			</div>

            <div class="title white">
                <h1>Sources of inspiration for the project</h1>
			</div>
			<div class="description cms">
				<p>Our inspiration came from our own lives as students. The theme was highly relevant to our own day-to-day lives, which makes brainstorming quite easy, as well as giving the finished app the potential to play an important role in its maker's lives as well. </p>
			</div>

            <div class="title white">
                <h1>Libraries, frameworks and other tools used</h1>
			</div>
			<div class="description cms">
				<p>PHP/SQL Webspace, GIT, GreenDroid, assembla.com</p>
			</div>

            <div class="title white">
                <h1>Art assets, logos and content not created by the team</h1>
			</div>
			<div class="description cms">
				<p>We relied mostly on the standard application buttons and graphics that are native to Android devices (accessed via GreenDroid library).</p>
			</div>

            <div class="title white">
                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>Although our first meeting was already late in the evening after the kick-off event, we were able to brainstorm multiple applications, determine which had the most impact in each contest category, and exchange contact information. Later that night we set up the Facebook group, created the design skeleton, and began delegating work.
						<br />One of the things we could get done regardless of how solid our understanding of the coding specifics was gather the GPS coordinated, so we took care of that the next day. Learning from tutorials and video walkthroughs was also useful to those of us who were less familiar with the terminology and form of large programming projects like this one.
						<br />At this point we decided to hold another group meeting to review our progress, plan our immediate next step, solidify our current understandings of the application's functions, and revise any part of our plans that needed it.
						<br />A diagram of the overall class relationships was added to our Google Doc page for the App overview, as well as a skeleton list of what methods each class would probably need.
						<br />As the project grew, we all joined assembla.com to access a repository for the project through GIT. We were able to update and clone our code data from there, so that even as we divided the work into different branches we could ensure the pieces still fit together.
						<br />The User Interface also became relevant at this stage, because It was necessary to determine how the user could interact with the app while the code was still in a flexible state. Although the visual appeal of the application is a much lower priority in a competition focusing on functionality, we took this aspect of the project seriously as well and brainstormed a handful of interfaces before settling on one.
						<br />To build the user interface we utilized GreenDroid and the built-in Graphical Layout provided by Eclipse. We have made adjustments and tested the intuitively of the layout through the emulator.
						<br />Functionality tests were conducted on team member's phones as well as the emulator on our laptops. We have created, edited, deleted, and shared multiple events to ensure the application works consistently.
						</p>
			</div>

            <div class="title white">
                <h1>Major hurdles that had to be overcome</h1>
			</div>
			<div class="description cms">
				<p>As this was everyone's first time participating in AppJam, it was also our first time working with Android application development. There are a lot of interlinked components to functional applications, and as programmers we had to research the terminology and sample code from the workshop project to better understand the API.
					<br />In addition to the challenge of learning a new form of Java programming, the team had to work efficiently as a group in order to finish within the 1-week deadline. Our scheduling conflicts during the school week posed an even greater challenge, as communication would be key to functioning as a team.
					<br />There are many, many pieces to the puzzle, and in order for the project to come together we would need each piece to fit in with the others despite potentially having been authored by a different programmer. To combat this, we used assembla and git to share up-to-date versions of the source code, and communicated frequently through Facebook, e-mail, Skype, and the occasional on-campus meet-up.
					<br />However, some days an in-person discussion seemed like the most efficient way of determining the next step to take, and delegating work, so we used charts of our schedules on Google Docs to organize meetings in person.
					</p>
			</div>

            <div class="title white">
                <h1>Lessons Learned</h1>
			</div>
			<div class="description cms">
				<p>As a team we learned to accommodate for everyone's busy schedules by holding meetings through Skype and Facebook, rather than meeting in-person. This challenge was relatively new, since students assigned to work in a group for a class assignment often share some similarity in their schedules, but our team members were all coming from different levels. In this way our teamwork paradigm resembled a more professional one, in which project partners in a business may live on opposite ends of the globe, and must communicate through online media as we did.
					<br />Another valuable lesson this project reinforced was to let no one's talents go to waste - even though we are all coming from different programming skill levels, we all wanted to contribute to the best of our abilities for the sake of our common goal. 
</p>
			</div>

