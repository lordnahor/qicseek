<div id="content_title">Noteworthy achievements 2004</div>

<blockquote>
<div id="content_text">

<p>Bren School of ICS faculty, students and research initiatives are some of the most well regarded successes on the UC Irvine campus. We are pleased to announce the following noteworthy achievements for 2004.</p>
<p>Awards, grants and other honors can be sent to <a href="mailto: ic%73%6ee%77%73@ics.%75ci%2e%65d%75 ">icsnews@ics.uci.edu </a> to be considered for publication. </p>
<p>
<strong>October 2004</strong>
</p>
<p>
<strong>Franz awarded gift for information infrastructure protection</strong>
</p>
<div class="img_caption_left">
<img alt="photo::professor michael franz" height="239" src="../../../bin/img/photos/noteworthy/photo_102004.jpg" width="200"/>
<p>
Michael<br/> Franz
</p>
</div>
<p><strong>Michael Franz</strong>, associate professor of computer science, has received unrestricted gifts in excess of $100,000 to support his work in information infrastructure protection. </p>
<p>A recent gift from Sun Microsystems joins earlier gifts from Microsoft and Intel. </p>
<p>"I am very grateful and very happy to receive these gifts", said Franz. "It is very encouraging to see that in times of deteriorating public finances, these companies are stepping forward and helping to fund the basic research that will keep America competitive and the country's computer infrastructure secure". </p>
<hr size="0"/>
<p>
<strong>September 2004</strong>
</p>
<p>
<strong>Informatics student receives ARCS fellowship</strong>
</p>
<p>
Informatics graduate student <strong>Keri Carpenter</strong> has received the prestigious Achievement Rewards for College Scientists <a href="http://www.arcsfoundation.org/OrangeCounty/ARCS_2.asp . " target="_blank">(ARCS) Foundation Fellowship Award</a> for outstanding achievement and dedication to the study of science. </p>
<p>The Orange County Chapter of the ARCS (Achievement Rewards for College Scientists) Foundation is one of twelve chapters that are dedicated to helping the best and brightest United States students by providing scholarships to scientists and engineers. </p>
<p><i>This is the third consecutive ARCS fellowship for graduate students in the Department of Informatics. Informatics graduate students Eric Dashofy and Emily Oh Navarro each received this award during the 2003-2004 academic year. </i></p>
<hr/>
<p>
<strong>August 2004</strong>
</p>
<p>
<strong>Professor's journal jumps to 6th in rankings</strong>
</p>
<p>
Professor <strong>Alfred Kobsa's</strong> <i><a href="http://www.kluweronline.com/issn/0924-1868" target="_blank">User Modeling and User-Adapted Interaction: The Journal of Personalization Research(UMUAI)</a></i> ranked No. 6 in 2003 among 451 international Computer Science journals according to the <a href="http://www.isiknowledge.com">ISI impact factor</a>. </p>
<p>The journal was ranked 20th in the 2002 rankings.  </p>
<p>The journal provides an interdisciplinary forum for the dissemination of new research results on interactive computer systems that can be adapted or adapt themselves to their current users, and on the role of user models in the adaptation process. </p>
<p>Kobsa founded UMUAI in 1991 and has since then been its 
  Editor-in-Chief. The journal is distributed by Kluwer Academic Publishers.</p>
<hr/>
<p>
<strong>Two ICS staff managers nominated for UCI award</strong>
</p>
<p>
Business office manager <strong>Kevin Kramp</strong> and interim director of student affairs <strong>Kris Bolcer</strong> have been nominated for the 2004 Staff Assembly Excellence in Leadership Award.</p>
<p>This program will recognize three staff supervisors who, through outstanding leadership, enhance staff morale, build an enriching work environment, and serve as a mentor or otherwise support the career development of their staff's all to the benefit of our UCI community.</p>
<p>Award winners will be announced at the 2004 Staff Picnic on September 15.</p>
<hr/>
<p>
<strong>July 2004</strong>
</p>
<p>
<strong>Professor Selected to Attend Prestigious Symposium</strong>
</p>
<p>
Informatics professor <strong>Cristina Lopes</strong> is one of three University of California scientists selected to participate in the prestigious National Academy of Engineering's 10th annual Frontier of Engineering symposium being held Sept. 9-11 on the UC Irvine campus. </p>
<p>The three-day event will bring together engineers ages 30 to 45 who are performing cutting-edge engineering research and technical work in a variety of disciplines. </p>
<p>UCLA professor Xiang Zhang and UC Merced professor Valerie Leppert  also were selected to participate in the 2004 event. </p>
<p>More information about Frontiers of Engineering is available <a href="../../../bin/pdf/notes/notes02_0704.pdf" target="_blank">here</a>. </p>
<p>Download the press release announcing all participants <a href="../../../bin/pdf/notes/notes01_0704.pdf" target="_blank">here</a>.</p>
<hr/>
<p>
<strong>June 2004</strong>
</p>
<p>
<strong>ICS Faculty, Staff Earn Year-end Awards</strong>
</p>
<p>
Professors <strong>Daniel Gajski</strong> and <strong>Andr&#233; van der Hoek</strong> have been awarded the school's first ever Mentor of the Year award for their work with ICS graduate students. The award, planned to be an annual recognition, was proposed by ICS students for the 2003-04 academic year.<br/>
              <br/>
              UCI honored three ICS staff members with service awards. </p>
<p>Assistant Dean <strong>Annette Luckow</strong> was recognized for 30 years service to UC; Student Personnel Coordinator <strong>Carol Rapp</strong> 15 years service to ICS; Cal-IT<sup>2</sup> RESCUE Administrative Manager <strong>Lynn Harris</strong> for 15 years service and Faculty Assistant <strong>Cynthia Kennedy</strong> for 10 years service.</p>
<hr/>
<p>
<strong>ICS Student Earns SURP Award for Research</strong>
</p>
<p>
<strong>James Sutandyo</strong> has received a Summer Undergraduate Research Program grant to continue his work with Informatics professor <strong>Thomas Alspaugh</strong> on using XML and XML tools to support analysis and management of requirements scenarios.</p>
<hr/>
<p>
<strong>U.S. DOE Awards Informatics Department with GAANN Award</strong>
</p>
<p>
The Department of Informatics has been awarded a Graduate Assistance in Areas of National Need (GAANN) Program fellowship totaling $622,665 from U.S. Department of Education. </p>
<p>The GAANN will support 5 graduate students annually, over a three year period in the study of problems in the design, application, use and impacts of computer and information technology. </p>
<p>The fellowships will be used to recruit and retain graduate students of superior academic qualifications, ones who aspire for eventual careers in research and teaching, and who have financial needs consistent with the federal standards.</p>
<p><i>This is the first Graduate Assistance in Areas of National Need (GAANN) Program fellowship that the Department of Informatics has received and is one of only 48 awards granted nationally this year.</i></p>
<hr/>
<p>
			<strong>Newkirk Center for Science and Society Awards $15,000 to Informatics Faculty</strong>
</p>
<p>
            Informatics professors <strong>Bonnie Nardi</strong> and <strong>Susan Elliott Sim</strong> have been awarded $15,000 in support of their project: "Eco-informatics to Support Hard Choices About Climate Change".</p>


