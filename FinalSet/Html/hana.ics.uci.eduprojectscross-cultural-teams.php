<h1>Cross-Cultural Teams</h1>
<p> Collaboration across different national cultures can be very challenging within teams. Workers 
  from China and Japan, for instance, differ from US workers on many aspects of conversation-- 
  from the differences in behavior depending on the rank of who you are talking, to differences in 
  the interpretation of a blank expression, to who is involved in making decisions and how trust is 
  established. Collaboration amongst team members from different cultures can be even more 
  challenging when it is distributed, and especially when team members' workdays do not overlap. </p>
<p> In order to better understand these challenges, we are conducting a micro-level, detailed study of 
  collaboration. We are synthesizing the literature on how people from different national cultures 
  communicate and collaborate, enumerating behavioral features of people from different countries 
  on the Pacific Rim and contrasting them with corresponding behaviors of Americans. Through 
  interviews, we are exploring what people have encountered when working with someone from 
  another culture, first looking at teams inside Google, then moving to cross-cultural teams from 
  organizations that have a very different organizational culture.</p>
