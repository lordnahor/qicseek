<div id="content_text">

<p><strong>Revealing Rhythms </strong>
</p>
<div class="img_caption_left">
<img alt="photo::pierre baldi " height="166" src="../../../bin/img/photos/people/pierre_baldi.jpg" width="126"/>
<p>
Pierre Baldi
</p>
</div>
Bren School  Professor Pierre Baldi is the best blues-flamenco guitarist ever to unravel the secrets of DNA.
<p>&#8220;I&#8217;ve always played acoustic guitar,&#8221; says Baldi, a professor of computer science and director of the UCI Institute for Genomics and Bioinformatics. &#8220;I&#8217;m attracted to blues and flamenco because their chord structures have a lot in common. They are simple in style with very precise rhythmic patterns. Both are soulful, and I like to mix them together.</p>
<p>&#8220;There are a lot of connections between art and science, and mathematics and music. Science research is by no means a cold rational enterprise. It&#8217;s very much a human enterprise with all its complex facets.&#8221;</p>
<p>It&#8217;s that next great human effort &#8211; unraveling the layers of data in human genes &#8211; where Baldi hopes to play his best.</p>
<p>&#8220;One of the greatest scientific endeavors over the next 10 to 20 years will be to understand the system level of biology &#8211; the circuitry. Today we have all the players, we know all the genes and all the proteins at the individual level. Now, we are starting to see how they interact with each other &#8211; what chords they are playing.</p>
<p>&#8220;Then in theory, you should be able to simulate a cell on the computer. This will open new avenues for medicine.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>"Pierre Baldi is a source of pride for us, &#8220; says Debra J. Richardson, the Ted and Janice Smith Family Foundation Dean of the Donald Bren School of Information and Computer Sciences. &#8220;The work going on at the Institute for Genomics and Bioinformatics is a leading example of the impact that interdisciplinary collaboration can have on advancing research and education. Pierre&#8217;s own research is advancing both the understanding of protein structures and further defining the prediction of gene modeling.&#8221;</p>
<p>A trained mathematician, Baldi applied his math skills to computer skills and his computer skills to biology.</p>
<p>&#8220;A cell is just a very much like complex molecular computer. We have trillions of cells that are interacting with each other. So we have very complex information processes at the molecular level that we&#8217;re trying to understand.</p>
<p> &#8220;We are able to collect all kinds of data that we never could before. The human genome project was just the tip of the iceberg. It was equivalent to the invention of the telescope or microscope. Suddenly, we have new tools that give us completely new vistas on the biological processes.<br/>
            <br/>
  &#8220;With sequencing methods scientists are now able to gather the list of all the gene and protein players. With DNA microarrays they are starting to investigate and reverse engineer the underlying circuitry and interactions. Using DNA microarrays is like being able to take a snapshot of the activation levels of all the genes in a given cell. Microarrays are contributing to our understanding of how genes are regulated &#8211; turned on and off &#8211; in very precise ways at different times depending on the environmental conditions.</p>
<p>&#8220;Biologists don&#8217;t yet quite know what to do with such huge amounts of information. They don&#8217;t always have the training or the proper mental framework to analyze the data. So biologists and mathematicians have to work together to understand the data and all the properties of the underlying systems.&#8221;</p>
<p>Baldi supplies the mathematical and computer expertise to help understand the biological reality. For Baldi, it&#8217;s no different from mixing flamenco with blues.</p>
<p>&#8212;<i>Alan Janson</i></p>


