<p><strong>The Apprentice</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: terica kindred" height="170" src="../../../bin/img/photos/people/terica_kindred.jpg" width="140"/>
<p>
Terica Kindred
</p>
</div>
<p>Although she's only 23, Bren School of ICS graduate Terica Kindred '03 has an impressive resume: Software engineer. Speaker. Author. Information technology consultant for Deloitte in Los Angeles. </p>
<p>One of Ebony Magazine's "Top 30 Under 30" young leaders of 2006. Founder and executive director of a nonprofit organization that helps minority students go from college to career. </p>
<p>Kindred credits much of her success to UCI&#8217;s SAGE Scholars Program, which gave her a chance to be a real-life apprentice. </p>
<p>&#8220;SAGE fine-tuned my ability to interact with business people,&#8221; she says. &#8220;There&#8217;s no college course for this kind of experience.&#8221;</p>
<p>Like Kindred, who grew up in South Central Los Angeles, SAGE Scholars are typically the first in their families to attend college and come from economically disadvantaged backgrounds. Through SAGE, companies offer students multi-year, paid internships related to their field of study as well as $4,000 scholarships to support their educational expenses.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;You get a ton of practical experience. It helps with school, because you can see how your studies connect to the business world,&#8221; says Kindred, who worked as an intern for Conexant Systems in Newport Beach for three years while pursuing her bachelor&#8217;s degree in information and computer science. &#8220;I had a terrific mentor at Conexant who helped me sort through my career options.&#8221;</p>
<p>She also gained valuable experience and mentors through CAMP &#8212; the California Alliance for Minority Participation in Science, Engineering and Mathematics &#8212; a National Science Foundation program offered UC systemwide. CAMP aims to increase the quality and quantity of underrepresented students completing a bachelor's degree and continuing on to graduate school.</p>
<p>Since graduating, Kindred has worked as a consultant for Deloitte as well as health care and entertainment companies. She also serves on Deloitte&#8217;s diversity taskforce to recruit minority students.</p>
<p>&#8220;While visiting college career fairs, I noticed a lot of minority students didn&#8217;t look good on paper. They weren&#8217;t prepared for entry-level jobs.&#8221;</p>
<p>Kindred decided to help. She wrote a book called Plan 4 Success and started her own nonprofit company by the same name to help minority students enter the business world.</p>
<p>&#8220;We help identify what they want to do and what companies provide those opportunities, and we see what experience they need to become the ideal person for the job.&#8221;</p>
<p>Kindred leads by example. She&#8217;s investing in real estate while continuing her consulting work. She&#8217;s also writing a second book on career development for young professionals &#8212; offering her own sage advice.</p>
<p><i>&#8211;Kathryn Bold </i></p>


</div>
