<div id="content_title">Project:ICS FAQ</div>

<blockquote>
<div id="content_text">

<p><strong>How do I submit a proposed project? </strong></p>
<p>
Please contact Kristin Huerth at 
<a href="mailto:khuerth@ics.uci.edu">khuerth@ics.uci.edu</a> or (949) 824-3074
who will get a brief summary of your proposed project.</p>
<p> The course instructor will contact you to evaluate the scope and level of the proposed project and offer details on the course (expectations, timeline, etc.). All projects must be approved by the instructor <strong>before</strong> the beginning of the quarter. </p>
<p> Although our intent is to assign every project with a student team, we cannot guarantee all projects will be assigned. </p>
<hr/>
<p> <strong>How long will the teams work on the project? </strong></p>
<p>
1-Quarter Project: 10 weeks <br/>
2-Quarter Project: 20 weeks <br/>
3-Quarter Project: 30 weeks</p>
<p>Complex projects may be accepted at the discretion of the instructor with the understanding that they would span multiple quarters and incur additional fees. </p>
<hr/>
<p> <strong>Will I have a project that is production ready at the end of the project? </strong></p>
<p>Final student projects are not intended to be &#8220;production quality&#8221; systems, however, final projects will fulfill the original requirements and specifications provided. </p>
<hr/>
<p> <strong>Donation </strong></p>
<p>Your donation for Project:ICS is due before final product/results are delivered to your company. This donation is a tax-deductible gift to the Bren School.</p>
<hr/>
<p> <strong>What contact will I have with my team? </strong></p>
<p>Once your project is approved, a time will be set during the first week of classes for you to come to campus and present the project to the Project:ICS class. </p>
<p> Although our intent is to assign every project with a student team, we cannot guarantee all projects will be assigned. </p>
<p> If your project is assigned to a team, you are encouraged to meet with them as often as necessary in the early stages. Three to four weeks into the project, the team will deliver its requirements document to you. This milestone requires that at least one person representing your company review and approve the requirements. </p>
<p> The instructor remains in touch with you throughout the quarter(s), especially during the early stages of the class, to make sure that the your needs are being met. Additional contact between the team and you are at a pace suitable for both. </p>
<p> The project is delivered to you at the end of the program. On or near the last day of class, the instructor holds a final project demo, where the team presents its final deliverable to you and the instructor. </p>
<hr/>
<p> <strong>Can I propose projects for future quarters? </strong></p>
<p>If you are interested in partnering with us in future quarters, please contact <a href="mailto:khuerth@ics.uci.edu">Kristin Huerth</a>. </p>
<hr/>
<p> <strong>How do I get removed from your mailing list?</strong></p>
<p>If your company is not interested in Project:ICS, please contact <a href="mailto:khuerth@ics.uci.edu">Kristin Huerth</a> to be removed from this mailing list. </p>
<p>If there is someone else in your company to whom we should be addressing these mailings, please have him or her contact <a href="mailto:khuerth@ics.uci.edu">Kristin Huerth</a> so that we can update our records.</p>


</div>
</blockquote>
