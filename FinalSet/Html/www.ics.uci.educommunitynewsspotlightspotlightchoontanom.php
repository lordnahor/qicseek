<p><strong>The Dilemma</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: trina choontanom" src="../../../bin/img/photos/people/trina_choontanom.jpg"/>
<p>Trina Choontanom</p>
</div>
<p>Sometimes it pays to be the youngest child.</p>
<p>Trina Choontanom, a senior computer science and engineer major, credits her two older sisters for sparking her interest in the field of computer science.</p>
<p>&#8220;My oldest sister taught me HTML and from there I taught myself how to make basic webpages and eventually moved into CSS and PHP,&#8221; Choontanom said. &#8220;I realized that I liked to look at code, tinker around with it, and be able to see the results of it.&#160; My other sister encouraged me to get into the hardware side by opening up computer towers and showing me the various parts.&#8221;</p>
<p>But when it came time to pick a college major, Choontanom couldn&#8217;t decide if she wanted to go into computer science or computer engineering. </p>
<p>Luckily, UC Irvine made the choice easy by offering the <a href="../../../ugrad/degrees/degree_cse.php">Computer Science and Engineering major (CSE)</a>.</p>
<p>Offered jointly by the Bren School of ICS, the first independent computer science school within the UC system, and The Henry Samueli School of Engineering, the Computer Science and Engineering major gives students access to multidisciplinary problems with a focus on total systems engineering.</p>
<p>&#8220;I saw that UCI had a combined CSE major and went with that,&#8221; Choontanom said. &#8220;It pretty much solved my dilemma of choosing.&#8221;</p>
<p><strong>BUILDING A FOUNDATION</strong></p>
<p>Choontanom, a San Bernardino native, hadn&#8217;t taken any computer programming classes until she set foot on UCI&#8217;s campus. </p>
<p>&#8220;The curriculum of the International Baccalaureate (honors) program at my high school didn&#8217;t have any computer related classes and there was no chance to take normal level classes &#8211; in fact, I&#8217;m not sure if there were any computer science related classes at my high school,&#8221; Choontanom said.</p>
<p>Despite not having had any formal computer science training, Choontanom found the CSE curriculum provided her a strong foundation that she can build on during her career. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;Although there is an emphasis on Java, I have been able to pick up other languages, such as C and C++,&#8221; Choontanom said. &#8220;The program has helped me overcome the intimidation of complex entities (such as languages and operating systems) by giving me the basics and from there I can expand with what&#8217;s needed and what I&#8217;m interested in.&#8221;</p>
<p>The CSE program also provides students a chance to gain a better understanding of the hardware side of computers too. </p>
<p>&#8220;The embedded systems class has a lab section where you actually implement what you&#8217;ve learned in class,&#8221; Choontanom said. &#8220;I had never touched physical circuits (actual resistor or capacitors) before coming to UCI. It's one thing to see a circuit drawn on paper and another to actually put it together. It feels like your CSE experience just isn't complete until you wire together a blinking LED or your own music player.&#8221;</p>
<p>UCI has also provided Choontanom opportunities to get valuable real world work experience to enhance what she has learned in the classroom. </p>
<p>For the past year, she has been a web intern for University Communications and works on the uci.edu web page as well as other UCI department and program web pages and has developed an RSS feed for the ZotWire news site.</p>
<p>Working for University Communications has allowed Choontanom to interact with students from various majors and the interdisciplinary interaction has helped expand her knowledge beyond the realm of computer science.</p>
<p>&#8220;It's nice to talk to people from other majors because they'll sometimes off handedly mention things you never knew before, but seems like common knowledge to them,&#8221; Choontanom said. </p>
<p>She is also conducting undergraduate research with professor Bonnie Nardi on the concept of build theory in Massively Multiplayer Online Games (MMOGs) like World of Warcraft.</p>
<p><strong>GIVING BACK</strong></p>
<p>During her sophomore year, Choontanom joined <a href="http://www.ics.uci.edu/&#126;wics/">UCI&#8217;s Women in Computer Science (WICS) group</a>, an organization established to help and encourage women to pursue a college degree and a successful career in the Computer Science fields. </p>
<p>She was encouraged by her mentor at WICS to apply for an officer position and her junior year she became the WICS Secretary. The position provided her an opportunity to encourage other young girls to pursue a career in the field of computer science. </p>
<p>&#8220;I was able to accompany the then president at Eureka!, a Girls Inc. summer program, where we taught girls how to use basic windows office programs, the internet, and Flash,&#8221; Choontanom said. &#8220;Some events are workshops (such as the Unix/Linux workshop) and those provide a fun learning environment where you go because you want to be there to be exposed to something new.&#8221;</p>
<p><strong>WORK HARD PLAY HARD</strong></p>
<p>The CSE program has been a challenge for Choontanom and she has had more of her fair share of late nights in the lab with some of her CSE friends and teammates.</p>
<p>But these late nights can lead to moments of levity, like when her group performed an impromptu magic trick late at night while trying to finish difficult homework and labs.</p>
<p>&#8220;There are a variety of people in your classes and they aren't all<br/>
              stereotypical geeks and nerds, always spending time in front of the<br/>
              computer - there's a lot that will socialize away from the computer,<br/>
              be involved in sports and other clubs, and party,&#8221; Choontanom said. </p>
<p>Choontanom likes to relax and unwind by taking a Filipino Martial Arts at the <a href="http://www.campusrec.uci.edu/">Anteater Recreation Center</a> or indulging in her hobbies of web development and design and watching anime or reading manga. </p>
<p>Every now and then she&#8217;ll pick up her violin and play for a few minutes, or mess around with a little gymnastics.</p>
<p>&#8220;Since school has been a kind of straightforward, driven path, my hobbies have taken the polar opposite by fueling my imagination for creativity in various form of arts,&#8221; Choontanom said. </p>
<p>With graduation fast approaching, Choontanom is still unsure of whether she wants to go on to graduate school or enter the work force. </p>
<p>But she is certain of one thing, she has loved her time at UCI and has made many life long friendships. </p>
<p>And to those students considering pursuing computer science at UCI, she offers this advice.</p>
&#8220;If you have the slightest interest in computer science, take the beginning 20-series classes or sit in on a few lectures of the upper division classes to see if it's something you want to do,&#8221; Choontanom said. &#8220;It will be hard, especially if you haven't had any programming experience before but if you have the determination and that interest grows as you continue, then you know you're in the right place.&#8221;


