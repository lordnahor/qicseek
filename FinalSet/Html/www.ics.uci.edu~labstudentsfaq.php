        <h5><strong>Q:</strong> <a name="job"></a>How can I get a job working
          in the ICS Labs?</h5>
        <blockquote>
          <p>If you want to apply as a Lab Assistant (the people behind the desk
            in the Lab) fill out and turn in the <a href="../lab_assistants/job_description.php">Job
            Application</a> at the appropriate time (Usually Week 7 of the quarter
            if we are hiring). </p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="24/7"></a>Why isn't the Lab open 24 hours
          a day?</h5>
        <blockquote>
          <p> We believe having an Assistant on duty is a greater deterrent than
            a few cameras (although we reserve the right to supplement the Assistants
            with cameras later). Recent surveys show that students don't want
            to work the graveyard shift.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="broken"></a>What do I do if a machine
          is broken?</h5>
        <blockquote>
          <p> Contact a Lab Assistant at the help desk. Do not attempt to fix
            it yourself.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="labbie_duty"></a>What does a Lab Assistant
          do anyway?</h5>
        <blockquote>
          <p>Lab Assistants do not substitute for TAs or instructors. Lab Assistants
            do not know everything there is to know about computers. Lab Assistants
            do attempt to deal with Lab problems or report the problems to someone
            who can deal with them. Lab Assistants do activate ICS computer accounts
            and maintain printers. They also watch over the Labs and maintain
            a safe and comfortable instructional environment.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="food"></a>Can I bring food into the Lab?</h5>
        <blockquote>
          <p>NO! It smells, creates a trash problem, attracts pests, and provides
            yet another danger to our precious computers.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="noisy"></a>What should I do if the people
          around me are making it hard to concentrate?</h5>
        <blockquote>
          <p>You should notify the Lab Assistant on duty, who will take care of
            the problem. </p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="non_ics"></a>Can I print out non-ICS
          stuff if I'm an ICS major?</h5>
        <blockquote>
          <p>Computers are for ICS use primarily. If a computer is free or the
            Lab is not busy you may complete and printout non-ICS coursework.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="equipment" id="equipment"></a>When can
          I use ICS equipment?</h5>
        <blockquote>
          <p> During your class Lab time when there is an instructor or TA present.
            There are also times when no classes are scheduled, and you may utilize
            the machines at those times as well. CS 364 serves as an Open Lab
            almost exclusively.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="games"></a>Can I play games on the Lab
          computers?</h5>
        <blockquote>
          <p>This may not directly answer the question, but know these things:
            Instructional purposes always have priority. Do not put unlicensed
            software on any of the machines. Do not create a disturbance in the
            Lab which may disrupt students who are working on course material.
            What this means is that academic and instructional purposes always
            take precendence over games and other leisurely activities.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="leave" id="leave"></a>Do I really have
          to leave when the Lab closes?</h5>
        <blockquote>
          <p> Yes. Even if you are very trustworthy, the motion sensors trust
            no one.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="slow"></a>Why are these machines so slow?</h5>
        <blockquote>
          <p> We have Pentium 400Mhz workstations. It must be you... No, seriously.
            If you are logging in to a Unix host to do course work you may be
            logged on with a hundred other people.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="lab_close" id="lab_close"></a>What time
          do the Labs close?</h5>
        <blockquote>
          <p>Check out the <a href="../lab_schedule/index.php">Lab
            Schedule</a> page.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="disk_space" id="disk_space"></a>How much
          disk space do I get for my class?</h5>
        <blockquote>
          <p> Disk space varies by courseload. The default amount is 40 MB.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="borrow"></a>Can I borrow one of these
          cool mouse pads?</h5>
        <blockquote>
          <p> Don't even think about it. In fact, do not remove any equipment
            from the Labs ever. If you're still thinking about it, perhaps you
            need some Philosophy courses.</p>
        </blockquote>
        <h5><strong>Q:</strong> <a name="suggestion"></a>Where can I make a suggestion
          about Lab?</h5>
        <blockquote>
          <p> As always, send an email to <a href="mailto:lab@ics.uci.edu">ICS
            Lab</a>. We need good ideas like plants need the sun. </p>
        </blockquote>
        <p>&nbsp; </p>

