            <p>We have included a short listing 
              of resources available on the web, that inform the SOC project, 
              as well as links to information on the <a href="#ImportantConferences">conferences</a> 
              and <a href="#ImportantCommunities">communities</a> that provide 
              the context and environment within which collaboration and 
              collaboratories are investigated.</p>
              
              <p><span class="subhead">ACT -- The Alliance for Community Technology</span><br>
              <a href="http://www.communitytechnology.org/" target="_blank" class="smallerText">http://www.communitytechnology.org</a><br>
              This site links 
              philanthropies, universities, and communities so that people 
              can use information and collaboration technologies to meet 
              social needs. The program is committed to a human-centered 
              focus on the creation, use, evaluation, and propagation of 
              appropriate technologies in support of communities, whether 
              these communities are defined by geography, organizational 
              structure, or common interest.</p>
              <p><span class="subhead">ACT database of groupware solutions</span><br>
               <a href="http://www.communitytechnology.org/products/groupware.html" target="_blank" class="smallerText">http://www.communitytechnology.org/products/groupware.html</a><br>
               Searchable database of reviewed and rated collaboration tools 
               with feature descriptions, indentified strengths and weaknesses,
               links to producers, screenshots, and more.</p>
              <p><span class="subhead">CREW Resources</span><br>
              <a href="http://www.crew.umich.edu/resources.htm" target="_blank" class="smallerText">http://www.crew.umich.edu/resources.htm</a></p>
              <p><span class="subhead">DOE National Collaboratories Research Program</span><br>
              <a href="http://doecollaboratory.pnl.gov/overview.html" target="_blank" class="smallerText">http://doecollaboratory.pnl.gov/overview.html</a><br>
               This program couples a coordinated set of infrastructure 
               and middleware projects with a set of real world pilot 
               projects. The goals of this program are to:
               <ul>
                 <li>Demonstrate the impact of collaboratories on DOE
                     science and engineering.</li>
                 <li>Develop new collaboration technologies and 
                     infrastructure.</li>
                 <li>Integrate new and existing collaboration
                     technologies into powerful environments.</li>
                 <li>Carry out pilot projects in partnership with DOE
                     science programs and industry.</li>
                 <li>Broker community-wide agreement on standards and
                     architecture.
               </ul>
              <p><span class="subhead">The EMSL Collaboratory Homepage</span><br>
              <a href="http://collaboratory.emsl.pnl.gov/" target="_blank" class="smallerText">http://collaboratory.emsl.pnl.gov/</a></p>
              <p><span class="subhead">Michigan Ohio Computer Human Interaction (MOCHI) </span><br>
              <a href="http://www.usabilityfirst.com/MOCHI/" class="smallerText" target="_blank">http://www.usabilityfirst.com/MOCHI/</a><br>
              MOCHI is the Southern Michigan Northern Ohio Local Special 
              Interest Group in Computer Human Interaction (SIGCHI) of the 
              Association for Computing Machinery. The scope of our chapter 
              is broadly defined to include assessment, design, evaluation 
              and implementation of computing technology to facilitate work, 
              education, entertainment and social interaction.</p>
              <p><span class="subhead">Multi-Disciplinary Collaboration</span><br>
              <a href="http://www.multi-collab.org" target="_blank" class="smallerText">http://www.multi-collab.org</a><br>
              This multi-disciplinary collaboration project is a joint effort 
              among researchers from three different universities examining 
              collaborations within and among disciplines, groups and organizations 
              using a variety of research strategies. A series of studies 
              is testing the role of geographic and functional distance 
              in the appropriation of ideas, privacy, attribution of credit, 
              and productivity of collaborative projects. </p>
              <p><span class="subhead">Usability First - Groupware resources </span><br>
		    <a href="http://www.usabilityfirst.com/groupware/cscw.txl" target="_blank" class="smallerText">http://www.usabilityfirst.com/groupware/cscw.txl</a><br>
		    This page is intended to be as comprehensive an index as possible 
		    for Groupware resources.</p>
            </p>
					
