            <div class="head"><a name="ImportantConferences"></a>Important 
              Conferences</div>
              <p><span class="subhead">CSCW 2004</span><br>
              <a href="http://www.acm.org/cscw2004/" target="_blank" class="smallerText">http://www.acm.org/cscw2004/</a><br>
              Conference on Computer Supported Cooperative Work<br>
              November 6-10, 2004, Chicago, Illinois, USA<br>
              CSCW is a leading forum for presenting and discussing research and development achievements in the design, introduction, and use of technologies that affect groups, organizations, communities, and societies. As well as covering traditional topics around working and working relationships, the conference is expanding its focus to include all contexts in which technology is used to mediate communication, coordination, cooperation and even competition among people, including entertainment and the remote sharing of intimate moments.</p>



            <p><span class="subhead">CHI 2004</span><br>
              <a href="http://www.chi2004.org/" target="_blank" class="smallerText">http://www.chi2004.org/</a><br>
              Conference on Human Factors in Computing Systems<br>
              April 24-29, 2004, Vienna, Austria<br>
              The annual CHI conference is the leading international forum for the exchange of ideas and information about human-computer interaction (HCI). Diverse members of the global HCI community meet at the CHI conference to share the excitement of discovery and invention, to make and strengthen professional relationships and friendships, and to tackle real world problems.</p>
              
              <p><span class="subhead">ECSCW 2003</span><br>
              <a href="http://ecscw2003.oulu.fi/" target="_blank" class="smallerText">http://ecscw2003.oulu.fi/</a><br>
              Eighth European Conference of Computer-supported Cooperative Work<br>
              September 14-18, 2003, Helsinki, Finland<br>
              CSCW is a multidisciplinary area of research devoted to the use of computers to support cooperative work. The community encompasses interests in systems development and design, the theory and practice of CSCW, the installation and use of CSCW systems, the application of novel technologies, and the relation between the social and the technical.</p>
              

              
	    <div class="toplink"><a href="#PageTop">(return
