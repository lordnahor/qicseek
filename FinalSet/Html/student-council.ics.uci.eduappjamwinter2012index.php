<div class="page-header">
    <div class="styled_title"><h1>What is AppJam?</h1></div>
</div>
<p>
A tournament where teams create a fully functional mobile application in only one week. At the kick-off event, registered participants will be assigned to a team where they will work together to plan, design, write, debug and test their mobile application. The application guidelines will also be announced at the kick-off
event.
<br /><br />
AppJam is a great way to test, learn, and master coding skills in addition to giving you real life team project experience. Any student with any skill level should participate; your experience and knowledge will take a large part when teams are assigned so experienced and novice members can benefit from each other. A project like this is an amazing resume builder.
<br /><br />
Click on <a href="benefits.php" title="">Benefits</a> to find out more about why you should participate.
