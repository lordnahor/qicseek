<div id="content_text">

<p><strong>A Thirst for Knowledge </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: charu srivastava" src="../../../bin/img/photos/people/charu_srivastava.jpg"/>
<p>Charu Srivastava  
</p>
</div>
<p>After graduating with a degree in computer engineering from the National Institute of Technology, Allahabad India in 2006, Charu Srivastava worked as an IT consultant in India and Spain.</p>
<p> While the work was rewarding, Charu had a thirst for knowledge that could only be quenched by returning to school for an advanced degree.</p>
<p>After talking to Bren School professor Alex Nicolau, Charu learned about the <a href="http://ms-es.cib.na.cnr.it/">Master of Science in Information and Computer Science with a concentration in Embedded Systems</a> offered by the Bren School.</p>
<p>The program provides students the unique opportunity to study for two five-week sessions in the United States at UC Irvine during the summer and for three quarters at the <a href="http://www.cib.na.cnr.it/cibcnr/">Istituto di Cibernetica Eduardo Caianiello</a> in Naples, Italy.</p>
<p>&#8220;My family put in resources and energy into giving me the best education all my life. It was my Dad&#8217;s vision to see me graduate from an American University," Charu said. "I chose UC Irvine because I wanted to be a part of the University of California. The innovation, research and the great exposure in the field of technology I got as a student while studying here, is phenomenal. I am living my father&#8217;s dream and it makes me and my family very happy.&#8221;</p>
<p>In addition to courses, students work on embedded system research projects as part of their thesis and can obtain a Master&#8217;s degree in as little as 15 months. </p>
<p>&#8220;Coming back to university was very refreshing,&#8221; Charu said. &#8220;The freedom of thought, the thirst for knowledge, the drive to conduct research with commitment and self motivation were things I learned from my fellow students and faculty in this program.&#8221;</p>
<p>The research Charu is conducting for her thesis focuses on trying to analyze the virtual machine on embedded devices.</p>
<p>&#8220;The web browsers on mobile phones or PDA's are not very efficient, they can use more improvement,&#8221; Charu said. &#8220;I&#8217;m analyzing the factors that can give the user an internet browsing experience on their mobile device similar to the one they experience on their desktop.&#8221;</p>
<p><strong>SEAMLESS TRANSITION</strong></p>
<p>Charu found the transition to life in the United States was seamless.</p>
<p>&#8220;I didn&#8217;t feel like an outsider at all,&#8221; Charu said. &#8220;I felt very much part of UCI.&#8221;</p>
<p>Charu lives in <a href="http://www.housing.uci.edu/av/">Arroyo Vista</a>, an on campus housing community that features 40 houses, each with 8, 12, or 16 bedrooms and a large shared kitchen and living/dining areas. </p>
<p>&#8220;The housing is very nice and my roommates are great people too,&#8221; Charu said. &#8220;The accommodation is very comfortable.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Charu especially enjoys relaxing at the Starbucks in the UC Irvine student center and taking dancing classes, playing racquetball, swimming, and rock climbing at the Anteater Recreation Center, a 89,000 square foot facility that includes a 10,000 square foot weight room, aerobics and martial arts rooms, three court gymnasium with an elevated running track, one court rounded gym, outdoor lap and leisure pool, a rock climbing wall, and three racquetball courts.</p>
<p>Despite the amenities and laid back living, Charu still misses the food and sights she experienced in Italy. </p>
<p><strong>LOOKING TO THE FUTURE</strong></p>
<p>On track to graduate in December of 2008, Charu would like to join a company in the United States that works on embedded systems solutions and feels that her education has prepared her well for any career path related to computer science.</p>
<p>&#8220;This program has prepared me to face the challenges of technologies in any company related to Computer Science.&#8221; Charu said. </p>
<p>Despite the intense commitment of resources and time, Charu remarks that her time in the Master&#8217;s program is one of the most memorable of her life and would recommend the program to anyone else considering pursuing a Master&#8217;s degree in embedded systems. </p>
<p>&#8220;I think for anyone who is even the least bit interested in technology and is curious of the world should enroll to spend at least one year in an MS program,&#8221; Charu said. &#8220;It opens whole new domains, exposes you to a lot of aspects of the field and brings a level of maturity to your knowledge of the field. It makes one appreciate the works of people in the past and motivates you to contribute something for the future.&#8221;</p>
<p><i>- Eric Kowalik</i></p>


</div>
