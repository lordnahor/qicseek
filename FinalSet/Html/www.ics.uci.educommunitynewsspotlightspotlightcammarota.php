<blockquote>
<div id="content_text">

<p><strong>Once in a Lifetime Opportunity </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: rosario cammarota" height="251" src="../../../bin/img/photos/people/rosario_cammarota.jpg" width="220"/>
<p>Rosario Cammarota
</p>
</div>
<p>Rosario Cammarota (a.k.a. Roio) was looking for a Master's program in information and computer science with a focus on embedded systems, but he wanted to stay in his hometown of Naples, Italy and continue to work full time at the local start-up he joined.</p>
<p>When he saw an advertisement for UC Irvine&#8217;s <a href="http://ms-es.cib.na.cnr.it/">Master&#8217;s Program in Information and Computer Science with a Concentration in Embedded Systems</a>, he knew he had found the program he was looking for.</p>
<p>&#8220;The attractive features of the program were that it was held partially in Italy and partially in the United States and also gave me the opportunity to continue working for my company,&#8221; Cammarota said.</p>
<p>The unique program provides students the opportunity to study for two five-week sessions in the United States at UC Irvine during the summer and for three quarters at the <a href="http://www.cib.na.cnr.it/cibcnr/">Istituto di Cibernetica Eduardo Caianiello</a> in Naples, Italy.</p>
<p>&#8220;For Italian students this is a once in a lifetime opportunity,&#8221; Cammarota said. &#8220;Pursuing a Master&#8217;s program in the United States may seem expensive at first, but if you compare the level of the education with the money you pay, then you&#8217;ll realize that to study here is a very good investment.  In the United States there are more opportunities for improving your future.&#8221;</p>
<p>In addition to courses, students work on embedded system research projects as part of their thesis and can obtain a Master&#8217;s degree in as little as 15 months.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Cammarota is doing his research thesis on performance analysis on modern microprocessors evaluations. Numbers that can give directions in developing improvements on the state of the art of modern microprocessors and/or compilers are important because they give directions for doing things better and better.</p>
<p>The research attempts to answer questions he first asked while doing an undergraduate thesis at the University of Naples &#8220;Federico II&#8221;. Questions like how do you do things faster while at the same time maintaining a well organized source code and which is the more appropriate system (architectures, compilers, operating systems, programming models) in order to solve electromagnetic problems efficiently.</p>
<p>After graduating in 2004 with a degree in electronic engineering, Cammarota wanted to continue his research and after winning a research grant, he began working with Professor Mario Mango Furnari on networking and distributed systems research at the Cibernetics Institute &#8220;E. Caianillo&#8221; of the National Council for Research in Naples.</p>
<p>Then he started working for a start-up company from Cibernetics Institute &#8220;E. Caianillo&#8221; of the National Council for Research in Naples. He believed pursuing a Master&#8217;s degree was the next step in both his education and career and would definitely recommend the program for students interested in learning more about embedded systems.</p>
<p>&#8220;If you are interested in emerging design and co-design techniques for the future embedded systems; and if you want a truly multidisciplinary program at the border of engineering and computer science, then you would probably do very well in this Master&#8217;s program,&#8221; Cammarota said. &#8220;So come on. This program is what you are looking for.&#8221;</p>
<p><strong>SMOOTH TRANSITION</strong></p>
<p>The transition to life in the United States was very smooth for Cammarota, thanks in part to the Bren School advisors and staff.</p>
<p>&#8220;To live in the United States as a UC Irvine student is pretty easy and comfortable,&#8221; Cammarota said. &#8220;Almost all the people I&#8217;ve met have been cordial and have made me feel like I was at home.&#8221;</p>
<p>But Cammarota warns that the transition may not be as easy for non-Italian students when they go to Italy. Students should try and learn a bit of Italian before going.</p>
<p>&#8220;Life in Italy - and Naples in particular - might be quite difficult without a proper tutor,&#8221; Cammarota said. &#8220;The main problem is related to the language, because very few people speak English in Naples, and the people that do are not employed in the offices, i.e. in the places where international students need to go for their accommodations.&#8221;</p>
<p>The growing pains are worth it, though, since students will get to experience the wonders of Naples and the surrounding region.</p>
<p>&#8220;To live in Naples for a while is a good opportunity to enjoy all the culture and beauty in and around it,&#8221; Cammarota said. &#8220;It&#8217;s a really nice city with a lot of culture, perhaps more than other places in the south of Italy.&#8221; </p>
<p><strong>SUN AND SURF</strong></p>
<p>Irvine lacks the splendor of Naples, but Cammarota finds the amenities at <a href="http://www.housing.uci.edu/vp/">Verano Place</a>, one of the on campus graduate communities, more than amenable.</p>
<p>&#8220;The apartment is large and sunny, I feel really comfortable there,&#8221; Cammarota said. &#8220;My next door neighbors are really kind and polite and everything around the apartment is green. I really love it.&#8221;</p>
<p>Despite being a world away, Cammarota doesn&#8217;t miss his family and friends too much and talks regularly with them via the Internet.</p>
<p>In his spare time, Cammarota takes the bus from UC Irvine to Seal Beach to enjoy some fun in the sun.</p>
<p>&#8220;The idea that you can go to the beach not only during the summer, but all year-round, is really exciting,&#8221; Cammarota said.</p>
<p>He also enjoys reading about mathematicians and mathematics, takes a weekly Kyudo class and often goes to the Anteater Recreation Center, a 89,000 square foot facility that includes a 10,000 square foot weight room, aerobics and martial arts rooms, three court gymnasium with an elevated running track, a pool, rock climbing wall and three racquetball courts.</p>
<p>Cammarota will finish his final exams at the end of August and his research thesis by the end of this fall, but finishing the program may just lead him to another beginning.</p>
<p>&#8220;I saw joining this program as a sort of step up in my education and my career, and I strongly feel that my decision has been the right one,&#8221; Cammarota said. &#8220;I&#8217;ve decided to continue working in the embedded system research area and plan to continue at UC Irvine in the Ph.D. program.&#8221;</p>
<p><i>- Eric Kowalik</i></p>


