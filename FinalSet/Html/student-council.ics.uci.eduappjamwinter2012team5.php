				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
				Our app, Gamify, uses gaming elements to incentivize users to set and accomplish goals.  The user sets up goals and sub-goals, represented by games and quests, and is encouraged to achieve these goals through a system of rewards, similar to a massively multiplayer online (MMO)  role-playing game. 
				</p>

