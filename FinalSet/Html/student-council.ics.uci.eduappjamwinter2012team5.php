			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
Our first task was to brainstorm ideas for our app. After mulling over several ideas, we decided on our Gamify app. The next step was to decide on the set of features we would implement, as well as the basic user interface. After this initial planning, each of us had to figure out what we would need to learn in order to create the app, then research and study those topics.  Our research led us to make some final changes on the app’s design. After all the design decisions were made, we delegated the work and started coding.  The final step was to combine all of our work, then test and debug our app.
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
One of the biggest hurdles for our team was scheduling. Week eight of the quarter was very busy for all of us with regards to school work and job interviews.  Finding times when we could all meet was difficult. Another struggle for us was learning new skills in order to program our app. Most of us were us were unfamiliar with developing for Android and working with XML layouts. We also did not have much experience with databases. We had to overcome these obstacles through research and trial and error. 
			</p>
			

            <div class="page-header">
				<div class="styled_title"><h1>Lessons Learned</h1></div>
			</div>
			<p>
We learned that it is very important to have a good design for an application before starting the coding process. Having a clear idea of how the user interface will look and work, as well as how different parts of the program will interact with each other makes the programming the application much smoother. Another lesson that we took from our experience was that it is always better to start working early. Seemingly simple tasks can turn out to be very time consuming, especially when you are working with things with which you are unfamiliar, such as we were with the Android SDK.
			</p>

