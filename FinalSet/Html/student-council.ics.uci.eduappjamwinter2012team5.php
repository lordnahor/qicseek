            <div class="page-header">
				<div class="styled_title"><h1>Sources of inspiration for the project</h1></div>
			</div>
				<p>
				We were inspired by roleplaying games, such as World of Warcraft, which use a system of quests and rewards to encourage people to continue playing them. We thought that “gamifying” tasks in life using a similar system could be used to help people achieve goals and improve themselves.
				</p>
				
