<blockquote>
<div id="content_text">

<p><strong>Foreign Exchange </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: pablo diaz-gutierrez" src="../../../bin/img/photos/people/pablo_diazgutierrez.jpg"/>
<p>Pablo Diaz-Gutierrez</p>
</div>
<p>After receiving a reciprocity fellowship from UC&#8217;s <a href="http://www.cie.uci.edu/index.shtml">Education Abroad Program </a>(EAP) and spending one year at UCI, Pablo Diaz-Gutierrez decided that graduate school was right for him. </p>
<p>The Grenada, Spain native got his B.S. degree from the University of Granada in 2002, and his M.S. from UCI in March 2005, both in computer science. </p>
<p>He had taken a few Individual Study units with professor of computer science Gopi Meenakshisundaram and enjoyed the experience so much, he left his job as a software engineer in Madrid, Spain to return to UCI for graduate school.</p>
<p>Always intrigued by programmable machines and their problem-solving capabilities, Diaz-Gutierrez also notes that computer science can be applied to pretty much anything, thus providing many career options and the opportunity to develop oneself in a variety of fields. </p>
<p><strong>CREATING USABLE 3D MODELS </strong></p>
<p>Diaz-Gutierrez&#8217; research area involves working on computer graphics, specifically organization of geometric data in several ways.</p>
<p>He explains, &#8220;Currently we have technology to acquire extremely detailed 3D models, which are used in fields like architecture, medical imagery, automated rescue missions, entertainment, etc. However, because they are so huge and complex, in order to use those models fast enough to be useful, we need to represent them in non-obvious ways so that they take up less disk space. </p>
<p>That way they are faster to transmit to remote locations, and enable us to display them interactively. Right now, this kind of data is used only on massive super-computers at research centers, and I hope that my research makes it possible to expand its use to the less powerful computers everyone uses.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Along with his advisor, professor Meenakshisundaram, Diaz-Gutierrez has published and presented several conference and journal papers one of which received the 2nd best paper award at Eurographics Conference 2005 in Dublin, Ireland. </p>
<p>Diaz-Gutierrez described the award as &#8220;a rush&#8221; and the most satisfactory feeling he&#8217;s had as a graduate student. </p>
<p>"Pablo is a talented researcher with a charming personality and mature outlook. He is bound to become a popular researcher because of the combined strengths of his technical ability, hard-work, ethics and integrity,&#8221; Meenakshisundaram said. &#8220;His current research will make a big impact in both graphics and database research." </p>
<p><strong>OVERCOMING TEACHING FEARS </strong></p>
<p>Diaz-Gutierrez finds the occasional individual research breakthroughs exciting but also enjoys the daily contact with fellow students his position as a TA for Computer Graphics offers him. </p>
<p>&#8220;The first time I held a discussion session I broke out into a cold sweat just thinking about it. As time went on, I started relaxing and enjoying it. Nowadays I have a blast teaching. Students in the Bren School are generally very understanding with new TAs, which helps a lot with the initial fears. </p>
<p>I have found that the key is preparing well for each class, and bringing new examples and different points of view, whether it is a lecture or an exercise session. Now it seems obvious, but some TAs overlook it, especially the first few times they teach,&#8221; Diaz-Gutierrez said.</p>
<p>Though he communicates daily with his family whom live in Granada, Spain, Diaz-Gutierrez isn&#8217;t lonely. </p>
<p>He has a girlfriend who lives and works in Los Angeles and his roommate in his Verano Place graduate housing apartment is a Philosophy student who offers him an opportunity to take a break from research and exposure to another field of study. </p>
<p>In addition to cooking, which helps him relax; Diaz-Gutierrez also plays soccer regularly, reads, watches movies, is trying to learn Mandarin, Chinese and frequents local coffee shops since the Anthill Pub is closed during the Student Center renovation. </p>
<p><strong>LOOK TO THE FUTURE </strong></p>
<p>His aim is to finish his Ph.D. by Fall of 2007 and he is leaning toward a corporate career path, but has not ruled out academic pursuits. </p>
<p>&#8220;Academia gives you more freedom to pursue research for which you have an interest. On the other hand, industry gives you more resources if what you do is aligned with their goals. If I could choose, right now I would go for an industry research institution. This past summer I interned at IBM Almaden Research Center, and I felt it nicely blended the best of both worlds. </p>
<p>They gave their employees quite a bit of freedom in choosing their research topic as long as IBM could ultimately benefit from it somehow. Another interesting option is trying to launch a start-up company. I&#8217;d like to try while I am still young and have the energy it takes,&#8221; Diaz-Gutierrez said.</p>
<p>A Ph.D. from UCI should provide Diaz-Gutierrez with many career options. </p>
<p>&#8220;UCI is a growing institution with lots of energetic young faculty that want to work with bright young students,&#8221; he says with enthusiasm. &#8220;It is not worth being at the best school if your advisor does not care about your work.&#8221; </p>
<p>He recommends that future Ph.D. students look at the individuals involved, meet their potential advisors before committing even if it means spending a summer doing unpaid research. </p>
<p>The hands-on experience is a realistic taste of what one may end up doing down the road. </p>
<p>A good fit is what&#8217;s needed to make the Ph.D. experience a positive one and it appears that is what Pablo Diaz-Gutierrez has found at the Bren School. </p>
<p><i>- Diane Triantis</i></p>


