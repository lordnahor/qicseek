<div id="content_text">

<p><strong>Father of CAD/CAM </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: patrick hanratty" height="166" src="../../../bin/img/photos/people/pat_hanratty.jpg" width="126"/>
<p>
Patrick Hanratty
</p>
</div>
Sometimes we are motivated by pride. Other times, we are motivated by love. At 71 years of age, both motivate Patrick Hanratty still.
<p>&#8220;Since I have always been a purveyor of 3-D systems, it got to be a little embarrassing that after 30 years of work, 60 percent of the world is still using 2-D,&#8221; laughed Hanratty, founder and CEO of Manufacturing and Consulting Services (Scottsdale, Ariz.).</p>
<p>&#8220;So what I have been working on is automating 2-D drawings to 3-D solids.&#8221; &#8220;Basically,&#8221; he continued, &#8220;it has become my labor of love.&#8221;</p>
<p>His plan is ambitious: to take the entire design process that people use for designing an array of products: golf clubs, watchcases, guns, airplanes, cars, computer housing &#8211; and simplify it. He hopes to cut the training time of designers from six months or more to a day or two.</p>
<p><strong>DOING THE IMPOSSIBLE</strong></p>
<p>&#8220;It was actually declared mathematically impossible (to automate the rendering of 2-D drawings into 3-D) by New York State University (SUNY),&#8221; explained Hanratty. &#8220;And I thought, &#8216;Well, that&#8217;s a good challenge. I like that.&#8217; &#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Hanratty credits artificial intelligence practices that he learned while an ICS Ph.D. student at UC Irvine for providing the foundation for his project. His software not only is able to perform character recognition but also can identify the different annotations associated with the drawing &#8211; and that is where the real meat of the design is. </p>
<p>The first iteration of Hanratty&#8217;s most recent product went recently into beta testing, and the initial response is positive.</p>
<p>&#8220;It has been a very exciting project,&#8221; said Hanratty. &#8220;I think I&#8217;ve succeeded in something that nobody has been able to do before and it has given me a lot satisfaction.&#8221;</p>
<p><strong>CAD&#8217;s EVOLUTION</strong></p>
<p>Success is somewhat commonplace for the trailblazing Hanratty. Anointed the &#8216;Father of CAD/CAM&#8217; for his pioneering work in computer-aided design and manufacturing, industry analysts estimate that 70 percent of all 3-D mechanical CAD/CAM systems available today trace their roots back to Hanratty&#8217;s original code. <br/>
          In the early stages, CAD systems used to be designated into two camps: Hanrattybased and non-Hanratty-based. (See &#8216;Milestones&#8217; below for additional<br/>
        information.) </p>
<p><strong>WALKING ABOUT</strong></p>
<p>Five decades later, Hanratty hasn&#8217;t shown any signs of slowing down. A believer of the peripatetic philosophy &#8211; and grandfather of 13 &#8211; he can be found in the gym three mornings a week, &#8220;as regular as a clock,&#8221; boasted Hanratty. &#8220;I am a strong believer that you just can&#8217;t be a potato and still have your mind active. I think it was Aristotle who taught his students while walking because he believed their minds were so much more active that way.&#8221;</p>
<p>When not in the office (where Hanratty admits that there are weeks he spends between 80-100 hours) or in the gym, one can often find him &#8220;playing in the dirt.&#8221;</p>
<p>Taking in stride the good-natured ribbing from his wife, Sandra, for his gold prospecting hobby, husband (and wife) enjoy the satisfaction of finding beauty among the muck.</p>
<p>&#8220;My goal is to eventually find enough gold so that I can make jewelry to set the opals that I have cut over the years,&#8221; Hanratty said. &#8220;Successful prospecting comes down to just one thing: work.&#8221;</p>
<p>Armed with his shaker pan and digging like hell, Hanratty isn&#8217;t afraid of a little work. His track record proves it. And if he has his way about it, Hanratty plans to wrap up his work in the CAD/CAM field in the next 30 years &#8211; allowing him plenty of free time to go prospecting.</p>
<p>If he ever decides to retire.</p>


</div>
