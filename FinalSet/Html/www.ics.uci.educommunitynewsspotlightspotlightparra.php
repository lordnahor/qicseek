<div id="content_text">

<p><strong>Fun and Games</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: michelle ang" height="146" src="../../../bin/img/photos/people/hector_parra.jpg" width="300"/>
<p>Hector Parra poses in front of <br/>
  Legend of Fiasco, which  he helped <br/>
  create 
  in the   ICS computer <br/>game development class.
</p>
</div>
<p>Hector Parra didn't want to graduate without taking the computer game development class, and while it gave him an opportunity to spend time playing in a virtual world, it also taught him intangible skills he can apply in the real world. </p>
<p>&#8220;This class taught me how to work with other people in a team and it turned out to be an awesome team that was perfect for this sort of game,&#8221; Parra, a senior computer science major said. &#8220;Not only did everyone put in a chunk of the core of the game, but they also put their own little magic into it.&#8221;</p>
<p>The team tossed around various ideas, did they want to make an RPG or fantasy based game, before deciding on a classic beat&#8217;em up game in the mold of Double Dragon and Teenage Mutant Ninja Turtles. </p>
<p>In the game, The Legend of Fiasco, the player uses karate skills and various weapons to defeat black pajama clad ninjas while navigating a colorful 2D world. </p>
<p>It was video games that initially ignited Parra&#8217;s passion for computers.<br/>
  When he was 7 years old, his dad installed Leisure Suite Larry on the family&#8217;s 386 computer.</p>
<div class="img_caption_left">
<object classid="clsid:d27cdb6e-ae6d-11cf-96b8-444553540000" codebase="http://download.macromedia.com/pub/shockwave/cabs/flash/swflash.cab#version=6,0,40,0" height="100" width="100"><param name="src" value="http://www.youtube.com/v/hwOr8-e55us"/><embed height="100" src="http://www.youtube.com/v/hwOr8-e55us" type="application/x-shockwave-flash" width="100"/></object>
<p>Hear Hector's story in his<br/> 
own words, and be sure to <br/>
check out our <a href="http://www.youtube.com/UCIBrenICS">YouTube page</a><br/> 
to watch more student profiles.
</p>
</div>
<p>Parra didn&#8217;t know what the game was about, but he knew he couldn&#8217;t get in to play it because it would ask questions about the band Chicago and President Reagan. </p>
<p>The questions were intended to prevent minors from being able to access the game, due to its adult story line in which the main character spends the game trying to seduce women. </p>
<p>But Parra saw patterns in the questions the game was asking based on how you answered the previous question. He started memorizing the correct answers depending on the patterns and eventually got into the game. </p>
<p>&#8220;I had no interest in the game, I didn&#8217;t know how to play it, I actually had more interest in breaking into it and seeing how it worked and seeing the patterns,&#8221; Parra said.  &#8220;That&#8217;s what initially peaked my interest in computing.&#8221;</p>
<p><strong>NOT GETTING LOST</strong></p>
<p>When deciding where to transfer to from Saddleback College, Parra was looking for someplace he could feel connected to and a place where he wouldn&#8217;t get lost in the crowd. </p>
<p>&#8220;What really caught my attention was the fact that there is a Bren School, the fact that a university would give the respect to a subject that is so widespread and so integrated into other fields it&#8217;s own school, I really feel connected here,&#8221; Parra said. &#8220;I know at a lot of the really big schools, it is really easy to get lost and especially in the other schools that do not have computer science separate, the program is part of the engineering school and gets lost in the school itself.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;It is a very intimate place, you can talk to people, you can express your ideas freely,&#8221; Parra said. &#8220;If you want to do research you can just ask, its so simple.&#8221;</p>
<p>Parra also likes how the Bren School fosters an environment that stimulates innovative collaborations and allows anyone, even undergraduates, to get involved in faculty research projects.</p>
<p>Working as a member of Informatics professor Gillian Hayes' research team, Parra is helping to develop a digital repository and visual schedule system for use in schools and homes by individuals with autism spectrum disorder (ASD).</p>
<p>This system will enable caregivers and individuals with ASD to more quickly and easily interact with the schedules, marking when activities are completed and rearranging schedule items with streamlined, simple interactions. </p>
<p>The system will also enable new modes of interaction, including greater communication and collaboration among caregivers. </p>
<p><strong>GIVING BACK</strong></p>
<p>The Bren School has provided Parra a lot of opportunities, so when there was an opportunity to give back he jumped at the chance. </p>
<p>Parra became an inaugural member of the <a href="../../../ugrad/ambassadors/index.php">Bren School Student Ambassador</a> program launched last year. </p>
<p>The group of undergraduate students conducts community outreach visits to students in high schools, middle schools and community colleges and shares their reasons  why the Bren School has met or exceeded their expectations for a dynamic undergraduate experience.</p>
<p> &#8220;If you are driven, if you know what you want to do, if you know what you like and you just want an opportunity to do it this is the right place to come to,&#8221; Parra said. </p>
<p><i>- Eric Kowalik</i></p>


