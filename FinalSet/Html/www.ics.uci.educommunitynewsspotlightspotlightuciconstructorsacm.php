<p>
Three undergraduate students from the Donald Bren School of Information and Computer Sciences have earned a spot in the finals of the Association for Computing Machinery&#8217;s 
<a href="http://icpc.baylor.edu/">International Collegiate Programming Contest</a>.
</p>
<p>
The UCI Constructors team, consisting of students&#160;<b>Michael Cappe</b>, <b>Nick Ajalat</b> and <b>Alan Castro, </b>and sponsored by computer science senior lecturer <b>Richard Pattis</b>, took second place in the ICPC&#8217;s Southern California Regional on Nov. 9. They competed in a field of 91 teams from 31 colleges and universities from Southern California and Las Vegas to earn the honor. They are among only 120 teams invited to compete at the ICPC World Finals, which will be held in Ekaterinburg, Russia, in June 2014.&#160;</p>
<p>&#8220;<span>For the first time in my memory, UCI's teams started seriously studying and practicing for this contest, so I expected our standings to improve, but I was bowled over by a second-place finish,&#8221; Pattis said. &#8220;These students did an extraordinary job.&#8221;</span></p>
<p>
Now in its 38th year, the ICPC attracts computer science students from around the globe. This time, nearly 30,000 contestants from more than 2,300 universities in 91 countries competed in regional competitions at some 300 sites worldwide. According to the ICPC world finals <a href="http://icpc.baylor.edu/download/worldfinals/pdf/Factsheet.pdf">fact sheet</a>, "The contest fosters creativity, teamwork, and innovation in building new software programs, and enables&#160;students to test their ability to perform under pressure. Quite simply, it is the oldest, largest, and most&#160;prestigious programming contest in the world."</p>
<div style="float: right; margin: 1em 0;"> 
