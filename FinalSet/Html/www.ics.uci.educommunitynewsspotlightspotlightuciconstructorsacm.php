<p>
<strong>UCI team takes second place in regional ACM competition, invited to finals in 
Russia next June</strong>
</p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<div style="margin-bottom: 1em;">
<a href="https://www.facebook.com/media/set/?set=oa.614969481894336&amp;type=3"><img alt="The UCI Constructors team, consisting of students Michael Cappe, Nick Ajalat and Alan Castro," src="../../../bin/img/photos/spotlight/UCIConstructorsACM.jpg" style="width: 100%; border: 0;"/></a>
<br/>
<small>From front to back: Nick Alajat, Alan Castro and Michael Cappe</small>
</div>
