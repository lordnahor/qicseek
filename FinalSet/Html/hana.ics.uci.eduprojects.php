        <h1>Projects</h1>
        <img src="images/projects/success_wizard.jpg" alt="" width="191" height="191" class="fltlft" />
        <h2>Collaboration Success Wizard</h2>
        <p> Are you working on a collaborative project, with people in other organizations and/or locations, that doesn't seem to be as effective as it could be? Based on nearly 2 decades of research on scientific collaboratories, the Collaboration Success Wizard is a web-based service that asks members of a project about their collaboration whether it's currently ongoing, occurred in the past, or is being planned for the future. The Wizard can identify potential vulnerabilities and areas for improvement, based on an individual respondent's answers, and can suggest things to do and to discuss as starting points for overcoming them.  In addition, the Collaboration Success Wizard team will review and analyze the aggregate responses from a project and provide feedback to the project.</p>
        <a href="projects/cross-cultural-teams.php"><img src="images/projects/cross_cultural.jpg" alt="" width="191" height="191" class="fltrt" /></a>
        <h2><a href="projects/cross-cultural-teams.php">Cross-Cultural Teams</a></h2>
        <p> Collaboration across different national cultures can be very challenging within teams. Workers from China and Japan, for instance, differ from US workers on many aspects of conversation-- from the differences in behavior depending on the rank of who you are talking, to differences in the interpretation of a blank expression, to who is involved in making decisions and how trust is established. Collaboration amongst team members from different cultures can be even more challenging when it is distributed, and especially when team members' workdays do not overlap.</p>
        <img src="images/projects/google_apps_2.jpg" alt="" width="191" height="191" class="fltlft" />
        <h2>The Rollout of Google Applications on Campuses</h2>
        <p>Google Apps are being deployed in thousands of organizations around the world, including schools and universities.  We are examining the adoption and use of these cloud-based services on four campuses of the University of California system.  We are interested to learn how Google Apps may go beyond simply replacing existing IT services to being integrated into the classroom curricula and the research activities of these learning environments.</p>
        <a href="projects/shape-factory.php"><img src="images/projects/shape.jpg" alt="" width="191" height="191" class="fltrt" /></a>
        <h2><a href="projects/shape-factory.php">Shape Factory</a></h2>
        <p> An increasing amount of work happens over distance, with some collaborators able to work face-to-face and others working from distributed locations, whether telecommuting from home or working from satellite offices. Shape Factory is a simulation game that serves as a platform for studying this sort of distributed work. We examine questions about the complexities and challenges of working across distance and explore strategies for overcoming some of these challenges. </p>
        <a href="projects/science-collaboratories.php"><img src="images/projects/collaboratories.jpg" alt="" width="191" height="191" class="fltlft" /></a>
        <h2> <a href="projects/science-collaboratories.php">Science of Collaboratories</a></h2>
        <p> We have identified over 200 Collaboratories (distributed teams) in the Sciences and Humanities and compiled descriptions and basic facts about each.</p>
        <br clear="all" />
        <a href="projects/creativity-intercultural-collaboration.php"><img src="images/projects/intercultural.jpg" alt="" width="191" height="191" class="fltrt" /></a>
        <h2><a href="projects/creativity-intercultural-collaboration.php">Creativity in Intercultural Collaboration: A Lab Study</a></h2>
        <p>Cultural exchange and intercultural collaboration are seemingly at the heart of creative power and innovation. Despite the persistence of the myth of the individual genius, history shows us that the technologies that have enabled us to thrive were the result of group collaboration and the exchange of ideas between societies. Seemingly then, embracing intercultural collaboration would be a boon for organizations that want to generate the most creative ideas and develop new technologies. However, little research has focused on creative idea generation in intercultural collaboration. We are investigating creative tasks and idea generation in intercultural pairs to understand the relationship between diversity of national culture, the interaction process, and the innovativeness of the group product.</p>
        <a href="projects/collaboration-nonprofits.php"><img src="images/projects/non-profit.jpg" alt="" width="191" height="191" class="fltlft" /></a>
        <h2> <a href="projects/collaboration-nonprofits.php">Collaborative Work of Nonprofit Organizations</a></h2>
        <p> In this research, we study the work of nonprofit organizations and, in particular, the diversity of collaborations they undertakeâ€”collaborations with other nonprofits, with governmental agencies, and with members of the public. We examine the challenges that surface in these collaborations and explore opportunities for information systems to support both the collaborative work and the causes around which these collaborators have rallied together.</p>
        <a href="projects/virtual-team.php"><img src="images/projects/virtual_teams.jpg" alt="" width="191" height="191" class="fltrt" /></a>
        <h2> <a href="projects/virtual-team.php">Virtual Teams</a></h2>
        <p>Globally distributed teams have become an essential part of how companies conduct businesses in the modern day era. &quot;Out of sight, out of mind&quot; is a problem that all remote satellite team members face under this setting. Past literature has suggested best practices on how to mitigate the issues that are inherent in collaboration across distance.  Our research focuses on understanding how remote workers modify their work patterns in order to maintain visibility while not being able to be physically present with their core teams.</p>
        <a href="projects/telemedicine.php"><img src="images/projects/telemedicine.jpg" alt="Telemedicine" width="191" height="191" class="fltlft" /></a>
        <h2><a href="projects/telemedicine.php">Telemedicine</a></h2>
        <p>Providing quality patient care has always been an ongoing issue and priority in medicine. It is even more so the case when the physician is not physically present at the same location as the patient. A current solution being used in some hospitals is the idea of a remote control and videoconferencing robot, where the physician has remote control of the robot's position and motion. Patients are able to interact with the physician through a series of cameras and microphones that are attached to the robot and vice versa. We want to understand the perspectives of physicians on social ergonomics and the idea of presence of using this remote presence telemedicine robot to interact with patients</p>
        <a href="projects/brainstorming.php"><img src="images/projects/brainstorm.jpg" alt="brainstorming" width="191" height="191" class="fltrt" /></a>
        <h2><a href="projects/brainstorming.php">Brainstorming</a></h2>
        <p> Brainstorming is an idea generation technique that occurs in early-stage design activities.  Brainstorming and its variations have become synonymous with idea generation, and are used virtually across all phases of software development. Our research aims to better understand how brainstorming is practiced and how we can better support it in a variety of different settings.</p>
