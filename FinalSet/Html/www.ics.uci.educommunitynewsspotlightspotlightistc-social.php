</p>
<p>
<b>ISTC-SOCIAL 2013-14 FELLOWS &amp; PROJECTS:</b>
</p>
<!-- -->
<!-- -->
<!-- -->
<p>
JED BRUBAKER, ICS <br/>
<b>&#8220;Digital Gravetending: Moderating and Mediating Online Identities for the Deceased&#8221; </b>
<br/>
Demonstrates how social media platforms and user practices are changing the conditions under which Americans experience death and mourning.
</p>
<p>
MARK DUROCHER, Social Sciences <br/>
<b>&#8220;Evaluating Data: The Emerging Economy of Social Media Marketing&#8221; </b>
Explores the evolving landscape of the marketing industry and the specific services and technologies that social media agencies offer.
</p>
<p>
JULIA HAINES, ICS <br/>
<b>&#8220;The Seed Accelerator Model and the Development of Global Innovation
Ecosystems&#8221; </b>
<br/>
Focuses on three innovation hubs in different stages of growth &#8212; Silicon Valley, Singapore and Santiago, Chile &#8212; to investigate the effects of startup programs at the micro and macro levels.
</p>
<p>
ELLIE HARMON, ICS <br/>
<b>&#8220;Experiencing (Dis)Connection: Mobile Technologies in the Wilderness&#8221; </b>
<br/>
A study of the experience of connectivity through a multi-sited ethnographic study of three situations: working professionals, suburban families and long-distance hikers on the Pacific Crest Trail.
</p>
<p>
ALEXANDER KNOEPFLMACHER. Social Sciences <br/>
<b>&#8220;Digital Treasure Chests: Ethnographic Approaches to File-Sharing Communities&#8221; </b>
<br/>
An ethnographic portrait of the materialities of file-sharing and hard-drive curation at an online file-sharing community.
</p>
<p>
ALLISON LASKEY, Social Ecology <br/>
<b>&#8220;Permaculture and Dynamics of Rootedness and Connectivity&#8221; </b>
<br/>
An investigation into knowledge-making and knowledge-sharing practices among the permaculture community.
</p>
<p>
COURTNEY LODER, ICS <br/>
<b>&#8220;Beyond Non-Use: Cryptographic Practice as Infrastructural Negotiation and Resistance&#8221; </b>
<br/>
An inquiry into the ways people engage with systems, platforms and web services in the presence of ideological difference.
</p>
<p>
STEPHEN REA, Social Sciences <br/>
<b>&#8220;Garbage People and Model Workers: Bodies, Computers, and Productivity in South Korea&#8221; </b>
<br/>
An examination of macro-level institutional concerns with the embodied micro-practices of computer use in Korea.
</p>
<p>
NICK SEAVER, Social Sciences <br/>
<b>&#8220;Computing Taste: The Making of Algorithmic Music Recommendations&#8221; </b>
<br/>
A multi-sited ethnographic study of music recommendation researchers at corporate and academic sites in the United States, that will explore the motivations behind design decisions that shape filtering algorithms.
</p>
<p>
JOHN SEBERGER, ICS <br/>
<b>&#8220;We the Author: Synchronous and Deeply Asynchronous Collaboration in Humanities Scholarship&#8221; </b>
<br/>
A yearlong, qualitative study of the role of collaboration in the acquisition of expertise among six newly matriculated Ph.D. students in the humanities.
</p>
