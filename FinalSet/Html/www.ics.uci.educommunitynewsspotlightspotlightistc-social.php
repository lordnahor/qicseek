<p><b>ISTC-Social welcomes an interdisciplinary class of fellows for 2013-2014</b></p>
<!-- -->
<!-- -->
<!-- -->
<p>
<img alt="Bren School Ph.D. student Ellie Harmon is among 10 ISTC-Social graduate research fellows" class="left" src="../../../bin/img/photos/spotlight/EllieHarmon_cropped3x2.jpg" width="100%/"/>
<br/>
<small>Bren School Ph.D. student Ellie Harmon is among 10 ISTC-Social graduate research fellows</small>
<!-- -->
<!-- -->
<!-- -->
</p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<p>
Why do some backcountry hikers joke about how great it is to find a &#8220;three-bar forest&#8221; 
&#8212; a stretch of wilderness where their cell phones get a strong signal? How do people 
decide who gets to run the Facebook page of their deceased friend? How do the engineers 
behind online music-recommendation systems guess that fans of The National might also 
like Arcade Fire?
</p>
<p>
These are just a few of the questions being explored by this year&#8217;s class of fellows at the <a href="http://socialcomputing.uci.edu/">
Intel Science and Technology Center (ISTC) for Social Computing</a>, one of a network of 
university-based research centers supported by Intel Corporation. Based at UC Irvine, 
ISTC-Social is focused particularly on interdisciplinary investigations of the social and 
cultural aspects of information technology and digital media.
</p>
<p>
Graduate fellows are expected to participate in all ISTC activities and not only at UCI: 
Visits to Intel Labs in Portland and the center&#8217;s partner campuses (Georgia Tech, NYU, 
Cornell and Indiana University) are also part of the program. 
</p>
<p>
Informatics professor Paul Dourish of the Donald Bren School of Information and 
Computer Sciences, one of ISTC-Social&#8217;s co-directors, says this sort of collaborative
thinking has been a great fit at UCI.
</p>
<p>
&#8220;That&#8217;s one of the reasons Intel came to us when thinking of developing an ISTC in the 
area of social computing,&#8221; Dourish says. &#8220;They were very attracted to the way we put 
informatics and anthropology together. The question of what technology does is not
simply a technical question. You need to understand technology as a social and cultural
phenomenon.&#8221;
</p>
<p>
When it first launched, ISTC-Social awarded four yearlong fellowships at UCI for the 
2012-2013 academic year. This year, all four of those graduate student researchers have
received another fellowship, and they have been joined by six more scholars.
</p>
<p>
&#8220;As we&#8217;ve ramped up and figured out how to work on different, independent projects 
at the same time &#8212; all of which feed into larger-scale programs &#8212; we were more 
comfortable with more fellows this year,&#8221; says Bill Maurer, dean of the School of Social 
Sciences and the other co-director of ISTC-Social.
</p>
<p>
Five of the 2013-2014 ISTC-Social Fellows are from the Bren School of ICS, one is 
from the School of Social Ecology, and four are from Social Sciences. Nick Seaver, a 
Ph.D. student from the Department of Anthropology, is investigating how online music-
streaming companies like Pandora develop the algorithms that determine what kind of 
music to recommend to users, based upon their existing choices.
</p>
<p>
&#8220;My last project was on the history of the player piano, and this one draws on that: 
They&#8217;re both examples of what happens when technology gets mushed up with music 
&#8212; which is supposed to be an expressive thing, not an automatic thing,&#8221; Seaver says. 
&#8220;People say there&#8217;s no accounting for taste. Well, these big systems are trying to do 
exactly that, and they have very complicated ideas about whether or not this is something 
you can compute.&#8221;
</p>
<p>
Like Seaver, Ellie Harmon from the Department of Informatics is in her second year as 
an ISTC-Social fellow. Last year, she researched how suburban families use smartphone 
technology to stay connected; this year she has just returned from traversing the Pacific 
Crest Trail, the 2,663-mile track that stretches from the U.S.-Mexico border to the U.S.-
Canada border, where she examined how hikers experience smartphone connectivity (or 
the lack of it).
</p>
<p>
&#8220;There&#8217;s this idea in popular media that getting rid of this technology lets you be better, 
more present, which I don&#8217;t think is quite right,&#8221; Harmon says. &#8220;We&#8217;re all kind of used to 
being able to get in touch with anyone at any time, and when we can&#8217;t, it&#8217;s stressful.&#8221;
</p>
<p>
&#8220;One of the things that&#8217;s so interesting about smartphones is how personal they are for 
people,&#8221; she says. &#8220;I think it becomes an important part of who we are, and how we 
experience ourselves, and how we experience other people.&#8221;
</p>
<p>
Harmon adds that, coming from a computer-science background, she appreciates the 
opportunity to kick around ideas with the social scientists at the center. &#8220;That&#8217;s one of 
the really nice things about the ISTC,&#8221; she says. &#8220;It&#8217;s one thing to go over to another 
department and take a bunch of classes. It&#8217;s another to have a space where everyone&#8217;s 
just coming together anyway.&#8221;
</p>
<p>
Which is, of course, the whole point of ISTC-Social. &#8220;I think we&#8217;re on the cutting 
edge here,&#8221; says Bill Maurer. &#8220;We&#8217;re really blazing a trail for multi-investigator, 
multidisciplinary work.&#8221;
<!-- -->
