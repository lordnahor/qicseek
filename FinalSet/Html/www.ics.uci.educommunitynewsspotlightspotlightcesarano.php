<p><strong>Staying Current </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: marco cesarano" height="167" src="../../../bin/img/photos/people/marco_cesarano.jpg" width="220"/>
<p>Marco Cesarano
</p>
</div>
<p>Marco Cesarano knows the field of computer science is constantly evolving and if he wanted to keep his skills current, he would have to take advantage of opportunities to improve his skills and knowledge.</p>
<p>When he saw an advertisement at the National Council of Research in his hometown of Naples, Italy for UC Irvine&#8217;s <a href="http://ms-es.cib.na.cnr.it/">Master&#8217;s in Information and Computer Science with a Concentration in Embedded Systems</a>, he knew he found an avenue that would allow him to take his skills and knowledge to the next level.</p>
<p>&#8220;A Master&#8217;s degree gives students an opportunity to go beyond course work and get a taste of academic research,&#8221; Cesarano said. &#8220;A Master&#8217;s gives students training for a career and an idea about whether they would like to continue in a Ph.D. program.&#8221;</p>
<p>The unique Master&#8217;s program provides students the opportunity to study for two five-week sessions in the United States at UC Irvine during the summer and for three quarters at the <a href="http://www.cib.na.cnr.it/cibcnr/">Istituto di Cibernetica Eduardo Caianiello</a> in Naples, Italy.</p>
<p>In addition to courses, students work on embedded system research projects as part of their thesis and can obtain a Master&#8217;s degree in as little as 15 months.</p>
<p>Cesarano&#8217;s research project focuses on system virtualization, the possibility to run different operating systems, and different runtime environments, on the same hardware.</p>
<p>&#8220;This has many implications for traditional systems, for example to consolidate different applications on the same server, saving a lot of money; but also for embedded systems, to enhance the security and the isolation between different modules, making it more simple to debug them,&#8221; Cesarano said.</p>
<p><strong>LIFE IN THE OC</strong></p>
<p>Cesarano misses his family and friends while studying at UC Irvine, but feels it is worth the time away in order to acquire the knowledge and skills needed to fulfill his career goals.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>The seamless to transition to life in the United States has made the time away less difficult and Cesarano has noted that life in the United States is not very different from life in the Italy, except for an Italian, &#8220;it&#8217;s always difficult to find good food in another place.&#8221;</p>
<p>Cesarano found the on campus apartments in Verano Place, one of three campus communities for graduate students, beautiful.</p>
<p>&#8220;The apartments are well-furnished with everything a student needs for a pleasant and comfortable stay,&#8221; Cesarano said. &#8220;Moreover, the academic departments aren't too far due to the campus topology: everything is placed in a circle around Aldrich Park.&#8221;</p>
<p>An avid naturalist, Cesarano enjoys walking through Aldrich Park and soaking in the 19-acre botanical garden in the center of the campus with more than 11,000 trees and shrubs.</p>
<p>&#8220;UC Irvine is a big campus, much different that the Universities in Italy,&#8221; Cesarano said. &#8220;Here I found an enormous quantity of facilities for students, which makes the hard task of studying more relaxing and fulfilling.&#8221;</p>
<p>Cesarano will finish his thesis and obtain his degree in September of 2008 and plans to continue his academic career at UC Irvine, as a Ph.D. candidate or researcher.</p>
<p>&#8220;Even though embedded systems is a huge field, with numerous applications, a Master's in Embedded Systems provides the broad understanding and methods necessary to deal with them,&#8221; Cesarano said.</p>
<p><i>- Eric Kowalik</i></p>


