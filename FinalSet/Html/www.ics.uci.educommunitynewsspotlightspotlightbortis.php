<blockquote>
<div id="content_text">

<p><strong>Dashboard Confessional </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: gerald bortis" height="182" src="../../../bin/img/photos/people/gerald_bortis.jpg" width="171"/>
<p>Gerald Bortis</p>
</div>
<p>Orange County native, Gerald Bortis, got a taste of computer science as an undergraduate at UCI, but that did not take him as far as he wanted to go. </p>
<p>It was apparent to him that the Bren School was a hot bed of research in software engineering and new areas like Interactive and Collaborative Technology (ICT), programs which other more traditional computer science schools did not offer. </p>
<p>Bortis entered the Ph.D. program because he wanted to delve deeper into studies and become an expert in the field.</p>
<p>Bortis received his bachelor&#8217;s degree from UCI in Spring of 2005 and entered the Ph.D. program directly in Fall, 2005. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
"Computer science seemed elegant to me because it has the structure and methodologies of a science, while still maintaining elements of art and creativity," Bortis said. "This applies especially to software development since the materials you're working with are just bits, meaning your options as a designer and a problem solver are virtually limitless."
<p>The fact that UCI is surrounded by high-tech companies providing opportunities through internships while in the Ph.D. program was not lost on Bortis. </p>
<p>He currently juggles an ongoing internship as a Software Engineer at <span>Mirth Corporation</span> in Irvine with his academic endeavors while residing in Fullerton with his parents and two brothers.</p>
<p>Looking to complete his Ph.D. by 2009, Bortis fills his limited free time with reading, playing bass and drums in his garage band on weekends, training to compete in the Orange County Marathon, and catching as many Anteater basketball games as possible. </p>
<p>While Bortis finds the fast paced corporate world, especially in the software development field, very appealing, he is also drawn to teaching as a profession. </p>
<p>From his advisor, Professor Andre van der Hoek of the Department of Informatics, Bortis learned that even a computer science class can be interesting and fun. </p>
<p>While he enjoys working on innovative projects which explore areas like healthcare and open-source software in a competitive corporate setting, he is drawn to academia as an avenue to share his experience and knowledge and help others expand theirs. He is keeping his long-term options open. </p>
<p>"Gerald is a bright and talented young man who undoubtedly will make deep research contributions that, because of his unique perspective from working at <span>Mirth</span>, will have an important effect on the day-to-day lives of all software developers," van der Hoek said. </p>
<p>Bortis is working on a new research project called Dashboard which allows software developers to become more aware of the work that is going on in their environment and helps them to more effectively collaborate with their teammates. </p>
<p>&#8220;I believe that the research can be applied beyond just software development to awareness of tasks in general, whether in the workspace or elsewhere, allowing teams to communicate and collaborate more easily,&#8221; Bortis said. </p>
<p>&#8220;I am currently in the design phase and will soon begin developing an early prototype system on a large interactive screen which will be on display in the new Bren Hall building in early 2007. </p>
<p>&#8220;The diversity of knowledge and expertise offered by both the Computer Science and Informatics courses and faculty lets you study subjects like algorithm theory and software engineering alongside anthropology and philosophy. </p>
<p>"For me, the program has made computer science not only about writing code, but also about people and society and how technology can affect both, which has definitely broadened my knowledge and scope of research. </p>
<p>"I believe that UCI will continue to excel through the Informatics program and be one of the top schools in the nation for software engineering, and I wanted to be part of the progression.&#8221; </p>
<p><i>- Diane Triantis</i></p>


</div>
</blockquote>
