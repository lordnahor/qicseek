		<div id="content_title">Replacing Unix .(dot) Files &raquo;</div>

			<p>
			</p><blockquote>

			<div id="content_text">

        <p>If you are unable to use the module command or if your prompt looks
          unual, then you're probably missing &quot;.&quot; files. Every account
          start off with basic statup files. These files may have been removed
          mistakenly if the user was not careful. </p>
        <p>
          The following files MUST be in your home directory (H-drive):</p>
        <ul>
          <li> .login</li>
          <li> .logout</li>
          <li> Depending on which shell you're using, you will also have
on of the following file:<br>
            <ul>
              <li>.cshrc (<i>If you've never changed your shell, this is the default one setup for all users.</i>)</li>
              <li>.bashrc</li>
              <li>.tcshrc</li>
              <li>.zshrc</li>
            </ul>
          </li>
        </ul>
        <p>If one or all of the above files is missing, you will have problems
          logging onto the ICS hosts.</p>
        <p>You can add them by following these steps:</p>
        <ol>
          <li> SSH to openlab.ics.uci.edu from a Windows machine<br>
            <ul>
			  <li>If you can NOT SSH to openlab, have the lab assistant reset your
            password and try again.</li>
              <li>If it still does not work, please ask the lab assistant to file
                an online report form.</li>
			</ul>
		  </li>
		  <br>
          <li>Type in the following command at the prompt:<br>
            <ul>
			  <li><font face="Courier New, Courier, mono">%cd /opt/local/etc/skel/</font></li>
			</ul>
		  </li>
		  <br>
          <li>Copy all missing files ending with &quot;@&quot; to /home/&quot;login_name&quot;/.&quot;filename&quot;<br>
            Remember to put the dot before the destination file.<br>
            <ul>
			  <li>For example, if you are missing the .cshrc file you would type the
            following at the prompt:</li>

              <ul>
                <li><font face="Courier New, Courier, mono">%cp cshrc /home/&quot;login_name&quot;/.cshrc</font></li>
              </ul>
              <li>You may also want to look at the other <font face="Courier New, Courier, mono">example.*</font> files as some have more options than other that you may like to use.</li>
            </ul>
          </li>
		  <br>
          <li>After copying all the files you are missing, return to your home
            directory by typing the following command at the prompt:<br>
            <ul>
              <li><font face="Courier New, Courier, mono">%cd</font></li>
            </ul>
		  </li>
		  <br>
          <li>Finally, you need to <font face="Courier New, Courier, mono">chmod</font>
            all the files you copied. This basically means changing the permission
            on the file so that it is only changeable by you. Type the following
            command for each of the files you copied:
            <ul>
			  <li><font face="Courier New, Courier, mono">%chmod 744 .&quot;filename&quot;</font></li>
			  <li>For example, for the cshrc file, it would be:<ul>
                  <li><font face="Courier New, Courier, mono">%chmod 744 .cshrc</font></li>
                </ul></li>
			</ul>
          </li>
		  <br>
          <li>You're DONE! </li>
        </ol>
        <p><em>If you have any questions regarding the above procedure, please
          contact the Lab Assistant. </em></p>

			</div>

