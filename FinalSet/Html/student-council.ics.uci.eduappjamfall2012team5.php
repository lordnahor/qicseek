            <div class="title white">
                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>We chose to keep the current version of our project on Alex's
					computer and only make changes to it from there. We all worked on
					the coding, so Alex was responsible for sending out updated versions
					of the code every day. We didn't have strict individual roles, only
					general day-to-day assignments, so our updates varied quite a lot.
					We would all email in the changes we worked on, and Alex would
					manually include them in the current version. This solution worked
					out alright for us.</p>
			</div>

            <div class="title white">
                <h1>Major hurdles that had to be overcome</h1>
			</div>
			<div class="description cms">
				<p>None of us had any experience developing for any mobile platform
						before, so we were very confused at first. We read tutorials and
						sample code for days before starting work on the project, which
						made it difficult for us to complete what we originally had planned.
						We wanted to have a database and GPS functionality, but neither of
						those are present in our submission. We possibly could have
						improved if we had assigned roles to each of our group members and
						met more frequently during the week.</p>
			</div>

