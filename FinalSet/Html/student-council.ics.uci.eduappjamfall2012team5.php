            <div class="title white">
                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>ICS Earth is an application written for Android 4.0 that aids ICS
					students in locating professors and events on the UCI campus. It
					allows for users to search through a list of professors by name,
					phone number, office location, or department, and through a list of
					events by name, date, time, location, or category. The user enters a
					search term and a list of matching results are listed. Selecting a result
					will bring up a more detailed view that lists more information about
					the event or professor. A map of the ICS buildings is provided as a
					reference.</p>
			</div>

            <div class="title white">
                <h1>Programming languages, compilers and IDEs used</h1>
