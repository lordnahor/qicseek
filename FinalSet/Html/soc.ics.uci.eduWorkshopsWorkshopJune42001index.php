                  <p>This 
                    workshop explored the social underpinnings of collaboration 
                    in science. The workshop examined three broad classes of issues:</p>
                  <ol> 
                    <li>How would one judge a collaboration 
                      or a collaboratory to be successful?</li>
                    <li>What are the most important individual 
                      and group processes that affect the success of collaborations?</li>
                    <li>What organizational, professional, 
                      and cultural issues affect the character of collaborations?</li>
                  </ol>
                  <p>The goal of 
                    the workshop was to define issues, assess the current state 
                    of understanding, and lay the groundwork for future SOC activities.<br>
                    <br>
                    To this end, 42 experts from the <img src="/images/MargaretWorkshop.jpg" width="225" height="169" align="left">University 
                    of Michigan and other institutions gathered on June 4-5 to 
                    consider the social aspects of collaboratory development and 
                    use. After some introductory material, each of these themes 
                    was addressed in parallel breakout sessions, followed by plenary 
                    sessions in which each breakout group reported their results, 
                    which were discussed by all. On the final morning separate 
                    breakout groups were assigned each question, and were asked 
                    to identify next steps for research on each question. What follows is a high-level description of each of these sessions, with links to further notes on each session.<br>
