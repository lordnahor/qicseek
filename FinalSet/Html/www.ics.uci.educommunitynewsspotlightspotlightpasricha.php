<div id="content_title">Sudeep Pasricha spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>Better, Faster, Smaller </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: gabriela marcu" height="161" src="../../../bin/img/photos/people/sudeep_parischa.jpg" width="171"/>
<p>Sudeep Pasricha
</p>
</div>
<p>Growing up in New Delhi, India, Sudeep Pasricha was fascinated with computer games, programming and networking and from a very early age knew he wanted to work in the field of computers. </p>
<p>In 2000 Pasricha received a bachelor's degree in Electronics and Communications Engineering from the Delhi Institute of Technology in Delhi, India. </p>
<p>Research lab work at a multi-national semiconductor company followed, where Pasricha's interest in electronic system design was piqued. </p>
<p>He found himself challenged with design problems and determined to find solutions to alleviate the burdens of designers working in this arena. </p>
<p>As he delved into deeper study of electronic system design, Pasricha realized that time, guidance and focus were needed to achieve his goal. </p>
<p>Impressed by the excellent research papers authored by the Center for Embedded Computer Systems (CECS) faculty and graduate students, Pasricha knew he wanted to pursue graduate studies at UCI. </p>
<p>After receiving his master's degree from UCI in 2004, Pasricha entered the Ph.D. program which he plans to complete in June 2007.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>"UCI and Bren School have some of the best faculty in the area of computer system design," Pasricha said. "It has been a privilege to interact with the knowledgeable faculty and enthusiastic graduate students for my research." </p>
<p>&#160;</p>
<p><strong>MAN BEHIND THE MACHINE  </strong></p>
<p>In a nutshell, Pasricha's research focuses on enabling digital electronic systems such as mobile phones, digital cameras and portable MP3 players to be designed faster, be smaller in size, perform better and consume less power (especially useful for battery-driven gadgets).</p>
<p>Those gauges inside our cars, the medical device that saves lives, as well as the headphones that pipe music into our ears as we walk or fly all have designers.</p>
<p>What Calvin Klein, Tommy Hilfiger and Donna Karan are to fashion, people like Pasricha are to small electronics we use on a regular basis.</p>
<p>The next time you purchase a smaller, flatter cell phone or a digital camera that is the size of a credit card and weighs about as much, you'll have Pasricha to thank. </p>
<p>"I believe that my research will be extremely useful in the design of the next generation of small, feature-rich electronic devices which will be used in the automotive, industrial, entertainment, communications and medical domains," Pasricha said. </p>
<p>Pursuit of a Ph.D. does not come without challenges, goal-setting and mapping out a path of study is paramount as is careful selection of a research area after diligent topic exploration. </p>
<p>Acknowledging that a Ph.D. program is not for everyone, Pasricha offers some advice to those that do choose that course. </p>
<p>"Once you select an area to do research in and decide on an advisor, do a comprehensive survey of existing research and try to identify what areas and open problems you can address in your Ph.D," Pasricha said. "Learn to think outside the box, if you hope to make a lasting contribution in your chosen area."</p>
<p><strong>ACHIEVING BALANCE </strong></p>
<p>It also is imperative students learn to balance school and a social life. </p>
<p>"Make sure you have a hobbies that can help you relax, because there will be times when the stress from work will become almost unbearable," Pasricha said.  "That's when you need to dust off that old guitar from your garage and start strumming or take a crack at yoga/meditation to calm yourself." </p>
<p>Living in Verano Place, one of the on campus graduate housing community, allows Pasricha to take advantage of the many relaxing activities available on or near campus. </p>
<p>His favorite campus hangout is the UCI Bookstore where he enjoys lounging on the comfortable chairs, browsing through the wide variety of books and magazines. </p>
<p>He frequently attends plays and musical performances at the Bren Events Center and Irvine Barclay Theater and also enjoys travel, video games, running, racquetball, photography, music, movies and an occasional bungee jump, hike or rock climb. </p>
<p>All these things help him relax and temporarily escape the stress of conference and journal research and paper deadlines. </p>
<p><strong>HARD WORK PAYS OFF </strong></p>
<p>Grueling work can be rewarding, however, as Pasricha found out when he received the Best Paper Award at the ASPDAC 2006 (Asia and South Pacific Design Automation Conference) held in Yokohama, Japan in January.</p>
<p>Selected from over 400 papers submitted by industrial and academic researchers from all over the world, this was quite an accomplishment of which Pasricha is justifiably proud. </p>
<p>"It was a validation of the work I had been doing for my Ph.D. and meant that all those sleepless nights spent trying to wrestle with difficult research problems did not go in vain after all!"</p>
<p>As much as Pasricha enjoys research and would be open to corporate offers, his first choice upon receiving his Ph.D. is to go into academia. </p>
<p>Through his experience as a teaching assistant, he realized that he truly likes teaching. </p>
<p>"It gives me a great deal of satisfaction to get people excited about topics that I am passionate about," he said. </p>
<p>It is that passion that drives him to achieve his goals and eventually will motivate others to do the same.</p>
<p><i>- Diane Triantis</i></p>


