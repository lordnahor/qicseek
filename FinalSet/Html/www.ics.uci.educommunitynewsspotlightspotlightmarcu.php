<blockquote>
<div id="content_text">

<p><strong>Trailblazer  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: gabriela marcu" height="166" src="../../../bin/img/photos/people/gabriela_marcu.jpg" width="171"/>
<p>Gabriela Marcu</p>
</div>
<p>A college level software engineering class is the last place one would expect to find Play-Doh. </p>
<p>But for Informatics major Gabriela Marcu, Play-Doh played an important role in helping her understand software engineering. </p>
<p>"We used Play-Doh and other materials to build bridges and towers to learn how other fields go about designing their products, in order to then apply those lessons to designing software," Marcu said. "Disciplines like architecture and civil engineering have long-established methods and processes that software development doesn't. You wouldn't see a bridge that can't support anything or a house without any doors, but unfortunately software is still often this incomplete and faulty." </p>
<p>As part of the first class of the new Informatics major, Marcu, will learn how to create better, more people friendly software.</p>
<p>"It's a really exciting program," Marcu said "The curriculum is very unique and we're really breaking ground. The Bren School always told us that companies are saying that we're the kinds of students they want. Well now I'm actually hearing that myself from company employees and representatives." </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>The Informatics major covers the design, implementation, use and impact of information technology. Compared to traditional computer science, which primarily focuses on the internal features of computer systems, Informatics is also concerned with computer systems and their surrounding context -- the people and organizations that use them and the problems they need to solve.</p>
<p>"Without knowing how to get out from behind our keyboards and communicate with our customers and users, we can't do anything," Marcu said. "Informatics is bringing these missing pieces to the puzzle. We can create better software with fewer bugs that is easier to use." </p>
<p><strong>MOTHER KNOWS BEST</strong></p>
<p>Initially Marcu was contemplating a Computer Science major, feeding a passion that her mother kindled in her during high school. </p>
<p>Not too keen about joining a bunch of computer geeks in the Advanced Placement (AP) Computer Science class, Marcu's mother encouraged her to give it a try. </p>
<p>"Once I took the class, everything about it just clicked," Marcu said. "I ended up doing better than the computer geeks in that class, and went on to take the second AP Computer Science exam before I decided it would be my major." </p>
<p>But her choice of computer science as a major came with two conditions. She wanted to work with people, not sit in front of a computer all day and she didn't want to work with hardware, computer chips weren't her thing. </p>
<p>Informatics' orientation toward people and non-hardware focus were what made Marcu decide to switch to Informatics before the start of her freshmen year.</p>
<p>Change has always been a part of Marcu's life. Born in Romania, she lived in Canada for nine years before her family settled in Windsor, California in Sonoma County. </p>
<p>"Windsor is a very small town, so I was ready for a big change," Marcu said. "I always saw college as a chance to meet many new people and experience and learn many new things." </p>
<p>Los Angeles was too chaotic and polluted for Marcu who saw the OC as the perfect place to spend her college years. </p>
<p>"There are so many things to do right here, and many exciting places nearby like Los Angeles, Disneyland, the Orange County Performing Arts Center, and the beach," Marcu said. "But you don't have to live every second in the middle of all that excitement &#8211; it's a great place to study, too."</p>
<p>She was also no stranger to campus. Her brother Cristian, was an electrical engineering major, and Marcu had visited campus and heard all of his stories about how great the school, campus and surrounding area was. </p>
<p>"I primarily chose UCI because of the Bren School," Marcu said. "I knew UCI would be at the forefront of computer science education since it was a separate school for computer science." </p>
<p><strong>SPREADING THE WORD</strong></p>
<p>UCI has also offered Marcu a plethora of opportunities for campus involvement. </p>
<p>Marcu was so ecstatic about Informatics she formed the Informatics Student Association, (INSA). The idea had been floating around in her head for a while but only after one of her Informatics professor's encouraged the class to be involved on campus did Marcu decide to make it happen.</p>
<p>INSA's mission is to help Informatics majors succeed academically and professionally, and to help build a strong Informatics community by helping all Informatics majors in different classes get to know each other and get to know their professors. </p>
<p>The group is not exclusive to Informatics majors and INSA tries to hold joint events with other clubs in order to give Informatics majors more opportunities to meet people, and give others opportunities to learn about Informatics. <br/>
                <br/>
			  "Within the Bren School, UCI, our community, and in the industry -- we want everyone to know about it," Marcu said. "We want students to know this may be the right major for them. We want Informatics to have a strong place as a well-known major on campus, just like the Information and Computer Science major is."</p>
<p>The group has held various events to achieve its mission, including Scheme, Java and Eclipse workshops and review sessions. </p>
<p>INSA also holds social events and game nights with free pizza and put together a Robocode Tournament which pitted undergraduates, graduates and professors in a robot building challenge. </p>
<p>The group has also held recruiting and information sessions with companies such as Apple, Microsoft, Deloitte, Hitachi Consulting, and Sun Microsystems and has worked to expose students to other careers and companies that are less known and which represent some more nontraditional areas of computer science. </p>
<p>With the help of the group's faculty advisors, INSA held a unique and exclusive corporate visit to State Street, a provider of financial services. The visit included presentations, guided tours and visits with employees. </p>
<p>At the end of the year, INSA hosted their first annual Informatics Banquet. All Informatics students and professors were invited to celebrate the successful first two years of the program over a free dinner provided by the Informatics department. </p>
<p>"It's amazing to think that we've come this far and we really have made a difference in our own little Informatics community," Marcu said. "More students have met each other, and more cliques have been merged. Many students have given us positive feedback about our workshops, review sessions, and tutoring. It's amazing to look back and feel like this has all affected people in some positive way."</p>
<p><strong>UNLOCKING THE CLUBHOUSE</strong></p>
<p>Marcu's on campus involvement is not limited to INSA, she is also the Vice President of the Women in Computer Science (WICS) group on campus. </p>
<p>"We have outreach activities which target young girls and expose them to computer science," Marcu said. "I know how important our efforts are because many girls aren't exposed to computer science, and I myself wasn't interested in trying it until my mom encouraged me."</p>
<p>Marcu helped plan WICS' first Dinner with the Dean and Women of Information and Computer Sciences. The event brought together female students, professors, and Dean Debra J. Richardson to discuss issues that face women in computer science. The professors shared stories about how they overcame obstacles and how they became successful. </p>
<p>"There were some amazing discussions that evening and it was really great to hear about professors talking about the same things I'm going through," Marcu said. "Everyone there had a great time and I not only felt that my hard work had paid off, but I was really inspired and motivated by the experience." </p>
<p><strong>GUIDED BY EXPERIENCE</strong></p>
<p>Marcu is also a member of the SAGE (Student Achievement Guided by Experience) Scholars Program. </p>
<p>Created in 1999 by the UC Office of the President at UCI and UC Berkeley, the program enlists corporate sponsors who team with UCI to offer scholarships and paid multi-year internships to talented, highly motivated students.</p>
<p>About 30 local companies sponsor the program and offer students interviews for paid internships with a 2-year time commitment and up to $12,000 in scholarships.</p>
<p>In addition to the internship, SAGE also offers classes and individual guidance on how to interview, write quality resumes, give professional presentations and good cubicle manners that are meant to prepare students to interview successfully with companies. </p>
<p>"The part of this program which has affected me most is the encouragement and guidance I received to help me figure out what I'm really passionate about" Marcu said. "The focus is not on getting just any job or making money, it's finding a career that you are really passionate about. That's what's going to make you good at what you do, successful, and most of all, happy."<br/>
                <br/>
			  Only a junior, Marcu is already thinking about life after graduation. There are two areas she is currently interested in; software engineering and computer generated imagery (CGI) and animation for films.</p>
<p>Due to a SURF-IT fellowship -- Summer Undergraduate Research Fellowship in Information Technology, Marcu worked with Informatics professor Andr&#233; van der Hoek on visualizing parallel workspace activities on a next-generation, massively-tiled display system. </p>
<p>"We're working on putting a visualization of developers' activities within a software project on a large display system in order to support the continuous coordination of developers and managers," Marcu said. "By having a large visualization of their parallel activities, those involved on the project can view and discuss this data, and use it to coordinate their efforts as they see fit" </p>
<p>She is also working with Informatics professor Bill Tomlinson on studying how to make the movements of an animated character more fluid, elegant, and lifelike using her 15 years of experience in dance. </p>
<p>"I hope that through continued research and some internships, I can find what I really want to do, and what I'd like to study in graduate school," Marcu said.</p>
<p>Marcu will also be a Microsoft Student Partner 2006-07, a role that will see her bringing Microsoft's newest technologies to campus and giving demos and teaching students about the newest uses and applications of technology.</p>
<p>Her active involvement has helped her define her college experience, something she says future students should think about. </p>
<p>"If you take advantage of many opportunities on campus, you'll get the most out of college," Marcu said. "Once you stop telling yourself you don't have time and you start making time, you find yourself with the most amazing experiences and learning so much more. Opening yourself up to opportunities opens so many doors. Getting involved in clubs, doing research, getting an internship, going abroad--they all define your college experience, and lead to even bigger and better things."</p>
<p>In spite of her hectic schedule, Marcu still finds time for fun. She was part of an intramural flag football team her freshmen year and also enjoys going to the Anteater Recreation Center to work out or ride a stationary bike while watching the big screen TV. </p>
<p>She also enjoys watching movies and playing video games, walking around Aldrich Park, taking trips to the local malls and the beach.</p>
<p><i>- Eric Kowalik</i></p>


