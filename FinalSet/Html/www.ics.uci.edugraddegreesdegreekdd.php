</p><div class="blue_bar"><strong>More about the degree...</strong></div>
<p>The goal of the M.S. concentration in Knowledge Discovery and Data is
 to educate students in both the fundamental principles of computational
 methods for modeling data, as well as to provide a broad foundation in 
emerging methods for knowledge discovery and data mining. </p>
<p>Technological advances in digital data collection, memory capacity, 
and computational power have revolutionized our view of data analysis in
 recent years. The volumes of data being collected in science, business,
 medicine, and government are truly vast in nature.</p>
<p> Across all of these areas, there is a rapidly increasing demand for 
better theories and tools to provide users with improved understanding 
of their data and to leverage their data for decision support.</p>
<p>Knowledge discovery in databases (KDD) is an emerging discipline 
within computer science, focused on the principles of how patterns and 
structure can be inferred from large data sets. It is an area of 
significant academic interest and research opportunity. </p>
<p>For example, a Special Interest Group in Knowledge Discovery in 
Databases (SIGKDD) was recently started by the Association for Computing
 Machinery (ACM) to promote both research and professional activities in
 this area; a journal called Data Mining and Knowledge Discovery was 
started in 1997; and the field sponsors an annual international 
conference with over 500 attendees. </p>
<p>In addition, the National Science Foundation has recently begun a 
large interdisciplinary research program in Knowledge and Distributed 
Intelligence (KDI), based in part on recent research and interest in 
KDD. </p>
<p>Industry participation is also very active with broad demand for 
graduates in this area, across a wide variety of companies engaged in 
leveraging scientific and business data for strategic purposes.</p>


