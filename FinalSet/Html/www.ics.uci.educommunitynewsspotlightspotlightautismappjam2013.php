<p><b>Student-developed apps benefit persons with autism</b></p>
<p>
This spring 108 students from various schools across the UC Irvine campus participated in the very first <a href="http://thecenter4autism.org/appjam">Autism AppJam</a>, presented by the Center for Autism &amp; Neurodevelopmental Disorders of Southern California and directed by informatics assistant professor Gillian Hayes. 
</p>
<p>
<!-- -->
<!-- -->
<iframe frameborder="0" height="260" src="http://www.youtube.com/embed/khiPzNG-qX8" width="450"></iframe>
<!-- -->
<!-- -->
</p>
<p>
Over the course of two weeks, 20 interdisciplinary teams, each with five to six students, developed mobile applications designed to improve the lives of children and/or adults with autism spectrum disorder. Nine industry judges, including representatives from Blizzard Entertainment, Google, IBM, the Irvine Unified School District and Yahoo, evaluated the apps based on their relevance to autism, uniqueness, creativity and usability, among other criteria. 
</p>
