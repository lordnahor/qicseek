<div class="section">
<h3>AUTISM APPJAM 2013 WINNERS:</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="Visual Reader" class="right" src="../../../bin/img/photos/spotlight/autism_appjam_2013/AutismAppJam_VisualReader.jpg"/></p>
<p><b> 1<sup>st</sup> place <br/> Visual Reader </b></p>
<p>Allows users to touch a word in a presented reading passage and see a visual representation of the word, to help teach children the relationship between text and the physical world. If the user taps on the word &#8220;happy,&#8221; for example, happy faces and images of happy people appear on the screen. The framework behind this simple concept can be applied for readers at any level.</p>
<b>Team Imaginateurs:</b> Eddie Lei, Ryan Phung, Sreevatsa Sreeraman, Kenneth Fernandez, Eliena Mahgerefteh
<!-- -->
<!-- -->
<p class="image_with_wrapped_text"><img alt="ImLost" class="right" src="../../../bin/img/photos/spotlight/autism_appjam_2013/AutismAppJam_ImLost.jpg"/></p>
<p><b> 2<sup>nd</sup> place <br/> ImLost </b></p>
<p>ImLost addresses a security issue in the autism community. While lost and confused, it is often difficult to call for help and communicate one&#8217;s location. ImLost presents a simple one-button interface that allows lost individuals to notify emergency contacts via text messaging, and to relay real-time GPS information. </p>
<b>Team Solace:</b> Sivan Goldstein, Timothy Chan, Lucinda Lim, Derek Omuro, Ramsha Qasim
<!-- -->
<!-- -->
<p>&#160;</p>
<p class="image_with_wrapped_text"><img alt="College Hero" class="right" src="../../../bin/img/photos/spotlight/autism_appjam_2013/AutismAppJam_CollegeHero.jpg"/></p>
<p><b> 3<sup>rd</sup> place <br/> College Hero </b></p>
<p>College Hero is a visual task management iOS application. Via a questionnaire, it assists college students with the executive function &#8212; the set of mental processes that involve activities like planning, organizing and managing time. The app&#8217;s pre-made questions and its built-in templates help create different types of tasks based on priority. The app features a configurable interface, allowing users to customize colors, sounds and profile based on their individual preferences. </p>
<b>Team College Hero Guild:</b> Albert Jo, Jeremy Stroman, Johannadel Leovonchiong, Michael Matthews, Faisal Chabani
