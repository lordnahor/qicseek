<div id="content_title">Debra J. Richardson spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>Climbing High </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: dean debra j. richardson" height="256" src="../../../bin/img/photos/people/debra_richardson.jpg" width="182"/>
<p>
Debra J. <br/>Richardson
</p>
</div>
As an undergraduate at UC San Diego, Debra Richardson held thoughts of becoming a math teacher. Fortunately for the Bren School and computer technology in general, an introductory programming course caught her eye. The rest, as they say, is history.
<p>Today, Richardson is the Ted and Janice Smith Family Foundation Dean of the Donald Bren School of Information and Computer Sciences &#8211; and her ambitions stretch far beyond teaching mathematics.</p>
<p>"During my tenure, I am striving for ICS to climb into the top 10 among computer science programs in the country," says Richardson. </p>
<p>Also very important is bringing more women and minorities into the field of computer science, she says.</p>
<p>During Richardson's time as dean, the roster of ICS faculty and staff has grown 25 percent in an effort to keep pace with student interest. Since 1998, enrollments have grown by more than 200 percent while maintaining ICS' traditional high quality of students, Richardson notes.</p>
<p><strong>BUILDING ON STRENGTHS<br/>
          </strong><br/>
        One of the most flexible computer science programs in the nation, ICS has built upon its broad foundation &#8211; from computer systems to the social impacts of computing &#8211; as it expands into graphics and visualization, security and cryptography, Internet-based information systems and ubiquitous computing (what's been termed the "third wave" of computing in which technology recedes into the background of daily life). </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>The school also is well known for its strength in interactive and collaborative technologies based in biology, medicine and physical sciences, as well as social sciences and humanities.</p>
<p>"It would have been difficult to do a better job than Debra has done at steering the school through a period of rapid change and growth," says Pierre Baldi, professor of computer science and director of the UCI Institute of Genomics and Bioinformatics. "She has been able to keep her focus on the most important issues and use diplomatic skills that are not always easy to find among computer scientists."</p>
<p><strong>A VISIONARY LEADER<br/>
              <br/>
        </strong>Richardson made her mark in the field of information and computer science with advances in an area known as "specification-based testing."</p>
<p>Traditionally, most software testing has consisted of determining whether a program performed its required tasks only after implementation. Richardson theorized that testing software throughout its development &#8211; particularly early on &#8211; would reduce problems and costs.</p>
<p>"If an error is made early in the lifecycle of software and it goes undetected until the end product, it costs hundreds of times more to fix than if it is caught early on," says Richardson.</p>
<p>Because of her success as a researcher, teacher and administrator, Richardson increasingly finds herself becoming a role model for other women in the field, and she is taking advantage of that status to attract more women into the profession.</p>
<p>However, she finds current trends disturbing. She notes that across the country only 9 percent of computer science faculty members are women. (At UCI, the figure is 20 percent.) Even more troubling is the decline in female students. During the 1980s, 35 percent of computer science students were female. Today, they make up only 28 percent &#8211; and only 15 percent in research universities.</p>
<p>"Women have always been underrepresented in the sciences as a whole," Richardson says. "But over the last 15 years the representation of women in other sciences has gone up, while in computer science it's actually gone down.</p>
<p>"Part of the problem," she continues, "is that most people think of computer scientists as geeks who sit in front of a computer all day designing programs. Computer science is much broader than that. There are applications of computer science in the arts, biology, medicine, the environment and transportation, just to name a few. When recognized, these social and collaborative sides of computing energize women in the field."</p>
<p>Popular computer games create another problem, she says. "Many computer games over the last 15 years have had a violent aspect to them in which most girls are not interested. So boys play games and think they are computer savvy. Unfortunately, in the classroom then, boys think they know it all and girls feel incompetent."</p>
<p>To counter these effects, Richardson is concentrating on outreach efforts to bring more women and other underrepresented populations into the field. She is on the Board of Directors at Girls Inc., an organization that encourages girls to finish high school and enter college, particularly as math and science majors. </p>
<p>As part of these efforts, she has arranged for young girls to spend a day on campus observing and learning. </p>
<p>"Computing is so pervasive in our society right now," Richardson says. "We need more women and underrepresented minorities in computer science. Without these different perspectives, we're limiting the way we can transform society through technology."</p>
<p>&#8212; <i>Alan Janson</i></p>


