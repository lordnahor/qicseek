<blockquote>
<div id="content_text">

<p><strong>Eligibility</strong></p>
<p>All full-time UC Irvine students who are registered with the university for the entire academic year are eligible to enter the competition. </p>
<p>Individuals who do not meet this requirement may be part of an existing team provided at least two (2) of the competitors on the team are current UCI students. </p>
<p>At least one of the team members must be an ICS student. Teams must have a minimum of three (3) members and not exceed a maximum of six (6) members. Teams must be led by a current UC Irvine student. </p>
<p>To participate in the competition, you must register online. </p>
<p>Once registered, teams will be assigned industry/faculty mentors according to the criteria delineated in the registration form.</p>
<p>Teams can independently identify and secure an industry and/or faculty mentor for the competition.</p>
<p><strong>Forming a Team/Obtaining Additional Team Members</strong></p>
<p>Searching for team members? Attend the graduate student Matrix Mixer on October 15 or the undergraduate student mixer on October 29. </p>
<p>Students working to form a team or add team members also are encouraged to use the <a href="extreme_recruit.php">hITEC forum</a> as a resource. </p>
<p>In addition, interested participants will have one last chance to find team members at the competition kickoff even in January.</p>
<p><strong>Refining a Product Idea</strong></p>
<p>Teams are encouraged to meet with our volunteer experts  to refine their ideas prior to their first competition submission. </p>
<p>Luis Vasquez, Director of OCTANe's LaunchPad program, is available Thursday afternoons in the Calit2 building, office 4106. Or email him at <a href="mailto:luis.vasquez@octaneoc.org">luis.vasquez@octaneoc.org</a>. </p>
<p>Teams also can contact <a href="http://techcoastworks.com/">Tech Coast Works</a> CEO  <a href="http://techcoastworks.com/"></a>  <a href="mailto:jeff.greenberg@techcoastwork.com">Jeff Greenberg</a>. </p>
<p><strong>Workshops</strong></p>
<p>The Merage School of Business offers an extensive array of workshops related to both competitions. </p>
<p>Additionally, students have access to most OCTANe workshops free of charge by signing up with <a href="http://www.octaneoc.org/load_content.asp?page_id=44&amp;from=44">OCTANe Next.</a></p>
<p>Students are also encouraged to view the <a href="../../../bin/pdf/ics_hitec_ppt.pdf">hITEC overview presentation</a>. </p>
<p><strong>Team Integrity</strong></p>
<p>Once the Preliminary Product Description has been submitted, 50% of the team must remain intact for the rest of the competition. </p>
<p>100% of the team must remain intact after submitting the Preliminary Product Specifications.</p>
<p><strong>Product Plan/Idea</strong></p>
<p>Teams must present an original idea.  </p>
<p>The product/idea must not be backed by incubators, existing companies, venture capitalists or other investors. </p>
<p>Teams or team members that have received any form of venture capital financing for their competition entry plan or a likeness thereof may not participate in the competition. </p>
<p>Any team receiving venture capital funding while participating in the competition will be disqualified.</p>
<p><strong>Documents and Demos</strong></p>
<p>Documents should be single spaced with a font size between 10 and 12 points. </p>
<p>Demos are required for the final phase of the competition. </p>
<p>Teams will have a chance to present their projects and demos in front of a panel of judges during that time.</p>
<p><strong>Awards</strong></p>
<p>Cash awards will be given to the top three teams. </p>
<p>The first three teams will automatically qualify as finalists in the Merage School Business Plan Competition for a chance to win additional prizes and the chance to present to a VC company and potentially receive funding up to $1 million. </p>
<p>Please note in order to advance to the <a href="http://www.merage.uci.edu/ResearchAndCenters/Beall/SYCR.aspx">Stradling Yocca Carlson &amp; Rauth Business Plan Competition,</a> the team must include a student from the Merage School (either an MBA or BIM student). </p>
<p> In addition, all teams receiving an award will qualify to participate as a sponsor in Project ICS in order to help develop their products. Project ICS submissions must occur within the academic year immediately following the competition (certain restrictions apply). </p>
<p><strong>Judging Guidelines</strong></p>
<p>The judges are professionals who come from various backgrounds.</p>
<p>Judges are experts in the process of starting a business including business plan developments, or in the technologies the students are developing. </p>
<p>The judges consider many different criteria when evaluating concept papers and the business plans. </p>
<p>Some of the elements that are considered are:</p>
<ul>
<li>Does the technological/design innovation provide a sustainable, competitive advantage?</li>
<li>Does a competitive advantage exist over other products in this application?</li>
<li>Does the functionality satisfy the application?</li>
<li>Is there a significant return on investment to user?</li>
<li>Is the product description clear, detailed and descriptive?</li>
<li>Is there a market need for this product?</li>
<li>Has the user been realistically identified?</li>
<li>Are the benefits to the user clear and sufficient to result in purchase?</li>
<li>Have specific user interfaces been provided? </li>
<li>Is the user interface well described? </li>
<li>Are the requirements thorough, realistic and accurate?</li>
<li>Is there a block diagram? </li>
<li>Have all of the major modules been described in detail?
			    </li>
</ul>


