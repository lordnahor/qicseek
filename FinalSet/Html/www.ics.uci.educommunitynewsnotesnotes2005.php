<a name="lathrop_nih"></a><strong>IGB awarded $1.8 million NIH grant</strong>
</p>
<p>
Richard Lathrop, professor of  computer science, in collaboration with professors from the Departments of Medicine, Biological Chemistry, and Pathology in the UCI School of Medicine, has received a grant for $1.8 million to apply machine learning to cancer. Researchers will study mutations in a central tumor suppressor protein (p53), which is involved in half of all human cancers.
</p>
<p>The research is fostered by the UCI Institute for Genomics and Bioinformatics (IGB), an umbrella of collaboration among over 80 laboratories spanning five Schools at UCI. </p>
<p>Funding is provided by the Innovations in Biomedical Computational Science and Technology (BISTI) program at NIH.  Lathrop, professor of computer science, is the principle investigator and the co-PIs are Pierre Baldi (computer science) and Rainer Brachmann (School of Medicine). </p>
<p>Brachmann is involved in a large multi-laboratory collaboration to find pharmaceutical rescue agents for mutant p53 in human cancers; and Baldi, Director of the IGB, applies statistical and machine learning methods to drug discovery in cancer and other diseases.</p>
