<p>
<strong><a name="suprise"></a>NSF study will probe the quintessence of surprise</strong>
            </p>
<div class="img_caption_left">
<img alt="photo::professor pierre baldi" height="90" src="../../../bin/img/photos/faculty/pfbaldi.jpg" width="70"/>
<p>
Pierre<br/> Baldi</p>
</div>
Professor of computer science <strong>Pierre Baldi</strong> and information theorist Laurent Itti of the University of Southern California are joining with electro physiologist Douglas Mu&#241;oz of Queens University, Canada on a
           
              
              study of how brains measure &#8220;surprise&#8221; in a data stream.
<p>The computational theory of surprise was developed by Baldi in 1999 and first published in 2002. The NSF award will allow the multi-organizational team to put the theory to test. Early laboratory experiments have shown the Bayesian model of surprise outperforms previous theories in predicting human responses to suddenly unusual stimuli.</p>
<p>&#8220;We believe this approach has potential for profoundly changing our current notions of how &#8216;important&#8217; or &#8216;surprising&#8217; information is understood,&#8221; said Baldi, also the Director of the Institute for Genomics and Informatics (IGB) at UCI. </p>
<p>Baldi and Itti will present the outlines of their new mathematical understanding of surprise &#8212; along with their empirical studies on human subjects that support their theory &#8212; on Wednesday, December 7 at the Neural Information Processing Systems (NIPS) Conference in Vancouver, British Columbia. </p>
<p>The new NSF project combines forces of Baldi&#8217;s computational and theory labs with the modeling and psychophysics lab of USC&#8217;s Laurent Itti, and expands the research to include neurophysiological studies of brain mechanisms in Mu&#241;oz electrophysiology lab at Queen&#8217;s University (Canada). Both experiments and modeling will be combined in the project to develop a new understanding of how the brain codes for novelty and importance.</p>
