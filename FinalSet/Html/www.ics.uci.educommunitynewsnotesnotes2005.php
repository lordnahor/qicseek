<a name="welling"></a><strong>Professor receives NSF CAREER award</strong>
</p>
<p>
  <strong>Max Welling</strong>, assistant professor of computer science,  received a $450,000 NSF CAREER award. The award will fund research into a new class of probabilistic "undirected bipartite graphical models'' (UBG), that extract meaningful representations of input data. </p>
<p>The special structure of UBG models allows handling of large data-sets and very fast query processing which could make them useful in image restoration and information retrieval.</p>
<p> NSF's <a href="http://www.nsf.gov/home/crssprgm/career/start.htm">Faculty Early Career Development (CAREER) award</a>, recognizes a young researcher's dual commitment to scholarship and education. </p>
