<strong>November 2005</strong>
<p>
<strong><a name="lathrop_innovation"></a>Professor to be honored with Innovation Award</strong> 
</p>
<div class="img_caption_left">
<img alt="photo::professor richard lathrop" src="../../../bin/img/photos/faculty/rickl.jpg"/>
<p>
Richard<br/> Lathrop</p>
</div>
Professor of computer science <strong>Richard Lathrop</strong> and professor of microbiology and molecular genetics G. Wesley Hatfield will be recognized for their unique entrepreneurial contributions at a November 9 ceremony honoring UCI&#8217;s most significant innovators. The event is part of celebration activities surrounding UCI&#8217;s 40th anniversary.
<p>Lathrop and Hatfield have seen their joint research in the emerging field of synthetic biology, applying computational and biological methods to optimize the self-assembly of chemically synthesized genes, blossom into UC patents, a fully functioning Computational Biology Research Laboratory (CBRL), a thriving start-up company (CODA Genomics, Irvine, CA), a cascade of new research projects criss-crossing the campus, a flood of new external funding for the university, and a growing list of industry applications.</p>
<p>Novel synthetic genes assembled for UCI researchers by the CBRL include a red fluorescent protein that allows cell biologists to track metabolic events through cellular process in living cells; a gene instrumental in the development of a DNA vaccine; and a synthetic version of a newly discovered protein that causes cancer.</p>
<hr size="0"/>
<strong><a name="welling_vision"></a>Welling receives grant to study learning taxonomies of the visual world</strong>
<p>&#160;</p>
<div class="img_caption_left">
<img alt="photo::professor max welling" src="../../../bin/img/photos/faculty/mwelling.jpg"/>
<p>
Max<br/> Welling</p>
</div>
Assistant professor of computer science <strong>Max Welling</strong> along with Pietro Perona at Caltech have received a joint NSF grant to build a computer vision system that learns a taxonomy of visual object categories without supervision.
<p>Learning visual object categories, and recognizing objects in images, is perhaps the most difficult and exciting problem in machine vision today. In light of the fast growing data deluge in science, engineering, industry and society, recognition systems must be able to operate without human supervision. This poses new challenges: How can one learn automatically models of a large number of object classes from unlabelled images? How can one represent these object classes such that they can be searched efficiently? How can one leverage the learnt models to learn new object classes from very few examples?</p>
<p>Welling and Perona plan to combat these challenges by inferring hierarchical representations of object classes from unlabelled image data. Object classes are represented as constellations of parts, where each part extracts shape and appearance information. Non-parametric Bayesian techniques may be employed to organize these object classes into tree-structured representations. The richness of this representation grows incrementally as more data is presented to the system. New similarity measures between object classes naturally derive from this representation facilitating recognition.</p>
