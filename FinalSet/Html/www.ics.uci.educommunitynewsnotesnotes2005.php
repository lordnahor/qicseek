<strong><a name="humbolt_award"></a>Professor earns Humboldt Research Award </strong>
<p>
<strong>Alfred Kobsa</strong>, professor of informatics, has been awarded the coveted Alexander von Humboldt Research Award, conferred by the government of Germany upon senior foreign scientists and scholars. He will be accepting the award next Spring in Germany.             
			</p>
<p>The research award honours the academic achievements of the award winner&#8217;s lifetime. Furthermore, award winners are invited to carry out research projects of their own choice in Germany in cooperation with colleagues in Germany.             
			</p>
<p>Kobsa will work with Professor Oliver G&#252;nther and Dr. Sarah Spiekermann from Humboldt University, Berlin, and jointly study privacy implications of the usage of radio frequency identification tags in the retail industry.
            </p>
<p>The award program was inaugurated in thankful response for the European Recovery Program, or Marshall Plan established by the United States government after World War II. </p>
<p>The Alexander von Humboldt Foundation was entrusted with implementation of the program, which initially recognized senior natural scientists from the U.S., but which has since been extended to internationally recognized scholars in the field of humanities, including lawyers, social scientists, and economists, throughout the world.</p>
<p>However, the emphasis of the program continues to be in the honoring of American natural scientists. The object is to pay tribute to academic accomplishments of award winners and to foster long-term cooperation between foreign and German researchers.</p>
<hr size="0"/>
<strong><a name="taylor_award"></a>Taylor receives NSF cybertrust grant</strong>
<p>
<strong>Richard N. Taylor</strong>, professor of informatics, has been awarded $455,000 in funding from the National Science Foundation to explore software architecture-based approaches for engineering secure decentralized applications with Paul Dourish, associate professor of informatics. 
</p>
<p>The project will, over a period of three years, focus on developing effective design principles and software architectural styles for incorporating trust management in decentralized applications. 
</p>
<p>The project will also study existing decentralized reputation-based systems and investigate how they can better protect and respond against potential malicious attacks.
</p>
