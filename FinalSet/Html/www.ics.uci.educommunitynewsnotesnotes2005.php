<strong>Faculty awarded two Nicholas Prizes</strong>
</p>
<p>
Informatics assistant professors <strong>Cristina Lopes</strong> and <strong>Bill Tomlinson</strong> were awarded two of the four first Nicholas Foundation Prizes for Cross-Disciplinary Research, announced Calit2 Irvine Division Director Albert Yee. The $300,00 prize will support four proposals in all.</p>
<p>Cristina Lopes and Steve C. Cramer, assistant professor in Neurology, were awarded $80,000 to continue researching an integrated computing/communications platform that measures and transmits physical therapy information from the homes of spinal-cord injury patients to UCI via the Internet for more detailed monitoring of patient recovery.</p>
<p>Bill Tomlinson, assistant professor of Informatics and Drama, along with F. Lynn Carpenter, professor of Ecology and Evolutionary Biology, also were awarded $80,000 for their <a href="http://www.ics.uci.edu/%7Ewmt/virtualRaftProject.html">EcoRaft project</a>. Their intent is that the project, a combination fixed/mobile interactive environmental exhibit, can be installed at science museums around the country to help teach environmental science to children.</p>
