<a name="dechter_radcliffe"></a><strong>Dechter awarded Radcliffe fellowship</strong>
<p>
  <strong>Rina Dechter</strong>, professor of computer science, has been awarded a fellowship from the Radcliffe Institute for Advanced Study at Harvard University to pursue her research in automated reasoning.
</p>
<p>Dechter will spend the 2005-2006 academic year at the Radcliffe Institute to pursue her project on advanced search strategies for mixed probabilistic and deterministic networks.</p>
<p>Dechter's research has focused on automated reasoning, i.e., writing computer programs that assist in solving problems and answering questions by drawing conclusions from knowledge (e.g., data and facts). Automated reasoning research has evolved around two distinct ways of representing knowledge. </p>
<p>One way of representing knowledge uses hard constraints. An example of a hard constraint might be "a student cannot enroll in two courses if they meet at the same time." </p>
<p>The other recognizes that knowledge involves uncertainty and therefore uses probabilistic statements such as "if a student has a solid math background, he or she is likely (but not certain) to do well in computer classes." </p>
<p>Many real-life applications involve both types of information. The goal of Dechter's research project during her fellowship year is to work on a framework for automated reasoning that would accommodate both hard constraints and probabilistic information. </p>
<p>Reasoning about such "mixed" knowledge bases often requires lengthy and complex computation. Dechter will therefore focus on the development of efficient computational techniques that will enhance the usefulness of such automated reasoning systems.</p>
<p>Radcliffe Institute fellowships are designed to support scholars, scientists, artists, and writers of exceptional promise and demonstrated accomplishments who wish to pursue work in academic and professional fields and in the creative arts.<br/>
    <br/>
  To learn more about Dechter's work, <a href="http://www.ics.uci.edu/%7edechter">visit her website</a>.</p>
