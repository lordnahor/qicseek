<a name="trio_grant"></a><strong>Interdisciplinary trio to study social dynamics of technology</strong>
</p>
<p>
Paul Dourish, Simon Cole and Jennifer Terry have been awarded a $750,000 NSF award to investigate the ways new and emerging technologies such as blogging and text messaging are transforming peoples' privacy and identity in today's society. </p>
<p>By bringing together researchers from the Schools of Humanities, Social Ecology, and Information and Computer Sciences, this project takes a broad view of the ways in which technology and everyday life intersect, placing technology in a broader social and cultural context.</p>
<p>The interdisciplinary team of researchers will examine three core questions of social dynamics: how are practices of privacy and identity changing, what new patterns are evolving and how is the relationship between technical and social being developed? </p>
<p>The three-year research project is titled "Privacy, Identity and Technology."</p>
