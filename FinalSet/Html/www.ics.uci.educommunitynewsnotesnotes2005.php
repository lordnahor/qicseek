<strong>October 2005</strong>
<p>
<strong><a name="smyth_grant"></a>Professors awarded grant for satellite image analysis</strong>
</p>
<div class="img_caption_left">
<img alt="photo::professor padhraic smyth" src="../../../bin/img/photos/faculty/pjsmyth.jpg"/>
<p>
Padhraic<br/> Smyth</p>
</div>
Professor of computer science <strong>Padhraic Smyth</strong> along with professor of earth systems science Gudrun Magnusdottir and professor of statistics <strong>Hal Stern</strong> have been awarded a 3-year $618,000 National Science Foundation grant to develop statistical image analysis and tracking algorithms for analyzing satellite images of large-scale atmospheric disturbances over time.
<p>The project will focus specifically on developing tools to better understand a phenomenon known as the Inter-Tropical Convergence Zone (ITCZ), an important part of global large-scale atmospheric circulation. New models and algorithms will be investigated for tracking dynamic objects in images over time, using historical image data from visible and infra-red imagery, as well as satellite estimates of winds and liquid cloud water content.</p>
<p>Research on this project will be carried out by a collaborative effort between the Bren School of ICS and the Department of Earth System Sciences at UC Irvine, including faculty members Smyth and Stern in ICS and Magnusdottir (in Earth System Sciences), Ph.D. student Lucas Scharenbroich and postdoctoral fellow Alex Ihler in the Computer Science department, and postoctoral fellow Chai-chi Wang in the ESS department. </p>
