<div id="content_text">

<p>Bren School of ICS faculty, students and research initiatives are some of the most well regarded successes on the UC Irvine campus. We are pleased to announce the following noteworthy achievements for 2005.</p>
<p>Awards, grants and other honors can be sent to <a href="mailto: ic%73%6ee%77%73@ics.%75ci%2e%65d%75 ">icsnews@ics.uci.edu </a> to be considered for publication. </p>
