		<div id="content_title">Lab guidelines &raquo;</div>

			<p>
			</p><blockquote>

			<div id="content_text">

        <p>The following are official ICS policies which every student using the
          ICS Labs is expected to follow. Students should read and understand
          these guidelines because they can and will be held accountable for them.
          If you do not agree with any of the rules or guidelines, feel free to
          contact us and share your thoughts. UCI has rules that apply to all
          Computing Labs. This page also contains specific rules that apply to
          ICS Labs.</p>
        <h3>General Information about Ethics</h3>
        <ul>
          <li> <a href="http://www.ics.uci.edu/computing/policy/ethics_summary.php">Summary
            of Ethical Use of Computing Resources</a> - A Summarized version of
            the Ethical Use of Computing Resources.</li>
          <li> <a href="http://www.ics.uci.edu/computing/policy/ethics.php">Ethical
            Use of Computing Resources</a> - The formal ethics document which
            every student using the ICS labs and/or an ICS account must abide
            by.</li>
          <li><a href="http://www.policies.uci.edu/adm/pols/714-18.html">Computer
            Use Policy</a> - The University's computer ethics document.</li>
        </ul>
        <h3>Lab Specific Guidelines</h3>
        <h5>ICS LABS ARE FOR ICS MAJORS AND STUDENTS ENROLLED IN ICS COURSES ONLY.</h5>
        <ul>
          <li> The funding for our labs is only for those students that are ICS
            majors and/or enrolled in ICS classes.</li>
          <li>The privilege of using the ICS Labs does not extend to relatives,
            friends or roomates of ICS majors.</li>
          <li>Don't feel homeless if you aren't an ICS major or enrolled in an
            ICS class. You can either change your major or take advantage of the
            campus-wide resources provided by <a href="http://www.oit.uci.edu/">OIT</a></li>
        </ul>
        <h5>LAB WORKSTATIONS ARE FOR EDUCATIONAL PURPOSES</h5>
        <ul>
          <li> Do not do any commercial work, whether or not you are paid for
            it.</li>
          <li>Do not use the lab's network connection to transfer, in or out,
            copyrighted material and other non-academic data, e.g., .mp3s.</li>
          <li> Be aware that electronic sources(web pages, email, etc.) are meant
            to be viewed electronically and printing of these resources not only
            wastes paper but destroys the purpose of electronic resources.</li>
          <li> Do not print coursework for non-ICS classes. You may make modest
            use of computing resources for non-ICS coursework if you are an ICS
            major and if the lab is not full. If the lab is full, please do the
            coursework elsewhere.</li>
        </ul>
        <h5>NO FOOD OR DRINK IN ANY OF THE ICS LABS</h5>
        <ul>
          <li> You must get rid of food and/or drinks before you enter the lab.
            Once you pass the doorway entrance of the lab, you are in the lab.</li>
          <li>Food and drinks can damage the computer equipment.</li>
          <li>If you are found with food you will be asked to remove the food
            immediately from the Lab and be subject to a citation.</li>
        </ul>
        <h5>DO NOT DISTURB THE OTHER PEOPLE AROUND YOU</h5>
        <ul>
          <li> Try to keep talking noise to a minimal level.</li>
          <li>Don't lock your display and leave. Displays locked for more than
            15 minutes can be unlocked by the lab attendant.</li>
          <li>Respect personal space of others.</li>
        </ul>
        <h5>DO NOT STEAL</h5>
        <ul>
          <li> This includes mouse balls, mouse pads, printer paper, monitors
            and computers.</li>
          <li>Lab equipment is expensive and anything taken must be replaced.
            The need to replace stolen equipment may result in increased fees
            and/or decreased resources.</li>
          <li>When caught you will be reported to the proper authorities.</li>
        </ul>
        <h5>DO NOT COPY SOFTWARE OFF OF LAB WORKSTATIONS</h5>
        <ul>
          <li> Much of the software on the lab machines is commercial software.
            We have bought licences for this software and if you want a copy,
            you must go buy it. Fortunately, you are a student and are eligible
            for educational prices on software. Check out the <a href="http://www.computerstore.uci.edu/">UCI
            Computer Store</a> for more information.</li>
        </ul>
        <h5>DO NOT DOWNLOAD OR INSTALL SOFTWARE ONTO LAB WORKSTATIONS</h5>
        <ul>
          <li> It is illegal.</li>
          <li>The software may be tampered in such a way that it has a virus or
            will cause some unexpected activity.<br>
          </li>
        </ul>

        <p>&nbsp; </p>

			</div>

			</blockquote>

