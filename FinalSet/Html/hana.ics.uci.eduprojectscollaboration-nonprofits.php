<h1><strong>The Collaborative Work of Nonprofit Organizations</strong></h1>
<p>Nonprofit organizations serve a variety of critical  functions including the delivery of goods and services that are underprovided  by for-profit corporations or governments, promoting initiatives for the common  good, advocating for societal issues, and more generally enabling individuals  to engage in social welfare. Most nonprofit organizations donâ€™t operate alone,  however. They work in collaboration with other nonprofits; with governmental  agencies; and with members of the public who serve key roles as advocates,  volunteers, and donors. In this research, we study the work of nonprofit  organizations and, in particular, the diversity of collaborations they  undertake. We examine the challenges that surface in these collaborations and  explore opportunities for information systems to support both the collaborative  work and the causes around which these collaborators have rallied together.</p>
