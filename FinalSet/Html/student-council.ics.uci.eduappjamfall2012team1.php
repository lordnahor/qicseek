            <div class="title white">
                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>
				1. First we created an idea document for all of us to go through just to see which ideas we 
				favored and didn’t favor.<br />
				2. After narrowing the ideas down we talked about the flow of the application and the UI.<br />
				3. We then worked on getting the essential components set up on our computers (such as 
				Git, Android SDK, etc.)<br />
				4. We then had daily meetings in which we would just take parts of the project to work on 
				and just sat with our computers to code.
				</p>
			</div>

            <div class="title white">
                <h1>Major hurdles that had to be overcome</h1>
			</div>
			<div class="description cms">
				<p>
				Git became a problem for everyone in our group for some reason. Some people did not have an 
				emulator working on their computer so there was no way for them to test the code that they 
				wanted to commit. For other people, various problems plagued people that prevented them from 
				being able to commit their code. In the end, we had to fall back to e-mail since Matthew was the 
				only one able to commit to the Git repository.
				<br /><br />
				Another hurdle was connecting our app with a MySQL database. Since all of us felt most 
				comfortable coding in Java we first attempted to use JDBC to connect our app with our 
				database; however, after many attempts we decided that using PHP was a more effective and simpler. We spent several hours getting error messages after error messages. We finally 
				overcame this hurdle by after spending many hours fixing errors and looking online for solutions.
				<br /><br />
				Since most of members had no experience developing for Android, many had trouble using the 
				Android library. We overcame this by searching and reading online examples of other Android 
				Projects. It seemed like at times that not everyone had a clear vision of what the project was or 
				what they should be doing to help the project. More formal meetings could of helped us to keep 
				to date on what problems everyone was facing. It also would have lead to increased productivity 
				since everyone would have had a clear vision on what they were doing regarding their tasks.
				<br /><br />
				Finally time availability was indeed a problem for us. It felt like we were pretty restricted during 
				the weekdays and we couldn’t be productive until the weekend.
				</p>
			</div>

            <div class="title white">
                <h1>Lessons Learned</h1>
			</div>
			<div class="description cms">
				<p>
				Going over the formalities of SCRUM would have helped us organize. If we had better time 
				management we would have more time to improve and optimize our app instead of spending 
				the last couple of days of the competition debugging errors.
				</p>
			</div>

