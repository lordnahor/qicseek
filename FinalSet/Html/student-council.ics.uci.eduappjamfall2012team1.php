            <div class="title white">
                <h1>Sources of inspiration for the project</h1>
			</div>
			<div class="description cms">
				<p>
				We found out that the main way that ICS students find about jobs is through ICS events such as 
				company visits. We wanted an easier way for students to find about these ICS events so we 
				thought a calendar that would list these events would be useful. We decided to expand this idea 
				to academic events and social events as well.
				</p>
			</div>

            <div class="title white">
                <h1>Libraries, frameworks and other tools used</h1>
