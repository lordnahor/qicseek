                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>
				The app is a calendar that lists events for ICS students. The events may be categorized as 
				academic (such as seminars), professional (such as company visits) and/or social (such as 
				socials and parties). Users can add events either through their phone or a webpage online. 
				Users can filter the events they want to see by their category.
				</p>
			</div>

            <div class="title white">
