<p><strong>Creating games from scratch in 7 days</strong></p>
<div style="width: 450; margin: auto auto; font-size: 90%;">
   <img alt="Massteroid" height="260" src="../../../bin/img/photos/spotlight_gamejam_winter_2012/GameJam_Massteroid.jpg" width="450"/>
   <br/><i>Computer games science major Jason Ames Lee, producer for the winning team, 
       said his group was &#8220;concerned with the feeling of the game,&#8221; so 
       they incorporated such features as screen-shaking to enhance the experience 
       of gameplay. </i>
</div>
<p>
<br/>There&#8217;s an asteroid in space, falling towards Earth. Your mission: Help the asteroid achieve maximum destructive impact by having it collect debris as it travels through space. Depending on your ability to collect debris as you steer the asteroid, you could decimate an entire continent or wipe out only one person.&#160;</p>
<p>Such is the premise of Massteroid, winner of this quarter&#8217;s Game Jam, a "build a video game in a week" tournament organized by the Video Game Development Club (VGDC) at UCI.</p>
<!-- -->
<!-- -->
<div style="width: 230; float: right; margin: 0 0 1em 1em; font-size: 90%;">
 
<iframe frameborder="0" height="130" src="http://www.youtube.com/embed/Ne73MF1-1_A" width="225"></iframe>
<br/><i>Tournament participants share their thoughts about Game Jam at the Unveiling Event.</i>
</div>
<!-- -->
<!-- -->
<p>
Competitors were randomly assigned to five groups at the start of the competition; each team of about 15 members was given one week to design a fully functional game that incorporates the theme of &#8220;growth.&#8221; An unveiling event was held 10 days later, with teams demonstrating their work and discussing their successes and challenges. (Click&#160;<a href="http://www.clubs.uci.edu/vgdc/blog/forums/topic/download-links">here</a>&#160;to download and play the games.)<br/><br/>Judges included Bren School faculty and representatives from the game industry. Scores were based on design, aesthetics, &#8220;fun factor&#8221; and topic integration.
</p>
<p>
&#8220;Game Jam participants are essentially given a theme and a team, and are pushed out into the wilderness on their own,&#8221; incoming VGDC president and computer game science (CGS) major Justin Britch. &#8220;Creating a game from scratch in such a small amount of time leaves no room for slacking, and the level of competition is high &#8212; but so is the payoff. 
</p>
<p>
&#8220;After an intense week, students have a completed game that will help them get a job in the video game industry. At the conclusion of this most recent Game Jam, everyone agreed that we are making better, richer games with less experienced people, and that is a testament to our new freshman members.&#8221;
</p>
<p>
Current VGDC president Chris Lang, an informatics major, said the games &#8212; particularly the tournament champion &#8212; received high praise from the judges.
</p>
<p>
"Massteroid set the bar for what we expect out of VGDC projects,&#8221; said Lang. &#8220;The presentation was simple and crisp, but appropriate to the theme. The controls were intuitive and easy to jump into. And most importantly, the scope was small enough to ensure player success but expandable enough to allow for further development. Massteroid was universally praised by all of our industry&#160; judges, some of who continue to play it in their offices.
</p>
<p>
&#8220;Even with the large influx this year of young and inexperienced game developers, mostly from the Bren School&#8217;s CGS degree program, the VGDC is still improving and is producing higher quality projects than ever before. If this is the quality of work that can be accomplished in a week, I have high hopes for our yearlong projects."
</p>
<p>Four other games rounded out this quarter&#8217;s competition:</p>
