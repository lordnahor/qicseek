<p><b>Novel program supported by the Kay Family Foundation brings together students from UC Irvine&#8217;s Bren School and School of Medicine</b></p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Med App Jam" class="left" src="../../../bin/img/photos/spotlight/medappmain.jpg" style="padding: 0.5em;"/>
<!-- -->
<!-- -->
More than 30 medical students and 100 information and computer sciences (ICS) students teamed up in the 
<a href="http://www.som.uci.edu/news_releases/medappjam.asp">first-ever Med AppJam</a> 
&#8212; the biggest, so far, of the &#8220;build a fully functional mobile application in one week&#8221; AppJam franchise first launched in 2011 by the Bren School&#8217;s 
<a href="http://student-council.ics.uci.edu/">ICS Student Council</a>.  
<!-- <P> -->
The competition began Nov. 9 with a 
<a href="http://sites.uci.edu/imeded/2012/11/12/the-med-appjam-kicks-off/">kickoff event</a> 
in Bren Hall, where 19 teams were formed. Over the next 10 days, the groups worked together to create iOS-based applications useful to both physicians and patients.  
</p>
<p>
Participants were given exclusive access to a new <a href="http://www.flickr.com/photos/brenschool/sets/72157632189508727/with/8253096838/">iOS mobile lab</a>, designed to foster student collaboration and cross-campus interaction. Funded by a generous gift from the Kay Family Foundation, the lab features state-of-the-art equipment, including MacBook Airs, iPads, Apple TVs and a PolyVision smartboard.
</p>
<p>
&#8220;The Kay Family Foundation is committed to fostering the application of technology to improve health care. Their interests in this area are consistent with our focus on interdisciplinary collaboration,&#8221; says Hal Stern, dean of the Bren School. &#8220;We were absolutely thrilled to partner with them on the Med AppJam.&#8221;
</p>
<p>
Med AppJam teams 
<a href="http://www.blog.medjam.icscouncil.com/">blogged</a>
about their experience during competition week and on Nov. 19 
<a href="http://www.flickr.com/photos/brenschool/sets/72157632177377909/">showed off their work</a>
at the UCI Student Center, where a panel of academic and tech industry judges selected the top three apps. 
</p>
<p>
&#8220;It&#8217;s truly amazing what each team was able to accomplish given the time frame and resources,&#8221; says computer science major Adam Brenner, who helped organize Med AppJam with his ICS Student Council colleagues. &#8220;Each team and their members were pretty much strangers before Med AppJam, and 10 days later, they were working as one unit toward a common goal.&#8221;  
</p>
<p>
The winners are:
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="Life Buoy" class="right" src="http://farm9.staticflickr.com/8206/8248637680_95b6955f00_n.jpg"/>
<b> &#8220;Life Buoy&#8221; </b>
<br/> 1st place
</p>
<p>
Life Buoy is a telemedicine application that provides up-to-date information regarding the location of relief centers and urgent care clinics in disaster zones. It wirelessly connects remotely isolated victims of natural disaster with physicians and health care personnel. The app features two user interfaces: an iPhone-based application for patients to upload their medical information and emergent medical complaints, and an iPad-based application for physicians to respond to those medical complaints.
</p>
<p>
<b>Team Members:</b>  
Matthew Chan (Information and Computer Science), Bryan Lam (Computer Science and Engineering), Drake Tetreault (Computer Science), Lita Patel (Business Information Management), Peggy Bui (MD/MBA), Joe Hanson (MD/MBA) </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Global EMR (gEMR)" class="right" src="http://farm9.staticflickr.com/8337/8247575339_d3f5191370_n.jpg"/>
<b> &#8220;Global EMR (gEMR)&#8221; </b>
<br/> 2nd place 
</p>
<p>
Global EMR enables portable, accurate, organized and checklist-centered medical charting in areas of the world most in need of resources. It features an interface easily used in non-ideal environments, as well as the ability to transfer patient files through Bluetooth to create a central patient file database on one Apple product. &#8220;Global EMR&#8221; also includes Spanish translations for patient interviews and physicals for use by the medical mission 
<a href="https://www.flyingsamaritans.net/Web/Sams/default.asp">Flying Samaritans</a>, 
whose volunteers travel to Mexico monthly.
</p>
<p>
<b>Team Members:</b>  
Eugene Yang (Computer Science), Alan Chang (Business Information Management), Jaskaran Singh (Computer Science), Benjamin Pan (Business Information Management), Brenton Alexander (Medicine), Edsel Abud (Medicine) </p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="HappierU" class="right" src="http://farm9.staticflickr.com/8070/8248640230_48b8ed7f07_n.jpg"/>
<b> &#8220;HappierU&#8221; </b>
<br/> 3rd place
</p>
<p>
HappierU is a tool to help patients combat depression. It has four main features: a mood tracker for daily use, a medication reminder, a resources page, and a &#8220;Get Help&#8221; function that either connects patients to 911 or the national suicide hotline during crisis situations, or connects them to a more relaxed suicide warm-line in non-crisis situations. The data compiled in the app can then be presented to a physician who can track trends and help their patients deal with events that may have triggered negative emotions. 
</p>
<p>
<b>Team Members:</b>
Milin Shah (Informatics), Anna Cebula (Computer Science), Jesse Puente (Information and Computer Science), Derek Wang (Business Economics), Adedamola Tombrown (Computer Science), Kambria Nguyen (Medicine) </p>
