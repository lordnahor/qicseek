<div id="content_title">Jeff Fulkerson spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>King of the Anthill </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: michael golightly" height="200" src="../../../bin/img/photos/people/jeff_fulkerson.jpg" width="220"/>
<p class="left">
Peter the Anteater crowns Jeff <br/>
Fulkerson UCI's Homecoming <br/>
King while Homecoming Queen <br/>
Emily Yee looks on.</p>
</div>
<p>Jeff  Fulkerson, a senior ICS major, made Bren   School history by  becoming the school&#8217;s first student to run for and win UCI Homecoming King. </p>
<p>He  was coronated during halftime of the February 3rd men's basketball game along with  Homecoming Queen Emily Yee, a fourth-year biological sciences major. </p>
<p>&#8220;I will never forget how  shocked and excited I was when I heard my name announced as the 2007 UCI  Homecoming King,&#8221; Fulkerson said. &#8220;It was definitely something that I will  never forget.&#8221;</p>
<p>Fulkerson, who is involved  with a host of different campus organizations, entered the competition to see  how close he could come to winning. </p>
<p>&#8220;As I wrote my application  essays, I began to realize that I could actually have a chance at winning, and  that this was a chance for me to break some of the stereotypes that are  normally associated with homecoming,&#8221; Fulkerson said. </p>
<p>The coronation capped a  hectic, but fun filled week of campaigning and made Fulkerson an instant campus  celebrity. Students he has never met have come up to him on campus and offer  their congratulations, or say "Hey you're the guy who won  homecoming!"&#160; </p>
<p>&#8220;It really is an  honor.&#160; I am so excited, I can hardly explain it,&#8221; Fulkerson said. &#8220;I find  myself smiling as I walk around campus, simply because I was chosen by my  fellow students to represent them.&#8221;</p>
<p><strong>A MENTOR TO PEERS </strong></p>
<p>Fulkerson is no stranger to  his populace, having impacted many of them during the past two years as a  resident advisor in Mesa Court. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;When working with  freshmen, it's rewarding because you get to help them transition to college  life,&#8221; Fulkerson said. &#8220;It's fun watching them go from timid shell-shocked  freshman to finals-hardened independent students.&#160;And as a nice bonus, you  get a swank room and a full meal plan.&#8221;</p>
<p>He is also a peer mentor  for incoming members of the <a href="http://www.honors.uci.edu/">Campuswide Honors Program</a>, a job that begins long  before the students get to campus. </p>
<p>&#8220;I sent postcards to my  mentees last summer before they came to Irvine,&#8221;  Fulkerson said. &#8220;I actually was able to answer questions through email and Facebook  before the year even started.&#8221;&#160; </p>
<p>During the fall quarter there  are various peer mentor events, where mentors and mentees get to know each  other and discuss any remaining questions there may be about college life at  UCI.&#160; </p>
<p>&#8220;Once the mentees are on their  feet however, the job of the peer mentor becomes more passive,&#8221; Fulkerson said. </p>
<p><strong>FROM NOVICE TO EXPERT </strong></p>
<p>When he isn&#8217;t aiding new  students in their transition to college life, Fulkerson is navigating the world  of computer science as an ICS major, fueling a passion for computers instilled  in him at an early age. </p>
<p>&#8220;I have always been  interested in computers, ever since we had an old 386 in my dad's home office,&#8221;  Fulkerson said. &#8220;I studied them a little bit in high school, and I knew that  when I went to college I wanted to continue studying computer science.&#8221;&#160; </p>
<p>Fulkerson, whose only prior  programming experience before coming to UCI was with HTML, credits the Bren School  for providing him with the knowledge and experience to get job offers even  before graduation. </p>
<p>During the summer of his  sophomore year, Fulkerson got an internship at Sigil Games, where worked on the  game Vanguard: Saga of Heroes. By the end of the summer the company had offered  him a full-time job, even though he was still two years away from graduation. </p>
<p>&#8220;If someone like me, whose  only programming experience came from their college education, can be offered a  job after two years in the ICS program, just think of what opportunities await  after four years with an ICS degree from UCI,&#8221; Fulkerson said. </p>
<p>Fulkerson has also had the  opportunity to conduct undergraduate research with Informatics Professor Andre  van der Hoek on the <a href="http://www.isr.uci.edu/projects/lighthouse/">Lighthouse Project</a>, a coordination platform that is based  on the concept of Emerging Design. He contributed to the creation of the interface  between the GUI and the data model. </p>
<p><strong>CREATING A BALANCE </strong></p>
<p>With the help of a daily  planner, Fulkerson is able to create a balance between his ICS course load, extracurricular  activities and of course sleep. </p>
<p>&#8220;I have a bag that I fill  every week. There are three main units that can go into this bag: sleep, good grades,  and extracurricular activities,&#8221; Fulkerson said. &#8220;The trick here is that my bag  can only hold two units.&#160; So each week I must decide how to divide the  units to fill my bag in the most efficient way possible.&#8221;<br/>
                <br/>
              Despite a busy schedule  full of activities and school work, Fulkerson is grateful to UCI for offering students  a plethora of involvement opportunities that add to an all-around great college  experience. </p>
<p>&#8220;UC Irvine has been a place  where I can be independent but still have support.&#160; It has been a place  where I could try new things, but still foster my current interests,&#8221; Fulkerson  said. &#8220;It has been a place where I can learn both inside and outside of the  classroom.&#160; I have been able to try so many new things, and the extensive  resources available on campus are quite impressive.&#8221;</p>
<p>Fulkerson advises students  to not be afraid to try something new; college is a place to learn new things  whether it is saber fencing or speaking Greek. </p>
<p>Just be sure to create a balance  so you don&#8217;t risk burning yourself out.<br/>
  &#160;<br/>
  &#8220;If you can find a good way  to fill your bag with enough units of sleep, extracurricular activities, and good  grades, then you will be healthy, happy, and successful,&#8221; Fulkerson said. </p>
<p><i>- Eric Kowalik</i></p>


