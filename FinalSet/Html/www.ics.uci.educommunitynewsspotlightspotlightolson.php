<blockquote>
<div id="content_text">

<p><strong>Running on OCTANe  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: robert olson" height="187" src="../../../bin/img/photos/people/robert_olson.jpg" width="200"/>
<p> Robert Olson
</p>
</div>
<p>While growing up, Robert Olson viewed playing with a computer as equivalent to playing with any other toy. </p>
<p>&#8220;The foundation for my passion for computers came from being exposed to video games, computers, and the Internet from a young age.&#8221; Olson, a sophomore in information and computer sciences, said.</p>
<p>But his inspiration to pursue computer science as a career came from a person he never event meet. </p>
<p>During 8th grade, Olson played a text-based multiplayer online role-playing game (MMORPG) called Alien Adoption Agency. He really got into the game which offered players the ability to design a personalized skin in HTML. </p>
<p>&#8220;The guy who created the game went by the alias of Webby and I thought this guy was just incredible to have made this game that I loved so much and I told myself that I wanted to be like him,&#8221; Olson recalled. </p>
<p>After discovering the game had been programmed in Perl, Olson, a Bakersfield native, began his journey into the world of computer programming and found it really interesting. </p>
<p>&#8220;I would say more than any single other person, Webby, who I have never even talked to, influenced me to pursue computer science,&#8221; Olson said. </p>
