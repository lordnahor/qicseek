to watch more student profiles.
</p>
</div>
<p>Due in part to his gaming experiences, Olson had his heart set on attending the Digipen Institute of Technology in Washington state, a specialized school that offers degree programs related to the field of digital interactive entertainment technology.</p>
<p>However Olson&#8217;s parents convinced him that it was not a good idea to get such a specialized education and he began checking out schools in California that had quality computer science programs. </p>
<p>Olson didn&#8217;t consider himself &#8220;UC quality&#8221; as he had not finished in the top of his graduating high school class and he turned in his application to UCI on the last day, figuring he would soon get a rejection letter. </p>
<p>&#8220;Luckily the effort was worth it because I got in,&#8221; Olson said. &#8220;A lot of people come to UCI saying that this was just their fall back school but for me it really was where I wanted to go.&#8221;</p>
<p>Olson chose the information and computer sciences degree because of the programming projects which allow students to see how the skills they are learning are useful in the real world. </p>
<p>&#8220;I don't think a lot of majors experience this because while they are learning a lot of information they don't really see how they can use it,&#8221; Olson said. </p>
<p><strong>FUELED BY HITEC OCTANE </strong></p>
<p>During his freshmen year, it was Olson&#8217;s experience during the Bren School&#8217;s <a href="../../events/extreme/index.php">hITEC OCTANe product development competition</a> that ignited his passion for entrepreneurship. </p>
<p>The competition is designed to foster a spirit of entrepreneurship among Bren School and UC Irvine students, and fuel the development of new technologies that have the potential to positively impact the marketplace.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Teaming with fellow freshmen Abhishek Amit, whom he meet in his first ICS class, the pair worked with their mentor, Bren School alumnus Gregory Moulton to come up with a workable product. </p>
<p>&#8220;Our mentor was a lot of help because he made us think about the business side of the competition when Abhishek and I just wanted to focus on the technical side,&#8221; Olson said. </p>
<p>The dynamic duo ended up winning the top prize of $5,000 for their Web-based integrated development environment used to write and compile source code in different languages from within a Web browser.</p>
<p>It was a seminal moment for Olson. </p>
<p>&#8220;Abhishek and I knew it was a long shot for us the win the competition, but we decided to try it and as a result we won some nice prize money. Money we never would have won if we didn't try,&#8221; Olson said. &#8220;We knew we would be going up against teams that were older and smarter than us and that inspired us to work even harder on it.&#8221;</p>
<p><strong>TAKING ADVANTAGE </strong></p>
<p>Even though he is only a sophomore, Olson has taken advantage of a number of internship and research opportunities the Bren School offers to its undergraduates. </p>
<p>During the summer Olson worked as a Java programmer for The Tech Factory, a small company in Irvine. </p>
<p>&#8220;This was my first time working on a truly large software application with tens of thousands lines of code and hundreds of files and to be honest I was scared at first by the scope of it,&#8221; Olson said. &#8220;However, by the third week I'd picked up the gist of what everything did and was making meaningful contributions to the code base.&#8221;</p>
<p>During the fall of 2006 he did research for Project RESCUE in collaboration with Motorola. </p>
<p>&#8220;It was the first time I'd done programming for a cell phone so that was cool,&#8221; Olson said. &#8220;I decided that I enjoy the research "environment" and now I am considering graduate school as a possibility after I graduate.&#8221;</p>
<p>Despite his hectic schedule, Olson finds time to kick back and relax with his roommates at their ocean view apartment in Newport Beach. </p>
<p>&#8220;Never in my life did I think I would be living on the beach but thanks to the cumulative rent of eight people it has become a reality,&#8221; Olson said. &#8220;I know that later in my life I'll look back at my time in Newport with fond memories.&#8221;</p>
<p>He also takes advantage of the many classes offered at the Anteater Recreation Center.</p>
<p>&#8220;This quarter I'm taking scuba diving which is something I've always wanted to try and next quarter I'm looking forward to trying rock climbing and sailing,&#8221; Olson said. &#8220;It is a really cheap and laid-back environment to learn a new activity in.&#8221;</p>
<p>His many experiences in only his first two years at UC Irvine have shown Olson that the old clich&#233; of try everything is true. </p>
<p>&#8220;You don't realize how many things you miss out on just because you never try it,&#8221; Olson said. </p>
<hr/>
<p><strong>UPDATE - 08.28.2008 </strong></p>
<p><i>Since Robert's profile published, he has gone on to participate in many other endeavors at UCI.  Of note is his first place finish in the Undergraduate Division of Staddling Yocca Carlson &amp; Rauth Business Plan Competition.  Robert has expanded his leadership skills as well, by serving as the ICS representative on Legislative Council for ASUCI and as President of the ACM CS club.</i></p>
<p><i>- Eric Kowalik</i></p>


