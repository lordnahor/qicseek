<div id="content_title">Network Synthesis spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>Dynamic Duo</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: victor liu and adam bonner, network synthesis founders" height="134" src="../../../bin/img/photos/people/bonner_liu.jpg" width="171"/>
<p>
Adam Bonner (l)<br/>
and Victor Liu
</p>
</div>
Victor Liu and Adam Bonner took a risk when graduating from the Bren School by foregoing the opportunity to join established companies. Instead they decided to branch out on their own. That gamble is starting to pay off.
<p>&#8220;At first I wondered what it would be like to work for one of the &#8216;big&#8217; names &#8211; Microsoft or Andersen Consulting,&#8221; said Liu. &#8220;But I think by starting a whole new business, I was able to find a new way of thinking and a new way of accomplishing my goals. It takes a lot of willpower and determination to venture out on your own, but you learn so much more about who you really are.&#8221;</p>
<p>Liu and Bonner co-founded Network Synthesis even before claiming their degrees from the Bren School in May 2000.</p>
<p>A privately held company based in Irvine, Network Synthesis employs a staff of 13 &#8211; most of which have ties to UC Irvine and the Bren School.</p>
<p>&#8220;All of our technical staff comes from the Bren School,&#8221; said Bonner. &#8220;We like to keep close ties with the school and, having gone through the program ourselves, we know those students will be able to do the jobs that we are asking of them.&#8221;</p>
<p>Originally crafted as another consulting company, the small firm had early success. But a sluggish economy and an interest in specialization encouraged them to modify their course of action. And in October 2002, Network Synthesis launched a new digital security surveillance system, DS3, that was some 15 months in development. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>DS3 is a complete digital security surveillance solution that provides full-motion video capture with unlimited camera support, digital and redundant storage, and remote and wireless digital control. DS3&#8217;s complete online interface enables security officers as well as any authorized personnel to monitor and manage a facility from literally anywhere.</p>
<p>&#8220;The system replaces the countless number of VHS tapes companies use in traditional surveillance systems with webbased video cameras recording all of the activity digitally,&#8221; explained Bonner. &#8220;One of the differences in our system is the ability to manage all of this video via the Internet, giving companies true 24/7 access to all of their video data.&#8221;</p>
<p>Currently in negotiations with several entertainment and emergency services organizations, Network Synthesis is on the cusp of realizing its vision and seeing that early gamble pay big dividends.</p>


</div>
