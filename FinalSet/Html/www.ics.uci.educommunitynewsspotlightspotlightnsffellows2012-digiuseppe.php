<p>
<b>Q&amp;A with Nicholas DiGiuseppe<br/>
2nd-year Ph.D. student in Software Engineering</b>
</p>
<p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
</p>
<p>
<i>
Nicholas DiGiuseppe is one of <a href="spotlight_nsf_fellows_2012.php">two</a> Bren School Ph.D. students who received a coveted 2012 National Science Foundation Graduate Research Fellowship. The NSF GRF program recognizes and supports outstanding graduate students who have demonstrated their potential for significant achievements in science and engineering research. Fellows receive three years of support, including a $30,000 annual stipend.
</i>
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="photo: " class="left" height="200" src="../../../bin/img/photos/spotlight/Spotlight-NicholasDiGiuseppe.jpg" width="138"/>
</p>
<p>
<b>Where are you from?</b>
<br/>
I was raised in Alta Loma, Calif.
</p>
<p>
<b>What are your previous academic degrees?</b>  
<br/>
I got my B.S. in Information and Computer Science from UCI.
</p>
<p>
<b>Why did you choose to pursue graduate studies at UCI?</b>
<br/>
I chose UCI because of the high-quality software engineering program (as noted by the emphasis in faculty and research) and because of my advisor, Jim Jones. Other schools treat software engineering as a second-class citizen and subfield of computer engineering (if it&#8217;s treated at all). In contrast, UCI has a <a href="http://www.ics.uci.edu/grad/degrees/degree_se.php" title="Ph.D. in Software Engineering">Ph.D. in software engineering</a>! Concerning Jim Jones, I felt he was performing really valuable research, and I wanted the opportunity to work with him. Simply put, he does the exact type of research I want to do. Thus, out of a desire to be at a school that values software engineering, and to work with Jim Jones, I came to UCI. 
</p>
<p>
<b>What is your current research focus? Why did you choose to go into this field of study?</b>
<br/>
My current research focus is on automating the debugging process through enhancing fault comprehension, localization and clustering. I attempt to create tools that automatically provide information that will assist developers in fixing their code.  Sometimes that information is natural language descriptions of their faults so they know <em>why</em> their code is breaking; sometimes that information is general locations of the code which are likely to contain the bug so they know <em>where</em> their code is breaking; and sometimes it&#8217;s simply eliminating the noise surrounding their failure so they can better understanding <em>how</em> their code is breaking. I chose this field because everyone who develops software ends up debugging their code, and it&#8217;s often difficult. My hope is to alleviate the burden that is placed upon those who create or maintain software. This originated for me when I was learning to program and would end up spending hours sometimes trying to fix bugs. All developers sooner or later &#8220;bang their head against the wall&#8221; trying to fix their code, and I hope to make the time between those circumstances farther and farther apart with my research.  
</p>
<p>
<b>What are your future plans?</b> 
<br/>
Currently my plans are to stay in academia and become a research professor. I&#8217;m willing to go into industry, but I appreciate the potential freedom that exists (in terms of what I choose to research) in academia.
</p>
<p>
<b>Any tips for fellow students who may want to apply for the NSF GRFP?</b>
<br/>
Honestly the biggest thing that I would do is to make sure that in your future research plans paper, you give adequate time describing the problem your research addresses. Don&#8217;t be afraid to spend half a page (out of two) talking about the problems that exist in the world or in the research community your research addresses. The more practical and down-to-earth this section can be, the better reviewers seem to like it. Also, start performing your community outreach now! Don&#8217;t wait until you get the grant. Being able to say, &#8220;I <em>already</em> do X in the community with my research,&#8221; sounds much better than saying, &#8220;I&#8217;m <em>going</em> to do X in the community with my research.&#8221; It inspires confidence that you care about the community and that you will actually do what you say (because heck, you already are doing some). Lastly, make sure your three papers tell a good comprehensive story. For example, reviewers should understand to some extent why you are researching your specific topic based upon your personal statement, or understand your past research interest based upon your expected research. All three papers need to tell the same story, YOU! They need to be clear that you deserve the award. In the end, you want the reviewer to say, &#8220;We would be <em>crazy</em> not to give this person the award.&#8221;
</p>
<p>
<b>How do you like to spend your time outside of school?</b>
<br/>
I am an active member of the LDS church and participate in church-related activities almost every day of the week. My wife and I enjoy board games and video games, and in terms of my family life, my first child (a baby girl) is due in June. I also train with Japanese swords (iaido) most days, am an experienced whittler, and am trying to teach myself Japanese. 
</p>


