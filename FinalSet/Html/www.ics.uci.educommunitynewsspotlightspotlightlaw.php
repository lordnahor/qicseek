<blockquote>
<div id="content_text">

<p><strong>Unlocking the Club House </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: jennifer law" height="166" src="../../../bin/img/photos/people/jennifer_law.jpg" width="126"/>
<p>
Jennifer Law
</p>
</div>
Jennifer Law has a lot to be proud of looking back on her four year involvement with UCI&#8217;s Women in Computer Science (WICS) group, an organization established to help and encourage women to pursue a college degree and a successful career in the Computer Science fields.
<p>Law, a senior ICS major and former  WICS president, has seen the organization double its membership and become one of the most active computer science organizations on campus. </p>
<p>&#8220;It&#8217;s a great feeling to know that WICS is making a difference and you can see the results right in front of you,&#8221; Law said. &#8220;It makes me proud that I lead this organization and I know I&#8217;m going to have a hard time stepping down.&#8221;</p>
<p>In addition to recruiting and networking events, where students get the opportunity to meet company recruiters or mingle with female faculty, WICS runs other events and programs that help and encourage women toward a successful career in the field of Computer Science.</p>
<p>Once such program is the Big/Lil mentoring program where upper class students are matched with under class students. The program is geared towards women so they can have an older person guide them when they have questions or are struggling in their classes. But the program is not female exclusive; there are a couple male mentors who participate as big brothers. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>WICS is also planning a &#8220;Dinner with the Dean and ICS females&#8221; event where Bren School Dean Debra J. Richardson and female Bren School professors talk about their experiences and successes. The event hopes to help women realize they are not alone in this process and there is a lot of support around them. </p>
<p>&#8220;There are many computer science females that have gone through the process and became successful, and current students should realize they have the same potential,&#8221; Law said. &#8220;We hope it&#8217;ll become a successful event, where many females will gain confidence as well as realize the numerous opportunities given to females in computer science.&#8221;</p>
<p>WICS involvement is not limited to on campus activates. Working with the Association of Women in Technology and Girls Inc. WICS holds classes that teach elementary and high school women how to use power point, Photoshop, access e-mail, build a website, and use the Internet. </p>
<p><strong>FINDING HER PASSION </strong></p>
<p>Despite her heavy involvement with WICS, Law wasn&#8217;t always so sure about her passion for computer science. </p>
<p>&#8220;I actually had a hard time deciding if I should major in art or computer science,&#8221; Law said. &#8220;My parents were very supportive and allowed me to take classes at Pasadena Art and Design College, but I decided I was not that passionate about art so I went with computer science.&#8221; </p>
<p>Law encourages future students to keep in touch with their passions and be patient, even if the initial going is tough. </p>
<p>&#8220;If there&#8217;s something you&#8217;re really passionate about, don&#8217;t let anything stop you from pursuing it. If you&#8217;re really passionate about it, you&#8217;ll excel in your abilities and develop the self-confidence to know you&#8217;ll do well in it,&#8221; Law said. &#8220;We may not always get what we want in the first couple of attempts, but maintain your persistence and understand why you love what you do. Be patient and don&#8217;t give up.&#8221; </p>
<p>Law still indulges her artistic side through her Digital Arts minor and her research with Informatics professor Bill Tomlinson into different lighting techniques to enhance interactive scenes for animation. </p>
<p>She also interns at Thales-Raytheon Systems implementing SQL and Java code to conduct regression tests and writing up procedures for testing to meet system specifications, a long way from programming her graphing calculator in high school which she credits with inspiring her to pursue a career in computer science.</p>
<p>&#8220;I enjoy both aspects of art and computer science and hope I&#8217;ll be able to incorporate both of them into my future,&#8221; Law said. &#8220;I really want to do something with animation, human computer interface or something design related.&#8221;</p>
<p><strong>LEARNING OUTSIDE THE CLASSROOM </strong></p>
<p>Citing the prestige of the Bren School and its status as an independent school separate from engineering, as the major reasons she was drawn to UCI, Law recommends the ICS major for those students looking for a well-rounded, challenging degree. </p>
<p>&#8220;Other students can incorporate their major into ICS, which would make their major more beneficial. If you enjoy business, you can go into system analyst. If you like biology, you can try biotechnology,&#8221; Law said.  &#8220;It&#8217;s also a challenging major that not only tests your knowledge, but also your strength as a person (i.e. persistence, maintaining self confidence/ motivation, and studying hard).&#8221;</p>
<p>But while Law credits her UCI education with making her a well-rounded person, she realizes that not everything can be learned in the classroom, and there is more to the college experience than lectures and labs. </p>
<p>&#8220;If you just go to school and go back home, you don&#8217;t experience college life and grow from the college experience,&#8221; Law said. &#8220;Not only has UCI provided me with the knowledge I need to acquire a job, but after being involved with WICS and working at Middle Earth (on campus dormitory), I&#8217;ve met numerous people and became inspired by others and what they do.&#8221;</p>
<p>In fact her two most memorable experiences while at UCI have nothing to do with school. One was the experience of living in the on campus dorms, with a diverse group of people, many of whom were ICS majors. </p>
<p>&#8220;When someone needed help in programming, there was always someone in the dorm who could help you. Our dorm bonded very quickly and I continue to hang out with them today.&#8221;</p>
<p>The other memorable experience was when she performed in a play to promote Chinese culture during the Chinese Association&#8217;s culture night. </p>
<p>&#8220;It was a great experience seeing behind the scene of how plays work and working with different people who were excited to make the performance successful,&#8221; Law said. &#8220;I have never acted before, so it was interesting being on stage and playing a Chinese mom.&#8221;</p>
<p>When not busy listening to a lecture, programming some Java or organization a WICS event, Law enjoys indulgencing her taste buds and clearing her head at one of the many restaurants in the University Center located across the street from campus. She is particularly fond of Gen&#8217;s, not because it sounds like her name, but because it serves up great Japanese food fast. </p>
<p>Law also manages to find time to enjoy snowboarding, painting, drawing, Hapkido (Korean martial arts), swimming, supporting her sorority, Kappa Kappa Gamma and of course, hanging out with her friends.</p>
<p><i>- Eric Kowalik</i></p>


