<h3>A salute to Judith Olson:</h3>
<!-- -->
<!-- -->
<!-- -->
<div class="entry">
   &#8220;The moment Judy offered me a job as a post doc, I emailed several of her former students and post docs to ask what she was like as a mentor. It became instantly clear that Judy didn&#8217;t cultivate students. She cultivated a community of scholars and she cultivated a family. Over time, that family has grown to span disciplines, universities and continents, and I realized that one of the best choices I could make was to join that family. As I move into a faculty position myself, I feel blessed to continue Judy&#8217;s work, cultivating the next generation of that extended academic family.&#8221;
<div class="quote_attribute">
      &#8212;	<b>Amy Voida</b>, assistant professor <br/>
      School of Informatics &amp; Computing <br/>
      Indiana University, Indianapolis
   </div>
