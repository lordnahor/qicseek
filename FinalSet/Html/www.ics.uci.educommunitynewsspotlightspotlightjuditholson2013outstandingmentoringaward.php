<p><b>Professor Judith Olson honored as outstanding mentor</b></p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<div class="floating_image_with_caption right" style="width: 156px;">
<img alt="Judith Olson" src="../../../bin/img/photos/spotlight/judith_olson_2013_outstanding_mentoring_award/Judy_Award.jpg" style="width: 156px;"/>
</div>
<!-- -->
<!-- -->
Judith Olson, Bren Professor of Information and Computer Sciences and renowned researcher in the realm of human-computer interaction, has received a 2013 Outstanding Mentor Award from the UC Irvine Emeriti Association (UCIEA).
<p>
Over the course of her 42-year career &#8212; including the last five at UCI &#8212; Olson has influenced the lives and careers of countless students and professionals. Her open-door policy has become legendary across campus, and she gives of her time and friendship equally to undergraduates, graduate students, postdoctoral scholars and junior (as well as not-so-junior) faculty.
</p>
<p>
&#8220;If you were to ask anyone in our department who they would go to for advice, the answer undoubtedly would be Professor Judy Olson,&#8221; says Andr&#233; van der Hoek, chair of the Bren School&#8217;s <a href="http://www.informatics.uci.edu/">Department of Informatics</a> and a self-professed Judy Olson mentee. &#8220;She possesses an enormous wealth of wisdom, and I always leave her office optimistic and positive about life.&#8221;
</p>
<div class="floating_image_with_caption left">
<img alt="Olson with Team Portfelo, a group of informatics students who won the 2012 Butterworth Product Development Competition " src="../../../bin/img/photos/spotlight/judith_olson_2013_outstanding_mentoring_award/Judy_Portfelo.jpg"/>
<br/>
Olson with Team Portfelo, a group of informatics students who won the 2012 Butterworth Product Development Competition </div>
Olson&#8217;s studies explore what succeeds &#8212; and fails &#8212; for team members working together remotely. Prior to joining UCI, she was a professor at the University of Michigan, first in the Psychology Department and then in the School of Business. Olson had helped found the university&#8217;s School of Information and thought she would retire at Michigan, which had served as her professional and academic home for 35 years. Then she and fellow faculty member Gary M. Olson, who also happens to be her husband, received a call from Professor Gloria Mark at UCI. The Bren School had two endowed chairs, and the Olsons were at the top of the recruitment list.
<p>
It was an opportunity they couldn&#8217;t pass up. &#8220;The informatics department at UCI is probably the best in the world,&#8221; she says. 
</p>
<p>
Former informatics chair David F. Redmiles was instrumental in attracting the Olsons to UCI. &#8220;Judy was an amazing resource to me as department chair,&#8221; he says. &#8220;She had dealt with certain kinds of challenges that our department and school were just beginning to face as we grew.&#8221; 
</p>
<p>
Olson even helps those she may never meet face to face, through her &#8220;<a href="http://women.acm.org/news/newsletter/newsletter_issues.cfm">Ask Judy</a>&#8221; column, which appears regularly in the Association for Computing Machinery-Women (ACM-W) newsletter. &#8220;Ask Judy&#8221; came into being when the ACM-W awarded Olson its 2011-2012 <a href="http://www.ics.uci.edu/community/news/press/view_press?id=113">Athena Lecturer</a> award, which celebrates female researchers who have made profound contributions to computer science. At the award ceremony, the newsletter editor asked Olson if she had any suggestions for the publication. Olson came up with the idea of a &#8220;Dear Abby&#8221;-type column, and she has been helping readers navigate tricky career situations ever since.
</p>
<p>
In addition to the Athena award, Olson&#8217;s numerous honors include being a Fellow of the Association for Computing Machinery and induction by the Special Interest Group on Computer&#8211;Human Interaction (SIGCHI) as one of the inaugural seven members of its CHI Academy. She and her colleague-husband also received SIGCHI&#8217;s Lifetime Achievement Award together in 2006.
</p>
<div class="floating_image_with_caption right">
<img alt="Judith and Gary Olson at the 2013 Bren School commencement ceremony" src="../../../bin/img/photos/spotlight/judith_olson_2013_outstanding_mentoring_award/Judy_Gary.jpg"/>
<br/>
Judith and Gary Olson at the 2013 Bren School commencement ceremony</div>
For someone who was considering retiring five years ago, Olson shows little sign of slowing down. Currently, she and her husband are working on a new book, <em>Working Together Apart</em>, which constitutes their findings in the wake of their previous book, <em>Scientific Collaboration on the Internet</em>, and their influential article &#8220;<a href="http://dl.acm.org/citation.cfm?id=1463019">Distance Matters</a>.&#8221; She is also co-editing what promises to be a seminal book about research methods in the field, called <em>Ways of Knowing in HCI</em>.
<p>
Despite the personal and professional accolades throughout her career, Olson remains modest. &#8220;Mainly, I just like to talk to people,&#8221; she says. &#8220;And when they&#8217;re trying to make a particular life decision, if I can offer any insight from my own experiences, then of course I&#8217;ll share it with them.&#8221;
</p>
