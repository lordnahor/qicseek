<div class="entry">
&#8220;Her superpower [is] a seemingly endless reserve of attention and lightning-quick feedback [that] grows students faster and more fully. Every year since I have graduated, I have reached out to Judy for mentoring advice, and she has responded as if I was still one of her students.&#8221;
<div class="quote_attribute">
&#8212;	<strong>Erik Johnston</strong>, associate professor <br/>
School of Public Affairs <br/>
Arizona State University
   </div>
