<div id="content_title">Students Publish Work on Xbox Live</div>

<blockquote>
<div id="content_text">

<p>
The <a href="http://marketplace.xbox.com/en-US/Games/XboxIndieGames">Xbox Live Indie Arcade</a> now features two new games developed at UC Irvine by Bren School students.
</p>
<div style="margin: 1em 0;">
<div class="img_caption_left">
<img alt="photo:: Nucleon" src="http://a3.sphotos.ak.fbcdn.net/hphotos-ak-ash2/38330_141490669202354_137377822946972_322431_3000577_n.jpg" width="200"/>
<p>Screen shot from<br/>"Nucleon"</p>
</div>
&#8220;Nucleon,&#8221; released in May, and &#8220;Angry Hand of God,&#8221; released in June, highlight different elements of video game play. In &#8220;<a href="http://www.youtube.com/watch?v=E5ASeQusUmw">Nucleon</a>,&#8221; players use electron powers to defend the atomic nucleus from insidious radiation particles. As the player advances through each successive level, the game becomes quicker and more difficult, prompting players to improve their timing and speed.
</div>
<div style="margin: 1em 0;">
<div class="img_caption_left">
<img alt="photo:: Angry Hand of God" src="http://i48.photobucket.com/albums/f227/Pyrobug/Angry Hand of God Screenshots/AGoHDestruction.jpg" width="200"/>
<p>Screen shot from<br/>"Angry Hand of God" </p>
</div>
&#8220;<a href="http://www.youtube.com/watch?v=toYgURN0CSE&amp;feature=player_embedded">Angry Hand of God</a>&#8221; unfolds as a puzzle. Players must smite as many mortal beings and buildings as possible, using a series of chain reactions as diverse as electrical conduction and stampeding elephants. Because each &#8220;angry god&#8221; has a limited number of lightning strikes, the game quickly becomes complex as players conserve strikes and develop chain-reaction strategies.
</div>
<p>
Both games were born out of the first-ever &#8220;Game Jam,&#8221; hosted by UCI&#8217;s 
<a href="http://www.clubs.uci.edu/vgdc/blog/">Video Game Development Club</a> (VGDC) in April 2010. During the weeklong competition, student teams participated in marathon sessions of programming and testing in an effort to produce workable, original games. Based on the theme Electricity, both &#8220;Nucleon&#8221; and &#8220;Angry Hand of God&#8221; were built in a week, although teams continued to work on the games on and off for nearly a year, to get them ready for publication.
</p>
<p>
&#8220;It took longer than any of us expected,&#8221; said Reza Ghassemi &#8217;11, former VGDC president. &#8220;We finally got the game online for peer review at the last minute, only to be rejected due to one last bug we had missed!&#8221;
</p>
<p>
Ghassemi, Jay Igarashi &#8217;11 and Alex Kaiser &#8217;10 prepared &#8220;Angry God of Hand&#8221; for release on Xbox Live, while Craig Brown, Johnny Huynh &#8217;10 and Thomas Lu &#8217;10 worked on &#8220;Nucleon.&#8221; Trenton Ng &#8217;10 worked on audio/music for both games; the Game Jam was his original idea.
</p>
<p>The VGDC has hosted two additional Game Jams since the first, and the number of participants have more than doubled from the original competition. The club is planning to organize several more for the upcoming academic year.
The release of the games highlights the relevance of UCI&#8217;s new Computer Game Science (CGS) major, first offered by the Bren School in fall 2010. As Ghassemi puts it, &#8220;I think the success and popularity of Game Jam, and the dedication of the students involved in developing games, proves that the interest and passion exist to support a degree program like CGS.&#8221;
</p>
<p style="float: right; margin-top: 0em; margin-bottom: 2em;">&#8212; PG </p>


</div>
