  <h1>Telemedicine</h1>
  <p>Providing quality patient care has always been an ongoing
    issue and priority in medicine. It is even more so the case when the physician
    is not physically present at the same location as the patient.  One solution
    that is currently being used in some hospitals is the idea of a remote control
    and videoconferencing robot, where the head of the remote physician is
    displayed on a screen where the robot’s head would be.  The physician is able
    to move his “head” (the screen) to control where they are looking and for the
    patient to also know what the physician is looking at. Furthermore, the
    physician has remote control of the robot’s position and motion, including how
    close he/she is
    standing from the person they are talking to, “looking down” at the patient if
    in bed, etc. These features mesh with what is known in social psychology as <i>social
    ergonomics</i> and <i>presence</i>, the appearance of actually being somewhere
    because you can signal your intentions and actions with body gestures, such as
    how far away someone is when they talk to you, looking at someone when they are
  speaking to you, etc. </p>
<p>One such example of a remote control and videoconference
    robot is the InTouch Remote Present RP-7 robot. The RP-7 has two distinct
    components: a physician side and the remote side that typically interfaces with
    the patient (see photo).  </p>
  <p><img width="560" height="199" id="Picture 8"
