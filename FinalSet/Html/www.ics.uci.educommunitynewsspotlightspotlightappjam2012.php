<p><b>Latest mobile app tournament focuses on self-improvement</b></p>
<p class="image_with_wrapped_text">
<img alt="AppJam1" class="left" src="../../../bin/img/photos/spotlight/appjam_2012/6863399458_46b9649c13_z.jpg"/>
Need to brush up on your social skills? Bren School students have an app for that. 
Following the theme of self-improvement, a team of UC Irvine undergraduates took home the latest AppJam Tournament grand prize of $1,000 with &#8220;AwkTalk,&#8221; an Android application that provides users with instant feedback on their social skills.
</p>
<p>
&#8220;The app was inspired by the initial awkwardness of being placed together to compete as a team with strangers,&#8221; said senior computer science major Vance Cagle. &#8220;Our social awkwardness led to the app&#8217;s creation, and we got straight to business from there.&#8221;
</p>
<p class="image_with_wrapped_text">
<img alt="AppJam2" class="right" src="../../../bin/img/photos/spotlight/appjam_2012/6863399660_f823fdc70e_z.jpg"/>
Created by Team Socially Awkward Anteaters, &#8220;AwkTalk&#8221; was one of 10 student inventions entered in the Winter 2012 edition of AppJam, a &#8220;build a fully functional app in a week&#8221; contest developed by the ICS Student Council. Teams included undergraduate and graduate students &#8212; as well as novice and advanced programmers &#8212; from the Bren School and other parts of the UCI campus.  
</p>
<p>
Second place and $500 went to the creators of &#8220;Learn UCI,&#8221; which provides voice-activated campus tours, while third place and $200 went to the team behind &#8220;Nappkin,&#8221; a mind-mapping application that allows users to note spontaneous ideas and receive insight from other users.
</p>
<p>
&#8220;The theme of self-improvement really inspired the competitors&#8217; creative sides,&#8221; said Jerry Huynh, president of the ICS Student Council, whose members created a web application &#8212; also in one week &#8212; that instantly compiled scores to make the judging process more expedient. 
</p>
<p>
AppJam judges included faculty members Dan Frost, Shannon Tauro, Gu-Min Jeong and Nikil Dutt, as well as industry professionals Jason Leu of Kareo, Amir Banifatemi and David Cheng of K5, and Ed Cisek of MobilityWare. 
</p>
