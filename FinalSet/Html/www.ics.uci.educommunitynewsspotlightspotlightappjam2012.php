<h3>Winners:</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="AwkTalk" class="right" src="../../../bin/img/photos/spotlight/appjam_2012/AwkTalk.jpg"/>
</p>
<p> <b> 1st place <br/> &#8220;AwkTalk&#8221; </b> </p>
<p>
<b>Prize:</b> $1000 <br/>
<b>Team Members:</b> Vance Cagle (Computer Science), Tom Huang (Electrical Engineering), Zaheer Mohiuddin (Computer Science), Brian Payne (Computer Science), Dustin Tran (Information and Computer Science) <br/>
<b>App description:</b> AwkTalk provides users with vital feedback on their social skills. The app facilitates in-person meetings where two users participate in a timed topic discussion based on their preferences, then rate each other&#8217;s social skills through the app&#8217;s prescribed categories.  <br/>
<b>Team comment:</b> &#8220;We were all aware that many of our colleagues in the fields of computer science and engineering (ourselves included) lack excellent social skills.&#8221;
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Learn UCI" class="right" src="../../../bin/img/photos/spotlight/appjam_2012/LearnUCI.jpg"/>
</p>
<p> <b> 2nd place <br/> &#8220;Learn UCI&#8221; </b> </p>
<p>
<b>Prize:</b> $500 <br/>
<b>Team Members:</b> Yuhao Ma (Computer Science Engineering), Doug Ginther (Information and Computer Science), Kevin Jonaitis (Electrical Engineering), Kyle Boos (Information and Computer Science), Joshua Ferguson (Computer Science) <br/>
<b>App description:</b> Learn UCI, or LUCI for short, implements augmented reality and voice technology to give users the ability to take pre-loaded tours of UCI or upload their own custom tours.  <br/>
<b>Team comment:</b> &#8220;Our app is the whole package: It allows for self-guided tours that anyone can take and provides users with interesting information about UCI. It even talks to you, and the availability of augmented reality technology is a really great feature.&#8221;
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Nappkin" class="right" src="../../../bin/img/photos/spotlight/appjam_2012/Nappkin.jpg"/>
</p>
<p> <b> 3rd place <br/> &#8220;Nappkin&#8221; </b> </p>
<p>
<b>Prize:</b> $200 <br/>
<b>Team Members:</b> Vincent Kwok (Computer Science Engineering), Qasid Sadiq (Information and Computer Science), Rebecca De Villez (Computer Science Engineering), Ashish Patel (Information and Computer Science), Allen Roman (Computer Game Science), Edward Wu (Computer Science) <br/>
<b>App description:</b> Nappkin is a mind-mapping application that allows users to spontaneously jot down ideas as they come. With Nappkin, an intuitive interface allows users to create a &#8220;bubble&#8221; to be sent out to other users for insight. <br/>
<b>Team comment:</b> &#8220;Great ideas can begin anywhere, even on a napkin. In this way, our application is a virtual napkin for when inspiration strikes.&#8221;
</p>
