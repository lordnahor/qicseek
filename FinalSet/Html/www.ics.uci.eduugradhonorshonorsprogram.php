<p><strong>The application period for the 2009-10 Honors Program ends on Monday June, 1st. Please view the bulleted list below for necessary documents and forms. </strong> </p>
<p><strong>Please be sure you have reviewed the program details and eligibility requirements listed below before applying.</strong></p>
<p>
			  
			  <span style="color: #6699cc;"><strong>&#187;</strong></span> <a href="honors_application.php">Honors Program Application</a><br/>
                  <span style="color: #6699cc;"><strong>&#187;</strong></span> <a href="honors_plan.php">Research Planning Form </a> <br/>
              <span style="color: #6699cc;"><strong>&#187;</strong></span> <a href="../../bin/pdf/ics_honors_project.pdf">Final Project  Form (PDF, 12kb)</a> </p>
<p>The Honors Program in the Bren School provides an opportunity for selected students in any of the School's majors to carry out a research project under the direction of a faculty member in the School. Admission to the program is based on a formal application. </p>
<p>In the following, "Group 1 Courses" and "Group 2 Courses" are defined as shown in the table below:</p>
