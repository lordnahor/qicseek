<td width="94%">An overall grade point average of at least 3.2</td>
</tr>
<tr bgcolor="#f1e7a3">
<td><span style="color: #6699cc;"><strong>&#187;</strong></span></td>
<td>A grade point average of at least 3.5 in the Group 1 Courses </td>
</tr>
<tr bgcolor="#f1e7a3">
<td><span style="color: #6699cc;"><strong>&#187;</strong></span></td>
<td>A grade point average of at least 3.5 in the Group 2 Courses </td>
</tr>
</tbody>
</table>
</td>
</tr>
</tbody>
</table>
<p>Students who began as freshman at UCI must apply in the Spring of their sophomore or junior years. The applications should be submitted by June 2nd even by students who are completing the course requirements that quarter. </p>
<p>Students who transfered to UCI may use transferred units to meet the conditions   above. They also must have <strong>(a)</strong> completed a minimum of three courses in their   major at UCI, <strong>(b)</strong> a 3.2 cumulative UCI grade point average, and <strong>(c)</strong> a 3.5   grade point average in their major.</p>
<p>Transfer students should apply no later than the end of their third quarter at UCI. </p>
<p>Other applications with special circumstances will be considered on a case-by-case basis, and exceptions may be made for sound reasons. </p>
<p>In selecting students for the Honors Program, the School also considers evidence of ability and interest in research. Students admitted to the program participate in the Bren School Honors Seminar (ICS H197), which provides an introduction to research areas in the School. </p>
<p>They then affiliate with a Bren School faculty advisor who agrees to supervise a minimum of two quarters of independent research (ICS H198). </p>
<p>To ensure that students in the Honors Program are not taking excessive units, the following special allowances are made for ICS, CS, and CSE majors: </p>
<table border="0" width="100%">
<tbody>
<tr>
<td valign="top" width="4%"><span style="color: #6699cc;"><strong>&#187;</strong></span></td>
<td valign="top" width="96%">Students majoring in ICS or CS can count completion of two H198s as one project course.</td>
</tr>
<tr>
<td valign="top"><span style="color: #6699cc;"><strong>&#187;</strong></span></td>
<td valign="top">Students majoring in CSE can count CSE 181ABC as one of the ICS H198s required for the Honors Program.</td>
</tr>
</tbody>
</table>
<p>Upon completion of the independent research, the honors student prepares a final written report. If this is certified to be of  honors quality by the student's faculty advisor and the Honors Program advisor, the student is considered to have successfully completed the Honors Program and this is noted on his/her transcript.</p>
<p>For additional information or if you have any questions regarding the Honors Program or application please contact the Student Affairs Office at (949) 824-5156</p>


