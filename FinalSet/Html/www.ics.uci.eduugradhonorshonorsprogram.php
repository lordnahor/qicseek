</tr>
<tr bgcolor="#f1e7a3" valign="top">
<td><span style="color: #6699cc;"><strong>&#187;</strong></span></td>
<td bgcolor="#f1e7a3">The student must have the following grade point averages:</td>
</tr>
<tr valign="top">
<td bgcolor="#f1e7a3">&#160;</td>
<td bgcolor="#f1e7a3">
