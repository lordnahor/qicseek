    <div id="content_title">Account Activation &raquo;</div>
    <p> </p>
    <blockquote>
      <div id="content_text">
        <p>All students with a major under the Bren School of Information and
          Computer Sciences can get an ICS account. Non-majors are allowed accounts
          only if enrolled in a course under the Bren School of ICS (ICS, CS,
          CSE, Statistics, or Informatics course).</p>
        <hr>
        <p>Take your UCI Photo Student ID Card and see the Lab Assistant in the
          ICS computer Lab (CS 364). Ask the Lab Assistant to verify your identity
          by handing them your student ID card so that you can obtain an ICS instructional
          account.</p>
        <blockquote>
          <p> <em>* If you do not have an ID card, you may present other form
            of picture ID if you know your ID number.</em></p>
          <p> <em>** If you are enrolled through Extension please go to CS 346L
            and ask to see the Lab Manager regarding your account. You will need
            to show proof of enrollment, and some form of picture ID (such as
            a driver's license).</em></p>
        </blockquote>
        <p>After your card has been idverified by the lab assistant, you will
          be led to one of the Windows machine used for account activation. The
          lab assistant will login to the machine and start up an session for
          you.</p>
        <ul>
          <li>You will be asked to type in your Student ID number. Type in your
            number without any punctuation characters and press <font face="Courier New, Courier, mono">Enter</font>.</li>
          <li>You will then have to enter your name. Type your name as it appears
            on your ID card, in the form <font face="Courier New, Courier, mono">Lastname,Firstname
            Middlename </font> and press <font face="Courier New, Courier, mono">Enter</font>.</li>
          <li>A summary of your account status will appear.</li>
          <li>A document on the Ethical Use of Computing will be displayed. It
            is important that you actually read the document since it contains
            information that you are required to know. Remember that at all times
            you are responsible and accountable for knowing what constitutes ethical
            behavior when using ICS computer systems.</li>
          <li>You will be asked if you have read and understood the document.
            Press <font face="Courier New, Courier, mono">I agree</font> to continue.
            If you do not agree with the policies or have anyquestions, please
            see the Lab Manager in CS346L.</li>
        </ul>
        <p>Once you finished, you will be asked to supply an initial password
          for your account. Please remember this password. </p>
        <ul>
          <li>Do Not use your student ID number, your name, your birthday, your
            girl/boy-friend's name etc. </li>
          <li>Do not use any part of your name or username (ie. if your name is
            Sam, you cannot have S and A next to each other in the password).</li>
          <li>Do not use * in your password</li>
          <li>Passwords must be at least 6 characters long</li>
          <li>Passwords must contain characters from at least 3 of the following
            4 categories:
            <ul>
              <li>English Upper Case Letters A, B, C, ..., Z</li>
              <li>English Lower Case Letters a, b, c, ..., z</li>
              <li>Westernized Arabic Numerals 0, 1, 2, ..., 9</li>
              <li>Non-alphanumeric characters . , ; : &amp; % ! #</li>
            </ul>
          </li>
        </ul>
        <p>If all goes well, your account will be created within 2 hours. Be sure
          to write down your login name so you don't forget it.</p>
      <blockquote>
        <div><em>* If your username does not match your UCInetID, please let us
          know and we can make the change manually.</em></div>
      </blockquote>
      </div>

