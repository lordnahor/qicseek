<blockquote>
<div id="content_text">

<p><strong>Staying Connected</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: bryan semaan" height="172" src="../../../bin/img/photos/people/bryan_semaan.jpg" width="200"/>
<p>Bryan Semaan receives his <br/>
  CAHS 
   Fellowship award from<br/>
   CAHS co-char Susan Samueli.</p>
</div>
<p>When one of the Southern California firestorms broke out in Ontario last October, the city&#8217;s emergency response team utilized a disaster Web portal that allowed emergency responders to provide the public with real-time information in a crisis.</p>
<p>Ontario's residents used the portal&#8217;s interactive maps to check on the fire&#8217;s progress, find out which streets were closed, monitor mandatory evacuations and locate evacuation centers.</p>
<p>Systems like these are what Informatics Ph.D. student Bryan Semaan and his advisor Gloria Mark, along with UCI colleague Martha Feldman, are studying to better understand the extent which people use technology to continue their collaborative work despite living in a disrupted environment.</p>
<p>Semaan's research recently garnered him the $10,000 Coalition Advocating Human Security (CAHS) Fellowship.</p>
<p>Created in 2005 by the <a href="http://www.cusa.uci.edu/">UCI Center for Unconventional Security Affairs</a>, the fellowship selects researchers who will advance the organizations mission of promoting research, education, public awareness, and evidence based policy making to address urgent cases of vulnerability linked to global changes that impact the lives of individuals, communities, and nations.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;We are conducting an empirical study of people who experience prolonged disruptions to their work and personal lives and how technology helps them cope with adversity to continue living life as normally as possible,&#8221; Semaan said. &#8220;Our goal is to design and build systems to help people deal with the changing environment at work and at home.&#8221;</p>
<p>It was the mix of technology and social issues that drew Semaan to the Informatics Ph.D. program at UCI. </p>
<p>&#8220;When most people think of computer science they think of Office Space and the stereotypical programmer working out of a cubicle,&#8221; Semaan said. &#8220;I knew from my first day as an undergraduate that lifestyle was not for me.&#8221;</p>
<p>While he still enjoys solving technical problems, solving social problems with technical solutions is his real passion. </p>
<p>The Informatics program at UCI allowed him to blend his passion for better understanding the ways in which people coordinate work, with more technical solutions such as designing more usable interfaces, and the actual design of software systems.</p>
<p>&#8220;In order to better understand these problems the school has provided me with methods most wouldn&#8217;t expect out of a computer science program, such as those grounded in anthropological and sociological theory,&#8221; Semaan said. &#8220;It is this interdisciplinary blend that attracted me.&#8221;</p>
<p>The location of the campus between two major cities, is great too. </p>
<p>&#8220;Irvine is located in between San Diego and Los Angeles, which means there is a lot to do if you really want to, yet secluded enough to keep to yourself and get work done.&#8221;
            </p>
<p><strong>CONTRIBUTIONS FOR THE GREATER GOOD</strong></p>
<p>Semaan, a San Diego native, received his bachelor degree in 2005 from UCI and decided to pursue a Ph.D. because he was excited to work with other academics and contribute to the field of computer science.</p>
<p>&#8220;It&#8217;s not as if you&#8217;re only trying to learn material in a course you&#8217;re taking, rather, you&#8217;re doing something fresh and fun that can potentially benefit society,&#8221; Semaan said. &#8220;The academic environment is one that is conducive to the constant sharing of ideas and I wanted to actively participate.&#8221; </p>
<p>Semaan has taken the active participation of idea sharing a step further as a teaching assistant, where he enjoys teaching and learning from his peers.</p>
<p>&#8220;Rather than making my own intellectual contributions, the notion of teaching others is great, as I firmly believe it is equally important, if not more so, to put as much emphasis on teaching as we do research,&#8221; Semaan said. &#8220;We, as teachers, can potentially help inspire those who will go on and do great things.&#8221;</p>
<p>Though Semaan is still debating whether he wants to pursue a career in industry or academia, his decision will hinge on which career will allow him to use his expertise to develop technology that will aid people and benefit society.</p>
<p>&#8220;The beauty of being a computer scientist, especially in California, is the strong relationship between industry and academia in our field.&#8221; Semaan said. </p>
<p><strong>FINDING A BALANCE</strong></p>
<p>An avid player of video games, especially those on the Nintendo Wii, Semaan also loves playing and watching sports like soccer, baseball and basketball and hanging out with friends and family.</p>
<p>Semaan&#8217;s parents are both from Iraq, and are part of a very small Iraqi minority group known as Assyrians and Chaldeans, which consist of the Iraqi Catholic/Christian population. </p>
<p>&#8220;We have a tight-knit family oriented community and our cultural traditions continue here in the states,&#8221; Semaan said. &#8220;Thus it is difficult balancing home life and school life. However, it&#8217;s all about setting and maintaining a routine.&#8221;</p>
<p>His strong faith has lead him to become the club advisor for the Assyrian-Chaldean Student Alliance, an organization through which he has met many people and made many life long friendships. </p>
<p>Though at times it can be difficult for Semaan to find time for his hobbies, he stresses that finding a balance between work and play is one of the keys to success as a Ph.D. student.</p>
<p>&#8220;Balance work, studying and leisure activities,&#8221; Semaan said. &#8220;Make sure to discuss the program and the requirements with fellow graduates because their advice is invaluable.&#8221;</p>
<p><i>- Eric Kowalik</i></p>


