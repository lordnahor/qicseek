                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>We met up after the kick off to discuss several ideas such as an app that shares your location with friends, bumping phones to transfer contact info and a class scheduler. However, the 1 week timeline made us decide to go with the simplest idea in order to ensure a working result. Thus, we settled on the scheduler app. </p>
			</div>

            <div class="title white">
                <h1>Major hurdles that had to be overcome</h1>
			</div>
			<div class="description cms">
				<p>
					Time was a big hurdle because of scheduling issues so we were unable to meet as much as we would have liked. Given our inability to meet up physically, we utilized email and texting to the best of our abilities. 
					<br /><br />
				There was also a discrepancy in programming experience, with some members more knowledgeable about Java than others. We ended up assigning the main programing duties to our two strongest members, with the others writing code with supervision. This way, both parties would be able to learn since teaching concepts to a novice helps to solidify oneâ€™s knowledge. </p>
				
