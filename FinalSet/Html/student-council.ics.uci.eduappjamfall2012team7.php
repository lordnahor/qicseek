                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>
					The application carries the user to a screen where they can enter their classes and information about them. If the user clicks the "Go to assignments" button, the user will be sent to a screen where they can enter information about their assignments. If the user clicks the done button on either screen, the information is saved (when the user is brought back to that screen the information from before will be displayed) and the user is brought back to the previous screen.
					</p>
			</div>

