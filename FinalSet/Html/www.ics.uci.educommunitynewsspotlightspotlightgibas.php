<blockquote>
<div id="content_text">

<p><strong>Family Ties </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: sarah gibas" height="208" src="../../../bin/img/photos/people/sarah_gibas.jpg" width="200"/>
<p>Sarah Gibas</p>
</div>
<p>Blue and gold blood courses through Sarah Gibas' veins, both her parents attended UCI as ICS majors, but it wasn&#8217;t just the strong family ties that lead her to choose UCI. </p>
<p>&#8220;I visited a lot of different campuses in high school, but I finally chose UCI because it was close to home, it's the only UC that has a School of Computer Science, and I really liked the look and feel of the campus,&#8221; Gibas, a sophomore, said.</p>
<p>The Bren School would give Gibas, an ICS major and Digital Art minor, an opportunity to pursue a passion for computers instilled in her as a child by her parents.</p>
<p>&#8220;When I was little, my dad used to give screwdrivers to my brother and I and we would have fun breaking apart some of his old computers,&#8221; Gibas said. &#8220;We would look inside and my dad would explain what all the little parts were.&#8221;</p>
<p>Despite being heavily male dominated, Gibas relished the opportunity to enter a field with so few women. </p>
<p>&#8220;I thought it would be cool to work in a field where women are a minority,&#8221; Gibas said. &#8220;We shouldn't leave all the fun to the men.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>The Mission Viejo native is already contemplating her career after commencement, one she hopes will involve both computer science and art, like working on educational games for children. </p>
<p>This summer Gibas worked at the Lawrence Livermore National Laboratory (LLNL) developing some vector graphics as a computation technical scholar.  </p>
<p>The research focused on providing a graphical representation of chemical data for ChemTrack, LLNL&#8217;s online chemical inventory system. The numerical data that is currently only presented in tables can now be used to generate pie charts on the fly.  </p>
<p>After being given percentages of chemical levels from the database and running it through Extensible Stylesheet Language Transformation (XSLT) code, the program  can generate Extensible Markup Language (XML) that draws Scalable Vector Graphic (SVG) pie charts in real-time. </p>
<p>This new system allows  users to assess chemical levels more quickly and easily.</p>
<p>"I really enjoyed the academic and research-oriented environment," Gibas said. "I regularly attend seminars that are provided for the summer students to learn more about what new research and cutting-edge technologies are being developed at the Lab."</p>
<p>Her work has provided her an opportunity to interact with many individuals outside of computer science including chemists, soil scientists, an industrial hygienist, and more.  </p>
<p>"It is very exciting to have the chance to talk to and learn from these other scientists," Gibas said.</p>
<p>Her real world experience with interdisciplinary learning mimics what she experienced on campus at UCI.  </p>
<p>&#8220;UCI is a great school that has been gradually growing more prestigious,&#8221; Gibas said. &#8220;If you are not sure what you want to do, you have a wide variety of fields to choose from. Plus, the look and feel of the campus is wonderful.&#8221;</p>
<p>Gibas has taken advantage of her opportunity to sample other fields by enrolling in courses outside of computer science, like Writing 31 (Short Story) that allowed her to tap into her love of reading. </p>
<p>&#8220;I love reading books and I learned a great deal about writing fiction in Writing 31 (Short Story),&#8221; Gibas said. &#8220;It was difficult at first, but once I had an idea and started developing it, I really enjoyed writing short stories.  Additionally, if felt great to have the freedom to write pretty much whatever you wanted, so I had fun being creative with it.&#8221;</p>
<p>To students just starting out at UCI, like her younger brother who is a freshman, Gibas offers the following advice. </p>
<p>"Academics are definitely important, but remember to always give yourself some downtime to relax with friends or maybe take a nice walk around the campus. Don't slack off, but remember to also enjoy the college life."</p>
<p>It is advice Gibas takes to heart, as one can find her exploring the campus, discovering new routes, wandering through buildings she hadn't noticed before and taking in the good views from atop some of the taller campus buildings.</p>
<p>Gibas also works as Publicity Chair of the Hapa MIX (Multicultural Interracial eXperience) club on campus, which affords her the opportunity to make posters and help plan events and meetings, and she indulges her love for archery as a member of the UCI Archery Club. </p>
<p>She also finds time to take in a movie or attending a midnight premiere with friends,  playing the piano (especially Beethoven's sonatas), writing calligraphy (with old-fashioned pen and ink), painting and drawing, playing Dance Dance Revolution (DDR), skateboarding and hiking.</p>
<p><i>- Eric Kowalik</i></p>


