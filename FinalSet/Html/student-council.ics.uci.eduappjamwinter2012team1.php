				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
				<li>First Day: Generating Ideas and come up with a doable app</li>
				<li>Second Day: Set up git account and try to connect all members so that we can work collaboratively</li>
				<li>Third Day: Brain storm session for the UI and the main functions/features of the app. Created the basic framework for UI and divivided up the task for each members (UI - settings page & main page, set up and try to connect to database).</li>
				<li>Fourth Day: Finished with the UI (Tabs, Setting page) and started working on features of the apps like pop up, and ringtone.</li>
				<li>Fifth Day: Finish setting up the database and tried to create functions and methods that would help in retrieving data from the database. Finished the Pop Up Dialog features for the app.</li>
				<li>Sixth Day: Improvement on the UI with the addition of new tabs and changes in icons and background images. Finished the Notification features for the app.</li>
				<li>Seventh Day: Struggling with retrieving database and convert it in the correct format to be display on the applications. Continue improving the UI, asked if we can use ViewFlow library, but the time was too late.</li>
				<li>Eight Day: Begin working on the presentation and finishing up all the features. Still have some troubles with retrieving information from the database. </li>
				<li>Testing the applications occurred through out the process of creating the application
