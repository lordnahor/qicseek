    <td><font face="Verdana, Arial, Helvetica, sans-serif" size="2"><A NAME="23"><font size="3">Cohen, Michael</font></A> (<A HREF="mailto:mdc@umich.edu">mdc@umich.edu</A>)
<P>Michael Cohen is William D. Hamilton Professor of Complex Systems, Information, and Public Policy at The University of Michigan.  Cohen received his BA in history from Stanford University  (1966 with honors) and his Ph.D. in Social Science from the University of California at Irvine (1972).  He then held an NSF-funded postdoctoral fellowship at Stanford University.<BR>
<BR>
Cohen's research centers on processes of learning and adaptation that go on within organizations as they adjust to their changing environments. He has written numerous articles contributing to the theories of organizational decision making and learning -- many employing computer simulation.  The most influential of these is &quot;A Garbage Can Model of Organizational Choice,&quot; a &quot;citation  classic&quot; co-authored with James G. March and Johan P. Olsen.  It inaugurated the use of what is now called agent-based simulation as a tool for refining theories of organizational process. <BR>
<BR>
He is also the author, with Robert Axelrod, of Harnessing Complexity: Organizational Implications of a Scientific Frontier. (2000) This book aims to bring concepts derived from research on complex adaptive systems to bear on problems of management and design. <BR>
<BR>
In recent years his empirical research has focused increasingly on the organizational effects of information technology.  The work has involved laboratory studies as well as observation and prototype construction in field settings such as case management agencies and hospital radiology services.
