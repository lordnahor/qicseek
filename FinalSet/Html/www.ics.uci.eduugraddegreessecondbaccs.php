<p><a name="general"></a></p>
<h4>General Information</h4>
<p>Students requesting admission into the Information &amp; Computer Science second baccalaureate degree must satisfy all requirements <strong><span style="text-decoration: underline;">prior</span></strong> to applying. Applications for a second baccalaureate will not be considered until all required coursework is completed.</p>
<p><strong>Students whose first degree was from another institution other than UC Irvine:</strong></p>
<p>Applications will be accepted for the fall only. To ensure admission consideration for the fall or winter quarter, students should be sure to file their application by the UC deadline as indicated at www.admissions.uci.edu. Applications must be submitted to the Office of Admissions and Relations with Schools during the filing period. For more information on application procedures, please contact them at (949) 824-6703 or via the web at <a href="www.admissions.uci.edu">www.admissions.uci.edu</a>.</p>
<p><strong>Students whose first degree was completed at UC Irvine:</strong></p>
<p>Applications will be accepted for any quarter. Former UC Irvine students must meet with an academic counselor to discuss admission for the second baccalaureate. Former UCI students will be admitted via the campus&#8217; readmission policy and must submit a completed UCI Readmission Form to the ICS Student Affairs Office in order to be considered for admission. All interested individuals must adhere to the readmission deadlines that are set by the Registrar&#8217;s Office. Admission for former UCI students will be processed throughout the academic year.</p>
