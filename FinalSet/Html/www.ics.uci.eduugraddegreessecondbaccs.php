<h4>Computer Science and Engineering Second Baccalaureate</h4>
<p><strong>The following minimum requirements must be complete prior to applying for a CSE Second Baccalaureate:</strong></p>
<ol>
<li>A GPA of at least 2.5 for the first bachelor&#8217;s degree.</li>
<li>Completion of CSE 41 and 42 or its equivalents.</li>
<li>Completion of Math 2A-B-D or its equivalents.</li>
<li>Completion of Physics 7C and 7D with labs or its equivalents.</li>
<li>A combind GPA of at least 2.5 in all courses taken and no grade lower than a "C"; on all courses applicable towards the major (including those listed above)</li>
</ol>
<p>Math 2A-B: This is a year of calculus and Computer Science Statistics. A standard, yearlong sequence in calculus at another institution is normally considered equivalent to Math 2A-B.</p>
<p>Physics 7C-D and 7LC-LD: This is a two quarter sequence in classical physics. It is a sequence designed for engineering majors and covers force, energy, momentum, roation, and gravity, and electricity and magnetism.</p>
<p>CSE 41 and 42: This is the UCI introductory sequence in computer science and engineering. It covers history, computer system fundamentals, productivity tools, an introduction to programming and problem solving, and literacy topics in computer science.</p>
