<h4>Informatics Second Baccalaureate</h4>
<p><strong>The following minimum requirements must be complete prior to applying for a Informatics Second Baccalaureate:</strong></p>
<ol>
<li>A GPA of at least 2.5 for the first bachelor&#8217;s degree.</li>
<li>Completion of ICS 6B or equivalent,
with no grade lower than a "C".</li>
<li>Completion of ICS 31, 32, 33, 90, and Informatics 43 or equivalent with a GPA of at least 2.5 and no grade lower than a "C";.</li>
<li>Satisfactory completion of the lower-division writing requirement.</li>
</ol>Informatics 43: Concepts, methods, and current practice of software engineering. Large-scale software production, software life cycle models, principles and techniques for each stage of development. Laboratory project applying these concepts.
<p>ICS 6B: Relations and their properties; Boolean algebras, formal languages; finite automata.</p>
