<p><a name="tips"></a></p>
<h4>Tips for Second Baccalaureate Applicants</h4>
<p>Applications cannot be acted upon until all required coursework is completed and relevant transcripts are received.</p>
<p>Course descriptions can be found in the <a href="http://www.editor.uci.edu/catalogue">UCI catalogue.</a>
</p>
<p>Required coursework may be completed at UCI or with equivalent courses taken at an accredited institution. Courses taken at a four-year or two-year institution can be examined by UC Irvine faculty through a petition process to determine equivalency. In order to determine equivalency, a course syllabus/outline, a course description and a textbook (if necessary) must be submitted to the ICS Student Affairs Office. </p>
<p>Courses may be taken at UCI through ACCESS UCI via University Extension. You can get more information by contacting them at (949) 824-5414 or via the web at <a href="http://www.unex.uci.edu">www.unex.uci.edu</a>. </p>
<p>Courses taken at universities other than local community colleges should be evaluated for equivalency before submitting your application; this way you know you have completed all relevant coursework. Articulation agreements between California community colleges and the Bren School of ICS can be found at <a href="http://www.assist.org">www.assist.org</a>.</p>
<p>Grade point averages as stated in the policy are required. If you complete several math and computer science courses and find you are not earning high enough grades, you may need to reconsider your decision to apply for a second baccalaureate degree in ICS. You may decide not to finish the required course work if you will not be able to get into ICS. The academic counselors in ICS are available to discuss your chances of being admitted to the program and to help you to identify other options.</p>
