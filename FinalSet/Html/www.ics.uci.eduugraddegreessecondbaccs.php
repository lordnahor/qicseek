<p><a name="cs_ics"></a></p>
<h4>CS Second Baccalaureate</h4>
<p><strong>The following minimum requirements must be complete prior to applying for a CS Second Baccalaureate:</strong></p>
<ol>
<li>A GPA of at least 2.5 in first bachelor's degree from a four year institution.</li>
<li>Choose 2 courses from the following: ICS 31, ICS 32, ICS 33, ICS 45C, ICS 46, ICS 51, INF 43, or equivalents.</li>
<li>Choose an additional 2 courses from: Math 2A, Math 2B, ICS 6B, ICS 6D, ICS 6N, Stats 67, or equivalents</li>
<li>A combined GPA of at least 2.5 in all courses taken and no grade lower than a "C" on each course.</li>
</ol>
<p>ICS 6B-D-N: This is a yearlong sequence in discrete mathematics. It is a sequence designed for computer science majors and covers combinatorics and graph theory, logic and boolean algebra, and linear algebra.</p>
<p>Math 2A-B and Stats 67: This is a year of calculus and computer science statistics. A standard, yearlong sequence in calculus at another institution is normally considered equivalent to Math 2A-B. Stats 67 must be completed at UCI through ACCESS UCI via University Extension as there is currently no equivalent available at a community college.</p>
<p>ICS 31, 32, 32, 45C, 46 and 51: This is the UCI introductory sequence in computer science. It covers history, computer system fundamentals, productivity tools, an introduction to programming and problem solving, literacy topics in computer science, data structures and algorithms, and an introduction to system architecture.</p>
<p>Informatics 43: Concepts, methods, and current practice of software 
engineering. Large-scale software production, software life cycle 
models, principles and techniques for each stage of development. 
Laboratory project applying these concepts.</p>
