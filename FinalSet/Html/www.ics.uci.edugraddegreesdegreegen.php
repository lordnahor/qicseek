<div class="blue_bar"><strong>Comprehensive Examination</strong></div>
<p><strong>Each student must pass a written comprehensive examination 
administered and evaluated by a committee assembled by the Bren School 
Associate Dean for Student Affairs. Students cannot pursue the Thesis 
option for this degree.</strong></p>
<p>
<a name="more"></a>
</p><div class="green_bar"><strong>More about the degree...</strong></div>
<p>The general M.S. concentration is designed for students who do not 
wish to specialize in any specific area. </p>
<p>Students can explore many of the advanced fields in computer science,
 including: </p>
<ul>
  <li>Embedded systems </li>
  <li>Networking </li>
  <li>Databases </li>
  <li>Computational geometry </li>
  <li>Neural networks </li>
  <li>Data mining </li>
  <li>Machine learning </li>
  <li>Graph algorithms </li>
  <li>VLSI </li>
  <li>Parallel architectures </li>
  <li>User interfaces </li>
  <li>Bioinformatics </li>
  <li>Graphics and visualization </li>
  <li>Security and cryptography </li>
  <li>HCI/CSCW </li>
  <li>Software engineering </li>
  <li>Data structures </li>
  <li>Ubiquitous computing.&#160;</li>
</ul>


