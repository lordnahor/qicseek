<h3><p><strong>General M.S. Degree</strong></p></h3>
<p>The Information and Computer Science (ICS) general M.S. concentration
 is designed for students who do not wish to specialize in any specific 
area. Students can explore many of the advanced fields in computer 
science. <a href="degree_gen.php#more">more
