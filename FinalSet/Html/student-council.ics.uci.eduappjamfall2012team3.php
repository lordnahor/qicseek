            <div class="title white">
                <h1>Brief overview of the application and what it does</h1>
			</div>
			<div class="description cms">
				<p>Metis is an interactive scheduling tool that allows you to coordinate your schedule with friends, family, and fellow students.  With our unique visual timeline feature, you can view your schedule in real time and compare it with the schedules of others.  Combine all timelines with the special merge feature to easily see gaps of shared free time.  Plan meetings, set up study groups, or just hang out.  Maximize your free time with friends and family.  Metis will help you manage your schedule and make the most out of your time.</p>
			</div>

            <div class="title white">
                <h1>Programming languages, compilers and IDEs used</h1>
			</div>
			<div class="description cms">
				<ul>
				<li>Java, PHP, MYSQL, and SQLite programming languages</li>
				<li>Eclipse IDE</li>
				<li>Android SDK</li>
				</ul>
			</div>

            <div class="title white">
                <h1>Sources of inspiration for the project</h1>
			</div>
			<div class="description cms">
				<p>Working with random people in projects like AppJam, require a lot of scheduling and coordination.  The first order of business for our own AppJam project group was to figure out a schedule that would work for everyone.  After spending 20 minutes talking about our individual class and work schedules, we got absolutely nowhere and decided to schedule everything on a day-by-day basis.  What we needed was a program that would help us manage and coordinate our schedules; one that would allow us to find and create shared free time to meet.</p>
			</div>

            <div class="title white">
                <h1>Libraries, frameworks and other tools used</h1>
			</div>
			<div class="description cms">
				<ul>
				<li>Android SDK</li>
				<li>MYSQL like</li>
				<li>SQLite</li>
				<li>PHP</li>
				</ul>
			</div>

            <div class="title white">
                <h1>Art assets, logos and content not created by the team</h1>
			</div>
			<div class="description cms">
				<ul>
				<li>The app's logo</li>
				<li>The clock in the "About Screen" interface</li>
				</ul>
			</div>

            <div class="title white">
                <h1>The process for making the application</h1>
			</div>
			<div class="description cms">
				<p>We downloaded all the necessary tools for making an Android application.  Primarily, we downloaded the Android SDK.  Next, we brainstormed the concept and design for the program and scheduled tasks to accomplish every day. Finally, we carried the implementation and decided to use Tortoise SVN repository in order to share all of our work through updating and committing changes.</p>
			</div>

            <div class="title white">
                <h1>Major hurdles that had to be overcome</h1>
			</div>
			<div class="description cms">
				<p>Primarily, we all knew that we had only one week to complete this application so we had to be as productive as possible.  Although we had 5 members to begin with, one of the members decided to drop out of the competition in the middle of the week because he had too much other work to handle and was unable to contribute to this project to his fullest potential.  Finally, we had a lack of experience since only one of our group members has experience with programming for Android.</p>
			</div>

            <div class="title white">
                <h1>Lessons Learned</h1>
			</div>
			<div class="description cms">
				<p>We learned that programming an application within a week is very difficult.  Time management and communication is very important when working in a group dynamic with limited time and resources.  Furthermore, knowing other programming languages is very useful for not only understanding general concepts, but also for creating mobile applications.</p>
			</div>

