<p><b>Dan Russell &#8217;77 receives Lauds &amp; Laurels Distinguished Alumnus Award</b></p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<!-- -->
<div class="floating_image_with_caption right" style="width: 200px;">
<img alt="Dan Russell received the Bren School&#8217;s 2013 Lauds &amp; Laurels Distinguished Alumnus award." src="../../../bin/img/photos/spotlight/DanRussell-Lauds4.jpg" style="width: 200px;"/>
<br/>
Dan Russell received the Bren School&#8217;s 2013 Lauds &amp; Laurels Distinguished Alumnus award.
</div>
<p>
The world now knows him as a MOOC (massive open online course) pioneer and the creator of &#8220;A Google a Day.&#8221;
</p>
<p>
But 40 years ago, Dan Russell &#8217;77 was simply a freshman at UC Irvine, learning about a thing called artificial intelligence. 
</p>
<p>
Russell &#8212; honored earlier this year with a <a href="http://www.specialevents.uci.edu/events/celebrations/2013%20Lauds%20and%20Laurels%20.php">Lauds &amp; Laurels</a> Distinguished Alumnus Award &#8212; encountered his first computer as a Boy Scout and quickly developed an interest in programming. When it was time to go to college, he says choosing UCI was &#8220;a no-brainer.&#8221;
</p>
<p>
&#8220;Not many universities were offering a computer science degree at the time, and even fewer had good computing resources,&#8221; Russell recalls. &#8220;That wasn&#8217;t the case at UC Irvine. It was an advanced campus with a very good set of classes and great faculty. Another advantage: UCI created its CS program from nothing. All the other universities were struggling to define computer science as something that grew out of math, or engineering. At UCI, it was new, it was exciting, and even to a naive 17-year-old boy, it was clear that it was going to be a great program.&#8221;
</p>
<p>
From UCI, Russell went on to the University of Rochester where he earned a Ph.D. in computer science. Later he would return to his native California for positions at top technology companies Xerox PARC, Apple, IBM and, ultimately, Google, where he serves as &#252;ber tech lead for search quality and user happiness.
<!-- -->
<!-- -->
<!-- -->
</p>
<p>
<b>Educating the masses</b>
</p>
<div class="floating_image_with_caption left">
<img alt="Russell took part in &#8220;The Nuts and Bolts of Delivering a MOOC&#8221; spring 2013 panel hosted by the Bren School." src="../../../bin/img/photos/spotlight/DanRussell-MOOC1.jpg"/>
<br/>
Russell took part in &#8220;The Nuts and Bolts of Delivering a MOOC&#8221; spring 2013 panel hosted by the Bren School.
</div>
<p>
In his current role, Russell works to understand &#8220;what makes Google users tick,&#8221; enhances Google functionality for users, and teaches the world successful search strategies. Among his high-profile contributions: &#8220;<a href="http://www.agoogleaday.com/#game=started">A Google a Day</a>,&#8221; a daily puzzle that can be solved &#8220;using your creativity and clever search skills on Google,&#8221; according to the game website. Questions are posted online daily and also printed in <em>The New York Times</em>; the answer is revealed the next day along with related search tips. More than 1.5 million people play each week, and about 50 million have played since its spring 2011 launch. 
</p>
<p>
&#8220;It&#8217;s pretty exciting to walk into a coffee shop and see people use something you created,&#8221; says Russell, who designed the game and wrote the initial code. &#8220;And it works! People who play the game regularly actually become better searchers. I am completely happy with it.&#8221;
</p>
<p>
Russell also pioneered the MOOC &#8220;<a href="http://www.powersearchingwithgoogle.com/">Power Searching with Google</a>,&#8221; with the first course attracting 155,000 students from 196 countries. The platform used for these MOOCs has been released as <a href="http://code.google.com/p/course-builder/">open-source code</a>, enabling dozens of individuals and groups around the world to launch their own courses. 
</p>
<p>
&#8220;The idea of building a tool that enables hundreds of thousands of people access to better education&#8230; It&#8217;s so rewarding, because we&#8217;ve given back to the community,&#8221; Russell says. 
</p>
<p>
<!-- -->
<!-- -->
<!-- -->
</p>
<p>
<b>UCI roots</b>
</p>
<div class="floating_image_with_caption right">
<img alt="Russell and his family with (from left) professors Judy and Gary Olson, Dean Hal Stern and his wife, Hara Waldman." src="../../../bin/img/photos/spotlight/DanRussell-Lauds2.jpg"/>
<br/>
Russell (center) and his family with (from left) professors Judith and Gary Olson, and Dean Hal Stern and his wife, Hara Waldman.
</div>
<p>
Throughout his professional journey, Russell has kept in touch with his UCI family and continues to make new connections. 
</p>
<p>
&#8220;Just an hour ago, I was walking to my office and someone stopped me to say, &#8216;Meet my new intern. She&#8217;s from UC Irvine!&#8217;&#8221; he said in a recent interview. &#8220;UCI planted a lot of seeds in my life. It&#8217;s just a great community to be a part of.&#8221;
</p>
<p>
Today Russell is a proud supporter of the Anteater network. Bren Professors Judith and Gary Olson were among the first recipients of the Google Focused Research Award, a program developed by Russell and Google chief economist Hal Varian. Totaling $1.5 million, the award enables the Olsons and their graduate students to study how using collaboration tools can help people from different cultures communicate effectively. 
</p>
<p>
"Dan is the perfect research liaison for Google &#8212; a partner in ideas, a sounding board, someone who introduces us to amazing other Google research collaborators, and now an all around good friend,&#8221; says Gary Olson. &#8220;He is a joy to work with."
</p>
<p>
Russell and his wife, Lynne, also regularly support the Donald Bren School of Information and Computer Sciences&#8217; annual fund, which finances scholarships and fellowships for deserving students. 
</p>
<p>
&#8220;Dan Russell is a world leader in human-computer interaction research,&#8221; notes Hal Stern, the Ted and Janice Smith Family Foundation Dean of the Bren School. &#8220;His incredible achievements in industry since receiving his ICS degree in 1977 and his continued engagement with our school as a valued friend and supporter exemplify the spirit of the Lauds &amp; Laurels Award program.&#8221; 
<!-- -->
<!-- -->
<!-- -->
</p>
