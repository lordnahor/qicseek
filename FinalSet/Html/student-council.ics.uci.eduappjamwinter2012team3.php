			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
				After the kick-off event, we assembled to start brainstorming and getting to know each other. We came up with several ideas that fit with the theme of self-improvement; we each did research to see which idea would be the most useful for self-improvement. The following day we decided on the app idea and began mapping out the user interface and features. We setup a Dropbox for version control and setup Saros to collaborate with each other. We continued to meet up and we started our coding phase. Our team began to implement Google maps, MySQL and figure out the UI of the app. During this time, we began creating our images including the logo and the graphics for buttons in the application. At this time, we were five days into the competition and we all had found our roles whether it be database manager or coding master. We continued to type away and merge each other's code. During the process of coding, each person took on a separate part of the application. We would continually collaborate with each other and help each other during the process. Winding down to the last days of the competition, we had our basic working user interface for each tab and the setup for the SQL database had been created. We continuously created, edited, deleted, banged our heads and whipped out code to add to the app. 
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
				<ul>
					<li>Figuring out what features to implement in the time frame that we have to develop was difficult. Everyone had ideas that would make the app better or more interesting; we had to focus on the basic features. It was also difficult because we were not sure what was more difficult to implement or which procedure was the easiest to tackle the building of the program.</li>
					<li>Splitting existing user interfaces into multiple activities instead of one so we could utilize tabs and list views</li>
					<li>Although we had a few people who participated in AppJam last quarter, we did not have too much experience working with the Android development. In addition, we did not have any experience working with the APIs we were planning on working with. This was a big challenge that we had but through helping each other and looking at countless resources we were able to overcome this. </li>
					<li>Another hurdle was how we were going to share the code with each other, we didn't have any experience using GitHub or any repositories online. We figured that learning how to use it for this one week competition would be time that we could use figuring out how to build the app. We chose to use Dropbox to share files and we updated the folders and files collectively. </li>
				</ul>
			</p>
			

