<p> <strong>What is Informatics? </strong></p>
<ul>
<li>
				Informatics brings together software engineering, human-computer 
interaction, and the study of information technology in organizations 
into a single, focused degree program.				</li>
<br/>
<li>Informatics addresses the design and construction of 
information technology systems that will be effective in real-world 
situations.  It includes not only building software but also examining 
the human, organizational, and social context where the system will be 
used. </li>
<br/>
<li>Informatics is the interdisciplinary study of the design, 
implementation, use, and impact of information technology.</li>
<br/>
<li>Informatics is a common term for computer science in 
European universities; the number of Informatics programs in American 
universities is growing.</li>
</ul>
<p>Traditional computer science programs focus on analyzing and 
designing computers and computer systems, but the success of those 
systems depends not only on their intrinsic characteristics but also on 
the real-world situations where they will be used&#8212;the human users and 
their requirements, characteristics, and organizations. Informatics 
studies computer systems in their real-world context and how to design 
and build those systems so they work effectively in that context. </p>
<p>In Informatics we aim to:</p>
<ul>
<li>Design and develop new uses for information technology.</li>
<li>Understand the effect information technology has on 
people.</li>
<li>Apply information technology in fields ranging from 
medicine to music, from ecology to aerospace, from finance to 
entertainment.</li>
</ul>
