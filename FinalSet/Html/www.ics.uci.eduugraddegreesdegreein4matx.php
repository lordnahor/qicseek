<p> <a name="uci"></a> </p>
<h4>Is Informatics for me? </h4>
<p>Informatics is for you if you:</p>
<ul>
<li>Enjoy solving problems using all the tools you have available.</li>
<br/>
<li>Can work not only with technical details but also with 
&#8220;big-picture&#8221; issues</li>
<br/>
<li>Have strong reading and writing skills and can think freely, 
creatively, and systematically.</li>
</ul>
<p>Previous experience in computer programming is not required to 
start the Informatics major. Programming is just one aspect of 
Informatics and the major introduces all the necessary skills at a 
manageable pace. Students who do already have some programming 
experience will also find new concepts, even in the very first course. </p>
<p>The Informatics major offers a contemporary curriculum with an 
emphasis on group work (starting in the first course), studio-oriented 
design courses, and a year-long senior project.</p>
<p>The first year of the program provides students with a hands-on 
introduction to the broad field of Informatics, centering on the 
Informatics Core Course. This year-long course develops students&#8217; basic 
understanding of software: how to design and construct programs and how 
the programs operate as part of information technology systems.</p>
<p>The second year builds up a portfolio of foundational concepts 
and techniques that establish the discipline of Informatics; these 
contribute to the &#8220;toolbox&#8221; students will use in subsequent years to 
solve large-scale information and software design problems.  As 
sophomores, students begin to take more advanced courses that support 
their specialization in either software engineering, human-computer 
interaction, or the study of organizations and information technology. 
These may involve courses in Management, Psychology, Computer Science, 
or Engineering.</p>
<p>In the third year, all students study the design process, 
project management, and the impacts of technology on the real world. 
Students continue to take electives in their specialized area of study.</p>
<p>The fourth year is built around a year-long capstone project in 
which groups of students address a significant project, typically from 
an outside client.</p>
