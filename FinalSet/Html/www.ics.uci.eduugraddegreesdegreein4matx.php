<a name="get"></a>
<h4>What do I take? </h4>
<p>All Informatics majors take a common core of 16 
courses. After their first year, Informatics majors choose an additional
 10 courses in one of three specializations:</p>
<ul>
<li>Software Engineering</li>
<li>Human-Computer Interaction</li>
<li>Organizations and Information Technology</li>
</ul>
For a full listing of courses required for the major, see the <a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofinformatics/">General Catalogue</a>.
<p>Students are encouraged to consult an academic advisor 
in the Bren School of ICS to determine the coursework designed to meet 
their educational goals. </p>
<p>Additional information about this degree can be found 
on the <a href="../../../informatics/ugrad/index.php">Department of
 Informatics web site</a>. </p>
<p>&#8593; <a href="degree_in4matx.php#startcontent">Back
 to top</a><br/>
              <a name="what"></a> </p>
<h4>What can I do with this degree?  </h4>
<p>A degree in Informatics provides excellent preparation for 
work at the forefront of the computing industry.               
              </p>
<p>Our students work in many industrial settings, such
 as start-up companies, small software houses, consulting firms and 
multi-national corporations.               
              </p>
<p>They are prepared for careers including these:</p>
<ul>
<li>Software Engineer</li>
<li>Human-Computer Interface Designer</li>
<li>Information Architect</li>
<li>Game Designer</li>
<li>Usability Engineer</li>
<li>Mobile Computing Systems Designer</li>
</ul>
Many students will go on to graduate school in computer 
engineering, computer science, information science, management or law.
<p>&#8593;<a href="degree_in4matx.php#startcontent">Back to top</a></p>
<a name="minor"></a>
<h4>What can I do with the Informatics Minor?</h4>
<p>The minor provides a focused study of Informatics to 
supplement a student&#8217;s major program of study and prepares students for a
 profession, career, or academic pursuit in which information and 
software design is an integral part but is not the primary focus. </p>
<p>The minor allows students sufficient flexibility to 
pursue courses that complement their major field or address specific 
interests. The minor particularly centers on understanding the 
relationships among computers and people, and how these relationships 
must be addressed in information and software design. </p>
<p>Requirements for the Informatics minor can be found in the <a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofinformatics/">General Catalogue</a><strong>.</strong></p>
