            <div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
					TeamUp is an Android app that provides users with a quick, easy, and competitive self-improvement environment. Through the use of TeamUp challenges, users will be able to boost their skills in the following categories: Education, Health, Fitness, Financial, Social, and Spiritual. Upon completion of each challenge, users will be awarded points dependent on the difficulty of the challenge. Users can connect and interact with other users through our friends system. This system includes features such as suggesting challenges to friends and messaging. 
				</p>
				
			<div class="page-header">
				<div class="styled_title"><h1>Features</h1></div>
			</div>
				<p>
					Team Up allows users to create and complete challenges for self-improvement. It has social and sharing aspects for motivation, like
					<ul>
					<li>see tasks other people are doing</li>
					<li>invite friends to tasks</li>
					<li>suggest tasks for friends</li>
					<li>search for tasks and friends</li>
					<li>make friends with people interested in similar tasks</li>
					<li>make groups and complete tasks together</li>
					<li>share challenges on other social networking sites</li>
					</ul>
				</p>
				
			<div class="page-header">
				<div class="styled_title"><h1>Collaborate and Compete</h1></div>
			</div>
			<p>
				The application supports team spirit, encouraging users to create groups, and complete challenges together in teams. Users gain points for completing challenges, based on the difficulty level. They can check scores and ranks in leaderboards, compete with another team, and share their achievements on Facebook or Twitter. Team Up creates a competitive game-like environment, where people compete for points. 
				<br />
				<br />
				Users can also see how popular tasks are -- based on the number of people accepting and completing the challenge. Team Up also has a Reverse Popularity feature, which pushes up the tasks that are less popular, as a way to encourage users to take up harder tasks, that people may shy away or be afraid of doing. The application has a messaging system, which allows users to send personal messages to friends. We would like to implement the geolocation feature in the future, which allows users to search for tasks and people around them. Through this feature, users will also be able to join group challenges that they are interested in, which are happening in their vicinity.
			</p>
				
