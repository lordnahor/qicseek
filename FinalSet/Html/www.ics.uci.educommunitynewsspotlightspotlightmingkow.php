<div id="content_title">Yong Ming Kow spotlight</div>

<blockquote>
<div id="content_text">

<strong>Hybrid Vigor    </strong>
<p><img alt="photo: yong ming kow" height="266" src="../../../bin/img/photos/people/yong_mingkow.jpg" width="470"/><br/>
		  <i><strong>Doctoral student Yong Ming Kow gave a presentation at the Chinese Academy of Sciences in 2008 about his research into the community of modders for the World of Warcraft.</strong></i>
		</p>
<hr/>
<p>  In the early years of software development, products were internally driven &#8211; companies internal marketing, research and development and user experience team drove the user interfaces and functionalities of the product&#8217;s release.</p>
<p>Today open application programming interface&#8217;s (API) such as those for Google Maps and the Apple iPhone, have given rise to communities of users who modify a piece of hardware or software or anything else for that matter, to perform a function not originally conceived or intended by the designer</p>
<p>These users, or modders as they are called in gaming, can create functionality that is much more suited to their own needs and a lot faster than the manufacturer can, and as second year doctoral student Yong Ming Kow is discovering, modders will have an important impact on the future of software development. </p>
<div class="highlight_box_left"> <a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni. </div>
<p>&#8220;In a way, modding culture is important to the future of software and product development,&#8221; Kow said. &#8220;The main stream development paradigm is still internally driven. Even though these design leaders would hold periodic surveys with users, you still lose time and data going back and forth. But imagine what will happen when you open up some of these components to modding communities, and they are able to modify the product how and when they need to?&#8221;</p>
<p>Kow and informatics professor Bonnie Nardi are trying to understand, what are these communities like and how can their potential be unleashed.</p>
<p>To that end Kow and Nardi have spent the past two years observing and interviewing modders in the United States and China who play World of Warcraft, a massively multiplayer online role-playing game. These user created mods have systematically been incorporated into the game by its developer &#8211; Blizzard.</p>
<p>&#8220;Modding communities contribute to creativity because they modify the product to keep it relevant to users. They are players too, and almost all of them do it free of charge,&#8221; Kow said. &#8220;Also modding communities are virtual communities. As a result, studying modders is like positioning our fieldwork at the intersection of the real and virtual.&#8221; </p>
<p>Born in Singapore to a Chinese speaking family, Kow was sensitive to the differences of both the American and Chinese cultures and his backgrounds channeled him into his research topic. The work has attracted attention in China and Kow recently gave a speech about the work at China&#8217;s Institute of Psychology of the Chinese Academy of Sciences.</p>
<p>&#8220;The Chinese Academy of Sciences was also working on cross-cultural issues and was interested in how we made the comparison and what were our findings. It was timely as we had tested some of the ideas we had put forth and many of our claims got verified,&#8221; Kow said. &#8220;Our data seemed to show that the Chinese were less creative, which attracted many challenging questions from the audience. That unleashed an open discussion around cultural-historical conditions of China that helped position our research questions.&#8221; </p>
<p><strong>HYBRID VIGOR</strong></p>
<p>The ability to conduct research in an environment that is open to the blending of disciplines was one of the major reasons Kow chose the Bren School to pursue his doctoral study.</p>
<p>&#8220;The Bren School promotes a socio-technological mixture in its curriculum and it is important to tomorrow&#8217;s think tanks. There are many out there who still think that society is one thing and technology is another,&#8221; Kow said. &#8220;Many of us in the Bren School believe that technology is integrated into our social life and you can find many forward thinking researchers here. &#8220;</p>
<p>Kow had been working at Central Labs in Minneapolis and Singapore before deciding to pursue a Ph.D. </p>
<p>&#8220;I had always been a thinking type and I enjoyed exploring new ideas,&#8221; Kow said. &#8220;When I made up my mind on a Ph.D., I was certain I wanted to pursue Anthropology and Activity Theory, in the context of Information Society. I searched for a long time but it appeared only Bonnie Nardi was doing all of these.&#8221;</p>
<p>In addition to his research work and studies, for the past three quarters Kow has been a reader, in charge of grading homework, papers, and examinations and holding office hours for various classes. </p>
<p>&#8220;I enjoyed the opportunity to talk with students, though I can get a bit turned off by students who came to me only to bargain for grades,&#8221; Kow said with a smile. </p>
<p>Kow lives in Palo Verde, an on campus graduate and family community, with his wife Lee Peng who works at a local auditing company. While he tries his best to maintain a work life balance, it can be difficult when your conducting research in a different timezone.</p>
<p>&#8220;My wife has been extremely supportive and I tried not to work after 6 p.m. and on weekends but that was impossible for a couple of reasons, there were reader duties, I had to engage with members of the Chinese modding communities at night, and I am playing World of Warcraft in the United States, China, and Taiwan,&#8221; Kow said. &#8220;On some weekends, however, my wife and I spend time visiting the zoo, Disneyland and the local shopping mall.&#8221;</p>
<p>Graduation is still a few years away and Kow is still not sure whether he wants to stay in academia or pursue a career in the corporate world. </p>
<p>&#8220;I would like to explore a role that allows me to investigate first hand the technological world we are living in,&#8221; Kow said. &#8220;I believe in teams above organizations.&#8221;</p>
<p><i>- Eric Kowalik </i></p>


