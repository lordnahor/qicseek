<b>Student Innovations Featured in Annual Showcase</b>
<p>More than 60 undergraduates in 14 teams presented their projects at the annual Bren:ICS Student Showcase held March 17, 2011. The event highlighted a range of student innovations, from mobile apps, to search engines, interactive games and more.</p>
<p>"Students work on real-world projects addressing real needs of real users," says Informatics Lecturer Hadar Ziv. "The projects require hands-on application of all the skills and&#160;tools the students acquired and
is the ultimate culmination of their four-year curriculum. Students also&#160;learn and apply a host of non-technical skills
that will serve their entire professional careers &#8212; from project,
time and team management to communication, collaboration, negotiation and
conflict-resolution skills."</p>
<p>This year&#8217;s showcase participants are listed below. <a href="http://www.flickr.com/photos/brenschool/sets/72157626430267243/">Click here</a>&#160;to view photos of the event. For a short video summary, please visit the <a href="http://www.youtube.com/user/UCIBrenICS#p/u/0/ONFYdhmSAyU">Bren:ICS YouTube page</a>.</p>
<h4>INFORMATICS 191: SENIOR DESIGN PROJECT (YEAR-LONG)</h4>
<p>Team: Burble</p>
<p>Members: Fiel Gunt, Stacey Dao, Chris Combs, Sarah Lee, Viet
Pham</p>
<p>Project: Burble is a social iPhone game promoting
environmental sustainability. People spend hours playing games that plant fake
trees &#8212; why not let them plant real trees? Burble allows users to plant real
trees in various parts of the world. The tree planting is made possible through
nonprofit organizations affiliated with the game. By caring for your personal
pet, Burble will help plants grow and plant them in gardens that reflect those
in the real world.<br/>
<br/>
</p>
<p>Team: Creampuff</p>
<p>Members: Sabel Braganza, Mark Capil, Jonathan Fuentes, Kyle
Musler, Kyle Lutze</p>
<p>Project: Chowdr is a dual-sided mobile application. It
serves as a marketing platform for food truck chefs and owners, as well as a
mobile and web application for customers to find these food trucks. By tying
the two sides together, Chowdr builds a solid relationship between a food truck
and its fans.<br/>
<br/>
</p>
<p>Team: ExpediANT</p>
<p>Members: Gilberto Bardales, Norik Davtian, Georgio Moussan,
Donald Stern</p>
<p>Project: ExpediANT is a limousine management system that
focuses on routing and scheduling for vehicle rental companies. It is a free,
web-based solution built with extendibility in mind.</p>
<p>&#160;</p>
<p>Team: Prograam K</p>
<p>Members: Aaron Donk, Anthony Liu, Grace Uchida, Karen Quan,
Melanie Cheung, Ryan Nissenbaum</p>
<p>Sponsor: Elif Hanna, Greenwithus.com</p>
<p>Project: An e-commerce website that allows users to not only
browse through (and send) a selection of pre-made cards, but also upload their
own picture and customize it to their liking. Designers can upload their own
design and enter it in a weekly contest where users can vote on their favorite.
The winner of the weekly contest will have his/her design available for the
public to purchase on the website.</p>
<p>&#160;</p>
<p>Team: Inyofam</p>
<p>Members: Chris Lee, Frank Morales, JD Wolk, Masis Nguyen,
Ram Murthy, Zack Pelz</p>
<p>Sponsors: UC Irvine Medical Center, Bren:ICS</p>
<p>Project: I2b2 is a web tool funded by the NIH that accesses
medical and clinical repositories by academic research centers and
universities. I2b2 is a discovery tool used by clinical researchers to identify
cohort patients for research participation. Our sponsor, the UCI Medical
Center, needed a dictionary tool that integrated into the interface. I3PO
altered and enhanced i2b2 based on user testing and feedback. The user
interface was cleaned up to improve the workflow. The dictionaries map medical
codes to healthcare terminology. Fuzzy logic provides meaningful definitions to
codes and partial clinical diagnoses.</p>
<p>&#160;</p>
<p>Team: Team 411</p>
<p>Members: Ryan Hsu, Ellen Eramya, Alex Bretana, Danny Kim,
Josh Papa</p>
<p>Sponsors: Daniel Brienen, Ryan K. Olsen, UCI Medical Center</p>
<p>Project: The UCI Medical Center Department of Radiology medical
records contain two different sets of codes: diagnostic and treatment. These
are input by different people throughout the patient&#8217;s visits in the hospital.
Codes are supposed to match up perfectly, but they don&#8217;t always &#8212; and insurance
companies won&#8217;t reimburse the hospital unless they do. We&#8217;ve built a medical
code verification application that checks for mismatched codes and helps
correct them, potentially saving the UCI Medical Center $400,000 a year.</p>
<p>&#160;</p>
<p>Team: Bionic Centaurs</p>
<p>Members: Marianne Conner, Edward Gim, Roger Le, Danielle
Song, Duncan Tsai</p>
<p>Sponsors: Jon Teichrow, Gary Teichrow, Will Hartung, Mirth
Corporation</p>
<p>Project: Mirth Archive is a medical record search engine. It
stores and indexes medical records, as well as returning relevant medical
records based on the queries entered by the user.</p>
<p>&#160;</p>
<p>Team: Rebootin&#8217;</p>
<p>Members: Alex Chung, Andre Dai, Francesca Mantovani, Michael
Merchant, Hunter Gillane</p>
<p>Sponsor: Jeff Lindsay, Open-Source Developer, Independent</p>
<p>Project: Notify.io is an open platform for notifications. It
provides a simple way for developers to send notifications to their users via
Growl, Jabber, email, Twitter, Prowl and more.</p>
<p><br/>
Team: Ant Colony</p>
<p>Members: Azia Foster, Patrick Lu, Garrett Kim, Matthew
Palmer, Hiroe Ono</p>
<p>Project: Project Seon is a way for members of a community to
share and view the world around them. The project consists of a web application
and a mobile application that work together to map out, describe and view
places of interest.</p>
<p>&#160;</p>
<p>Team: Fire Tornado</p>
<p>Members: Ryan Cadavona, Jesse Joseph, LucasKam, Leo Zen,
Jesse Danielson</p>
<p>Sponsors: Robert Farmer, Rizza Barnes, Bren:ICS</p>
<p>Project: Created a system for managing the content displayed
on the digital signs in Donald Bren Hall. Expanded as an open-source signage
system called Xibo, focusing on improving the system&#8217;s usability.</p>
<h4>COMPUTER SCIENCE ENGINEERING 181: SENIOR DESIGN PROJECT (TWO-QUARTER)</h4>
<p>Team: Automated Labyrinth</p>
<p>Members: Patrick Murtha, Christopher Escobedo, Michael
Seviulla, Niraj Desai</p>
<p>Project: The labyrinth is a classic board game that is both
exciting and challenging. Our system plans to solve the labyrinth accurately
using visual feedback.</p>
<p><br/>
Team: Mouseless Mouse</p>
<p>Members: Melody Kwok, Candice Lai, Derek Lwin, Ellick Truong</p>
<p>Project: The Mouseless Mouse functions as a touch pad but
does not use a touch pad. Instead, it uses an infrared emitter and a camera to
detect finger movement.</p>
<p><br/>
Team: Pitch-er Perfect</p>
<p>Members: Alex Yee, Allison Moon, Bryan Koe, Vimala
Narasimhan</p>
<p>Project: A real-time pitch detection system that calculates
the accuracy of the user&#8217;s pitch against the pitch of a selected song.</p>
<p><br/>
Team: Super Spray</p>
<p>Members: Adrian Guzman, Jared Haren</p>
<p>Project: Super Spray is an interactive game where the player
must spray water on as many targets as possible before the end of the round.
The player will use a custom-built water gun controller, which uses various
sensors to apply input to the game.</p>
