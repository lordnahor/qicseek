<div id="content_text">

<p><strong>The Modeler </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: guy yosiphon" height="104" src="../../../bin/img/photos/people/guy_yosiphon.jpg" width="171"/>
<p>Guy Yosiphon</p>
</div>
<p>Guy Yosiphon, a Ph.D. student in Information and Computer Sciences, was looking for a challenge.</p>
<p>After earning his bachelor degree in 2000 from the Tel Aviv Academic College in Israel, he had worked in the software industry for two and a half years as a programmer and project leader. </p>
<p>Although it was rewarding, he missed the challenge of academic studies and wanted to get involved in a scientific research environment. </p>
<p>A little help from a previous manager and some research lead him to the Ph.D. program in the Bren School where he finds himself engaging in a plethora of academic and real world problems. </p>
<p>"It seems like this school is constantly growing and expanding. New buildings, new departments and new professors are getting recruited all the time," Yosiphon said. "So there is a wider collection of computer science and statistics courses available for graduate students and more research projects to choose from."</p>
<p>Southern California's perfect climate and relaxed atmosphere was a bonus too.</p>
<p>But returning to school in a different country made him a little nervous, until he got to campus, where he met fellow Bren School graduate students and it became an enjoyable experience. </p>
<p>"Coming back to lectures for the first time after couple of years was exciting," he said. "It was also the first time I got to meet the Bren School professors after only reading about them from their websites."</p>
<p>One of these professors, Eric Mjolsness, became Yosiphon's advisor and together the two have been working on constructing models for two primary applications. </p>
<p><strong>SPIRAL GALAXIES AND COMPUTABLE PLANTS </strong></p>
<p>The first application is a Spiral Galaxy morphology model in which the tool is used for inferring structural properties of spiral galaxies from images. </p>
<p>The second application is a growth model of stem cells niche where the objective is to reproduce patterns of growth and differentiation of stem cells observed by biologists.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>"My research goal is to build a modeling tool that is based on properties of stochastic processes," Yosiphon said. "The tool has numerous applications in such fields as machine learning, pattern recognition, and systems biology. We are developing the tool for doing both simulations and inference of the model's parameters."</p>
<p>The research has lead to a joint paper with Mjolsness that is in the advanced stages of acceptance to a computer science journal and Yosiphon also has recently attended the Space Mission Challenges for Information Technology conference held at the Jet Propulsion Laboratory in Pasadena. </p>
<p>Yosiphon also has gained valuable insights during his three quarters as a teaching assistant. </p>
<p>"It gave me an opportunity to interact with fellow students and act as a mentor," Yosiphon said. "Not only that, I even acquired deeper understanding in the subjects that I was teaching and got some insights to my own research when helping students with their projects.</p>
<p>There is a real diversity among the students at UCI and in the Bren School specifically, which I think is one of the keys for its success." </p>
<p>Yosiphon's experience at the Bren School has provided the challenging problems he was looking for, but also has helped fuel a passion for computer science stoked in him while growing up in a small suburb of Tel Aviv, Israel. </p>
<p>"My first computer was a ZX Spectrum, which got me instantly hooked on computers and programming,"  Yosiphon said. "Ten years later when I was contemplating about a future major in college, we got connected to the brand new Internet. This new world was fascinating and I decided to follow a computer science program." </p>
<p>Despite some initial doubts about his choice when entering college, he encountered some remarkable computer science theory and discrete math teachers, who inspired him to pursue a career as a computer scientist. </p>
<p><strong>STRIKING A BALANCE</strong></p>
<p>But life hasn't been all work and no play, Yosiphon lives with his wife Tali in Verano Place, one of the on campus graduate housing communities. </p>
<p>"Although I don't have a fixed schedule as most graduate students," he said. "I try to maintain normal working hours and enjoy my evenings and most of my weekends with my wife."</p>
<p>He also enjoys playing a lot of Racquetball at the Anteater Recreation Center, riding his bike on the numerous bike trails in Irvine and fulfilling a childhood dream to surf. </p>
<p>"Since I was a kid I always wanted to surf and Southern California is the place for it," he said. "I recently started surfing and got addicted not only for the waves but also for the regular encounters with dolphins, yep amazing."</p>
<p>One can also find him hanging out in the University Town Center across from campus enjoying a coffee at Starbucks or catching the latest independent movie at the Edwards theater. </p>
<p>Within a year Yosiphon will have completed his time at the Bren School and hopes to work for a corporate research lab, and eventually achieving his ultimate goal of getting involved in a startup company.</p>
<p>Looking back at his time, Yosiphon has the following advice to offer for those pondering the pursuit of an advanced degree. </p>
<p>Don't overtake courses, especially in the first quarter. It's tempting to do so but graduate courses usually require more work than an undergraduate course (in the form of a final project). </p>
<p>Try to audit some courses and get acquainted of the professors in your preferred field(s) until you choose your specific path and advisor.</p>
<p>Don't discount biology oriented courses in the Bren School. Biological research provides a nice set of problems for computer science and applied math.</p>
<p>And most important of all find time for surf's up!</p>
<p><i>- Eric Kowalik</i></p>


