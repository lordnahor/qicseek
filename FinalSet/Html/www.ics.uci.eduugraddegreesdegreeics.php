<h4>Is Information and Computer Science for me?</h4>
The Information and Computer Science major is intended for highly motivated students who are currently enrolled at UCI, who find that their academic and career interests are not well served by any of the existing ICS majors, and would be better served by a uniquely designed course of study.<br/>&#160; &#160; &#160; &#160; &#160;&#160;
