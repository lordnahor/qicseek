<strong>DEGREES AVAILABLE: Major and Minor</strong> <br/>
<p><img alt="photo:: computer science student" class="left" height="200" src="../../bin/img/photos/degrees/ics_header.jpg" width="160"/>
		      </p>
<p>The overall field of information and computer science spans a vast spectrum of topics.              
		      </p>
The degree in Information and Computer Science is an individually designed degree. Students must submit a proposal for a four-year plan of study along with a rationale for why the proposed plan is a well-motivated set of courses that does not fit into any of the existing ICS majors. Students submitting proposals are strongly encouraged to follow the lower-division requirements for one of the Bren School majors (or provide a rationale for why this is not appropriate) and should include at least 48 units of upper-division ICS, Computer Science, Informatics, or Statistics courses. Proposals must be approved by the ICS Associate Dean for Student Affairs.<br/>
