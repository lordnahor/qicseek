<h4>Bren School Academic Honesty Policy<i>&#160;</i></h4>
<p>The UCI Academic
Senate policies on academic honesty apply within the Bren School of ICS. These
policies are published in the <i>UCI General Catalogue</i> and can be found <a href="http://catalogue.uci.edu/appendix/#academichonestytext">here</a>. While these policies suffice for much of the work that occurs within the
School, including examinations and written assignments, they do not deal
explicitly with course work involving computers. Thus, the policies below have
been created to cover those cases.</p>
<p><b><span style="text-decoration: underline;">Definition of Academic Honesty</span></b></p>
<p>The decision as to
whether a student has cheated depends on the intent of an assignment, the
ground rules specified by the instructor and the behavior of the student. Two
guidelines help an instructor decide if cheating has occurred:</p>
<ol>
<li>Program
plagiarism will be suspected if an assignment that calls for independent
development and implementation of a computer program results in two or more
solutions so similar that one can be converted to another by a mechanical
transformation.</li>
</ol><ol>
</ol>
<p>&#160;&#160;&#160;&#160; 2.&#160; Cheating
will be suspected if a student who was to complete an assignment independently
cannot explain both the intricacies of his or her solution and the techniques
used to generate that solution.</p>
<p>It is unreasonable
to expect a complete definition of cheating; each case is important enough to
be given careful, individual scrutiny. It is, however, helpful to have
guidelines and precedents. Here are some examples of cases which are clearly
cheating and clearly not cheating.</p>
<p><b><span style="text-decoration: underline;">Not Academically Honest</span></b></p>
<ul>
<li>Turning
in someone else&#8217;s work as your own (with or without the other person&#8217;s
knowledge). Turning in a completely duplicated assignment is a flagrant
offense, but even copying only a portion of the assignment and turning it in as
your own is considered cheating.</li>
<li>Allowing
someone else to turn in your work as his or her own.</li>
<li>Several
people writing one program and turning in multiple copies, all represented
(implicitly or explicitly) as individual work.</li>
<li>Using
any part of someone else&#8217;s work without proper acknowledgment.</li>
<li>Stealing
an exam or a solution from the instructor. This is an extremely flagrant
offense.</li>
</ul>
<p>&#160;<b><span style="text-decoration: underline;">Academically Honest</span></b></p>
<ul>
<li>Turning
in work done alone with the help of the course staff.</li>
<li>Submission
of one assignment for a group of students if group work is explicitly permitted
(or required).</li>
<li>Getting
or giving help on how to operate the computer or terminal.</li>
<li>Getting
or giving help on how to eliminate minor syntax errors.</li>
<li>High-level
discussion of course material for better understanding.</li>
<li>Discussion
of assignments to understand what is being asked.</li>
</ul>
<p><b><span style="text-decoration: underline;">Penalties</span></b></p>
<p>The procedures that
are followed and the sanctions that may be imposed for an incident of academic
dishonesty are outlined in the UCI Academic Senate Policies on Academic
Honesty.</p>
<p>Non-Bren School
majors should be aware that an incident of academic dishonesty is sufficient to
cause denial of a petition to change major into the Bren School.</p>
<p>All students should
be aware that a recorded incident of academic dishonesty will disqualify them
for consideration for honors at graduation.</p>
<p>All students should
also be aware that a first incident of academic dishonesty (if egregious) may
be sufficient to cause suspension or dismissal from the University and that a
second incident likely will result in such a penalty.</p>
<p>In the event that an instructor
writes a letter accusing a student of academic dishonesty, the student may
prepare a statement giving his/her side of the case for inclusion in the
student&#8217;s file.</p>
