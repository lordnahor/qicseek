<strong>
<h3>POLICIES &#187;</h3>
</strong>
<h4>Transfer of Coursework Policy </h4>
<p>Previously taken graduate-level courses may count toward Bren School degree 
requirements if they meet the following criteria: </p>
<p>1) Must have been passed with a grade of B or better<br/><br/>2) Must not have 
been applied toward a bachelor&#8217;s degree or M.S. degree from another 
university<br/><br/>3) Cannot have been taken while the student was on a leave of 
absence from the program<br/><br/>4) Must be approved by the Bren School Associate 
Dean for Student Affairs and the Dean of Graduate Division </p>
<p>In addition, no more than one-fifth of the total units required for your M.S. 
or Ph.D. can have been transferred from another institution, from UC Extension, 
or from Summer Session at any other UC campus. </p>
<p><strong>Note:</strong> Students can only transfer <strong>two</strong> 
courses toward their degree requirements </p>
<p>To transfer a course from another University, submit a general petition form. 
</p>
<p>Transcripts from the other University must also be provided when you submit 
your petition to the Bren School Graduate Office. Once approved/denied, you will 
receive notification via email.</p>
<hr/>
<h4>Waiver of Coursework Policy </h4>
<p>If you have taken a graduate course at another University, and believe that 
it is identical to a Bren School course, please contact the Bren School 
professor who usually teaches the course. He/she can determine whether or not 
the course is equivalent. </p>
<p>If it is, he/she can sign your petition form before you submit it to the Bren 
School Graduate Office. </p>
<p>A syllabus of the course, as well as your transcripts from the other 
University, must accompany the petition. </p>
<p>Once approved/denied, you will receive notification via email.</p>
<hr/>
<h4>Substitution of Coursework Policy </h4>
<p>A UCI course can be substituted for another UCI course, with approval. To do 
so, speak to the professor who teaches the course and to your faculty advisor. 
</p>
<p>If they agree with the substitution, have them sign the petition form and 
return it to the Bren School Graduate Office.</p>
<p>Once approved/denied, you will receive notification via email.</p>
<hr/>
<h4>Leave of Absence Policy </h4>
<p>All graduate students can petition for a leave of absence from the program 
for a period of up to, but no more than, three quarters total.</p>
<p>The LOA form is available at the Bren School Graduate Office. Submit the form 
well in advance of the requested leave to make sure it gets approved. </p>
<p>The leave must be approved by both the Bren School Graduate Office and the 
Dean of Graduate Division (the International Center, as well, for all 
international students). </p>
<p>The Bren School Graduate Office will notify you via email once it has been 
approved/denied.</p>
<hr/>
<h4>Grading Policy </h4>
<p><strong>Traditional grades:</strong> <br/>A, B, C, etc&#8230; Students are expected 
to maintain a B (3.0) average (this is an absolute requirement if on a 
fellowship&#8212;GSR, TA, GAANN, etc.). Some fellowships require students to maintain 
a GPA higher than 3.0. </p>
<p>In order to receive graduation credit, students must earn a grade of B or 
better in all required courses. </p>
<p>A grade of B- or below does not have to be improved and can remain on your 
transcript if that grade is not going to be counted toward your graduation 
requirements. </p>
<p><strong>Pass/No Pass:</strong> <br/>P/NP is for undergraduate courses only. 
Graduate students should <strong>not</strong> enroll in a course for P/NP </p>
<p>Graduate students must receive a letter grade in all graduate courses. </p>
<p><strong>Satisfactory/Unsatisfactory:</strong> <br/>S/U is for graduate courses 
only and is considered to be a letter grade. </p>
<p>Faculty can assign an S/U instead of an A, B, C, etc. Students are 
responsible for asking the professor if they can receive the S/U option instead 
of an A, B, C, etc. </p>
<p>Please keep in mind that the S grade is equal to a B or better and the U 
grade is equal to a B- or below. </p>
<p>If you receive a U grade, you will have to repeat the course if it is going 
to count towards your degree requirements. </p>
<p><strong>Incompletes:</strong> <br/>The "I" grade is reserved for occasions 
when a student&#8217;s work is satisfactory but is incomplete because of circumstances 
beyond the student&#8217;s control, and when the student has been excused in advance 
from completing the quarter&#8217;s work. </p>
<p>You may ask the professor to assign an "I", but it may or may not be granted. 
If granted, you have three quarters to make up the Incomplete. If the work is not completed and a grade has not been assigned after three quarters, the "I" will turn to an F.</p>
<p>The UCI Graduate Division will not continue to approve your student 
employment (TA, GSR, etc.) if more than two Incompletes appear on your 
transcript. </p>
<p><strong>No Reports:</strong> <br/>NR means no grade was reported. This 
can be for a variety of reasons: <br/><br/>&#8226; The faculty member did not submit 
grades on time<br/>&#8226; The faculty member does not recognize your name on the class 
roster<br/>&#8226; The faculty member turned in the wrong grade for you<br/>&#8226; The 
course&#8217;s grade roster was unreadable<br/><br/>An NR will turn into an F after one 
quarter. If you receive an NR, talk to your professor immediately. </p>
<hr/>
<h4>Graduate Student Review Policy </h4>
<p>Each spring quarter the Bren School faculty conducts a formal review of all 
graduate students enrolled in the graduate program. The Bren School Graduate 
Office, the Bren School Associate Dean for Student Affairs, and the student&#8217;s 
advisor review each student&#8217;s progress. </p>
<p>If unsatisfactory progress is being made, a letter of probation is sent to 
the student and placed in their file. The probation period varies, depending on 
the individual&#8217;s situation. If the problem is not cleared during the 
probation period, the Bren School Graduate Office may recommend to the UCI 
Graduate Division that the student be dismissed from the program. </p>
<p>It is the student&#8217;s responsibility to make sure the Bren School Graduate 
Office is aware that a problem has been cleared during the probation period. 
</p>
<p><strong>Possible reasons for probation:</strong> <br/>GPA below 3.0<br/>Too 
many &#8220;I&#8221; grades (Incompletes) or not finishing &#8220;I&#8217;s&#8221;<br/>Unsatisfactory 
progress<br/>Not affiliated with an advisor<br/>Not meeting degree milestones </p>
<hr/>
<h4>Advancing to Candidacy for the M.S. Degree Policy </h4>
<p>Both M.S. and Ph.D. students must advance to candidacy for the M.S. degree 
(not applicable to Ph.D. students who already hold an M.S. degree in Computer 
Science). This implies that you are almost finished with the M.S. portion of 
your degree. Students must file the Advancement to M.S. Candidacy form <strong>one 
quarter prior</strong> to M.S degree conferral.</p>
<hr/>
<h4>M.S. Thesis Option Policy </h4>
<p>A thesis option is available to M.S. students who do not plan to take the 
comprehensive exam and who are in good academic standing with the School. 
Students pursuing this option must enroll in at least two quarters of Thesis 
Supervision (CS 298 or INF 298) which will substitute for two required courses 
as specified under the concentration area or specialization of choice (not available to students in the Statistics program). </p>
<p>A committee of three faculty members will guide the student and give final 
approval of the thesis. The committee consists of an advisor (from the student's 
department/program), who is willing to supervise the thesis project, and two 
other faculty members (one of whom must be from the student's 
department/program), who are willing to serve on the committee as readers. An 
oral presentation of the thesis to the committee is required. </p>
<hr/>
<h4>Teaching Requirement Policy </h4>
<p>All Ph.D. students are required to participate in teaching activities for two 
quarters. Summer teaching or teaching at another university may be accepted in 
fulfillment of this requirement. </p>
<p>Students who fulfill this requirement with a teaching assistantship at UCI 
are required to take an orientation course, ICS 398A, enroll in four units of 
ICS 399 each quarter they serve as a TA, and attend the school's TA training 
seminar during Welcome Week of fall quarter.</p>
<hr/>
<h4>Students with a Previously Earned Master&#8217;s Degree </h4>
<p>Bren School Ph.D. students with an M.S. degree in computer science or a related field from another university may qualify to receive credit for some or all 
required courses. Course equivalency will be determined by the Bren School 
Associate Dean for Student Affairs following a written recommendation from the 
student's advisor. Final approval will come from the UCI Graduate Division.</p>
<p><strong>Ph.D. students waived from the M.S. portion of their degree may not, 
in future, request that they be granted an M.S. degree in Computer Science or a 
closely related field from UC Irvine. </strong></p>
<p>Be aware that advisors can require that students take additional courses when 
appropriate. </p>
<hr/>
<h4>Written Comprehensive Exam/Phase II Exam </h4>
<p>Each student must pass a written comprehensive examination offered by the 
student&#8217;s department. </p>
<p>See specific degree requirements for more information as this exam may vary 
by degree/concentration. </p>
<p>Students should speak to their advisor, research area representative or the ICS graduate counselors 
regarding test dates, preparation, and reading lists. Faculty from the 
particular research area develop the exam, so the content and structure may vary 
from year to year. </p>
<p>The exam is graded on three levels: M.S. Pass, Ph.D. Pass, and Fail.</p>
<p>Ph.D. students who receive an M.S. Pass can qualify for the M.S. degree, but 
must re-take the exam and pass at the Ph.D. level to continue in the Ph.D. 
program. </p>
<p>Students may re-take the exam once. </p>
<p>If a student would like to take the exam for a third time, s/he must petition 
the Bren School Associate Dean for Student Affairs and the Dean of Graduate 
Division for permission. </p>
<p>If the petition is denied, the student may be dismissed from the program. 
</p>
<p>Once the exam is passed at the desired level, please make sure the 
appropriate paperwork is filed with the Bren School graduate counselor. </p>
<hr/>
<h4>Advancing to Candidacy for the Ph.D. degree </h4>
<p>Prior to advance to candidacy for the Ph.D. degree, students must complete 
the following steps: </p>
<p>1) Complete coursework<br/>2) Pass the Phase 
II exam at the Ph.D. level (if applicable to the student's program) <br/>3) 
Complete a research or survey paper of publishable quality (if applicable to the student's program)</p>
<p>The advancement exam is an oral presentation to a five-member committee of 
senate faculty. Each committee member must agree on the research you would like 
to complete for your doctoral dissertation. </p>
<p>Committee membership policies may vary by department/program. Consult the ICS 
graduate counselors for more information. </p>
<p><strong>Preparing for the Advancement Exam:</strong> <br/><br/>&#8226; Arrange a date 
when all involved parties can meet<br/><br/>&#8226; Reserve a conference room with your 
department <br/><br/>&#8226; Print out or pick up a Ph.D. Form I from Graduate Division <a href="http://www.grad.uci.edu/forms/index.html">here</a><br/><br/><strong>After the Advancement Exam is 
complete:</strong> <br/><br/>&#8226; Get appropriate signatures on the Ph.D. Form I 
(including your own!) <br/>- The ICS Graduate Office can provide the Department 
Chair and Associate Dean signatures. <br/><br/>&#8226; Pay $90 advancement fee at the 
Cashier's office <br/><br/>&#8226; Turn form into Graduate Division (120 Aldrich Hall) 
</p>
<hr/>
<h4>Candidacy Committee Duties and Responsibilities </h4>
<p>The candidacy committee is charged with determining the fitness of the 
student to proceed with the doctoral dissertation through a formal candidacy examination. </p>
<p>The examination should evaluate both general preparedness in the discipline, 
and specific competence to pursue the proposed dissertation topic. </p>
<p>In its deliberation, the committee ordinarily will review the student's 
academic record, preliminary examinations, evaluations by other faculty, and may 
conduct any other examination it thinks appropriate. </p>
<p>The candidacy committee will also review an outline of the proposed 
dissertation project, and will determine by oral examination the student's 
competence in that area. 
</p>
<p>When, by unanimous vote, the committee decides the student is qualified for 
the dissertation phase, it should recommend advancement to candidacy to the 
Graduate council via the Graduate Dean. 
</p>
<p>Following its formal appointment, the committee is free to adopt whatever 
procedures it thinks appropriate to conduct the qualifying examination for 
candidacy, subject to the rules of the program and those specified below. 
</p>
<p>Administration of the qualifying examination must conform to the policies 
established by the Graduate Council. 
</p>
<p>The student must be given adequate notice of the content, form and time of 
the examination. The committee must meet to decide upon the procedures to be 
followed, and the student must be given an opportunity to comment upon the 
selected procedures. 
</p>
<p>Before voting upon its recommendation for or against candidacy, the committee 
as a whole shall meet with the student, and any member of the committee will 
have the right to pose appropriate questions to the student. 
</p>
<p>If it decides to do so, the committee may conduct part of the examination on 
an individual basis, e.g. the student may meet with each in turn. However, the committee must conclude its examination when convened with the student present. 
</p>
<p>Although the formal candidacy examination ordinarily is conducted in a single 
day, the committee may meet intermittently over a longer period, and may decide 
to reexamine the student on one or more topics after a specified interval. 
</p>
<p>When the committee meets to conduct the oral candidacy examination, it must 
report to the Graduate Council via the Graduate Dean within 30 days. 
</p>
<p>If the committee decides to reexamine the student at a later date or does not 
pass the student for any reason, this must be reported. The final vote and 
recommendation of the committee must be unanimous and unequivocal. 
</p>
<p>Upon completion of the candidacy examination, the results should be submitted 
to the UCI Graduate Division on the Report of the Ph.D. Candidacy Committee, 
Ph.D. Form I. 
</p>
<p>Ph.D. Form I must be signed by all committee members at the time the 
candidacy examination is concluded, and submitted even if the examination was 
failed. 
</p>
<p>If the unanimous recommendation of the committee is favorable, the $90 advancement to candidacy fee must be paid by the student to the campus Cashier's 
Office. This will validate the Ph.D. Form I, after which it should be submitted 
to the UCI Graduate Division. The signed and validated Ph.D. Form I serves as 
the application for advancement to candidacy. 
</p>
<p>The candidate and graduate counselor will be notified of formal advancement 
and the appointment of a doctoral committee. </p>
<hr/>
<h4>Topic Defense Policy </h4>
<p>Once you determine your dissertation topic, you must present it in a defense 
to your doctoral committee. Each area differs as to when students present this 
defense, so please confirm with your advisor as to when you should be completing 
this requirement. 
</p>
<p>At the time of your defense, you must bring a &#8220;Topic Defense &#8221; form for your 
committee to sign after they have approved your defense. These forms are 
available from the Bren School Graduate Office. The signed form is then returned 
to the Bren School Graduate Office. </p>
<hr/>
<h4>Final Defense Policy </h4>
<p>For the final defense of your dissertation, e-mail the Bren School Graduate 
Office with the date, time and location of the defense, the names of your committee 
members, and the title and abstract of your dissertation. This information will 
be made available to all Bren School faculty and graduate students. In 
addition to your committee members, anyone else who wishes to attend your 
defense may do so. 
</p>
<p>Also, be sure to obtain a Ph.D. Form II: Report on Final Examination for the 
Ph.D. Degree, from Graduate Division <a href="http://www.grad.uci.edu/forms/index.html">here</a>. At the end of the 
defense, have each of your committee members sign it. 
</p>
<p>When you have finished your dissertation, submit it and the Ph.D. Form II to 
the UCI Library Archives (Langson Library, Room 525). Alternatively, the dissertation may be submitted electronically. Please see the Thesis Submission checklist <a href="http://www.grad.uci.edu/forms/index.html">here</a>.</p>
<p>The Library will notify the UCI Graduate Division that your dissertation has 
been turned in and your degree is ready for conferral. Graduate Division will 
notify you once your degree has been conferred. </p>
<hr/>
<h4>Residency Requirement Policy </h4>
<p>A minimum of six quarters (for PhD students) or three quarters (for MS 
students) in academic residence is required prior to the award of the degree. 
Typically, a longer period of study (two to six years) is required for 
completion of all degree requirements. </p>
<p>After a Ph.D. student has been registered for over 21 quarters (seven years), 
and a MS student has been registered for over six quarters (two years), they are 
considered to be beyond normative time for completing the degree. </p>
<p>A letter regarding this status is sent to the student and to the department 
requesting a deadline be implemented for the student to complete all 
requirements and submit their dissertation. If this deadline is missed the 
student is subject to academic disqualification.</p>
