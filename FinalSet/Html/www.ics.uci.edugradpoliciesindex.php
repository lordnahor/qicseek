<h4>Bren School Copyright Infringement Policy </h4>
<p>It is illegal to distribute copyrighted materials -- such as software, 
movies, music, pictures -- without proper authorization. 
</p>
<p>If a complaint of copyright infringement is reported involving a computer 
assigned to a Bren School student, the student's account will be locked out 
until Bren School support personnel have a chance to investigate the 
allegations. If copyrighted materials are found, they will be removed 
immediately. 
</p>
<p>For the initial complaint (first violation), the Bren School Student Affairs 
Office (SAO) staff and the appropriate Associate Dean (as well as the academic 
advisor, for grad students) will be notified. 
</p>
<p>The student will be required to meet in person with appropriate SAO staff to 
review applicable campus policies, and to sign a statement verifying receipt and 
notice of such policies. This signed statement will be placed in the student's 
file. 
</p>
<p>For any subsequent complaints, the appropriate Associate Dean, academic 
advisor (for grad students) and SAO staff will be notified. 
</p>
<p>The student will then be referred to the campus Director of Student Judicial 
Affairs for academic suspension (upon second violation) or expulsion from the 
university(upon third violation).</p>
