<div id="content_text">

<p><strong>Man of the House</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: michael golightly" height="166" src="../../../bin/img/photos/people/michael_golightly.jpg" width="171"/>
<p>
Michael Golightly
</p>
</div>
College can be a big and intimidating place for new students; it can be difficult to find what resources are available and where you can turn to get help.
<p>Luckily there are students like Michael Golightly, a junior ICS major who is also the housing assistant for the ICS Theme House, one of 17 themed houses in the Arroyo Vista campus community. The theme houses have residents with similar interests live together and provide specialized programming to explore those specific interests.</p>
<p>Since the 24 residents (16 males and 8 females) all share the common bond of being ICS related majors, they are able to expand their peer group and meet many new people in their major, a unique experience students don&#8217;t get in the dorms or an off campus apartment. </p>
<p>&#8220;Here at the house you live with a diverse group of students that share the same field of study, computer science,&#8221; Golightly said. &#8220;We have transfer students from all over the world including Hong Kong and Japan as well as transfers from Northern California and other local community colleges.</p>
<p>When it comes to study for midterms or work on your next ICS project, it&#8217;s great to know that someone in the house is only a few steps away to help you out or point you in the right direction to someone who can.&#8221;</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>In addition to providing mentorship, Golightly organizes events that provide community development and enrichment for the residents. In addition to events that several theme houses cosponsor, such as a dance workshop, Golightly invites faculty members and ICS clubs such as Women in Computer Science (WICS), Association for Computing Machinery (ACM), Informatics Student Association (INSA) and the Undergraduate Computing Facility (UCF) to visit the house and mix with the residents. </p>
<p> &#8220;Living in the house makes it easier to know about faculties&#8217; research, ICS clubs, ICS school events, internship opportunities, research opportunities and what&#8217;s going to be on the next big midterm,&#8221; Golightly said. </p>
<p><strong>A SCARY PROPOSITION </strong></p>
<p>Although Golightly relishes his job as the ICS Theme House housing assistant, he was initially scared of the prospect of living with a bunch of geeky ICS majors. It wasn&#8217;t until the summer between his freshmen and sophomore year when he was a resident teaching assistant for the California State Summer School for Mathematics and Science (COSMOS) program at UCI that he decided he could handle it.</p>
<p>&#8220;I liked the position and figured if I could survive high school students, then college students couldn&#8217;t be much worse.&#8221; Golightly said. &#8220;Really, it was a lot of fun and I figured if I could get a free single room out of it, help other students, and have fun then why not? I wanted to bring students together with faculty, programs, and other resources I knew and at the same time strengthen my own network of resources.&#8221; </p>
<p>His experience as housing assistant has shown Golightly that the residents are far from geeky and know how to kick back and have fun with weekend basketball games, late night video game playing or an occasional TV-series marathon of <i>Nip Tuck</i> or <i>24</i>.</p>
<p>Students interested in living in the ICS Theme House should visit the <a href="http://www.housing.uci.edu/av/theme.asp">UCI Student Housing web site</a>. The online application requires prospective residents to write three essays expressing why they want to live in the theme house and why they would be a good addition to the house. </p>
<p><strong>REACHING OUT </strong></p>
<p>Golightly hasn&#8217;t limited his mentoring efforts to ICS students, as co-chair of the Mexican American Engineers and Scientists (MAES) student group, Golightly works to promote excellence in engineering and the sciences while cultivating cultural diversity. </p>
<p>&#8220;We strive to see more minority students graduating from college and being successful in whatever path they may take afterward,&#8221; Golightly said. &#8220;We develop leaders of both their community and their profession.&#8221; </p>
<p>One of MAES&#8217; biggest efforts is Science Extravaganza which is an outreach event to approximately 300 high school students across southern California. The students are bused to UCI for a Saturday of fun and educational workshops that will help raise their interest in attending college. It is also an ongoing program that involves high school students doing research and college level work.</p>
<p>&#8220;There are thousands of dollars, dozens of people, and schedules to manage,&#8221; Golightly said. &#8220;If this event will change the decision of one high school student to attend college, then that will be my greatest achievement as Co-Chair.&#8221; </p>
<p>Science Extravaganza is MAES&#8217; biggest program, but they also facilitate a plethora of other initiatives. The group recently helped out with Anaheim High School&#8217;s robotics competition which is funded in part by MAES national chapter. The group has also helped with Engineering Week&#8217;s Shadow Day which involves 75 MESA (Mathematics Engineering Science Achievement) high school students from Anaheim/Santa Ana. MAES also co-sponsor the Great Race competition with SHPE, also held during Engineering Week. </p>
<p>Throughout the quarter, the group also holds various workshops such as a resume workshop with Paul Solce from Unisys, a graduate school panel made of various UCI graduate students and a healthy eating workshop during finals week. </p>
<p>Despite all his extracurricular involvement, Golightly still finds time to attend class and do research. Last summer, through the Leadership Alliance, he had the opportunity to do research at Princeton University on Scalable Large File Distribution. </p>
<p>&#8220;I got to play with a system of computers all across the United States and in the end our research was published and it was great to know I had a part in advancing the current state of technology,&#8221; Golightly said. &#8220;I plan to continue on with research in the same field, or wherever my interests might take me.&#8221; </p>
<p><strong>FUELING A PASSION </strong></p>
<p>His work at Princeton continued to fuel a passion for computer science that began during high school at Don Bosco Technical Institute in Rosemead.</p>
<p>&#8220;I was able to take a rotation of mechanical, electrical, construction and computer science technologies my freshman year there and in the end computer science appealed to me the most,&#8221; Golightly said. &#8220;Programming has become like learning a second language, where 1s and 0s turned into whatever I wanted them to be and I loved it from making my first Missile Command game to my latest research.&#8221; </p>
<p>When looking at colleges, the Bren School&#8217;s reputation as a great program with great professors, the UC system&#8217;s focus on research and the beautiful and lively campus were the factors Golightly attributed to vaulting UCI over Cal Poly Pomona and UCSD. </p>
<p>&#8220;The campus is beautiful, the beach is right next door, the weather is awesome, and it is in one of the safest cities in America, what else could you ask for,&#8221; Golightly said. &#8220;Academically, the Bren School is growing extremely fast in prestige and this brings some of the best professors here to do their research and teach.&#8221; </p>
<p>When he isn&#8217;t mentoring a resident, planning a MAES event or attending class, Golightly relaxes by playing classical guitar, playing video games with his residents and occasionally going to a few clubs with his girlfriend, a biology major. </p>
<p>But Golightly knows that there will be time for sleep and fun after graduation and offers the following advice to future students.</p>
<p>&#8220;Take advantage of every resource on campus and connection you know. You&#8217;re only in college for four years, but you blink and they will be gone,&#8221; Golightly said. &#8220;Now is the time to build up that impressive resume and start building your networking connections that will set you apart from the other thousands of students in college, because when it comes time to apply for that job, or graduate school, you want every edge you can get.&#8221; </p>
<p><i>- Eric Kowalik</i></p>


