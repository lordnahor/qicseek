<div id="content_text">

<p><strong>Experiential Expert </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: ramesh jain" height="166" src="../../../bin/img/photos/people/ramesh_jain.jpg" width="126"/>
<p>
Ramesh Jain
</p>
</div>
Ramesh Jain, former professor of embedded and experiential systems at Georgia Institute of Technology, was recently named the first Donald Bren Professor of Information and Computer Sciences at UC Irvine.
<p>Jain is a renowned pioneer in multimedia information systems, image databases, machine vision and intelligent systems, a collection of work that is classed as experiential computing &#8211; exploring information through the senses.</p>
<p>&#8220;We are extremely pleased to have Dr. Jain join ICS,&#8221; said Debra J. Richardson, dean of the Donald Bren School of Information and Computer Sciences. &#8220;He is an exceptionally talented scientist who brings to the position an outstanding reputation, a strong commitment to teaching and a passion for impacting industry as well as creating new research opportunities.&#8221;</p>
<p>Jain has been named a fellow by the Association of Computing Machinery (ACM), the Institute for Electrical and Electronics Engineers (IEEE), the American Association for Artifi cial Intelligence (AAAI), the Society for Photo-Optical Instrumentation Engineers (SPIE) and the International Association for Pattern Recognition (IAPR). In 1999, he was a fi nalist for the prestigious Ernst and Young Entrepreneur of the Year award.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>Active in industry, Jain has founded three companies, managed them in initial stages and then turned them over to professional management. Most recently, he was the co-founder, CEO and CTO of PRAJA Inc. located in San Diego. Prior to PRAJA, he was the founding CEO and chairman of Virage, a San Mateo-based company developing systems for media management solutions and visual information management. </p>
<p>He also was the founder and chairman of ImageWare Inc. that provided solutions for surface modeling, reverse engineering rapid prototyping and inspection. He serves as adviser to several companies.</p>
<p>&#8220;I am very pleased to be joining ICS at such an exciting time in the school&#8217;s history,&#8221; said Jain. &#8220;Technology is in transition &#8211; in the process of transforming an information society to an experiential society, which will require systems and applications that allow us to immerse ourselves in rich sets of multimedia data and information that we can directly observe, and I am eager to continue my research along side some of technology&#8217;s greatest contributors.&#8221;</p>
<p>Jain received his bachelor&#8217;s degree in electrical engineering from Nagpur University in 1969 and earned his doctorate in electronics and communications engineering from the Indian Institute of Technology in 1975. </p>
<p>He comes to UCI from the Georgia Institute of Technology where he held the Rhesa &#8220;Ray&#8221; S. Farmer, Jr. Distinguished Chair in Embedded Experiential Systems and was named a GRA Eminent Scholar. At Georgia Tech, Jain founded the Experiential Systems Group, his fourth signifi cant research laboratory. Other academic posts include University of Hamburg (1976-1978), Wayne State University (1979-1982), University of Michigan (1982-1993) and the University of California, San Diego (1993-1999).</p>
<p>The Donald Bren Foundation created and continues to fund the Donald Bren Endowment to help UCI attract and retain the nation&#8217;s foremost scholars. In addition to the Donald Bren Professors, the endowment provides support for Claire Trevor Professors at the Claire Trevor School of the Arts.</p>
<p>To learn more about Ramesh and his research <a href="http://ngs.ics.uci.edu/blog/">check out his blog.</a></p>


