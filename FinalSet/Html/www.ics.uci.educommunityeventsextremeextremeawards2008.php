<div id="content_title">hITEC Awards</div>

<blockquote>
<div id="content_text">

The Donald Bren School of Information and Computer Sciences would like to express our its heartfelt appreciation to Ted Smith and Ramesh Jain for their extraordinary leadership of the 2008  <strong>hITEC OCTANe </strong> Student Competition.
<p>In addition to thanking each of the student teams for their commitment and hard work, the Bren School of ICS would like to thank each of its valuable volunteers: presenters, judges and mentors for their time, commitment and support. </p>
<p><strong>2008 hITEC OCTANe  Winners</strong></p>
<div class="img_caption_left">
<img alt="photo:: 1st place hitec team" height="143" src="../../../bin/img/photos/hitec/2008hITECFirstPlace.jpg" width="215"/>
<p class="left">
<span class="small_font">Bren School Dean Debra J. Richardson<br/> 
(left) poses with the Adball.com team<br/>
as they accept the award for their 1st <br/>
place finish in the 
hITEC OCTANe <br/>
competition.</span>
          </p>
</div>
<p><strong>1st Place ($5,000 sponsored by Paul Mockapetris)</strong> </p>
<p><strong>Product:</strong> <a href="http://www.adball.com">Adball.com</a></p>
<p><strong>Product Description: </strong>Adball is an online collaborative platform that creates content for advertising campaigns. </p>
<p>It leverages the creativity of large and diverse online community of consumers themselves. Adball empowers users to facilitate the collaborative creation of multi-media based advertising communications. </p>
<p>From print advertisements, web banner ads, radio jingles, to videos for both television and the web, Adball gives corporate clients access to the creative power of their most powerful resource, their own consumers. </p>
<p>It has been said that successful marketers speak to consumers in the ways they want to be spoken to; Adball says why not let the consumers do the talking?</p>
<p>This team also won 3rd place at the <a href="http://www.merage.uci.edu/ResearchAndCenters/Beall/SYCR.aspx">Merage School Business Plan Competition</a> and the OC Register "Reader&#8217;s Poll".</p>
<p><strong>Team members: </strong>Karim El Defrawy, Minas Gjoka, Michael Sirivianos from the Bren School of ICS; and Karl Ring and Nayana Wagle, from the Merage School of Business.</p>
<p><strong>Mentor:</strong> Arie Shen</p>
<hr/>
<p><strong>2nd Place ($2,500 sponsored by MIND Research Institute)</strong></p>
<p><strong>Product Name:</strong>Click2School</p>
<p><strong>Product Description:</strong> Click2School aims to bridge the gap between the school education and the Internet and provide a platform to use the Internet as an effective educational tool. It aims to provide a rich and user-friendly environment for learning, problem solving, information sharing, and social networking for school students. It provides content filtering based on school grades (4th, 5th, etc.) and students' interests to cater to the needs of a specific age group.</p>
<p><strong>Team Members: </strong>Deepika Gandhi, Ajeet Kumar, Ajay Mishra</p>
<p><strong>Mentor: </strong>Janell So</p>
<hr/>
<p><strong>3rd Place</strong> <strong>($1,500 sponsored by Northwind Ventures)</strong> </p>
<p><strong>Product Name: </strong>VideoOnTheWall</p>
<p><strong>Product Description:</strong> VideoOnTheWall gives everyday people the ability to become "news reporters" on the Internet. </p>
<p>Users capture footage via the camera functionality on a cell phone and transmit to our servers. </p>
<p>These streaming news reports can be viewed by anyone on the Internet live. The experience is interactive. A trusted network of viewers can send real-time messages to the reporters for instant feedback. </p>
<p>The system ushers social web interaction to the next level. Reporters can have their own personal page with all their previous video casts, which can spur a following for the most popular reporters, in the same way in which people follow popular bloggers.</p>
<p><strong>Team Members:</strong>Tommy Chheng, Ala Khalifeh, Avi Mehta, Arjun Satish, Ajey Shah</p>
<p><strong>Mentors:</strong> Sherman Chen</p>


