<p>            Don't see your question here? Please e-mail it to us at <a href="mailto:gcounsel@ics.uci.edu">gcounsel@ics.uci.edu</a>.</p>
<h4>For Prospective Graduate Students</h4>
<ul>
<li>
<h3 class="trigger"> How long does it take for a decision to be reached on my application?</h3>
<div>
<p>Decisions are ongoing, therefore, you may hear as early as March or as late as May/June. <br/>
			  <br/>
			  You will be notified of the final decision via email and the online Tracker. We cannot provide you with the final decision over the phone.</p>
</div>
</li>
<li>
<h3 class="trigger"> When will I be notified of my funding offer?</h3>
<div>
<p>If funding has been awarded, notification will be included with your admissions offer.</p>
</div>
</li>
<li>
<h3 class="trigger"> How will I know if my file is missing any documents or whether my file is complete?</h3>
<div>
<p>Applicants may login to the online Tracker to check for application materials received.  </p>
</div>
</li>
<li>
<h3 class="trigger"> Do I need to take the GRE General Exam?</h3>
<div>
<p>Yes, the general GRE exam is required of all applicants. GRE scores are good for five years, after which time the exam must be repeated. We cannot accept GMA scores in lieu of GRE scores.<br/>
			  <br/>
			  Only the General exam is required; we do not require the subject GRE exam, although it is recommended for applicants without a background in Computer Science or a related area.  </p>
</div>
</li>
<li>
<h3 class="trigger"> If my undergraduate degree is not in the same area as the graduate degree in which I am interested, can I still apply?</h3>
<div>
<p>Yes, but it is helpful if you have taken courses in computer science and math, and/or have some related work experience.</p>
</div>
</li>
<li>
<h3 class="trigger"> Do I have to submit online letters of recommendation?</h3>
<div>
<p>You may elect to submit online or hard copy letters of recommendation.</p>
</div>
</li>
<li>
<h3 class="trigger"> May I submit more than three letters of recommendation?</h3>
<div>
<p>You may submit as many recommendation letters as you wish.</p>
</div>
</li>
<li>
<h3 class="trigger"> Can I attend part time or take evening classes?</h3>
<div>
<p>M.S. students may petition to attend part time, but Ph.D. students are required to attend full time. <br/>
			  <br/>
			  The Bren School does not offer evening or weekend classes at this time; all classes are offered during the day.</p>
</div>
</li>
<li>
<h3 class="trigger"> Can I be waived out of the TOEFL requirement?</h3>
<div>
<p>UCI will only waive the TOEFL requirement for applicants who have completed <strong>ALL</strong> requirements for a B.S., M.S. or Ph.D. degree in the U.S. prior to submitting their application. </p>
</div>
</li>
<li>
<h3 class="trigger"> Can the application fee be waived or deferred?</h3>
<div>
<p>No. In order for your application to be processed, the application fee must be received.</p>
</div>
</li>
<li>
<h3 class="trigger"> Is on-campus housing guaranteed?</h3>
<div>
<p>No, on-campus housing is not guaranteed. <br/>
			  <br/>Admitted students may apply for housing after they submit their Statement of Intent to Register (SIR).</p>
</div>
</li>
<li>
<h3 class="trigger"> Can I send photocopies of my transcripts, GRE scores and TOEFL scores?</h3>
<div>
<p>Only official transcripts are acceptable in order to complete your application. Use institutional code 4859 to have official GRE and TOEFL scores sent from the testing agency to UCI.</p>
</div>
</li>
<li>
<h3 class="trigger"> Do I need to send bank certification or financial verification at the time of application?</h3>
<div>
<p>No. Once admitted, all international students who accept the admission offer are required to submit a financial questionnaire and financial verification documents.</p>
</div>
</li>
<li>
<h3 class="trigger"> If I am re-applying to the program, do I need to re-submit all my documents?</h3>
<div>
<p>You will need to submit an online application and pay the application fee in order to re-apply to the program. <br/>
			  <br/>
If you are re-applying within a one-year period, you may re-use the original documents; however, we encourage you to update your file with a new statement of purpose and letters of recommendation.<br/>
<br/>
GRE and TOEFL scores must be current at the time of re-application. <br/>
			  Please send an email to gcounsel@ics.uci.edu letting us know which documents you would like to re-use.<br/>
			  <br/>
			  If you are re-applying after one year, you will need to re-submit all new documents. </p>
</div>
</li>
<li>
<h3 class="trigger"> Do you admit for the Winter or Spring quarter?</h3>
<div>
<p>No. The Bren School only admits for the Fall quarter.</p>
</div>
</li>
<li>
<h3 class="trigger"> Who do I contact if I&#8217;m having trouble with the online application? </h3>
<div>
<p>Send an email to UCI Graduate Division: <a href="mailto:ogs@uci.edu">ogs@uci.edu</a>. Please note that you may not make changes to your application after it has been submitted.</p>
</div>
</li>
</ul>
<h4>For Current Graduate Students</h4>
<ul>
<li>
<h3 class="trigger"> How do I advance to candidacy for the Master's degree?</h3>
<div>
<p>Complete and submit the MS Advancement to Candidacy form one quarter prior to when you expect your M.S. degree to be conferred. To access the form <a href="http://www.grad.uci.edu/forms/index.html">click here</a>.<a href="http://www.grad.uci.edu/forms/"><br/></a>
			  <br/>
			  Please check with the ICS Graduate Counselors for quarterly deadlines.</p>
</div>
</li>
<li>
<h3 class="trigger"> How often can I go on a Leave of Absence?</h3>
<div>
<p>Up to three quarters total.</p>
</div>
</li>
<li>
<h3 class="trigger"> When is the Leave of Absence deadline?</h3>
<div>
<p>The campus deadline is Friday of the third week of classes. However, ICS recommends that students submit the Leave of Absence petition by the registration deadline each quarter. To access the leave of absence form <a href="http://www.grad.uci.edu/forms/index.html">click here</a>.</p>
</div>
</li>
<li>
<h3 class="trigger"> When is the deadline to apply for Part-Time status?</h3>
<div>
<p>The campus deadline is noon on Wednesday of the third week of classes. However, ICS recommends that students submit the Part-Time petition by the registration deadline each quarter.</p>
</div>
</li>
<li>
<h3 class="trigger"> Can a Ph.D. student apply for Part-Time status?</h3>
<div>
<p>No, only Master&#8217;s students are eligible to apply for part-time status. To access the part-time petition, titled Reduced Fee Part-Time Study Program, <a href="http://www.grad.uci.edu/forms/index.html">click here</a>.</p>
</div>
</li>
<li>
<h3 class="trigger"> What is the composition of the Ph.D. advancement committee?</h3>
<div>
<p>The Ph.D. advancement committee is a five member committee of senate faculty. The Ph.D. advancement committee must be composed of the Chair, three general members and one outside member. The Chair of the candidacy committee must hold either a primary or joint appointment in the student's department. At least two members in addition to the Chair must hold either a primary or joint appointment in the student's department. The majority of the committee must be from the student's department. The outside member must be a UC Irvine faculty member and may not be affiliated with the student's department.  Consult with the ICS graduate counselors for more information.
			</p>
</div>
</li>
<li>
<h3 class="trigger"> What is the composition of the Ph.D. final defense committee?</h3>
<div>
<p>The Ph.D. final defense committee is a three member committee of senate faculty drawn from your advancement committee.  The majority of the committee MUST be from your department. Consult with the ICS graduate counselors if changes to the composition of your defense committee occur.</p>
</div>
</li>
<li>
<h3 class="trigger"> Who exactly are "senate faculty?"</h3>
<div>
<p>Senate faculty have the following titles: Assistant Professor, Associate Professor, Professor, and Lecturer/Senior Lecturer with Security of Employment (SOE).</p>
</div>
</li>
<li>
<h3 class="trigger"> Can I combine my Ph.D. advancement and my topic defense?</h3>
<div>
<p>Yes, with the consent of your advisor. To access the topic defense form <a href="#">click here</a>. </p>
</div>
</li>
<li>
<h3 class="trigger"> Do I have to complete my courses before I can advance to candidacy for the PhD?</h3>
<div>
<p>Yes, you must complete all required course work before you can advance to candidacy for the PhD.</p>
</div>
</li>
<li>
<h3 class="trigger"> Do research units count toward course requirements?</h3>
<div>
<p>No.  Course numbers 290, 298 and 299 do not count toward course requirements.</p>
</div>
</li>
<li>
<h3 class="trigger"> My degree requires a "publication-quality" research paper.  Does that mean it has to be published before I can submit it to my advisor/committee?</h3>
<div>
<p>No, the paper does not have to be published.  Your advisor/committee will assess the quality of your paper.</p>
</div>
</li>
</ul>
<h4>For Current &amp; Prospective TAs/Readers</h4>
<ul>
<li>
<h3 class="trigger"> Who do I see regarding TA/Reader assignments?</h3>
<div>
<p>The Department Managers.</p>
</div>
</li>
<li>
<h3 class="trigger"> Do I need to submit a TA/Reader application for each quarter?</h3>
<div>
<p>Yes.&#160; You'll need to submit a TA/Reader application each quarter you wish to serve as a TA/Reader.            		</p>
</div>
</li>
<li>
<h3 class="trigger"> When will I be informed of the TA/Reader appointments?</h3>
<div>
<p>You will be notified via email as soon as you are assigned.</p>
</div>
</li>
<li>
<h3 class="trigger"> Do I need to fill out a TA/Reader application if I'm on a TA/Reader fellowship?</h3>
<div>
<p>Yes.</p>
</div>
</li>
<li>
<h3 class="trigger"> If I've been assigned as a TA/Reader, do I get academic credit?</h3>
<div>
<p>Yes, each quarter you are assigned as a TA/Reader, you will receive four units of academic credit by signing up for ICS 399.  <strong>Note: ICS 399 does not count toward degree requirements.</strong></p>
</div>
</li>
</ul>


</div>
