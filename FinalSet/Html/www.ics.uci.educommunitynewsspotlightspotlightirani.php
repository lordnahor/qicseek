<blockquote>
<div id="content_text">

<p><strong>Understanding Influence </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: lilly irani" height="159" src="../../../bin/img/photos/people/lilly_irani.jpg" width="210"/>
<p>Lilly Irani 
</p>
</div>
<p>While an undergraduate at Stanford University, Lilly Irani did an undergraduate thesis that examined why women left the computer science core at Stanford. The experience was challenging but very rewarding and stuck with her after graduation, while she spent 4 years as a user experience designer at Google.</p>
<p>Eventually her desire for research instead of product development led her to pursue a Ph.D.</p>
<p>&#8220;I wanted a chance to try to get deep into a topic, rather than launching products quickly without getting to deeply consider what it meant,&#8221; Irani said. &#8220;It seemed like the researchers who had a Ph.D. had the experience and opportunity to do that sort of focused, long term work.&#8221;</p>
<p>The Bren School&#8217;s reputation as a top place for Human Computer Interaction research and the interdisciplinary nature of the program drew Irani to UC Irvine. </p>
<p>&#8220;I saw an opportunity to pursue my own research agendas here in concert with professors, rather than just working on pieces of professor's existing projects,&#8221; Irani said. &#8220;UC Irvine also has exciting humanities and anthropology departments that enable me to pursue a much deeper grounding in the sorts of social analysis we do within the Informatics department.&#8221;</p>
<p>Irani draws support not only from Informatics faculty, but also Women&#8217;s Studies faculty by participating in UCI&#8217;s  Graduate Feminist Emphasis.</p>
<p><strong>EXAMINING THE INFLUENCE ON TECHNOLOGIES</strong></p>
<p>Irani&#8217;s research focuses on studying how the experiences of gender and cultural background, such as national background, influence the way technologies get designed.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;I've always been fascinated by how interactive and communication technologies like the Internet have become huge cultural forces, and I've wanted to understand and intervene in those technological changes to make sure diverse perspectives and interests are represented,&#8221; Irani said.</p>
<p>Irani works in the Bren School&#8217;s <a href="http://luci.ics.uci.edu/">Laboratory for Ubiquitous Computing and Interaction (LUCI)</a> that address a range of research problems that arise from the ubiquitous computing vision.</p>
<p>&#8220;I would recommend the Informatics program to other Ph.D. students who are interested in learning from the many top notch professors in the department in an environment that offers low pressure with high expectations, matched with mentorship and a real chance to pursue your own research interests,&#8221; Irani said. &#8220;The department also offers a unique opportunity to develop a deep understanding of social analysis while still being able to build, design, and explore those analytics through making.&#8221;</p>
<p>Her research has lead to one of her papers being accepted to the Computer Supported Cooperative Work Conference 2008 (CSCW) and she also presented a paper at the Cultures of Virtual Worlds conference that was held at UC Irvine in 2008.</p>
<p>Irani was also selected for the NSF Graduate Research Fellowship Program (GRFP), which provides students with three years of funding &#8211; up to $121,500 &#8211; for research-focused Master&#8217;s and Ph.D. degrees in Science, Technology, Engineering and Mathematics fields.</p>
<p>NSF Fellows are expected to become knowledge experts who can contribute significantly to research, teaching, and innovations in science and engineering.</p>
<p>The work can be rewarding and memorable, but it can also be stressful and nerve wracking.</p>
<p>&#8220;I remember staying up late into the night for a week writing a CSCW paper in the LUCI lab with a fellow student,&#8221; Irani said. &#8220;We rallied together to get the papers done, fueled by snack runs to the Albertson's across the street from campus.&#8221;</p>
<p><strong>INTERDISCIPLINARY INSIGHT</strong></p>
<p>Irani, Seattle born and raised in Rancho Palos Verdes, California, lives with two fellow graduate students in Palo Verde, one of the graduate student communities on the UC Irvine campus.</p>
<p>&#8220;It is an affordable and spacious apartment with a beautiful view and a community garden,&#8221; Irani said.</p>
<p>Living on campus allows her more opportunity to mingle with other graduate students from different disciplines, which can often add a new view to the projects she is a working on. </p>
<p>&#8220;I love the graduate student reading room at Langson Library,&#8221; Irani said. &#8220;A lot of humanities and anthropology graduate students, as well as some Informatics students, go there regularly so after a while, I've come to recognize and know lots of dedicated students campus' from across campus.&#8221;</p>
<p>Though she lives on campus, Irani finds time to explore other pursuits beyond school.</p>
<p>She is an editor for <a href="http://www.ambidextrousmag.org/">Ambidextrous Magazine</a>, published at Stanford; this publication is a forum for the cross-disciplinary, cross-market community of people with an academic, professional and personal interest in design. </p>
<p>Irani also enjoys expanding her musical taste at the Detroit Bar in Costa Mesa, taking yoga classes, loitering in coffee shops, reading a lot and enjoying the wide variety of ethnic foods near campus.</p>
<p>On track to graduate in 2012, Irani hopes to remain in academia as a professor or researcher.</p>
<p>&#8220;I'm interesting in becoming a research professor so I can combine my passion for teaching and research,&#8221; Irani said. &#8220;I love to work with motivated students who want to learn.&#8221;</p>
<p><i>- Eric Kowalik </i></p>


