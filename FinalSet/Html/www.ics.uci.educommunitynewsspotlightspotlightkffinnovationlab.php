<p><b>Kay Family Foundation funds new mobile technology lab</b></p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="Kay Family Foundation Innovation Lab" class="left" src="../../../bin/img/photos/spotlight/kff_innovation_lab/KayFamilyFoundationInnovationLab.jpg" style="padding: 0.5em;"/>
<!-- -->
<!-- -->
</p>
<p>
What served last fall as a launch pad for the nation&#8217;s first-ever 
<a href="http://www.ics.uci.edu/community/news/spotlight/spotlight_medappjam_2012.php">Med AppJam</a> is now a schoolwide resource for Bren School of ICS majors. 
</p>
<p>
<a href="http://www.ics.uci.edu/about/kfflab/index.php">Kay Family Foundation Innovation Lab</a> &#8212; featuring Apple TVs, a PolyVision smartboard, and MacBook Airs and iPads available for checkout by students &#8212; is the first of its kind on the UC Irvine campus and was specifically designed to encourage teamwork. 
</p>
<p>
&#8220;Computing at UC Irvine is highly collaborative,&#8221; said Dean Hal Stern at the lab&#8217;s grand opening in February. &#8220;I&#8217;m very excited about this space because it provides an environment where students can learn and reinforce that mindset. I want to thank Kay Family Foundation for making this lab possible.&#8221;
</p>
<p class="image_with_wrapped_text">
<img alt="Kay Family Foundation Innovation Lab" class="right" src="../../../bin/img/photos/spotlight/kff_innovation_lab/130222_Mac_Lab_Kickoff-017.jpg" style="height: 260px; width: 173px;"/>
Students, faculty, staff, alumni and supporters attended the event, which included remarks by Stern and members of the ICS Student Council.  
</p>
<p>
&#8220;We used this space in fall 2012 to launch Med AppJam, a collaboration between ICS and the medical school,&#8221; said computer science major Nithin Jilla. &#8220;Nearly 20 teams used it to develop iOS applications, and the tournament was really successful. In the future this space will be used for more competitions and projects. We hope that professors will incorporate it into mobile-based classes as well.&#8221;
</p>
