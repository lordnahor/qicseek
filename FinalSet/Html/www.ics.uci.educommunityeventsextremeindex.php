<p><strong>ABOUT hITEC</strong></p>
<p>The ICS Technology and Entrepreneurship Competition (hITEC) is the cornerstone of the Bren School entrepreneurship program. </p>
<p>The competition is designed to foster a spirit of entrepreneurship among Bren School and UC Irvine students, and fuel the development of new technologies that have the potential to positively impact the marketplace.</p><p><strong>Benefits</strong></p>
			   Gain experience using your skills to turn an idea into a 
potentially viable product. <br/>
			   Compete for $9,000 in prizes.

			   <p><strong>Notice</strong></p>
			   Teams wishing to move on to the
			   <a href="http://www.merage.uci.edu/ResearchAndCenters/Beall/SYCR.aspx">Stradling
 Yocca Carlson &amp; Rauth Business Plan Competition</a>
			   (BPC) must register in both competitions. Registration in the BPC 
requires at least one team member be a student of the Merage School
			   of Business.





