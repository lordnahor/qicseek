<h1>Brainstorming</h1>
<p>Idea Generation has been a topic  of creativity research for centuries. Brainstorming in particular has grown to  become synonymous with idea generation. Brainstorming was first coined in 1953  by Alex Osborn, a co-founder of the largest global advertising agency network.  Osborn claimed that by reducing the amount of self-criticism and criticism from  others during this creative process, a group of individuals can produce better  results in terms of both quantity and quality. </p>
<p>A wealth of research has been  devised to identify brainstorming hindrances and how to best improve the process  both in quantity and quality. Past studies experimented with factors such as  interaction mode (group brainstorming vs. individual brainstorming), electronic  support systems, group size, group history, topic sensitivity, contextual cues,  anonymity, social comparison stimulus, group heterogeneity, member proximity,  leadership, evaluative tone, task separation, and performance incentive that  could influence brainstorming productivity. However, most studies of the  brainstorming process focus on detailing the creative outcomes produced in  controlled laboratory settings. Little is known about how brainstorming is  appropriated and adapted to fit a variety of different contexts. </p>
<p>Our approach involves both  laboratory experiments and field studies. We continue to experiment with  factors such as visualization that could potentially impact the brainstorming  process in the laboratory. We also interview and observe how practitioners brainstorm  in real world settings. Our findings could generate empirical results that  could enhance the current idea generation model. The better understanding could  result in more naturalistic tasks that are better at measuring brainstorming  impacts and successes in the laboratory setting. The design recommendations  surfaced from our research will allow us to create future innovative  collaborative brainstorming systems. </p>
