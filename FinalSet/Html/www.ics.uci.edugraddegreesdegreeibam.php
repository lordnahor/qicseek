<p> <a name="more"></a>
</p><div class="blue_bar"><strong>More about the degree...</strong></div>
<p>Biology and medicine have become data-intensive information sciences.
 Massive data acquisition technologies, such as genome sequencing, 
high-throughput drug screening, and DNA arrays, generate biological 
information in quantities that overwhelm conventional approaches. </p>
<p>Cost-effective health care and quality medical decision-making 
require integrating large amounts of up-to-date information and 
knowledge. </p>
<p>Biological systems have also inspired computer science advances with 
new concepts, including genetic algorithms, artificial neural networks, 
computer viruses and synthetic immune systems, DNA computing, artificial
 life, and hybrid VLSI-DNA gene chips. </p>
<p>New computational opportunities such as these create a critical need 
for theoretical and algorithmic advances in storing, retrieving, 
networking, processing, analyzing, and visualizing biomedical 
information.</p>
<p>Informatics in Biology and Medicine is an interdisciplinary 
concentration at the interface between computer sciences, biological 
sciences, and medicine. It addresses problems that are specific to the 
information technologies in biology, medicine, and health care. </p>
<p>Research topics include gene finding, protein structure and function 
prediction, structural and functional genomics, proteomics, electronic 
patient record systems, medical decision support systems, 
guideline-based health care, medical information access, and 
human-computer interfaces for medical applications. </p>
<p>To make advances in these areas, society needs people with knowledge 
and skills that bridge those taught in conventional biological, medical,
 and computer science curricula. This area provides for such an 
interdisciplinary computer science education.</p>


