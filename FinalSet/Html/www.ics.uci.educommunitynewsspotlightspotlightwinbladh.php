<div id="content_text">

<p><strong>Changing Course  </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: kristina winbladh" height="159" src="../../../bin/img/photos/people/kristina_winbladh.jpg" width="220"/>
<p>
Kristina Winbladh</p>
</div>
<p>Ph.D. student Kristina Winbladh had her heart set on becoming a biomedical researcher, until she discovered that computers were a lot more logical than organic cells.</p>
<p>One day during her first semester as a Chemical Engineering major, she was complaining to her parents about the large number of exceptions-to-the-rule in Organic Chemistry and they encouraged her to try something more "logical". </p>
<p>Her dad, who is a professor of artificial intelligence at the Royal Institute of Technology in Stockholm Sweden, got her a Java book, which Winbladh worked through over Christmas break. </p>
<p>The experience inspired her to change her major to computer science. </p>
<p>&#8220;I realized that programming was a lot of fun and switched over to Computer Science,&#8221; Winbladh said. &#8220;I was fascinated by how I could describe my intentions in a programming language and that the computer would execute exactly what I described, this is not always the case in Biomedicine or Chemistry,&#8221; she said. </p>
<p>After receiving her B.S. in Computer Science from Cal State Long Beach in spring 2004, the Stockholm native chose UC Irvine to pursue her Ph.D. and a career in research.</p>
<p>Though Winbladh and her family talk a lot via Skype, a free Voip (voice over internet protocol) phone, at times it is tough for her to be so far from home. </p>
<p>&#8220;I miss them a lot, but I am fortunate that my parents are able to come and visit me quite frequently,&#8221; Winbladh said. &#8220;I also try to go home once or twice a year.&#8221;        </p>
<p><strong>REQUIREMENTS AND TESTING</strong></p>
<p>&#8220;I always knew I wanted to pursue a career in research, but that the topic of the interest would be software engineering was not always clear,&#8221; Winbladh said. &#8220;I later became interested in Software Engineering because of the interesting and practical problems it presents.&#8221;</p>
<p>The opportunity to work with Bren School Dean Debra J. Richardson, who is a well-known and respected researcher in the field of Software Engineering and specification based testing, was a major selling point for Winbladh and one of the reasons she came to UC Irvine.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;I think UCI and the Bren School offers a good research environment,&#8221; Winbladh said. &#8220;There are always professors and other students to talk to about your work, get feedback from, and to learn from. There are always interesting projects going on in a variety of different areas which makes these conversations even more interesting.&#8221; </p>
<p>One research project Winbladh is engaged in with Informatics professors Debra Richardson and Thomas Alspaugh, is working to exploit the natural symbiotic relation between requirements and testing -- driving the testing process based on requirements to demonstrate high quality software that meets requirements. </p>
<p>There are several benefits of testing against requirements. </p>
<p>First, early test design helps to build quality into the product by inhibiting defect multiplication by testing against a specification, which generates questions about that specification thereby challenging it. </p>
<p>If test design is considered early and against requirements, the requirements thus stand a better chance of getting validated before they cause misunderstandings that are costly later in the development process. </p>
<p>Second, generating tests from requirements as opposed to later specifications and models or code allows the generated test cases to be directly traced back to high-level requirements.</p>
<p>Requirements will be formulated in terms of goal models and scenarios so as to permit expression of user intent in a way that is mechanizable while being relatively easy to understand by a wide variety of stakeholders in the software development process. </p>
<p>&#8220;I intend to show how testing against requirements addresses many of the commonly recognized problems with creating quality software under acceptable budget and time constraints,&#8221; Winbladh said. </p>
<p><strong>OUTSIDE THE LAB</strong></p>
<p>Winbladh&#8217;s research has allowed her the opportunity to travel and present her work at a number of conferences. </p>
<p>&#8220;During my first year I had two research papers accepted; one at the ROSATEA workshop and one at ASE,&#8221; Winbladh said. &#8220;I also attended CRA-W, a conference for first- and second-year female graduate students in computer science, where we were coached by leading research professionals in both industry and academia. I also attended RE, ISSTA, and Grace Hopper during my first year.&#8221;</p>
<p>Life as a researcher can also be stressful, but even these pressure filled moments can be tinged with excitement. </p>
<p>&#8220;One of my most memorable experiences at UCI was my first paper submission, which came only one month after my arrival,&#8221; Winbladh said. &#8220;I remember the stress leading up to the submission, which took place ten minutes to deadline at 2:50 am. Once the file was submitted, I sat outside my apartment, inhaling the night air, and feeling a mix of excitement over my new journey as a researcher and relief that this one was over.&#8221;</p>
<p>But Winbladh&#8217;s life isn&#8217;t all work and no play, she finds time to kick back and relax, important things to do since graduation isn&#8217;t until 2010. </p>
<p>Living on campus in the Palo Verde graduate housing community affords Winbladh the opportunity to take part in campus activities and take advantage of the Anteater Recreation Center. </p>
<p>An avid outdoorswoman, Winbladh also finds time to take in a plethora of outdoor activities.</p>
<p>&#8220;One of the great things about living in California is the many outdoors activities it offers,&#8221; Winbladh said. &#8220;Since we are close to the mountains, I try to go skiing at least once in the winter, which is something I have enjoyed doing since I was about three years old. I also like camping and hiking when the weather is nice.&#8221;</p>
<p><i>- Eric Kowalik</i></p>


