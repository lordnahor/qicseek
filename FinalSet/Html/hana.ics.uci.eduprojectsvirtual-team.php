<h1>Virtual Teams </h1>
<p>A growing number of globally  operating companies have adopted distributed work processes. Collaborating with  distributed team members across the globe has become common practice in larger  companies. Modern conferencing systems allow for cost- and work-efficient  distributed meetings. Communication systems for distributed collaboration  increasingly take the importance of social factors such as awareness and social  ergonomics into account. Tools and systems available today support document  sharing and ad-hoc communication with multiple team members at the same time.  The research field of Computer Supported Collaborative Work (CSCW) has  contributed much to the modern workplace and enabled global organizations to  connect and manage their workforces. The next logical step for networked  organizations is to provide teams, and even complete organizations, with tools,  methods and processes to completely operate in virtually connected spaces. Our  research interest lies in virtual teams and organizations that generally do not  rely on physical office space but conduct all their team or organizational  activity based on a distributed communication infrastructure only.</p>
<p>The purpose of this research is to  explore the nature of totally virtual teams and organizations. We are  interested in how team members conduct their daily work in situations when they  can only communicate across distances using information and communication  technology (ICT). Distributed work has been studied in isolated, ad-hoc  situations. However, continuous distributed settings present new challenges for  teams and organizations. We are interviewing virtual workers in order to  understand how technology could help alleviate the issues.</p>
