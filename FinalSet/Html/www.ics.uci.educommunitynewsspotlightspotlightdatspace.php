<p><b>New student club inspired by maker subculture</b></p>
<p>
<!-- -->
<!-- -->
<!-- -->
<!-- -->
</p>
<p>
<a href="http://www.flickr.com/photos/brenschool/sets/72157628324560397/show/"><img alt="DAT Space" border="0" src="../../../bin/img/photos/spotlight/datspace.jpg" width="450"/></a></p>
<p>With workshop topics ranging from Arduino hacking to espresso brewing, new student organization Design, Art, and Technology Hackerspace (DAT Space) is making good on its promise to provide a student-run physical space where members of the UCI community can meet and work together on creative projects. 
</p>
<p>
Computer science and engineering major Vahan Hartooni, who founded the club along with Ivan Check, Rachel Rose Ulgado, and Nicholas LaJeunesse, says DAT Space aims to provide a venue for undergraduates to produce something tangible. &#8220;Our club is inspired by the <a href="http://en.wikipedia.org/wiki/Maker_subculture">maker</a> community,&#8221; he explains. &#8220;Just imagine the do-it-yourself movement, but geekier and with a drive to create a project that is unique in this world.&#8221; 
</p>
<p>
Sponsored by the <a href="http://luci.ics.uci.edu/">Laboratory for Ubiquitous Computing and Interaction</a>, DAT Space was founded in fall 2011 with funding from a Multidisciplinary Design Program grant through <a href="http://www.calit2.uci.edu/">Calit2</a> and the <a href="http://www.urop.uci.edu/">Undergraduate Research Opportunities Program</a>. The grant program is designed to help students develop skills and knowledge that will propel them into graduate studies or careers in fields that explore connections between different disciplines. 
</p>
<p>
DAT Space&#8217;s inaugural workshop, &#8220;Introduction to Arduino,&#8221; drew undergads, graduate students, researchers and faculty who learned how to program the popular microcontroller. Participants created a device that measured an input (e.g., light, sound or pressure) and did something in response (e.g., beep, light up, tweet). Next, &#8220;Introduction to Embroidery&#8221; had attendees stitching their own yarn designs. 
</p>
<p>
The third workshop, &#8220;<a href="http://www.flickr.com/photos/brenschool/sets/72157628324560397/" title="Play Dough Circuits Workshop">Play Dough Circuits</a>,&#8221; encouraged participants to learn the basics of electronics. Using batteries, a very small amount of wiring and two types of dough (one that conducts and another that insulates), student teams constructed light-up creations &#8212; some unique and others modeled after pop culture icons, like Pikachu and the deadmau5 logo. 
</p>
<p>
&#8220;The best part of the play dough circuits workshop? Finally getting my monster guy to light up,&#8221; said fourth-year biology major Dominique Lyakov.  
</p>
<p>
DAT Space wrapped up their fall quarter calendar with &#8220;Caffeine Night,&#8221; in which students shared their knowledge of making stovetop espresso, Persian black tea and malted cinnamon mochas. Check their <a href="http://www.datspace.net/">website</a> for future events and to view photos and videos of past workshops. 
</p>
