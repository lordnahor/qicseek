            <div class="page-header">
				<div class="styled_title"><h1>Brief overview of the application and what it does</h1></div>
			</div>
				<p>
				Our application for this project is to promote self-improvement through awareness of calories in the foods and beverages around the University of California Irvine Campus. This application is mainly intended to target students who are seeking to keep track of their daily caloric intake to maintain a healthy lifestyle. With this app, the user will not only be able to store his or her basic information in the application such as age, height, and weight, but also view nearby restaurant locations and their menus with indicated calorie counts. As more and more people become aware of their health and eating habits, people want to monitor their caloric intake in order to maintain or gain a healthier lifestyle.  
				</p>

            <div class="page-header">
				<div class="styled_title"><h1>Programming languages, compilers and IDEs used</h1></div>
			</div>
			<p>
				<ul>
					<li>Java, Eclipse</li>
				</ul>
			</p>
			<div class="page-header">
			   <div class="styled_title"><h1>Notable algorithm or other academic material learned in class</h1></div>
			    </div>
			     <p>
				We implemented an algorithm that utilized the accelerometer in order to calculate the number of steps the person was taking. While the phone is in the persons jeans or pants, whenever the accelerometer felt a certain movement it would update the steps taken. From there we were able to calculate the number of calories that were being burned by the person while walking. 
			     </p>
            <div class="page-header">
				<div class="styled_title"><h1>Sources of inspiration for the project</h1></div>
			</div>
				<p>
			Everyone nowadays is trying to be healthier, whether it is by eating healthier or exercising. We wanted to make an app that would assist those that are determined to eat healthier meals and stay under a certain amount of calories per day.
				</p>
				
           <div class="page-header">
				<div class="styled_title"><h1>Libraries, frameworks and other tools used</h1></div>
			</div>
			<p>
				<ul>
					We used sqlite for our database. We just used standard google libraries.
				</ul>
			</p>

            <div class="page-header">
				<div class="styled_title"><h1>Art assets, logos and content not created by the team</h1></div>
			</div>
			<p>
				App logos such as the user logo, recommendation logo, and my location logo were found on the internet. Other than that everything was custom made.
			</p>

			<div class="page-header">
				<div class="styled_title"><h1>The process for making the application</h1></div>
			</div>
			<p>
We first started off at a slow pace. We brainstormed ideas, and eventually developed a solid idea for our app. Within the same day, we drew up the mock-up of our app and the pace picked up right away. Every day and night, each team member worked on his or her specific part of the program. At the end of the day we reported our updates to our facebook group. Each day we would integrate our new code only if they were working properly.
			</p>


            <div class="page-header">
				<div class="styled_title"><h1>Major hurdles that had to be overcome</h1></div>
			</div>
			<p>
				Because of conflicting academic schedules, finding time to meet up as a group posed as a great challenge. Communication was essential to this project especially with different parts of the code being written by different team members. However, we overcame this problem by communicating via facebook, email, and we tried to have as many group discussions on campus as possible.<br /><br />
				Another challenge our team faced was the challenge of developing on an android platform for the first time. We had to familiarize ourselves with the structure of the platform as well as how to implement our programming skills in java, C, and html into Eclipse. <br /><br />
				In addition, we ran into a lot of bugs and errors while coding but group discussions helped resolve the issues in no time. Developing the algorithm that incorporated all the factors we wanted to measure took some time.<br /><br />
				Nevertheless, the biggest challenge we faced was putting all parts of the code together to ensure that the app functions the way we wanted.<br /><br />

			</p>
			

            <div class="page-header">
				<div class="styled_title"><h1>Lessons Learned</h1></div>
			</div>
			<p>
We learned how to work efficiently in a team. We also learned that making one application while multiple people are programming parts of it is difficult to integrate at the end. We had to communicate very efficiently and as be as clear as possible.
			</p>

