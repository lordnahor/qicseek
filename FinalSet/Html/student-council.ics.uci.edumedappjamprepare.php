            	<h2>Presenting your App and the Judging Process</h2>
                <div class="cleaner"></div>

                <p>Presentation is all about impressing the judge. Talk about a problem that exists that your app serves to solve, and be sure to show off the flashiest aspects of the app. As a heads up, your judges are from all backgrounds (this includes computer science, medical research, and medical industry), so be prepared to answer all sorts of in depth questions.<br/><br/>

				As mentioned before, you guys will have to prepare a poster board with information about your app. Make sure it is awesome. You may want to mention in general all the things that I just listed above, just so in the event you don't talk about all aspects with the judge, they can at least see that you and your team have thought about all sorts of uses and benefits that your app provides, in addition to some interesting technical details that your app may have.
				
				More specifics on how exactly your application will be judged will be posted here soon.
</p>

