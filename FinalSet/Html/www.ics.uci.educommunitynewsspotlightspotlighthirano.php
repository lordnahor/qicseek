<div id="content_title">Graduate Student Wins NSF Fellowship</div>

<blockquote>
<div id="content_text">

<p><b><br/></b></p>
<div class="img_caption_left">
<img alt="photo:: Sen Hirano" height="195" src="../../../bin/img/photos/people/sen_hirano.jpg" width="130"/>
<p>Sen<br/>Hirano</p>
</div>
<p><i>This spring, Sen Hirano was awarded a prestigious and highly coveted <a href="http://www.nsfgrfp.org/">National Science Foundation Graduate Research Fellowship</a>. He also earned his master&#8217;s degree from UC Irvine and will now pursue a Ph.D. in Informatics. As one of 20 UCI students who were named NSF Fellows in 2011, Hirano will receive an annual stipend and cost-of-education allowance for three years, plus a one-time travel allowance. The 20 fellowships add up to more than $2.5 million to support research at UCI. Following is a profile of Hirano and his work. </i> </p>
<p>Committed to helping others, Sen Hirano for the past two years has been working on Estrellia, a mobile device developed for new parents to track their baby&#8217;s activity throughout the day.</p>
<p>&#8220;It is meant to be a health indicator,&#8221; explains Hirano. &#8220;For example, parents can easily track how many diapers they go through each day, take that information to their doctor, and see if it&#8217;s normal.&#8221; 
</p>
<p>As an undergraduate at UCI, Hirano and his research teammates worked on vSked, an interactive visual scheduling system that helps children with autism reach their full potential in the classroom &#8212; by using a system of touch screen devices to streamline classroom activities and encourage collaboration among students and staff.</p>
<p>Hirano says his fondness for research stems from the children and communities that benefit from his work. </p>
<p>&#8220;Being able to work with populations like special education teachers is particularly motivating, because of the passion they have to care for their students,&#8221; he says.</p>
<p>Informatics Assistant Professor Gillian Hayes, who serves as Hirano&#8217;s advisor, first met him as an undergraduate in her Projects in Ubiquitous Computing class.</p>
<p>According to Hayes: &#8220;What struck me most about him then, and is still the case now, is that Sen has a unique combination of creativity, passion and work ethic that allows him to overcome challenges in his research and see opportunities where others may not. Sen is also an incredibly generous team player, which makes him a pleasure to advise.&#8221;</p>
<p>Hirano credits Hayes, fellow graduate student Jed Brubaker and others in his lab with helping him prepare a successful application for the NSF Fellowship.</p>
<p>
&#8220;Talking with other people about my NSF application helped me streamline my ideas,&#8221; says Hirano, a second-time applicant to the NSF Graduate Research Fellowship Program. &#8220;Also, the feedback I received from the NSF on my first application really helped make my second one better. This time around, I tried to make my application one story and tie all of it together.&#8221;</p>
