<div id="content_text">

<p><strong>The Producer</strong>
</p>
<div class="img_caption_left">
<img alt="photo:: jacob knobel" height="192" src="../../../bin/img/photos/people/jacob_knobel.jpg" width="200"/>
<p>Jacob Knobel enjoys a sunny  <br/>
  day in Aldrich Park. 
</p>
</div>
<p>As the Executive Producer of <a href="http://www.vdayuci.com/">UCI&#8217;s Vagina Monologues</a>, Jacob Knobel found himself spending a lot of time on repetitive tasks, such as scheduling audition times, sending e-mails to audition participants and tracking ticket sales, to name a few. </p>
<p>The senior <a href="../../../ugrad/degrees/degree_in4matx.php">Informatics major</a> drew on his programming skills to create a stage management software program that helped him streamline these tasks, work more efficiently, and gain time to focus on more important aspects of the production. &#160;</p>
<p>&#8220;Over time I&#8217;ve expanded on it and today it contains pictures of the cast which is helpful in the beginning when I'm learning everyone's names, it auto-generates rosters, has tons of e-mail capabilities, and allows me to keep track of attendance and rehearsal notes,&#8221; Knobel said. &#8220;It also syncs up with our online ticket sales database automatically and updates the current counts for each performance night so I can easily see how many tickets we've sold for each night whether they be online or in person.&#8221;</p>
<p>For Knobel, a Los Angeles native, computers and drama go hand in hand. During his freshman year in high school, Knobel&#8217;s passion for computers dovetailed with technical theater and he worked on a number of school productions.</p>
<p>&#8220;I fell in love with it and loved the ability to combine my talents with technology with the rush and social atmosphere of the theater world and it's something I've been heavily involved with ever since,&#8221; Knobel said. </p>
<p>Though he choose UCI specifically because of the Bren School of ICS and its extensive offering of degree programs and courses, Knobel found a way to still feed his passion for drama. </p>
<p>He worked on the crew of two stage productions his freshmen year and soon began to stage-manage small workshop productions in the drama department. </p>
<p>&#8220;Working in drama allowed me to make friends and kind of balance out my life in a way that worked well for me,&#8221; Knobel said. &#8220;Drama students and ICS students tend to be on two extremes of the spectrum and being able to be around both was the perfect fit for me.&#8221;</p>
<p><strong>MASTERING THE INTANGIBLES</strong></p>
<p>During his sophomore year, Knobel took a stage management class that taught him valuable skills he can apply even in the field of information technology. </p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;I've used what I learned in this class with just about everything I do -- so much of getting a job and working on a project with a team of people is communicating with that team effectively and knowing how to manage the team,&#8221; Knobel said. &#8220;The class was applicable even more in my ICS project teams than in my theater projects and I would recommend it as a required class for everyone in the school.&#8221;</p>
<p>Knobel has first-hand experience of the skills one needs to be successful in the real world. </p>
<p>During the summer before his sophomore year, he and a couple friends started a company called StickyDrive. </p>
<p>The company created software that made removable flash drives more usable by integrating software that allowed people to be more mobile and do what they had to do entirely on their flash drive. </p>
<p>&#8220;The company started out with just me and two other people as founders and over the course of that year grew to occupy a large office space in downtown Santa Cruz with 16 employees,&#8221; Knobel said.</p>
<p>Due to the success of the company, Knobel took the spring quarter of his sophomore year off and moved to Santa Cruz to work full time. </p>
<p>&#8220;This was one of the best decisions I ever made,&#8221; Knobel said. </p>
<p>StickyDrive eventually ended up splitting into two parts, one of which was bought out. The other part, which Knobel is the lead developer for, integrates advertisements and a corporate message on USB flash drives for use in the promotional items industry. </p>
<p><strong>V-DAY</strong></p>
<p>It was during the stage management class that Knobel met the person in charge of UCI&#8217;s Vagina Monologues. </p>
<p>&#8220;I instantly fell in love with the show, with it's message, and everything surrounding it and I ended up taking over during my junior year,&#8221; Knobel said. </p>
<p>The job of executive producer is an all encompassing and time intensive role with a long list of responsibilities.</p>
<p>&#8220;Essentially everything that isn't the actual direction of the monologues themselves and the design of the set falls under my job description and I delegate to my stage manager and other producers,&#8221; Knobel said. </p>
<p>UCI&#8217;s Vagina Monologues is part of V-Day, a global movement to stop violence against women and girls. </p>
<p>V-Day was brought to UCI in 2003 and in the past 5 years, V-Day UCI has raised well over $70,000 and introduced thousands of students and community members to The Vagina Monologues, a stage performance made up of a number of monologues read by a diverse cast of women. </p>
<p>The 2008 performance of the Vagina Monologues, held February 21 - 23 featured 21 performers made up of a wide variety of ethnicities and backgrounds. </p>
<p>Over 1,000 people attended the performances raising over $11,500 that will be donated to UCI&#8217;s Campus Assault Resource and Education center. </p>
<p>&#8220;Working on V-Day has by far been the most memorable thing I've done at UCI,&#8221; Knobel said. &#8220;There's nothing better than being able to work with amazing people, doing work that combines my interests in both technology and theater, and doing it all for a good cause.&#8221;</p>
<p><i>- Eric Kowalik</i></p>


